<h2>CHAPTER I<br />
THE BEGINNING OF THINGS</h2>
<p><strong>THEY</strong> were not railway children to begin with.
I don&rsquo;t suppose they had ever thought about railways except as means
of getting to Maskelyne and Cooke&rsquo;s, the Pantomime, Zo&ouml;logical
Gardens, and Madame Tussaud&rsquo;s. They were just ordinary suburban
children, and they lived with their Father and Mother in an ordinary
red-brick-fronted villa, with coloured glass in the front door, a tiled
passage that was called a hall, a bath-room with hot and cold water,
electric bells, French windows, and a good deal of white paint, and
&ldquo;every modern convenience,&rdquo; as the house-agents say.</p>
<p>There were three of them. Roberta was the eldest. Of course, Mothers
never have favourites, but if their Mother <em>had</em> had a
favourite, it might have been Roberta. Next came Peter, who wished to be an
Engineer when he grew up; and the youngest was Phyllis, who meant extremely
well.</p>
<p>Mother did not spend all her time in paying dull calls to dull ladies,
and sitting dully at home waiting for dull ladies to pay calls to her. She
was almost always there,<p>
</div>

<div class="bookPage" id="p2" name="2">

<p>ready to play with the children, and read to them,
and help them to do their home-lessons. Besides this she used to write stories for them while they were at school, and read them aloud after tea,
and she always made up funny pieces of poetry for their birthdays and for
other great occasions, such as the christening of the new kittens, or the
refurnishing of the doll&rsquo;s house, or the time when they were getting
over the mumps.</p>
<p>These three lucky children always had everything they needed: pretty
clothes, good fires, a lovely nursery with heaps of toys, and a Mother
Goose wallpaper. They had a kind and merry nurse-maid, and a dog who was
called James, and who was their very own. They also had a father who was
just perfect&mdash;never cross, never unjust, and always ready for a
game&mdash;at least, if any time he was <em>not</em> ready, he always
had an excellent reason for it, and explained the reason to the children so
interestingly and funnily that they felt sure he couldn&rsquo;t help himself.</p>
<p>You will think that they ought to have been very happy. And so they were, but they did not know <em>how</em> happy
till the pretty life in the Red Villa was over and done with, and they had
to live a very different life indeed.</p>
<p>The dreadful change came quite suddenly.</p>
<p>Peter had a birthday&mdash;his tenth. Among his other presents was a
model engine more perfect than you could</p></div>

<div class="bookPage" id="pf3" name="">
<img src="illustrations/railway/nesbit-railway003.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></div>

<div class="bookPage" id="p3" name="3"><p>ever have dreamed of. The other
presents were full of charm, but the Engine was fuller of charm than any of
the others were.</p>
<p>Its charm lasted in its full perfection for exactly three days. Then,
owing either to Peter&rsquo;s inexperience or Phyllis&rsquo;s good
intentions, which had been rather pressing, or to some other cause, the
Engine suddenly went off with a bang. James was so frightened that he went
out and did not come back all day. All the Noah&rsquo;s Ark people who were
in the tender were broken to bits, but nothing else was hurt except the
poor little engine and the feelings of Peter. The others said he cried over
it,&mdash;but of course boys of ten do not cry, however terrible the
tragedies may be which darken their lot. He said that his eyes were red
because he had a cold. This turned out to be true, though Peter did not
know it was when he said it, and next day he had to go to bed and stay there. Mother began to be afraid that he might be sickening for
measles, when suddenly he sat up in bed and said:&mdash;</p>
<p>&ldquo;I hate gruel&mdash;I hate barley water&mdash;I hate bread and
milk. I want to get up and have something <em>real</em> to
eat.&rdquo;</p>
<p>&ldquo;What would you like?&rdquo; Mother asked.</p>
<p>&ldquo;A pigeon-pie,&rdquo; said Peter, eagerly, &ldquo;a large
pigeon-pie. A very large one.&rdquo;</p>

</div>

<div class="bookPage" id="p4" name="4">
<p>So Mother asked the Cook to make a large pigeon-pie. The pie was made.
And when the pie was made, it was cooked. And when it was cooked, Peter ate
some of it. After that his cold was better. Mother made a piece of poetry
to amuse him while the pie was being made. It began by saying what an
unfortunate but worthy boy Peter was, and then it went on :&mdash;</p>
<pre>
He had an engine that he loved
With all his heart and soul,
And if he had a wish on earth
It was to keep it whole.
</pre>
<pre>
One day&mdash;my friends, prepare your minds;
I&rsquo;m coming to the worst&mdash;
Quite suddenly a screw went mad,
And then the boiler burst!
</pre>
<pre>
With gloomy face he picked it up
And took it to his Mother,
Though even he could not suppose
That she could make another;
</pre>
<pre>
For those who perished on the line
He did not seem to care,
His engine being more to him
Than all the people there.</pre>
<pre>
And now you see the reason why
Our Peter has been ill:
He soothes his soul with pigeon-pie
His gnawing grief to kill.
</pre>
</div>

<div class="bookPage" id="p5" name="5">
<pre>
He wraps himself in blankets warm
And sleeps in bed till late,
Determined thus to overcome
His miserable fate.
</pre>
<pre>
And if his eyes are rather red,
His cold must just excuse it:
Offer him pie; you may be sure
He never will refuse it.
</pre>

<p>Father had been away in the country for three or four days. All
Peter&rsquo;s hopes for the curing of his afflicted Engine were now fixed on
his Father, for Father was most wonderfully clever with his fingers. He
could mend all sorts of things. He had often acted as veterinary surgeon to
the wooden rocking-horse; once he had saved its life when all human aid was
despaired of, and the poor creature was given up for lost, and even the
carpenter said he didn&rsquo;t see his way to do anything. And it was Father who mended his doll&rsquo;s cradle when no one
else could; and with a little glue and some bits of wood and a penknife
made all the Noah&rsquo;s Ark beasts as strong on their pins as ever they
were, if not stronger.</p>
<p>Peter with heroic unselfishness did not say anything about his Engine
till after Father had had his dinner and his after-dinner cigar. The
unselfishness was Mother&rsquo;s idea&mdash;but it was Peter who carried it
out. And it needed a good deal of patience, too.</p></div>

<div class="bookPage" id="p6" name="6">

<p>At last Mother said to Father, &ldquo;Now, dear, if you&rsquo;re quite
rested, and quite comfy, we want to tell you about the great railway
accident, and ask your advice.&rdquo;</p>
<p>&ldquo;All right,&rdquo; said Father, &ldquo;fire away!&rdquo;</p>
<p>So then Peter told the sad tale, and fetched what was left of the
Engine.</p>
<p>&ldquo;Hum,&rdquo; said Father, when he had looked the Engine over very
carefully.</p>
<p>The children held their breaths.</p>
<p>&ldquo;Is there <em>no</em> hope?&rdquo; said Peter, in a low,
unsteady voice.</p>
<p>&ldquo;Hope? Rather! Tons of it,&rdquo; said Father, cheerfully;
&ldquo;but it&rsquo;ll want something besides hope,&mdash;a bit of brazing,
say, or some solder, and a new valve. I think we&rsquo;d better keep it for a rainy day. In other words,
I&rsquo;ll give up a Saturday afternoon to it, and you shall help
me.&rdquo;</p>
<p>&ldquo;<em>Can</em> girls help to mend engines?&rdquo; Peter asked
doubtfully.</p>
<p>&ldquo;Of course they can. Girls are just as clever as boys, and
don&rsquo;t you forget it! How would you like to be an engine-driver,
Phil?&rdquo;</p>
<p>&ldquo;My face would be always dirty, wouldn&rsquo;t it?&rdquo; said
Phyllis, in unenthusiastic tones, &ldquo;and I expect I should break
something.&rdquo;</p>
</div>

<div class="bookPage" id="p7" name="7">
<p>&ldquo;I should just love it,&rdquo; said Roberta,&mdash;&ldquo;do you
think I could when I&rsquo;m grown up, Daddy? Or even a stoker?&rdquo;</p>
<p>&ldquo;You mean a fireman,&rdquo; said Daddy, pulling and twisting at
the Engine. &ldquo;Well, if you still wish it, when you&rsquo;re grown up,
we&rsquo;ll see about making you a fire-woman. I remember when I was a
boy&mdash;&rdquo;</p>
<p>Just then there was a knock at the front door.</p>
<p>&ldquo;Who on earth!&rdquo; said Father. &ldquo;An Englishman&rsquo;s
house is his castle, of course, but I do wish they built semi-detached
villas with moats and drawbridges.&rdquo;</p>
<p>Ruth&mdash;she was the parlour-maid and had red hair&mdash;came in and said that two gentlemen wanted to see the
master.</p>
<p>&ldquo;I&rsquo;ve shewn them into the Library, Sir,&rdquo; said she.</p>
<p>&ldquo;I expect it&rsquo;s the subscription to the Vicar&rsquo;s
testimonial,&rdquo; said Mother, &ldquo;or else it&rsquo;s the
choir-holiday-fund. Get rid of him quickly, dear. It does break up an
evening so, and it&rsquo;s nearly the children&rsquo;s bed-time.&rdquo;</p>
<p>But Father did not seem to be able to get rid of the gentlemen at all
quickly.</p>
<p>&ldquo;I wish we <em>had</em> got a moat and drawbridge,&rdquo; said
Roberta; &rdquo;then when we didn&rsquo;t want people, we could just pull up
the drawbridge and no one could get in. I expect Father will have forgotten
about when he was a boy if they stay much longer.&rdquo;</p></div>

<div class="bookPage" id="p8" name="8">
<p>Mother tried to make the time pass by telling them a new fairy story
about a Princess with green eyes, but it was difficult because they could
hear the voices of Father and the gentlemen in the Library, and
Father&rsquo;s voice sounded louder and different to the voice he generally
used to people who came about testimonials and holiday funds.</p>
<p>Then the Library bell rang, and every one heaved a breath of relief.</p>
<p>&ldquo;They&rsquo;re going now,&rdquo; said Phyllis; &ldquo;he&rsquo;s
rung to have them shewn out.&rdquo;</p>
<p>But instead of shewing anybody out, Ruth shewed herself in, and she
looked queer, the children thought.</p>
<p>&ldquo;Please&rsquo;m,&rdquo; she said, &ldquo;the Master wants you to
just step into the study. He looks like the dead, mum; I think he&rsquo;s
had bad news. You&rsquo;d best prepare yourself for the worst,
&rsquo;m&mdash;p&rsquo;raps it&rsquo;s a death in the family or a bank busted
or&mdash;&rdquo;</p>
<p>&ldquo;That&rsquo;ll do, Ruth,&rdquo; said Mother, gently; &ldquo;you can
go.&rdquo;</p>
<p>Then Mother went into the Library. There was more talking. Then the bell
rang again, and Ruth fetched a cab. The children heard boots go out and
down the steps. The cab drove away, and the front door shut. Then Mother
came in. Her dear face was as white as her lace collar, and</p></div>

<div class="bookPage" id="p9" name="9">
<p>her eyes looked
very big and shining. Her mouth looked like just a line of pale
red&mdash;her lips were thin and not their proper shape at all.</p>
<p>&ldquo;It&rsquo;s bed-time,&rdquo; she said. &ldquo;Ruth will put you to
bed.&rdquo;</p>
<p>&ldquo;But you promised we should sit up late to-night because
Father&rsquo;s come home,&rdquo; said Phyllis.</p>
<p>&ldquo;Father&rsquo;s been called away&mdash;on business,&rdquo; said
Mother. &ldquo;Come, darlings, go at once.&rdquo;</p>
<p>They kissed her and went. Roberta lingered to give Mother an extra hug
and to whisper&mdash;</p>
<p>&ldquo;It wasn&rsquo;t bad news, Mammy, was it? Is any one
dead&mdash;or&mdash;&rdquo;</p>
<p>&ldquo;Nobody&rsquo;s dead&mdash;no,&rdquo; said Mother, and she almost
seemed to push Roberta away. &ldquo;I can&rsquo;t tell you anything
to-night, my pet. Go, dear, go <em>now</em>.&rdquo;</p>
<p>So Roberta went.</p>
<p>Ruth brushed the girls&rsquo; hair and helped them to undress. (Mother
almost always did this herself.) When she had turned down the gas and left
them she found Peter, still dressed, waiting on the stairs.</p>
<p>&ldquo;I say, Ruth, what&rsquo;s up?&rdquo; he asked.</p>
<p>&ldquo;Don&rsquo;t ask me no questions and I won&rsquo;t tell you no
lies,&rdquo;</p>
</div>

<div class="bookPage" id="p10" name="10">
<p>the red-headed Ruth replied. &ldquo;You&rsquo;ll know soon
enough.&rdquo;</p>
<p>Late that night Mother came up and kissed all three children as they lay
asleep. But Roberta was the only one whom the kiss woke, and she lay
mousey-still, and said nothing.</p>
<p>&ldquo;If Mother doesn&rsquo;t want us to know she&rsquo;s been
crying,&rdquo; she said to herself as she heard through the dark the
catching of her Mother&rsquo;s breath, &ldquo;we <em>won&rsquo;t</em>
know it. That&rsquo;s all.&rdquo;</p>
<p>When they came down to breakfast the next morning, Mother had already
gone out.</p>
<p>&ldquo;To London,&rdquo; Ruth said, and left them to their
breakfast.</p>
<p>&ldquo;There&rsquo;s something awful the matter,&rdquo; said Peter,
breaking his egg. &ldquo;Ruth told me last night we should know soon
enough.&rdquo;</p>
<p>&ldquo;Did you <em>ask</em> her?&rdquo; said Roberta, with
scorn.</p>
<p>&ldquo;Yes, I did!&rdquo; said Peter, angrily. &ldquo;If you could go to
bed without caring whether Mother was worried or not, I couldn&rsquo;t. So
there!&rdquo;</p>
<p>&ldquo;I don&rsquo;t think we ought to ask the servants things Mother
doesn&rsquo;t tell us,&rdquo; said Roberta.</p>
<p>&ldquo;That&rsquo;s right, Miss Goody-goody,&rdquo; said Peter,
&ldquo;preach away.&rdquo;</p>
</div>

<div class="bookPage" id="p11" name="11">
<p>&ldquo;<em>I&rsquo;m</em> not goody,&rdquo; said Phyllis, &ldquo;but
I think Bobbie&rsquo;s right this time.&rdquo;</p>
<p>&ldquo;Of course. She always is. In her own opinion,&rdquo; said
Peter.</p>
<p>&ldquo;Oh, <em>don&rsquo;t</em>!&rdquo; cried Roberta, putting down
her egg-spoon; &ldquo;don&rsquo;t let&rsquo;s be horrid to each other.
I&rsquo;m sure some dire calamity is happening. Don&rsquo;t let&rsquo;s make
it worse!&rdquo;</p>
<p>&ldquo;Who began, I should like to know?&rdquo; said Peter.</p>
<p>Roberta made an effort, and answered:&mdash;</p>
<p>&ldquo;I did, I suppose, but&mdash;&rdquo;</p>
<p>&ldquo;Well, then,&rdquo; said Peter, triumphantly. But before he went
to school he thumped his sister between the shoulders and told her to cheer
up.</p>
<p>The children came home to one o&rsquo;clock dinner, but Mother was not
there. And she was not there at tea-time.</p>
<p>It was nearly seven before she came in, looking so ill and tired that
the children felt they could not ask her any questions. She sank into an
armchair. Phyllis took the long pins out of her hat, while Roberta took off
her gloves, and Peter unfastened her walking-shoes and fetched her soft
velvety slippers for her.</p>
<p>When she had had a cup of tea, and Roberta had put eau-de-cologne on her
poor head that ached, Mother said:&mdash;</p>
</div>

<div class="bookPage" id="p12" name="12">
<p>&ldquo;Now, my darlings, I want to tell you something. Those men last
night did bring very bad news, and Father will be away for some time. I am
very very worried about it, and I want you all to help me, and not to make
things harder for me.&rdquo;</p>
<p>&ldquo;As if we would!&rdquo; said Roberta, holding Mother&rsquo;s hand
against her face.</p>
<p>&ldquo;You can help me very much,&rdquo; said Mother,</p>
<p>&ldquo;by being good and happy and not quarrelling when I&rsquo;m
away,&rdquo;&mdash;Roberta and Peter exchanged guilty
glances,&mdash;&ldquo;for I shall have to be away a good deal.&rdquo;</p>
<p>&ldquo;We won&rsquo;t quarrel. Indeed we won&rsquo;t,&rdquo; said
everybody. And meant it, too.</p>
<p>&ldquo;Then,&rdquo; Mother went on, &ldquo;I want you not to ask me any
questions about this trouble; and not to ask anybody else any
questions.&rdquo;</p>
<p>Peter cringed and shuffled his boots on the carpet.</p>
<p>&ldquo;You&rsquo;ll promise this, too, won&rsquo;t you?&rdquo; said
Mother.</p>
<p>&ldquo;I did ask Ruth,&rdquo; said Peter, suddenly. &ldquo;I&rsquo;m very
sorry, but I did.&rdquo;</p>
<p>&ldquo;And what did she say?&rdquo;</p>
<p>&ldquo;She said I should know soon enough.&rdquo;</p>
</div>

<div class="bookPage" id="p13" name="13">

<p>&ldquo;It isn&rsquo;t necessary for you to know anything about it,&rdquo;
said Mother; &ldquo;it&rsquo;s about business, and you never do understand
business, do you?&rdquo;</p>
<p>&ldquo;No,&rdquo; said Roberta, &ldquo;is it something to do with
Government?&rdquo; For Father was in a Government Office.</p>
<p>&ldquo;Yes,&rdquo; said Mother. &ldquo;Now it&rsquo;s bed-time, my
darlings. And don&rsquo;t <em>you</em> worry. It&rsquo;ll all come right
in the end.&rdquo;</p><p>&ldquo;Then don&rsquo;t <em>you</em> worry either, Mother,&rdquo;
said Phyllis, &ldquo;and we&rsquo;ll all be as good as gold.&rdquo;</p>
<p>Mother sighed and kissed them.</p>
<p>&ldquo;We&rsquo;ll begin being good the first thing to-morrow
morning,&rdquo; said Peter, as they went upstairs.</p>
<p>&ldquo;Why not <em>now</em>?&rdquo; said Roberta.</p>
<p>&ldquo;There&rsquo;s nothing to be good <em>about</em> now,
silly,&rdquo; said Peter.</p>
<p>&ldquo;We might begin to try to <em>feel</em> good,&rdquo; said
Phyllis, &ldquo;and not call names.&rdquo;</p>
<p>&ldquo;Who&rsquo;s calling names?&rdquo; said Peter. &ldquo;Bobbie knows
right enough that when I say &lsquo;silly,&rsquo; it&rsquo;s just the same
as if I said Bobbie.&rdquo;</p>
<p>&ldquo;<em>Well</em>,&rdquo; said Roberta.</p>
</div>

<div class="bookPage" id="p14" name="14">
<p>&ldquo;No, I don&rsquo;t mean what you mean. I mean it&rsquo;s just
a&mdash;what is it Father calls it?&mdash;a germ of endearment! Good
night.&rdquo;</p>
<p>The girls folded up their clothes with more than usual
neatness&mdash;which was the only way of being good that they could think
of.</p>
<p>&ldquo;I say,&rdquo; said Phyllis, smoothing out her pinafore,
&ldquo;you used to say it was so dull&mdash;nothing happening, like in
books. Now something <em>has</em> happened.&rdquo;</p>
<p>&ldquo;I never wanted things to happen to make Mother unhappy,&rdquo;
said Roberta. &ldquo;Everything&rsquo;s perfectly horrid.&rdquo;</p>
<p>Everything continued to be perfectly horrid for some weeks.</p>
<p>Mother was nearly always out. Meals were dull and dirty. The
between-maid was sent away, and Aunt Emma came on a visit. Aunt Emma was
much older than Mother. She was going to Germany to be a governess. She was
very busy getting her clothes ready, and they were very ugly, dingy
clothes, and she had them always littering about, and the sewing-machine
seemed to whir&mdash;on and on all day and most of the night. Aunt Emma
believed in keeping children in their proper places. And they more than
returned the compliment. Their idea of Aunt Emma&rsquo;s proper place was
anywhere where they were not. So they</p>
</div>

<div class="bookPage" id="p15" name="15">
<p>saw very little of her. They
preferred the company of the servants, who were more amusing. Cook, if in a
good temper, could sing comic songs, and the housemaid, if she happened not
to be offended with you, could imitate a hen that has laid an egg, a bottle
of champagne being opened, and could mew like two cats fighting. The
servants never told the children what the bad news was that the gentlemen
had brought to Father. But they kept hinting that they could tell a great
deal if they chose&mdash;and this was not comfortable.</p><p>One day when Peter had made a booby trap over the bath-room door, and it
had acted beautifully as Ruth passed through, that red-haired parlour-maid
caught him and boxed his ears.</p>
<p>&ldquo;You&rsquo;ll come to a bad end,&rdquo; she said furiously,
&ldquo;you nasty little lamb, you! If you don&rsquo;t mend your ways,
you&rsquo;ll go where your precious Father&rsquo;s gone, so I tell you
straight!&rdquo;</p>
<p>Roberta repeated this to her Mother, and next day Ruth was sent
away.</p>
<p>Then came the time when Mother came home and went to bed and stayed
there two days and the Doctor came, and the children crept wretchedly about
the house and wondered if the world was coming to an end.</p>
<p>Mother came down one morning to breakfast, very pale</p></div>

<div class="bookPage" id="p16" name="16">
<p>and with lines on
her face that used not to be there. And she smiled, as well as she could,
and said:&mdash;</p>
<p>&ldquo;Now, my pets, everything is settled. We&rsquo;re going to leave
this house, and go and live in the country. Such a ducky dear little white
house. I know you&rsquo;ll love it.&rdquo;</p>
<p>A whirling week of packing followed&mdash;not just packing clothes, like
when you go to the</p>
<p>seaside, but packing chairs and tables, covering their tops with sacking
and their legs with straw.</p>
<p>All sorts of things were packed that you don&rsquo;t pack when you go to
the seaside. Crockery, blankets, candlesticks, carpets, bedsteads,
saucepans, and even fenders and fire-irons.</p>
<p>The house was like a furniture warehouse. I think the children enjoyed
it very much. Mother was very busy, but not too busy now to talk to them,
and read to them, and even to make a bit of poetry for Phyllis to cheer her
up when she fell down with a screwdriver and ran it into her hand.</p>
<p>&ldquo;Aren&rsquo;t you going to pack this, Mother?&rdquo; Roberta asked,
pointing to the beautiful cabinet inlaid with red turtle-shell and
brass.</p>
<p>&ldquo;We can&rsquo;t take everything,&rdquo; said Mother.</p>
</div>

<div class="bookPage" id="p17" name="17">
<p>&ldquo;But we seem to be taking all the ugly things,&rdquo; said
Roberta.</p>
<p>&ldquo;We&rsquo;re taking the useful ones,&rdquo; said Mother;
&ldquo;we&rsquo;ve got to play at being Poor for a bit, my
chickabiddy.&rdquo;</p>
<p>When all the ugly useful things had been packed up and taken away in a
van by men in green-baize aprons, the two girls and Mother and Aunt Emma
slept in the two spare rooms where the furniture</p>
<p>was all pretty. All their beds had gone. A bed was made up for Peter on the
drawing-room sofa.</p>
<p>&ldquo;I say, this is larks,&rdquo; he said, wriggling joyously as
Mother tucked him up. &ldquo;I do like moving! I wish we moved once a
month.&rdquo;</p>
<p>Mother laughed.</p>
<p>&ldquo;I don&rsquo;t!&rdquo; she said. &ldquo;Good night,
Peterkin.&rdquo;</p>
<p>As she turned away Roberta saw her face. She never forgot it.</p>
<p>&ldquo;O, Mother,&rdquo; she whispered all to herself as she got into
bed, &ldquo;how brave you are! How I love you! Fancy being brave enough to
laugh when you&rsquo;re feeling like <em>that</em>!&rdquo;</p>
<p>Next day boxes were filled, and boxes and more boxes; and then late in
the afternoon a cab came to take them to the station.</p>
</div>

<div class="bookPage" id="p18" name="18">
<p>Aunt Emma saw them off. They felt that <em>they</em> were seeing
<em>her</em> off, and they were glad of it.</p>
<p>&ldquo;But, oh, those poor little German children that she&rsquo;s going
to governess!&rdquo; whispered Phyllis. &ldquo;I wouldn&rsquo;t be them for
anything!&rdquo;</p>
<p>At first they enjoyed looking out of the window, but when it grew dark
they grew sleepier and sleepier, and no one knew how long they had been in
the train when they were roused by Mother&rsquo;s shaking them gently and
saying:&mdash;</p><p>&ldquo;Wake up, dears. We&rsquo;re there.&rdquo;</p>
<p>They woke up, cold and melancholy, and stood shivering on the draughty
platform while the luggage was taken out of the train. Then the engine,
puffing and blowing, set to work again, and dragged the train away. The
children watched the tail-lights of the guard&rsquo;s van disappear into the
darkness.</p>
<p>This was the first train the children saw on that railway which was in
time to become so very dear to them. They did not guess then how they would
grow to love the railway, and how soon it would become the centre of their
new life, nor what wonders and changes it would bring to them. They only
shivered and sneezed and hoped the walk to the new house would not be long.
</p>
</div>

<div class="bookPage" id="p19" name="19">
<p>Peter&rsquo;s nose was colder than he ever remembered it to have been
before. Roberta&rsquo;s hat was crooked, and the elastic seemed tighter than
usual. Phyllis&rsquo;s shoelaces had come undone.</p>
<p>&ldquo;Come,&rdquo; said Mother, &ldquo;we&rsquo;ve got to walk. There
aren&rsquo;t any cabs here.&rdquo;</p>
<p>The walk was dark and muddy. The children stumbled a little on the rough
