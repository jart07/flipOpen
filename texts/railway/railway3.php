<h2>CHAPTER III<br />
THE OLD GENTLEMAN</h2>
<p><strong>AFTER</strong> the adventure of Peter&rsquo;s
Coal-Mine, it seemed well to the children to keep away from the
station,&mdash;but they did not, they could not, keep away from the
railway. They had lived all their lives in a street where cabs and
omnibuses rumbled by at all hours, and the carts of butchers and bakers and
candlestick makers (I never saw a candlestick-maker&rsquo;s cart; did you?)
might occur at any moment. Here in the deep silence of the sleeping country
the only things that went by were the trains. They seemed to be all that
was left to link the children to the old life that had once been theirs.
Straight down the hill in front of Three Chimneys the daily passage of
their six feet began to mark a path across the crisp, short turf. They
began to know the hours when certain trains passed, and they gave names to
them. The 9.15 up was called the Green Dragon. The 10.7 down was the Worm
of Wantley. The midnight town express, whose shrieking rush they sometimes
woke from their dreams to hear, was the Fearsome Fly-by-night. Peter got up
once, in chill starshine, and, peeping at it through his curtains, named it
on the spot.</p></div>

<div class="bookPage" id="p51" name="51">

<p>It was by the Green Dragon that the old gentleman travelled. He was a
very nice-looking old gentleman, and he looked as if he were nice, too,
which is not at all the same thing. He had a fresh-coloured, clean-shaven
face, and white hair, and he wore rather odd-shaped collars and a top-hat
that wasn&rsquo;t exactly the same kind as other people&rsquo;s. Of course
the children didn&rsquo;t see all this at first. In fact the first thing
they noticed about the old gentleman was his hand.</p>
<p>It was one morning as they sat on the fence waiting for the Green
Dragon, which was three and a quarter minutes late by Peter&rsquo;s
Waterbury watch that he had had given him on his last birthday.</p>
<p>&ldquo;The Green Dragon&rsquo;s going where Father is,&rdquo; said
Phyllis; &ldquo;if it were a really real dragon, we could stop it and ask
it to take our love to Father.&rdquo;</p>
<p>&ldquo;Dragons don&rsquo;t carry people&rsquo;s love,&rdquo; said Peter;
&ldquo;they&rsquo;d be above it.&rdquo;</p>
<p>&ldquo;Yes, they do, if you tame them thoroughly first. They fetch and
carry like pet spaniels,&rdquo; said Phyllis, &ldquo;and feed out of your hand. I wonder why Father never
writes to us.&rdquo;</p>
<p>&ldquo;Mother says he&rsquo;s been too busy,&rdquo; said Bobbie;
&ldquo;but he&rsquo;ll write soon, she says.&rdquo;</p></div>

<div class="bookPage" id="p52" name="52">

<p>&ldquo;I say,&rdquo; Phyllis suggested, &ldquo;let&rsquo;s all wave to
the Green Dragon as it goes by. If it&rsquo;s a magic dragon, it&rsquo;ll
understand and take our loves to Father. And if it isn&rsquo;t, three waves
aren&rsquo;t much. We shall never miss them.&rdquo;</p>
<p>So when the Green Dragon tore shrieking out of the mouth of its dark
lair, which was the tunnel, all three children stood on the railing and
waved their pocket handkerchiefs without stopping to think whether they
were clean handkerchiefs or the reverse. They were, as a matter of fact,
very much the reverse.</p>
<p>And out of a first-class carriage a hand waved back. A quite clean hand.
It held a newspaper. It was the old gentleman&rsquo;s hand.</p>
<p>After this it became the custom for waves to be exchanged between the
children and the 9.15.</p>
<p>And the children, especially the girls, liked to think that perhaps the
old gentleman knew Father, and would meet him &ldquo;in business&rdquo;
wherever that shady retreat might he, and tell him how his three children
stood on a rail far away in the green country and waved their love to him every morning, wet or fine.</p>
<p>For they were now able to go out in all sorts of weathers such as they
would never have been allowed to go out in when they lived in their villa
house. This was Aunt Emma&rsquo;s doing, and the children felt more and more
that</p></div>

<div class="bookPage" id="p53" name="53">
<p>they had not been quite fair to this unattractive aunt, when they
found how useful were the long gaiters and waterproof coats that they had
laughed at her for buying for them.</p>
<p>Mother, all this time, was very busy with her writing. She used to send
off a good many long blue envelopes with stories in them,&mdash;and large
envelopes of different sizes and colours used to come to her. Sometimes she
would sigh when she opened them and say:&mdash;</p>
<p>&ldquo;Another story come home to roost. O dear, O dear!&rdquo; and then
the children would be very sorry.</p>
<p>But sometimes she would wave the envelope in the air and say:&mdash;</p>
<p>&ldquo;Hooray, hooray. Here&rsquo;s a sensible Editor. He&rsquo;s taken my
story and this is the proof of it.&rdquo;</p>
<p>At first the children thought &ldquo;the Proof&rdquo; meant the letter
the sensible Editor had written, but they presently got to know that the proof war long slips of paper with the
story printed on them.</p>
<p>Whenever an Editor was sensible, there were buns for tea.</p>
<p>One day Peter was going down to the village to get buns to celebrate the
sensibleness of the Editor of the <strong>Children&rsquo;s Globe</strong>,
when he met the Station Master.</p></div>

<div class="bookPage" id="p54" name="54">
<p>Peter felt very uncomfortable, for he had now had time to think over the
affair of the coal-mine. He did not like to say &ldquo;Good morning&rdquo;
to the Station Master, as you usually do to any one you meet on a lonely
country road, because he had a hot feeling, which spread even to his ears,
that the Station Master might not care to speak to a person who had stolen
coals. &ldquo;Stolen&rdquo; is a nasty word, but Peter felt it was the
right one. So he looked down, and said Nothing.</p>
<p>It was the Station Master who said &ldquo;Good morning,&rdquo; as he
passed by. And Peter answered &ldquo;Good morning.&rdquo; Then he
thought:&mdash;</p>
<p>&ldquo;Perhaps he doesn&rsquo;t know who I am by daylight, or he
wouldn&rsquo;t be so polite.&rdquo;</p>
<p>And he did not like the feeling which thinking this gave him. And then
before he knew what he was going to do he ran after the Station Master, who stopped when he heard Peter&rsquo;s hasty boots crunching the
road, and coming up with him very breathless and with his ears now quite
magenta-coloured, he said:&mdash;</p>
<p>&ldquo;I don&rsquo;t want you to be polite to me if you don&rsquo;t know
me when you see me.&rdquo;</p>
<p>&ldquo;Eh?&rdquo; said the Station Master.</p></div>

<div class="bookPage" id="pf55" name="">
<img src="illustrations/railway/nesbit-railway055.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></div>

<div class="bookPage" id="p55" name="55">

<p>&ldquo;I thought perhaps you didn&rsquo;t know it was me that took the
coals,&rdquo; Peter went on, &ldquo;when you said &lsquo;Good
morning.&rsquo; But it was, and I&rsquo;m sorry. There.&rdquo;</p>
<p>&ldquo;Why,&rdquo; said the Station Master, &ldquo;I wasn&rsquo;t
thinking anything at all about the precious coals. Let bygones be bygones.
And where were you off to in such a hurry?&rdquo;</p>
<p>&ldquo;I&rsquo;m going to buy buns for tea,&rdquo; said Peter.</p>
<p>&ldquo;I thought you were all so poor,&rdquo; said the Station
Master.</p>
<p>&ldquo;So we are,&rdquo; said Peter, confidentially, &ldquo;but we
always have three pennyworth of halfpennies for tea whenever Mother sells a
story or a poem or anything.&rdquo;</p>
<p>&ldquo;Oh,&rdquo; said the Station Master, &ldquo;so your Mother writes
stories, does she?&rdquo;</p>
<p>&ldquo;The beautifullest you ever,&rdquo; said Peter.</p>
<p>&ldquo;You ought to be very proud to have such a clever
Mother.&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Peter, &ldquo;but she used to play with us more
before she had to be so clever.&rdquo;</p>
<p>&ldquo;Well,&rdquo; said the Station Master, &ldquo;I must be getting
along. You give us a look in at the Station whenever you feel so inclined.
And as to coals, it&rsquo;s a word that&mdash;well&mdash;oh, no, we never
mention it, eh?&rdquo;</p></div>

<div class="bookPage" id="p56" name="56">
<p>&ldquo;Thank you,&rdquo; said Peter. &ldquo;I&rsquo;m very glad it&rsquo;s
all straightened out between us.&rdquo; And he went on across the canal
bridge to the village to get the buns, feeling more comfortable in his
inside mind than he had felt since the hand of the Station Master had
fastened on his collar that night among the coals.</p>
<p>Next day when they had sent the threefold wave of greeting to Father by
the Green Dragon, and the old gentleman had waved back as usual, Peter
proudly led the way to the station.</p>
<p>&ldquo;But ought we?&rdquo; said Bobbie.</p>
<p>&ldquo;After the coals, she means,&rdquo; Phyllis explained.</p>
<p>&ldquo;I met the Station Master yesterday,&rdquo; said Peter, in an
offhand way, and he pretended not to hear what Phyllis had said; &ldquo;he
expresspecially invited us to go down any time we liked.&rdquo;</p>
<p>&ldquo;After the coals?&rdquo; repeated Phyllis. &ldquo;Stop a
minute&mdash;my bootlace is undone again.&rdquo;</p>
<p>&ldquo;It always <em>is</em> undone again,&rdquo; said Peter,
&ldquo;and the Station Master was more of a gentleman than you&rsquo;ll ever
be, Phil&mdash;throwing coals at a chap&rsquo;s head like that.&rdquo;</p>
<p>Phyllis did up her bootlace and went on in silence, but her shoulders
shook, and presently a fat tear fell off her</p></div>

<div class="bookPage" id="p57" name="57">
<p>nose and splashed on the metal
of the railway line. Bobby saw it.</p>
<p>&ldquo;Why, what&rsquo;s the matter, darling?&rdquo; she said, stopping
short and putting her arm round the heaving shoulders.</p>
<p>&ldquo;He called me un&mdash;un&mdash;ungentlemanly,&rdquo; sobbed
Phyllis. &ldquo;I didn&rsquo;t never call him unladylike, not even when he
tied my Clorinda to the firewood bundle and burned her at the stake for a
martyr.&rdquo;</p>
<p>Peter had indeed perpetrated this outrage a year or two before.</p>
<p>&ldquo;Well, you began, you know,&rdquo; said Bobby, honestly,
&ldquo;about coals and all that. Don&rsquo;t you think you&rsquo;d better
both unsay everything since the wave, and let honour be
satisfied?&rdquo;</p>
<p>&ldquo;I will if Peter will,&rdquo; said Phyllis, sniffing.</p>
<p>&ldquo;All right,&rdquo; said Peter; &ldquo;honour is satisfied. Here,
use my hankie, Phil, for goodness&rsquo; sake, if you&rsquo;ve lost yours as
usual. I wonder what you do with them.&rdquo;</p>
<p>&ldquo;You had my last one,&rdquo; said Phyllis, indignantly, &ldquo;to
tie up the rabbit-hutch door with. But you&rsquo;re very ungrateful.
It&rsquo;s quite right what it says in the poetry book about sharper than a
serpent it is to have a toothless child,&mdash;but it means ungrateful when
it says toothless. Miss Lowe told me so.&rdquo;</p></div>

<div class="bookPage" id="p58" name="58">

<p>&ldquo;All right,&rdquo; said Peter, impatiently, &ldquo;I&rsquo;m sorry.
<em>There</em>! Now will you come on?&rdquo;</p>
<p>They reached the station and spent a joyous two hours with the Porter.
He was a worthy man and seemed never tired of answering the questions that
begin with &ldquo;Why&mdash;&rdquo; which many people in higher ranks of
life often seem weary of.</p>
<p>He told them many things that they had not known before,&mdash;as for
instance that the things that hook carriages together are called couplings,
and that the pipes like great serpents that hang over the couplings are
meant to stop the train with.</p>
<p>&ldquo;If you could get a holt of one o&rsquo; them when the train is
going and pull &rsquo;emr apart,&rdquo; said he, &ldquo;she&rsquo;d stop dead
off with a jerk.&rdquo;</p>
<p>&ldquo;Who&rsquo;s she?&rdquo; said Phyllis.</p>
<p>&ldquo;The train, of course,&rdquo; said the Porter. After that the
train was never again &ldquo;It&rdquo; to the children.</p>
<p>&ldquo;And you know the thing in the carriages where it says on it, &lsquo;Five pounds fine for improper use.&rsquo; If you was
to improperly use that, the train &rsquo;ud stop.&rdquo;</p>
<p>&ldquo;And if you used it properly?&rdquo; said Roberta.</p>
</div>

<div class="bookPage" id="p59" name="59">
<p>&ldquo;It &rsquo;ud stop just the same, I suppose,&rdquo; said he,
&ldquo;but it isn&rsquo;t proper use unless you&rsquo;re being murdered.
There was an old lady once&mdash;some one kidded her on it was a refreshment-room bell, and she used it improper, not being in danger of her
life, though hungry, and when the train stopped and the guard came along
expecting to find some one weltering in their last moments, she says,
&lsquo;Oh, please, Mister, I&rsquo;ll take a glass of stout and a bath
bun,&rsquo; she says. And the train was seven minutes behind her time as it
was.&rdquo;</p>
<p>&ldquo;What did the guard say to the old lady?&rdquo;</p>
<p>&ldquo;I dunno,&rdquo; replied the Porter, &ldquo;but I lay she
didn&rsquo;t forget it in a hurry, whatever it was.&rdquo;</p>
<p>In such delightful conversation the time went by all too quickly.</p>
<p>The Station Master came out once or twice from that sacred inner temple
behind the place where the hole is that they sell you tickets through, and
was most jolly with them all.</p>
<p>&ldquo;Just as if coal had never been discovered,&rdquo; Phyllis
whispered to her sister.</p>
<p>He gave them each an orange, and promised to take them up into the
signal-box one of these days, when he wasn&rsquo;t so busy.</p>
</div>

<div class="bookPage" id="p60" name="60">
<p>Several trains went through the station, and Peter noticed for the first
time that engines have numbers on them, like cabs.</p>
<p>&ldquo;Yes,&rdquo; said the Porter, &ldquo;I knowed a young gent as used
to take down the numbers of every single one he seed; in a green note-book
with silver corners it was, owing to his father being very well-to-do in
the wholesale stationery.&rdquo;</p>
<p>Peter felt that he could take down numbers, too, even if he was not the
son of a wholesale stationer. As he did not happen to have a green leather
note-book with silver corners, the Porter gave him a yellow envelope and on
it he noted:&mdash;
<p>379<br />
663</p>
and felt that this was the beginning of what would be a most interesting
collection.</p>
<p>That night at tea he asked Mother if she had a green leather note-book
with silver corners. She had not; but when she heard what he wanted it for
she gave him a little black one.</p>
<p>&ldquo;It has a few pages torn out,&rdquo; said she; &ldquo;but it will
hold quite a lot of numbers, and when it&rsquo;s full I&rsquo;ll give you another. I&rsquo;m so glad you like the railway. Only,
please, you mustn&rsquo;t walk on the line.&rdquo;</p>
</div>

<div class="bookPage" id="p61" name="61">
<p>&ldquo;Not if we face the way the train&rsquo;s coming?&rdquo; asked
Peter, after a gloomy pause, in which glances of despair were
exchanged.</p>
<p>&ldquo;No&mdash;really not,&rdquo; said Mother.</p>
<p>Then Phyllis said, &ldquo;Mother, didn&rsquo;t you ever walk on the
railway lines when you were little?&rdquo;</p>
<p>Mother was an honest and honourable Mother, so she had to say,
&ldquo;Yes.&rdquo;</p>
<p>&ldquo;Well, then,&rdquo; said Phyllis.</p>
<p>&ldquo;But, darlings, you don&rsquo;t know how fond I am of you. What
should I do if you got hurt?&rdquo;</p>
<p>&ldquo;Are you fonder of us than Granny was of you when you were
little?&rdquo; Phyllis asked. Bobbie made signs to her to stop, but Phyllis
never did see signs, no matter how plain they might be.</p>
<p>Mother did not answer for a minute. She got up to put more water in the
teapot.</p>
<p>&ldquo;No one,&rdquo; she said at last, &ldquo;ever loved any one more
than my mother loved me.&rdquo;</p>
<p>Then she was quiet again, and Bobbie kicked Phyllis hard under the
table, because Bobbie understood a little bit the thoughts that were making
Mother so quiet&mdash;the</p></div>

<div class="bookPage" id="p62" name="62">
<p>thoughts of the time when Mother was a little girl and was all the world to her mother. It seems
so easy and natural to run to Mother when one is in trouble. Bobbie
understood a little how people do not leave off running to their mothers
when they are in trouble even when they are grown up, and she thought she
knew a little what it must be to be sad, and have no mother to run to any
more.</p>
<p>So she kicked Phyllis who said:&mdash;</p>
<p>&ldquo;What are you kicking me like that for, Bob?&rdquo;</p>
<p>And then Mother laughed a little and sighed and said:&mdash;</p>
<p>&ldquo;Very well, then. Only let me be sure you do know which way the
trains come&mdash;and don&rsquo;t walk on the line near the tunnel or near
corners.&rdquo;</p>
<p>&ldquo;Trains keep to the left like carriages,&rdquo; said Peter,
&ldquo;so if we keep to the right, we&rsquo;re bound to see them
coming.&rdquo;</p>
<p>&ldquo;Very well,&rdquo; said Mother, and I dare say you think that she
ought not to have said it. But she remembered about when she was a little
girl herself, and she did say it,&mdash;and neither her own children nor
you nor any other children in the world could ever understand exactly what
it cost her to do it. Only some few of you, like Bobbie, may understand a
very little bit.</p></div>

<div class="bookPage" id="p63" name="63">
<p>It was the very next day that Mother had to stay in bed because her head
ached so. Her hands were burning hot, and she would not eat anything, and
her throat was very sore.</p>
<p>&ldquo;If I was you, Mum,&rdquo; said Mrs. Viney, &ldquo;I should take
and send for the doctor. There&rsquo;s a lot of catchy complaints a-going
about just now. My sister&rsquo;s eldest&mdash;she took a chill and it went
to her inside, two year ago come Christmas, and she&rsquo;s never been the
same gell since.&rdquo;</p>
<p>Mother wouldn&rsquo;t at first, but in the evening she felt so much worse
that Peter was sent to the house in the village that had three laburnum
trees by the gate, and on the gate a brass plate with W.W. Forrest, M.D.,
on it.</p>
<p>W.W. Forrest, M.D., came at once. He talked to Peter on the way back. He
seemed a most charming and sensible man, interested in railways, and
rabbits, and really important things.</p>
<p>When he had seen Mother, he said it was influenza.</p>
<p>&ldquo;Now, Lady Grave-airs,&rdquo; he said in the hall to Bobbie,
&ldquo;I suppose you&rsquo;ll want to be head-nurse.&rdquo;</p>
<p>&ldquo;Of course,&rdquo; said she.</p>
<p>&ldquo;Well, then, I&rsquo;ll send down some medicine. Keep up a good fire. Have some strong beef tea made ready to give</p></div>

<div class="bookPage" id="p64" name="64">
<p>her as soon as the fever goes down. She can have grapes now, and Brand&rsquo;s Beef
Essence&mdash;and soda water and milk, and you&rsquo;d better get in a
bottle of brandy. The best brandy. Cheap brandy is worse than
poison.&rdquo;</p>
<p>She asked him to write it all down, and he did.</p>
<p>When Bobbie shewed Mother the list he had written, Mother laughed. It
<em>was</em> a laugh, Bobbie decided, though it was rather odd and
feeble.</p>
<p>&ldquo;Nonsense,&rdquo; said Mother, lying in bed with eyes as bright as
beads, &ldquo;I can&rsquo;t afford all that rubbish. Tell Mrs. Viney to boil
two pounds of scrag-end of the neck for your dinners to-morrow, and I can
have some of the broth. Yes, I should like some more water now, love. And
will you get a basin and sponge my hands?&rdquo;</p>
<p>Roberta obeyed. When she had done everything she could to make Mother
less uncomfortable, she went down to the others. Her cheeks were very red,
her lips set tight, and her eyes almost as bright as Mother&rsquo;s.</p>
<p>She told them what the Doctor had said, and what Mother had said.</p>
<p>&ldquo;And now,&rdquo; said she, when she had told all, &ldquo;there&rsquo;s no one but us to do anything, and we&rsquo;ve got to do
it. I&rsquo;ve got the shilling for the mutton.&rdquo;</p></div>

<div class="bookPage" id="p65" name="65">

<p>&ldquo;We can do without the beastly mutton,&rdquo; said Peter;
&ldquo;bread and butter will support life. People have lived on less on
desert islands many a time.&rdquo;</p>
<p>&ldquo;Of course,&rdquo; said his sister. And Mrs. Viney was sent to the
village to get as much brandy and soda water and beef for beef tea as she
could buy for a shilling.</p>
<p>&ldquo;But even if we never have anything to eat at all,&rdquo; said
Phyllis, &ldquo;you can&rsquo;t get all those other things with our dinner
money.&rdquo;</p>
<p>&ldquo;No,&rdquo; said Bobbie, frowning, &ldquo;we must find out some
other way. Now <em>think</em>, everybody, just as hard as ever you
can.&rdquo;</p>
<p>They did think. And presently they talked. And later, when Bobbie had
gone up to sit with Mother in case she wanted anything, the other two were
very busy with scissors and a white sheet, and a paint brush, and the pot
of Brunswick black that Mrs. Viney used for grates and fenders. They did
not manage to do what they wished, exactly, with the first sheet, so they
took another out of the linen cupboard. It did not occur to them that they
were spoiling good sheets which cost good money. They only knew that they were making a
good&mdash;but what they were making comes later.</p></div>

<div class="bookPage" id="p66" name="66">

<p>Bobbie&rsquo;s bed had been moved into Mother&rsquo;s room, and several
times in the night she got up to mend the fire, and to give her mother milk
and soda water. Mother talked to herself a good deal, but it did not seem
to mean anything. And once she woke up suddenly and called out,
&ldquo;Mamma, mamma!&rdquo; and Bobbie knew she was calling for Granny, and
that she had forgotten that it was no use calling, because Granny was
dead.</p>
<p>In the early morning Bobbie heard her name and jumped out of bed and ran
to Mother&rsquo;s bedside.</p>
<p>&ldquo;Oh!&mdash;ah!, yes&mdash;I think I was asleep,&rdquo; said
Mother. &ldquo;My poor little duck, how tired you&rsquo;ll be&mdash;I do
hate to give you all this trouble.&rdquo;</p>
<p>&ldquo;Trouble!&rdquo; said Bobbie.</p>
<p>&ldquo;Ah, don&rsquo;t cry, sweet,&rdquo; Mother said; &ldquo;I shall be
all right in a day or two.&rdquo;</p>
<p>And Bobbie said &ldquo;Yes,&rdquo; and tried to smile.</p>
<p>When you are used to ten hours of solid sleep, to get up three or four
times in your sleep-time makes you feel as though you had been up all
night. Bobbie felt quite stupid and her eyes were sore and stiff, but she tidied the room, and arranged everything neatly
before the Doctor came.</p>
<p>This was at half-past eight.</p></div>

<div class="bookPage" id="p67" name="67">
<p>&ldquo;Everything going on all right, little Nurse?&rdquo; he said at
the front door. &ldquo;Did you get the brandy?&rdquo;</p>
<p>&ldquo;I&rsquo;ve got the brandy,&rdquo; said Bobbie, &ldquo;in a little
flat bottle.&rdquo;</p>
<p>&ldquo;I didn&rsquo;t see the grapes or the Brand&rsquo;s Essence,
though,&rdquo; said he.</p>
<p>&ldquo;No,&rdquo; said Bobbie, firmly, &ldquo;but you will tomorrow. And
there&rsquo;s some beef stewing in the oven for beef tea.&rdquo;</p>
<p>&ldquo;Who told you to do that?&rdquo; he asked.</p>
<p>&ldquo;I noticed what Mother did when Phil had mumps.&rdquo;</p>
<p>&ldquo;Right,&rdquo; said the Doctor. &ldquo;Now you get your old woman
to sit with your mother, and then you eat a good breakfast, and go straight
to bed and sleep till dinner-time. We can&rsquo;t afford to have the
head-nurse ill.&rdquo;</p>
<p>He was really quite a nice doctor.</p>
<p>When the 9.15 came out of the tunnel that morning the old gentleman in
the first-class carriage put down his newspaper, and got ready to wave his hand to the three children on the fence. But this morning there
were not three. There was only one. And that was Peter.</p><p>Peter was not on the railings either, as usual. He was standing in front
of them in an attitude like that of a showman showing off the animals in a
menagerie, or of the kind clergyman when he points with a wand at the</p></div>

<div class="bookPage" id="p68" name="68">
<p>&ldquo;Scenes from Palestine,&rdquo; when there is a magic lantern and he
is explaining it.</p>
<p>Peter was pointing, too. And what he was pointing at was a large white
sheet nailed against the fence. On the sheet there were thick black letters
more than a foot long.</p>
<p>Some of them had run a little, because of Phyllis having put the
Brunswick black on too eagerly, but the words were quite easy to read.</p>
<p>And this was what the old gentleman and several other people in the
train read in the large black letters on the white sheet:&mdash;</p>
<strong>LOOK OUT AT THE STATION</strong>
<p>A good many people did look out at the station and were disappointed,
for they saw nothing unusual. The old gentleman looked out, too, and at
first he too saw nothing more unusual than the gravelled platform and the sunshine and the wallflowers and forget-me-nots
in the station borders. It was only just as the train was beginning to puff
and pull itself together to start again that he saw Phyllis. She was quite
out of breath with running.</p>
<p>&ldquo;Oh,&rdquo; she said, &ldquo;I thought I&rsquo;d missed you. My
bootlaces would keep coming down and I fell over them twice. Here, take
it.&rdquo;</p></div>

