<h2>CHAPTER IV<br />
THE ENGINE-BURGLAR</h2>
<p><strong>WHAT</strong> was left of the second sheet and the
Brunswick black came in very nicely to make a banner bearing the legend
<strong>SHE IS NEARLY WELL THANK YOU</strong>
and this was displayed to the Green Dragon about a fortnight after the
arrival of the wonderful hamper. The old gentleman saw it, and waved a
cheerful response from the train. And when this had been done the children
saw that now was the time when they must tell Mother what they had done
when she was ill. And it did not seem nearly so easy as they had thought it
would be. But it had to be done. And it was done. Mother was extremely
angry. She was very seldom angry, and now she was angrier than they had
ever known her. This was horrible. But it was much worse when she suddenly
began to cry. Crying is catching, I believe, like measles and whooping
cough. At any rate, every one at once found itself taking part in a
crying-party.</p>
<p>Mother stopped first. She dried her eyes and then she said:&mdash;</p>

</div>

<div class="bookPage" id="p74" name="74">
<p>&ldquo;I&rsquo;m sorry I was so angry, darlings, because I know you
didn&rsquo;t understand.&rdquo;</p><p>&ldquo;We didn&rsquo;t mean to be naughty, Mammy,&rdquo; sobbed Bobbie,
and Peter and Phyllis sniffed.</p>
<p>&ldquo;Now, listen,&rdquo; said Mother; &ldquo;it&rsquo;s quite true that
we&rsquo;re poor, but we have enough to live on. You mustn&rsquo;t go telling
every one about our affairs&mdash;it&rsquo;s not right. And you must never,
never, never ask strangers to give you things. Now always remember
that&mdash;won&rsquo;t you?&rdquo;</p>
<p>They all hugged her and rubbed their damp cheeks against hers and
promised that they would.</p>
<p>&ldquo;And I&rsquo;ll write a letter to your old gentleman, and I shall
tell him that I didn&rsquo;t approve&mdash;oh, of course I shall thank him,
too, for his kindness. It&rsquo;s <em>you</em> I don&rsquo;t approve of,
my darlings, not the old gentleman. He was as kind as ever he could be. And
you can give the letter to the Station Master to give him,&mdash;and we
won&rsquo;t say any more about it.&rdquo;</p>
<p>Afterward when the children were alone, Bobbie said:&mdash;</p>
<p>&ldquo;Isn&rsquo;t Mother splendid? You catch any other grown-up saying
it was sorry it had been angry.&rdquo;</p><p>&ldquo;Yes,&rdquo; said Peter, &ldquo;she <em>is</em> splendid; but
it&rsquo;s rather awful when she&rsquo;s angry.&rdquo;</p></div>

<div class="bookPage" id="p75" name="75">

<p>&ldquo;She&rsquo;s like Avenging and Bright in the song,&rdquo; said
Phyllis. &ldquo;I should like to look at her if it wasn&rsquo;t so awful.
She looks so beautiful when she&rsquo;s really downright furious.&rdquo;</p>
<p>They took the letter down to the Station Master.</p>
<p>&ldquo;I thought you said you hadn&rsquo;t got any friends except in
London,&rdquo; said he.</p>
<p>&ldquo;We&rsquo;ve made him since,&rdquo; said Peter.</p>
<p>&ldquo;But he doesn&rsquo;t live hereabouts?&rdquo;</p>
<p>&ldquo;No&mdash;we just know him on the railway.&rdquo;</p>
<p>Then the Station Master retired to that sacred inner temple behind the
little window where the tickets are sold, and the children went down to the
Porter&rsquo;s room and talked to the Porter. They learned several
interesting things from him,&mdash;among others that his name was Perks,
that he was married and had three children, that the lamps in front of
engines are called head-lights and the ones at the back tail-lights.</p>
<p>&ldquo;And that just shews,&rdquo; whispered Phyllis, &ldquo;that trains
really <em>are</em> dragons in disguise, with proper heads and
tails.&rdquo;</p>
<p>It was on this day that the children first noticed that all engines are
not alike.</p></div>

<div class="bookPage" id="p76" name="76">
<p>&ldquo;Alike?&rdquo; said the Porter whose name was Perks, &ldquo;lor
love you, no, Miss. No more alike nor what you an&rsquo; me are. That little
un without a tender as went by just now all on her own, that was a tank,
that was&mdash;she&rsquo;s off to do some shunting t&rsquo; other side
o&rsquo; Maidbridge. That&rsquo;s as it might be you, Miss. Then there&rsquo;s
goods engines, great, strong things with three wheels each
side&mdash;joined with rods to strengthen &rsquo;em&mdash;as it might be me.
Then there&rsquo;s mainline engines as it might be this &rsquo;ere young
gentleman when he grows up and wins all the races at &rsquo;is
school&mdash;so he will. The mainline engine she&rsquo;s built for speed as
well as power. That&rsquo;s one to the 9.15 up.&rdquo;</p>
<p>&ldquo;The Green Dragon,&rdquo; said Phyllis.</p>
<p>&ldquo;We calls her the Snail, Miss, among ourselves,&rdquo; said the
Porter. &ldquo;She&rsquo;s oftener be&rsquo;indand nor any train on the
line.&rdquo;</p>
<p>&ldquo;But the engine&rsquo;s green,&rdquo; said Phyllis.</p>
<p>&ldquo;Yes, Miss,&rdquo; said Perks, &ldquo;so&rsquo;s a snail some
seasons of the year.&rdquo;</p>
<p>The children agreed as they went home to dinner that the Porter was most
delightful company.</p>
<p>Next day was Roberta&rsquo;s birthday. In the afternoon she was politely
but firmly requested to get out of the way and keep there till
tea-time.</p></div>

<div class="bookPage" id="p77" name="77">
<p>&ldquo;You aren&rsquo;t to see what we&rsquo;re going to do till it&rsquo;s
done; it&rsquo;s a glorious surprise,&rdquo; said Phyllis.</p>
<p>And Roberta went out into the garden all alone. She tried to be
grateful, but she felt she would much rather have helped in whatever it was
than have to spend her birthday afternoon by herself, no matter how
glorious the surprise might be.</p>
<p>Now that she was alone, she had time to think, and one of the things she
thought of most was what mother had said in one of those feverish nights
when her hands were so hot and her eyes so bright.</p>
<p>The words were, &ldquo;Oh, what a doctor&rsquo;s bill there&rsquo;ll be
for this!&rdquo;</p>
<p>She walked round and round the garden among the rose-bushes that
hadn&rsquo;t any roses yet, only buds, and the lilac bushes and syringas and
American currants, and the more she thought of the doctor&rsquo;s bill, the
less she liked the thought of it.</p>
<p>And presently she made up her mind. She went out through the side door
of the garden and climbed up the steep field to where the road runs along
by the canal. She walked along until she came to the bridge that crosses
the canal and leads to the village, and here she waited. It was very
pleasant in the sunshine to lean one&rsquo;s elbows on the</p></div>

<div class="bookPage" id="p78" name="78">
<p>warm stone of the bridge and look down at the blue water of the canal.
Bobbie had never seen any other canal, except the Regent&rsquo;s canal, and
the water of that is not at all a pretty colour. And she had never seen any
river at all except the Thames, which also would be all the better if its
face were washed.</p>
<p>Perhaps the children would have loved the canal as much as the railway,
but for two things. One was that they had found the railway
<em>first</em>&mdash;on that first, wonderful morning when the house
and the country and the moors and rocks and great hills were all new to
them. They had not found the canal till some days later. The other reason
was that every one on the railway had been kind to them&mdash;the Station
Master, the Porter, and the old gentleman who waved. And the people on the
canal were anything but kind.</p>
<p>The people on the canal were, of course, the bargees who steered the
slow barges up and down, or walked beside the old horses that trampled up
the mud of the towing-path, and strained at the long tow-ropes.</p>
<p>Peter had once asked one of the bargees the time, and had been told to
&ldquo;get out of that,&rdquo; in a tone so fierce that he did not stop to
say anything about his having just as much right on the towing-path as the man himself.
Indeed, he did not even think of saying it till some time later.</p></div>

<div class="bookPage" id="p79" name="79">
<p>Then another day when the children thought they would like to fish in
the canal, a boy in a barge threw lumps of coal at them, and one of these
hit Phyllis on the back of the neck. She was just stooping down to tie up
her bootlace, and though the coal hardly hurt at all it made her not care
very much about going on fishing.</p>
<p>On the bridge, however, Roberta felt quite safe, because she could look
down on the canal, and if any boy shewed signs of meaning to throw coals,
she could duck behind the parapet.</p>
<p>Presently there was a sound of wheels, which was just what she
expected.</p>
<p>The wheels were the wheels of the Doctor&rsquo;s dog-cart, and in the
cart, of course, was the Doctor.</p>
<p>He pulled up, and called out:&mdash;</p>
<p>&ldquo;Hullo, head Nurse! Want a lift?</p>
<p>&ldquo;I wanted to see you,&rdquo; said Bobbie.</p>
<p>&ldquo;Your mother&rsquo;s not worse, I hope?&rdquo; said the Doctor.</p>
<p>&ldquo;No&mdash;but&mdash;&rdquo;</p>
<p>&ldquo;Well, skip in, then, and we&rsquo;ll go for a drive.&rdquo;</p>
<p>Roberta climbed in and the bony brown horse was made to turn round&mdash;which it did not like at all, for it was</p>
</div>

<div class="bookPage" id="p80" name="80">
<p>looking forward to its tea&mdash;I mean its oats.</p>
<p>&ldquo;This <em>is</em> jolly,&rdquo; said Bobbie, as the dog-cart
flew along the road by the canal.</p>
<p>&ldquo;We could throw a stone down any one of your three
chimneys,&rdquo; said the Doctor, as they passed the house.</p>
<p>&ldquo;Yes,&rdquo; said Bobbie, &ldquo;but you&rsquo;d have to be a jolly
good shot.&rdquo;</p>
<p>&ldquo;How do you know I&rsquo;m not?&rdquo; said the Doctor. &ldquo;Now,
then, what&rsquo;s the trouble?&rdquo;</p>
<p>Bobbie fidgeted with the hook of the driving apron.</p>
<p>&ldquo;Come, out with it,&rdquo; said the Doctor.</p>
<p>&ldquo;It&rsquo;s rather hard, you see,&rdquo; said Bobbie, &ldquo;to out
with it; because of what Mother said.&rdquo;</p>
<p>&ldquo;What <em>did</em> Mother say?&rdquo;</p>
<p>&ldquo;She said I wasn&rsquo;t to go telling every one that we&rsquo;re
poor. But you aren&rsquo;t every one, are you?&rdquo;</p>
<p>&ldquo;Not at all,&rdquo; said the Doctor, cheerfully.
&ldquo;Well?&rdquo;</p>
<p>&ldquo;Well, I know doctors are very extravagant&mdash;I mean expensive,
and Mrs. Viney told me that her doctoring only cost her twopence a week
because she belonged to a Club.&rdquo;</p></div>

<div class="bookPage" id="pf80" name="">
<p><img src="illustrations/railway/nesbit-railway080.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></p></div>

<div class="bookPage" id="p81" name="81">
<p>&ldquo;Yes?&rdquo;</p>
<p>&ldquo;You see she told me what a good doctor you were, and I asked her
how she could afford you, because she&rsquo;s much poorer than we are.
I&rsquo;ve been in her house and I know. And then she told me about the
Club, and I thought I&rsquo;d ask you&mdash;and&mdash;oh, I don&rsquo;t want
Mother to be worried! Can&rsquo;t we be Club, too, the same as Mrs.
Viney?&rdquo;</p>
<p>The Doctor was silent. He was rather poor himself, and he had been
rather pleased at getting a new family to attend. So I think his feelings
at that minute were rather mixed.</p>
<p>&ldquo;You aren&rsquo;t cross with me, are you?&rdquo; said Bobbie, in a
very small voice.</p>
<p>The Doctor roused himself.</p>
<p>&ldquo;Cross? How could I be? You&rsquo;re a very sensible little woman.
Now look here, don&rsquo;t you worry. I&rsquo;ll make it all right with your
Mother, even if I have to make a special brand-new Club all for her. Look
here, this is where the Aqueduct begins.&rdquo;</p>
<p>&ldquo;What&rsquo;s an Aque&mdash;what&rsquo;s its name?&rdquo; asked
Bobbie.</p>
<p>&ldquo;A water bridge,&rdquo; said the Doctor. &ldquo;Look.&rdquo;</p>
<p>The road rose to a bridge over the canal. To the left was a steep rocky
cliff with trees and shrubs growing in the cracks of the rock.</p></div>

<div class="bookPage" id="p82" name="82">
<p>And the canal here left off
running along the top of the hill and started to run on a bridge of its
own&mdash;a great bridge with tall arches that went right across the
valley.</p>
<p>Bobbie drew a long breath.</p>
<p>&ldquo;It is grand, isn&rsquo;t it?&rdquo; she said. &ldquo;It&rsquo;s
like pictures in the History of Rome.&rdquo;</p>
<p>&ldquo;Right!&rdquo; said the Doctor, &ldquo;that&rsquo;s just exactly
what it is like. The Romans were dead nuts on aqueducts. It&rsquo;s a
splendid piece of engineering.&rdquo;</p>
<p>&ldquo;I thought engineering was making engines.&rdquo;</p>
<p>&ldquo;Ah, there are different sorts of engineering&mdash;making roads
and bridges and tunnels is one kind. And making fortifications is another.
Well, we must be turning back. And, remember, you aren&rsquo;t to worry
about doctor&rsquo;s bills or you&rsquo;ll be ill yourself, and then
I&rsquo;ll send you in a bill as long as the aqueduct.&rdquo;</p>
<p>When Bobbie had parted from the Doctor at the top of the field that ran
down from the road to Three Chimneys, she could not feel that she had done
wrong. She knew that Mother would perhaps think differently. But Bobbie
felt that for once she was the one who was right, and she scrambled down
the rocky slope with a really happy feeling.</p></div>

<div class="bookPage" id="p83" name="83">
<p>Phyllis and Peter met her at the back door. They were unnaturally clean
and neat, and Phyllis had a red bow in her hair. There was only just time
for Bobbie to make herself tidy and tie up her hair with a blue bow before
a little bell rang.</p>
<p>&ldquo;There!&rdquo; said Phyllis, &ldquo;that&rsquo;s to shew the
surprise is ready. Now you wait till the bell rings again and then you may
come into the dining room.&rdquo;</p>
<p>So Bobbie waited.</p>
<p>&ldquo;Tinkle, tinkle,&rdquo; said the little bell, and Bobbie went into
the dining room, feeling rather shy. Directly she opened the door she found
herself as it seemed in a new world of light and flowers and singing.
Mother and Peter and Phyllis were standing in a row at the end of the
table. The shutters were shut and there were twelve candles on the table,
one for each of Roberta&rsquo;s years. The table was covered with a sort of
pattern of flowers, and at Roberta&rsquo;s place was a thick wreath of
forget-me-nots and several most interesting little packages. And Mother and
Phyllis and Peter were singing&mdash;to the first part of the tune of St.
Patrick&rsquo;s Day. Roberta knew that Mother had written the words on
purpose for her birthday. It was a little way of Mother&rsquo;s on
birthdays. It had begun on Bobbie&rsquo;s fourth birthday when Phyllis was a baby. Bobbie remembered learning the verses to say to Father
&ldquo;for a surprise.&rdquo;</p></div>

<div class="bookPage" id="p84" name="84">
<p> She wondered if Mother remembered, too. The
four-year-old verse had been:<p>
<pre>
Daddy dear, I&rsquo;m only four
And I&rsquo;d rather not be more.
Four&rsquo;s the nicest age to be,
Two and two and one and three.
What I love is two and two,
Mother, Peter, Phil, and you.
What you love is one and three,
Mother, Peter, Phil, and me.
Give your little girl a kiss
Because she learned and told you this.
</pre>
<p>The song the others were singing now went like this:&mdash;</p>
<pre>
Our darling Roberta
No sorrow shall hurt her
If we can prevent it
Her whole life long.
Her birthday&rsquo;s our f&ecirc;te day,
We&rsquo;ll male it our great day,
And give her our presents
And sing her our song.
May pleasures attend her
And may the Fates send her
The happiest journey
Along her life&rsquo;s way.
With skies bright above her
And dear ones to love her!
Dear Bob! Many happy
Returns of the day!
</pre></div>

<div class="bookPage" id="p85" name="85">
<p>When they had finished singing they cried, &ldquo;Three cheers for our
Bobbie!&rdquo; and gave them very loudly. Bobbie felt exactly as though she
were going to cry&mdash;you know that odd feeling in the bridge of your
nose and the pricking in your eyelids? But before she had time to begin
they were all kissing and hugging her.</p>
<p>&ldquo;Now,&rdquo; said Mother, &ldquo;look at your presents.&rdquo;</p>
<p>They were very nice presents. There was a green and red needle-book that
Phyllis had made herself in secret moments. There was a darling little
silver brooch of Mother&rsquo;s shaped like a buttercup, which Bobbie had
known and loved for years, but which she had never never thought would come
to be her very own. There was also a pair of blue glass vases from Mrs.
Viney. Roberta had seen and admired them in the village shop. And there
were three birthday cards with pretty pictures and wishes.</p>
<p>Mother fitted the forget-me-not crown on Bobbie&rsquo;s brown head.</p>
<p>&ldquo;And now look at the table,&rdquo; she said.</p>
<p>There was a cake on the table covered with white sugar, with &ldquo;Dear
Bobbie&rdquo; on it in pink sweets, and there were buns and jam; but the
nicest thing was that the big table</p></div>

<div class="bookPage" id="p86" name="86">
<p>was almost covered with flowers&mdash;wallflowers were laid all round the
tea-tray&mdash;there was a ring of forget-me-nots round each plate. The
cake had a wreath of white lilac round it, and in the middle was something
that looked like a pattern all done with single blooms of lilac or
wallflower or laburnum.</p>
<p>&ldquo;What is it?&rdquo; asked Roberta.</p>
<p>&ldquo;It&rsquo;s a map&mdash;a map of the railway!&rdquo; cried Peter.
&ldquo;Look&mdash;those lilac lines are the metals,&mdash;-and there&rsquo;s
the station done in brown wallflowers. The laburnum is the train, and there
are the signal-boxes, and the road up to here&mdash;and those fat red
daisies are us three waving to the old gentleman&mdash;that&rsquo;s him, the
pansy in the laburnum train.&rdquo;</p>
<p>&ldquo;And there&rsquo;s &lsquo;Three Chimneys&rsquo; done in the purple
primroses,&rdquo; said Phyllis. &ldquo;And that little tiny rose-bud is
Mother looking out for us when we&rsquo;re late for tea. Peter invented it
all, and we got all the flowers from the station. We thought you&rsquo;d
like it better.&rdquo;</p>
<p>&ldquo;That&rsquo;s my present,&rdquo; said Peter, suddenly dumping down
his adored steam-engine on the table in front of her. Its tender had been
lined with fresh white paper, and was full of sweets.</p>
</div>

<div class="bookPage" id="p87" name="87">
<p>&ldquo;Oh, Peter,&rdquo; cried Bobbie, quite overcome by this munificence, &ldquo;not your own dear little engine that you&rsquo;re
so fond of?&rdquo;</p>
<p>&ldquo;Oh, no,&rdquo; said Peter, very promptly, &ldquo;not the engine.
Only the sweets.&rdquo;</p>
<p>Bobbie couldn&rsquo;t help her face changing a little&mdash;not so much
because she was disappointed at not getting the engine, as because she had
thought it so very noble of Peter, and now she felt she had been silly to
think it. Also she felt she must have seemed greedy to expect the engine as
well as the sweets. So her face changed. Peter saw it. He hesitated a
minute; then his face changed, too, and he said: &ldquo;I mean not
<em>all</em> the engine. I&rsquo;ll let you go halves if you
like.&rdquo;</p>
<p>&ldquo;You&rsquo;re a brick,&rdquo; cried Bobbie; &ldquo;it&rsquo;s a
splendid present.&rdquo; She said no more aloud, but to herself she
said:&mdash;</p>
<p>&ldquo;That was awfully jolly decent of Peter because I know he
didn&rsquo;t mean to. Well, the broken half shall be my half of the engine,
and I&rsquo;ll get it mended and give it back to Peter for his
birthday.&rdquo;&mdash;&ldquo;Yes, Mother dear, I should like to cut the
cake,&rdquo; she added, and tea began.</p>
<p>It was a delightful birthday. After tea Mother played games with
them&mdash;any game they liked&mdash;-and of course their first choice was
blind-man&rsquo;s-buff, in the course of which Bobbie&rsquo;s forget-me-not wreath
twisted itself</p></div>

<div class="bookPage" id="p88" name="88">
<p>crookedly over one of her ears and stayed there. Then when
it was near bed-time and time to calm down, Mother had a lovely new story
to read to them.</p>
<p>&ldquo;You won&rsquo;t sit up late working, will you, Mother?&rdquo;
Bobbie asked as they said good night.</p>
<p>And Mother said, No, she wouldn&rsquo;t&mdash;she would only just write
to Father and then go to bed.</p>
<p>But when Bobbie crept down a little later to bring up her
presents,&mdash;for she felt she really could not be separated from them
all night,&mdash;Mother was not writing, but leaning her head on her arms
and her arms on the table. I think it was rather good of Bobbie to slip
quietly away, saying over and over, &ldquo;She doesn&rsquo;t want me to know
she&rsquo;s unhappy, and I won&rsquo;t know; I won&rsquo;t know.&rdquo; But it
made a sad end to the birthday.</p>
<p>The very next morning Bobbie began to watch her opportunity to get
Peter&rsquo;s engine mended secretly. And the opportunity came the very next
afternoon.</p>
<p>Mother went by train to the nearest town to do shopping. When she went
there, she always went to the Post-office. Perhaps to post her letters to Father, for she
never gave them to the children or Mrs. Viney to post, and she never went
to the village herself. Peter and Phyllis went with her. Bobbie wanted an
excuse not to go, but try as she would she couldn&rsquo;t think of a good</p>
</div>

<div class="bookPage" id="p89" name="89">
<p>one. And just when she felt that all was lost, her frock caught on a big
nail by the kitchen door and there was a great criss-cross tear all along
the front of the skirt. I assure you this was really an accident. So the
others pitied her and went without her, for there was no time for her to
change, be cause they were rather late already and had to hurry to the
station to catch the train.</p>
<p>When they had gone, Bobbie put on her everyday frock, and went down to
the railway. She did not go into the station, but she went along the line
to the end of the platform where the engine is when the down train is
alongside the platform&mdash;the place where there is a water tank and a
long, limp, leather hose, like an elephant&rsquo;s trunk. She hid behind a
bush on the other side of the railway. She had the toy engine done up in
brown paper, and she waited patiently with it under her arm.</p><p>Then when the next train came in and stopped, Bobbie went across the
metals of the up-line and stood beside the engine. She had never been so
close to an engine before. It looked much larger and harder than she had
expected, and it made her feel very small indeed, and, somehow, very
soft&mdash;as if she could very very easily be hurt rather badly.</p>
<p>&ldquo;I know what silk-worms feel like now,&rdquo; said Bobbie to
herself.</p></div>

<div class="bookPage" id="p90" name="90">

<p>The engine-driver and fireman did not see her. They were leaning out on
the other side, telling the Porter a tale about a dog and a leg of
mutton.</p>
<p>&ldquo;If you please,&rdquo; said Roberta,&mdash;but the engine was
blowing off steam and no one heard her.</p>
<p>&ldquo;If you please, Mr. Engineer,&rdquo; she spoke a little louder,
but the Engine happened to speak at the same moment, and of course
Roberta&rsquo;s soft little voice hadn&rsquo;t a chance.</p>
<p>It seemed to her that the only way would be to climb on to the engine
and pull at their coats. The step was high, but she got her knee on it, and
clambered into the cab; she stumbled and fell on hands and knees on the base of the great heap of coals that led up to the square opening in the
tender. The engine was not above the weaknesses of its fellows; it was
making a great deal more noise than there was the slightest need for. And
just as Roberta fell on the coals, the engine-driver, who had turned
without seeing her, started the engine, and when Bobbie had picked herself
up, the train was moving&mdash;not fast, but much too fast for her to get
off.</p>
<p>All sorts of dreadful thoughts came to her all together in one horrible
flash. There were such things as express trains that went on, she supposed,
for hundreds of miles without stopping. Suppose this should be one of them?</p>
</div>

<div class="bookPage" id="p91" name="91">
<p>How would she go home again? She had no money to pay for the return
journey.</p>
<p>&ldquo;And I&rsquo;ve no business here. I&rsquo;m an
engine-burglar&mdash;that&rsquo;s what I am,&rdquo; she thought. &ldquo;I
shouldn&rsquo;t wonder if they could lock me up for this.&rdquo; And the
train was going faster and faster.</p>
<p>There was something in her throat that made it impossible for her to
speak. She tried twice. The men had their backs to her. They were doing
something to things that looked like taps.</p>
<p>Suddenly she put out her hand and caught hold of the nearest sleeve. The man turned with a start, and he and Roberta
stood for a minute looking at each other in silence. Then the silence was
broken by them both.</p>
<p>The man said, &ldquo;Here&rsquo;s a bloomin&rsquo; go!&rdquo; and Roberta
burst into tears.</p>
<p>The other man said he was blooming well blest,&mdash;or something like
it,&mdash;but though naturally surprised they were not exactly unkind.</p>
<p>&ldquo;You&rsquo;re a naughty little gell, that&rsquo;s what you
are,&rdquo; said the fireman, and the engine-driver said:&mdash;</p>
</div>

<div class="bookPage" id="p92" name="92">
<p>&ldquo;Daring little piece, I call her,&rdquo; but they made her sit
down on an iron seat in the cab and told her to stop crying and tell them
what she meant by it, directly minute.</p>
<p>She did stop, as soon as she could. One thing that helped her was the
thought that Peter would give almost his ears to be in her place&mdash;on a
real engine&mdash;really going. The children had often wondered whether any
engine-driver could be found noble enough to take them for a ride on an
engine,&mdash;and now there she was. She dried her eyes and sniffed
earnestly.</p>
<p>&ldquo;Now, then,&rdquo; said the fireman, &ldquo;out with it. What do
you mean by it, eh?&rdquo;</p>
<p>&ldquo;Oh, please,&rdquo; sniffed Bobbie, and stopped.</p>
<p>&ldquo;Try again,&rdquo; said the engine-driver, encouragingly.</p>
<p>Bobbie tried again.</p>
<p>&ldquo;Please, Mr. Engineer,&rdquo; she said, &ldquo;I did call out to
you from the line, but you didn&rsquo;t hear me&mdash;and I just climbed up
to touch you on the arm&mdash;quite gently I meant to do it&mdash;and then
I fell into the coals&mdash;and I am so sorry if I frightened you. Oh,
don&rsquo;t be cross&mdash;oh, please don&rsquo;t!&rdquo; She sniffed
again.</p></div>

<div class="bookPage" id="p93" name="93">
<p>&ldquo;We ain&rsquo;t so much <em>cross</em>,&rdquo; said the
fireman, &ldquo;as inter<em>est</em>ed like. It ain&rsquo;t every day a
little gell tumbles into our coal bunker outer the sky, is it, Bill? What
did you <em>do</em> it for&mdash;eh?&rdquo;</p>
<p>&ldquo;That&rsquo;s the pint,&rdquo; agreed the engine-driver;
&ldquo;what did you do it <em>for</em>?&rdquo;</p>
<p>Bobbie found that she had not quite stopped crying. The engine-driver
patted her on the back and said: &ldquo;Here, cheer up, Mate. It ain&rsquo;t
so bad as all that &rsquo;ere, I&rsquo;ll be bound.&rdquo;</p>
<p>&ldquo;I wanted,&rdquo; said Bobbie, much cheered to find herself
addressed as &ldquo;Mate,&rdquo;&mdash;&ldquo;I only wanted to ask you if
you&rsquo;d be so kind as to mend this.&rdquo; She picked up the brown paper
parcel from among the coals and undid the string with hot, red fingers that trembled.</p>
<p>Her feet and legs felt the scorch of the engine fire, but her shoulders
felt the wild chill rush of the air. The engine lurched and shook and
rattled, and as they shot under a bridge the engine seemed to shout in her
ears.</p>
<p>The fireman shovelled on coals.</p>
<p>Bobbie unrolled the brown paper and disclosed the toy engine.</p>
<p>&ldquo;I thought,&rdquo; she said wistfully, &ldquo;that perhaps
you&rsquo;d mend this for me&mdash;because you&rsquo;re an engineer, you
know.&rdquo;</p></div>

<div class="bookPage" id="p94" name="94">
<p>The engine-driver said he was blowed if he wasn&rsquo;t blest.</p>
<p>&ldquo;I&rsquo;m blest if I ain&rsquo;t blowed,&rdquo; remarked the
fireman.</p>
<p>But the engine-driver took the little engine and looked at it&mdash;and
the fireman ceased for an instant to shovel coal, and looked too.</p>
<p>&ldquo;It&rsquo;s like your precious cheek,&rdquo; said the
engine-driver,&mdash;&ldquo;whatever made you think we&rsquo;d be bothered
tinkering penny toys?&rdquo;</p>
<p>&ldquo;I didn&rsquo;t mean it for precious cheek,&rdquo; said Bobbie;
&ldquo;only everybody that has anything to do with railways is so kind and
good, I didn&rsquo;t think you&rsquo;d mind. You don&rsquo;t really&mdash;do you?&rdquo; she added, for
she had seen a not unkindly wink pass between the two.</p>
<p>&ldquo;My trade&rsquo;s driving of a engine, not mending
her&mdash;especially such a hout-size in engines as this here,&rdquo; said
Bill. &ldquo;An&rsquo; &rsquo;ow are we a-goin&rsquo; to get you back to your
sorrowing friends and relations, and all be forgiven and
forgotten?&rdquo;</p><p>&ldquo;If you&rsquo;ll put me down next time you stop,&rdquo; said
Bobbie, firmly, though her heart beat fiercely against her arm as she
clasped her hands, &ldquo;and lend me the money for a third-class ticket,
I&rsquo;ll pay you back&mdash;honour bright. I&rsquo;m not a confidence trick
like in the newspapers&mdash;really I&rsquo;m not.&rdquo;</p></div>

<div class="bookPage" id="p95" name="95">
<p>&ldquo;You&rsquo;re a little lady, every inch,&rdquo; said Bill,
relenting suddenly and completely. &ldquo;We&rsquo;ll see you gets home
safe. An&rsquo; about this engine Jim&mdash;ain&rsquo;t you got ne&rsquo;er a
pal as can use a soldering iron? Seems to me that&rsquo;s about all the
little bounder wants doing to it.&rdquo;</p>
<p>&ldquo;That&rsquo;s what Father said,&rdquo; Bobbie explained eagerly.
&ldquo;What&rsquo;s that for?&rdquo;</p>
<p>She pointed to a little brass wheel that he had turned as he spoke.</p>
<p>&ldquo;That&rsquo;s the injector.&rdquo;</p>
<p>&ldquo;In&mdash;what?&rdquo;</p><p>&ldquo;Injector to fill up the boiler.&rdquo;</p>
<p>&ldquo;Oh,&rdquo; said Bobbie, mentally registering the fact to tell the
others; &ldquo;that <em>is</em> interesting.&rdquo;</p>
<p>&ldquo;This &rsquo;ere&rsquo;s the automatic brake,&rdquo; Bill went on,
flattered by her enthusiasm. &ldquo;You just move this &rsquo;ere little
handle&mdash;do it with one finger, you can&mdash;and the train jolly soon
stops. That&rsquo;s what they call the Power of Science in the
newspapers.&rdquo;</p>
<p>He shewed her two little dials, like clock faces, and told her how one
shewed how much steam was going, and the other shewed if the brake was
working properly.</p>
</div>

<div class="bookPage" id="p96" name="96">
<p>By the time she had seen him shut off steam with a big shining steel
handle, Bobbie knew more about the inside working of an engine than she had
ever thought there was to know, and Jim had promised that his second
cousin&rsquo;s wife&rsquo;s brother should solder the toy engine, or Jim
would know the reason why. Besides all the knowledge she had gained Bobbie
felt that she and Bill and Jim were now friends for life, and that they had
wholly and forever forgiven her for stumbling uninvited among the sacred
coals of their tender.</p>
<p>At Stacklepoole Junction she parted from them with warm expressions of
mutual regard. They handed her over to the guard of a returning train&mdash;a friend of theirs&mdash;and she had the joy of knowing what
guards do in their secret fastnesses, and understood how, when you pull the
handle in railway carriages, properly or improperly, a wheel goes round
under the guard&rsquo;s nose and a loud bell rings in his ears. She asked
the guard why his van smelt so fishy, and learned that he had to carry a
lot of fish every day, and that the wetness in the hollows of the
corrugated floor had all drained out of boxes full of plaice and cod and
mackerel and soles and smelts.</p>
<p>Bobbie got home in time for tea, and she felt as though her mind would
burst with all that had been put into it since she parted from the others.
How she blessed the nail that had torn her frock!</p>

</div>

<div class="bookPage" id="p97" name="97">
<p>&ldquo;Where have you been?&rdquo; asked the others.</p>
<p>&ldquo;To the station, of course,&rdquo; said Roberta. But she would not
tell a word of her adventures till the day appointed, when she mysteriously
led them to the station at the hour of the 3.19&rsquo;s transit, and proudly
introduced them to her friends, Bill and Jim. Jim&rsquo;s second
cousin&rsquo;s wife&rsquo;s brother had not been unworthy of the sacred trust
reposed in him. The toy engine was, literally, as good as new.</p>
<p>&ldquo;Good-by&mdash;oh, good-by,&rdquo; said Bobbie, just before the
engine screamed <em>its</em> good-by. &ldquo;I shall always always love you&mdash;and Jim&rsquo;s second cousin&rsquo;s
wife&rsquo;s brother as well!&rdquo;</p><p>And as the three children went home up the hill, Peter hugging the
engine, now quite its own man again, Bobbie told, with joyous leaps of the
heart, the story of how she had been an Engine-burglar.</p>


