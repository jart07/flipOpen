<h2>CHAPTER VIII</h2>
<h2>THE AMATEUR FIREMEN</h2>
<p>&ldquo;<strong>THAT&rsquo;S</strong> a likely little brooch
you&rsquo;ve got on, Miss,&rdquo; said Perks the Porter; &ldquo;I don&rsquo;t
know as ever I see a thing more like a buttercup without it
<em>was</em> a buttercup.&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Bobbie, glad and flushed by this approval.
&ldquo;I always thought it was more like a buttercup almost than even a
real one,&mdash;and I <em>never</em> thought it would come to be mine,
my very own&mdash;and then Mother gave it to me for my birthday.&rdquo;</p>
<p>&ldquo;Oh&mdash;have you had a birthday?&rdquo; said Perks; and he
seemed quite surprised, as though a birthday were a thing only granted to a
favoured few.</p>
<p>&ldquo;Yes,&rdquo; said Bobbie; &ldquo;when&rsquo;s your birthday, Mr.
Perks?&rdquo; The children were taking tea with Mr. Perks in the
Porter&rsquo;s room among the lamps and the railway almanacs. They had
brought their own cups and some</p></div>

<div class="bookPage" id="p162" name="162">
<p>jam turnovers. Mr. Perks made tea in a beer
can, as usual, and every one felt very happy and confidential.</p>
<p>&ldquo;My birthday?&rdquo; said Perks, tipping some more dark brown tea
out of the can into Peter&rsquo;s cup. &ldquo;I give up keeping of my
birthday afore you was born.&rdquo;</p>
<p>&ldquo;But you must have been born <em>sometime</em>, you
know,&rdquo; said Phyllis, thoughtfully, &ldquo;even if it was twenty years
ago&mdash;or thirty or sixty or seventy.&rdquo;</p>
<p>&ldquo;Not so long as that, Missie,&rdquo; Perks grinned as he answered.
&ldquo;If you really want to know, it was thirty-two years ago, come the
fifteenth of this month.&rdquo;</p>
<p>&ldquo;Then why don&rsquo;t you keep it?&rdquo; asked Phyllis.</p>
<p>&ldquo;I&rsquo;ve got something else to keep besides birthdays,&rdquo;
said Perks, briefly.</p>
<p>&ldquo;Oh! What?&rdquo; asked Phyllis, eagerly, &ldquo;not
secrets?&rdquo;</p>
<p>&ldquo;No,&rdquo; said Perks, &ldquo;the kids and the Missus.&rdquo;</p>
<p>It was this talk that set the children thinking, and, presently,
talking. Perks was, on the whole, the dearest friend they had made. Not so
grand as the Station Master, but more approachable&mdash;less powerful than
the old gentleman, but more confidential.</p>
<p>&ldquo;It seems horrid that nobody keeps his birthday,&rdquo; said
Bobbie. &ldquo;Couldn&rsquo;t <em>we</em> do something?&rdquo;</p></div>

<div class="bookPage" id="p163" name="163">
<p>&ldquo;Let&rsquo;s go up to the Canal bridge and talk it
over,&rdquo; said Peter. &ldquo;I got a new gut line from the postman this
morning. He gave it me for a bunch of roses for his sweetheart. She&rsquo;s
ill.&rdquo;</p>
<p>&ldquo;Then I do think you might have given her the roses for
nothing,&rdquo; said Bobbie, indignantly.</p>
<p>&ldquo;Nyang, nyang!&rdquo; said Peter, disagreeably, and put his hands
in his pockets.</p>
<p>&ldquo;He did, of course,&rdquo; said Phyllis, in haste; &ldquo;directly
we heard she was ill we got the roses ready and waited by the gate. It was
when you were making the brekker-toast. And when he&rsquo;d said
&lsquo;Thank-you&rsquo; for the roses so many times,&mdash;much more than
he need have,&mdash;he pulled out the line and gave it to Peter. It
wasn&rsquo;t exchange. It was the grateful heart.&rdquo;</p>
<p>&ldquo;Oh, I <em>beg</em> your pardon, Peter,&rdquo; said Bobbie,
&ldquo;I <em>am</em> so sorry.&rdquo;</p>
<p>&ldquo;Don&rsquo;t mention it,&rdquo; said Peter, grandly, &ldquo;I knew
you would be.&rdquo;</p>
<p>So then they all went up to the Canal bridge. The idea was to fish from
the bridge, but the line was not quite long enough.</p>
<p>&ldquo;Never mind,&rdquo; said Bobbie. &ldquo;Let&rsquo;s just stay here
and look at things. Everything&rsquo;s so beautiful.&rdquo;</p></div>

<div class="bookPage" id="p164" name="164">
<p>It was. The sun was setting in red splendour over the gray and purple
hills, and the canal lay smooth and shiny in the shadow&mdash;no ripple broke its surface. It was
like a gray satin ribbon between the dusky green silk of the meadows that
were on each side of its banks.</p>
<p>&ldquo;It&rsquo;s all right,&rdquo; said Peter, &ldquo;but somehow I can
always see how pretty things are much better when I&rsquo;ve something to
do. Let&rsquo;s get down on to the tow-path and fish from there.&rdquo;</p>
<p>Phyllis and Bobbie remembered how the boys on the canal-boats had thrown
coal at them, and they said so.</p>
<p>&ldquo;Oh, nonsense,&rdquo; said Peter. &ldquo;There aren&rsquo;t any
boys here now. If there were, I&rsquo;d fight them.&rdquo;</p>
<p>Peter&rsquo;s sisters were kind enough not to remind him how he had
<em>not</em> fought the boys when last coal had been thrown. Instead
they said, &ldquo;All right, then,&rdquo; and cautiously climbed down the
steep bank to the towing-path. The line was carefully baited, and for half
an hour they fished patiently and in vain. Not a single nibble came to
nourish hope in their hearts.</p>
<p>All eyes were intent on the sluggish waters that earnestly pretended
they had never harboured a single minnow when a loud rough shout made them
start.</p></div>

<div class="bookPage" id="p165" name="165">
<p>&ldquo;Hi!&rdquo; said the shout, in most disagreeable tones, &ldquo;get
out of that, can&rsquo;t you?&rdquo;</p>
<p>An old white horse coming along the towing-path was within half a dozen
yards of them. They sprang to their feet and hastily climbed up the
bank.</p>
<p>&ldquo;We&rsquo;ll slip down again when they&rsquo;ve gone by,&rdquo; said
Bobbie.</p>
<p>But, alas, the barge, after the manner of barges, stopped under the
bridge.</p>
<p>&ldquo;She&rsquo;s going to anchor,&rdquo; said Peter; &ldquo;just our
luck!&rdquo;</p>
<p>The barge did not anchor, because an anchor is not part of a
canal-boat&rsquo;s furniture, but she was moored with ropes fore and
aft&mdash;and the ropes were made fast to the palings and to crowbars
driven into the ground.</p>
<p>&ldquo;What you staring at?&rdquo; growled the Bargee, crossly.</p>
<p>&ldquo;We weren&rsquo;t staring,&rdquo; said Bobbie; &ldquo;we
wouldn&rsquo;t be so rude.&rdquo;</p>
<p>&ldquo;Rude be blessed,&rdquo; said the man; &ldquo;get along with
you!&rdquo;</p>
<p>&ldquo;Get along yourself,&rdquo; said Peter. He remembered what he had
said about fighting boys, and, besides, he felt safe halfway up the bank.
&ldquo;We&rsquo;ve as much right here as any one else.&rdquo;</p></div>

<div class="bookPage" id="p166" name="166">
<p>&ldquo;Oh <em>&rsquo;ave</em> you, indeed?&rdquo; said the man.
&ldquo;We&rsquo;ll soon see about that.&rdquo; And he came across his deck
and began to climb down the side of his barge.</p>
<p>&ldquo;Oh, come away, Peter, come away!&rdquo; said Bobbie and Phyllis,
in agonised unison.</p>
<p>&ldquo;Not me,&rdquo; said Peter, &ldquo;but <em>you&rsquo;d</em>
better.&rdquo;</p>
<p>The girls climbed to the top of the bank and stood ready to bolt for
home as soon as they saw their brother out of danger. The way home lay all
down hill. They knew that they all ran well. The Bargee did not look as if
<em>he</em> did. He was red-faced, heavy, and beefy.</p>
<p>But as soon as his foot was on the towing-path the children saw that
they had misjudged him.</p>
<p>He made one spring up the bank, and caught Peter by the leg, dragged him
down&mdash;set him on his feet with a shake&mdash;took him by the
ear&mdash;and said sternly:&mdash;</p>
<p>&ldquo;Now, then, what do you mean by it? Don&rsquo;t you know these
&rsquo;ere waters is preserved? You ain&rsquo;t no right catching fish
&rsquo;ere&mdash;not to say nothing of your precious cheek.&rdquo;</p>
<p>Peter was always proud afterwards when he remembered that, with the
Bargee&rsquo;s furious fingers tightening on his ear, the Bargee&rsquo;s
crimson countenance close to his own, the Bargee&rsquo;s hot breath on his neck, he had
the courage to speak the truth.</p></div>

<div class="bookPage" id="pf166" name="">
<img src="illustrations/railway/nesbit-railway166.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></div>

<div class="bookPage" id="p167" name="167">
<p>&ldquo;I <em>wasn&rsquo;t</em> catching fish,&rdquo; said Peter.</p>
<p>&ldquo;That&rsquo;s not <em>your</em> fault, I&rsquo;ll be
bound,&rdquo; said the man, giving Peter&rsquo;s ear a twist&mdash;not a
hard one&mdash;but still a twist.</p>
<p>Peter could not say that it was. Bobbie and Phyllis had been holding on
to the railings above and skipping with anxiety. Now suddenly Bobbie
slipped through the railings and rushed down the bank towards Peter, so
impetuously that Phyllis, following more temperately, felt certain that her
sister&rsquo;s descent would end in the waters of the canal. And so it would
have done if the Bargee hadn&rsquo;t let go of Peter&rsquo;s ear&mdash;and
caught her in his jerseyed arm.</p>
<p>&ldquo;Who are you a-shoving of?&rdquo; he said, setting her on her
feet.</p>
<p>&ldquo;Oh,&rdquo; said Bobbie, breathless, &ldquo;I&rsquo;m not shoving
anybody. At least, not on purpose. Do, please, don&rsquo;t be cross with
Peter. Of course, if it&rsquo;s your canal, we&rsquo;re sorry and we
won&rsquo;t any more. But we didn&rsquo;t know it was yours.&rdquo;</p>
<p>&ldquo;Go along with you,&rdquo; said the Bargee.</p>
<p>&ldquo;Yes, we will; indeed we will,&rdquo; said Bobbie, earnestly;
&ldquo;but we do beg your pardon&mdash;and really we haven&rsquo;t caught a single fish. I&rsquo;d tell you directly if
we had, honour bright I would.&rdquo;</p></div>

<div class="bookPage" id="p168" name="168">
<p>She held out her hands and Phyllis turned out her little empty pocket to
shew that really they hadn&rsquo;t any fish concealed about them.</p>
<p>&ldquo;Well,&rdquo; said the Bargee, more gently, &ldquo;cut along,
then, and don&rsquo;t you do it again, that&rsquo;s all.&rdquo;</p>
<p>The children hurried up the bank.</p>
<p>&ldquo;Chuck us a coat, M&rsquo;ria,&rdquo; shouted the man. And a
red-haired woman in a green plaid shawl came out from the cabin door with a
baby in her arms and threw a coat to him. He put it on, climbed the bank,
and slouched along across the bridge towards the village.</p>
<p>&ldquo;You&rsquo;ll find me up at the Rose and Crown when you&rsquo;ve got
the kid to sleep,&rdquo; he called to her from the bridge.</p>
<p>When he was out of sight the children slowly returned. Peter insisted on
this.</p>
<p>&ldquo;The canal may belong to him,&rdquo; he said, &ldquo;though I
don&rsquo;t believe it does. But the bridge is everybody&rsquo;s. Doctor
Forrest told me it&rsquo;s public property. I&rsquo;m not going to be bounced
off the bridge by him or any one else, so I tell you.&rdquo;</p>
<p>Peter&rsquo;s ear was still sore and so were his feelings.</p>
<p>The girls followed him as gallant soldiers might follow the leader of a
forlorn hope.</p></div>

<div class="bookPage" id="p169" name="169">
<p>&ldquo;I do wish you wouldn&rsquo;t,&rdquo; was all they said.</p>
<p>&ldquo;Go home if you&rsquo;re afraid,&rdquo; said Peter; &ldquo;leave me
alone. <em>I&rsquo;m</em> not afraid.&rdquo;</p>
<p>The sound of the man&rsquo;s footsteps died away along the quiet road.
The peace of the evening was not broken by the notes of the sedge-warblers
or by the voice of the woman in the barge, singing her baby to sleep. It
was a sad song she sang. Something about Bill Bailey and how she wanted him
to come home.</p>
<p>The children stood leaning their arms on the parapet of the bridge; they
were glad to be quiet for a few minutes because all three hearts were
beating much more quickly than is at all comfortable.</p>
<p>&ldquo;I&rsquo;m not going to be driven away by any old bargeman,
I&rsquo;m not,&rdquo; said Peter, thickly.</p>
<p>&ldquo;Of course not,&rdquo; Phyllis said soothingly; &ldquo;you
didn&rsquo;t give in to him! So now we might go home, don&rsquo;t you
think?&rdquo;</p>
<p>&ldquo;<em>No</em>,&rdquo; said Peter.</p>
<p>Nothing more was said till the woman got off the barge, climbed the
bank, and came across the bridge.</p>
<p>She hesitated, looking at the three backs of the children, then she
said, &ldquo;Ahem.&rdquo;</p></div>

<div class="bookPage" id="p170" name="170">
<p>Peter stayed as he was, but the girls looked round.</p>
<p>&ldquo;You mustn&rsquo;t take no notice of my Bill,&rdquo; said the
woman; &ldquo;&rsquo;is bark&rsquo;s worse&rsquo;n &rsquo;is bite. Some of the
kids down Farley way is fair terrors. It was them put &rsquo;is back up
calling out about who ate the puppy-pie under Marlowe bridge.&rdquo;</p>
<p>&ldquo;Who <em>did</em>?&rdquo; asked Phyllis.</p>
<p>&ldquo;I dunno,&rdquo; said the woman. &ldquo;Nobody don&rsquo;t know!
But somehow, and I don&rsquo;t know the why nor the wherefore of it, them
words is pison to a barge-master. Don&rsquo;t you take no notice. &rsquo;E
won&rsquo;t be back for two hours good. You might catch a power o&rsquo; fish
afore that. The light&rsquo;s good an&rsquo; all,&rdquo; she added.</p>
<p>&ldquo;Thank you,&rdquo; said Bobbie. &ldquo;You&rsquo;re very kind.
Where&rsquo;s your baby?&rdquo;</p>
<p>&ldquo;Asleep in the cabin,&rdquo; said the woman. &ldquo;&rsquo;E&rsquo;s
all right. Never wakes afore twelve. Reglar as a church-clock, &rsquo;e
is.&rdquo;</p>
<p>&ldquo;I&rsquo;m sorry,&rdquo; said Bobbie; &ldquo;I would have liked to
see him, close to.&rdquo;</p>
<p>&ldquo;And a finer you never did see, Miss, though I says it,&rdquo; the
woman&rsquo;s face brightened as she spoke.</p>
<p>&ldquo;Aren&rsquo;t you afraid to leave it?&rdquo; said Peter.</p>
<p>&ldquo;Lor love you, no,&rdquo; said the woman; &ldquo;who&rsquo;d hurt a
little thing like &rsquo;im? Besides, Spot&rsquo;s there. So long!&rdquo;</p></div>

<div class="bookPage" id="p171" name="171">
<p>The woman went away.</p>
<p>&ldquo;Shall we go home?&rdquo; said Phyllis.</p>
<p>&ldquo;You can. I&rsquo;m going to fish,&rdquo; said Peter, briefly.</p>
<p>&ldquo;I thought we came up here to talk about Perk&rsquo;s
birthday,&rdquo; said Phyllis.</p>
<p>&ldquo;Perk&rsquo;s birthday&rsquo;ll keep.&rdquo;</p>
<p>So they got down on the towing-path again, and Peter fished. He did not
catch anything. It was almost quite dark, the girls were getting tired,
and, as Bobbie said, it was past bed-time, when suddenly Phyllis cried,
&ldquo;What&rsquo;s that?&rdquo;</p>
<p>And she pointed to the canal-boat. Smoke was coming from the chimney of
the cabin, had indeed been curling softly into the soft evening air all the
time&mdash;but now other wreaths of smoke were rising, and these were from
the cabin door.</p>
<p>&ldquo;It&rsquo;s on fire&mdash;that&rsquo;s all,&rdquo; said Peter,
calmly. &ldquo;Serve him right.&rdquo;</p>
<p>&ldquo;Oh&mdash;how <em>can</em> you?&rdquo; cried Phyllis.
&ldquo;Think of the poor dear dog.&rdquo;</p>
<p>&ldquo;The <em>Baby</em>!&rdquo; screamed Bobbie.</p></div>

<div class="bookPage" id="p172" name="172">
<p>In an instant all three made for the barge.</p>
<p>Her mooring ropes were slack, and the little breeze, hardly strong
enough to be felt, had yet been strong enough to drift her stern against
the bank. Bobbie was first&mdash;then came Peter, and it was Peter who
slipped and fell. He went into the canal up to his neck, and his feet could
not feel the bottom, but his arm was on the edge of the barge. Phyllis
caught at his hair. It hurt, but it helped him to get out. Next minute he
had leaped on to the barge, Phyllis following.</p>
<p>&ldquo;Not you!&rdquo; he shouted to Bobbie; &ldquo;Me, because I&rsquo;m
wet.&rdquo;</p>
<p>He caught up with Bobbie at the cabin door, and flung her aside very
roughly indeed; if they had been playing, such roughness would have made
Bobbie weep tears of rage and pain. Now, though he flung her on to the edge
of the hold, so that her knee and her elbow were grazed and bruised, she
only cried:&mdash;</p>
<p>&ldquo;No&mdash;not you&mdash;<em>Me</em>,&rdquo; and struggled up
again. But not quickly enough.</p>
<p>Peter had already gone down two of the cabin steps into the cloud of
thick smoke. He stopped, remembered all he had ever heard of fires, pulled
his soaked handkerchief out of his breast pocket and tied it over his mouth. As he pulled it out he said:&mdash;</p></div>

<div class="bookPage" id="p173" name="173">
<p>&ldquo;It&rsquo;s all right, hardly any fire at all.&rdquo;</p>
<p>And this, though he thought it was a lie, was rather good of Peter. It
was meant to keep Bobbie from rushing after him into danger. Of course it
didn&rsquo;t.</p>
<p>The cabin glowed red. A paraffin lamp was burning calmly in an orange
mist.</p>
<p>&ldquo;Hi,&rdquo; said Peter, lifting the handkerchief from his mouth
for a moment. &ldquo;Hi, Baby&mdash;where are you?&rdquo; He choked.</p>
<p>&ldquo;Oh, let <em>me</em> go,&rdquo; cried Bobbie, close behind
him. Peter pushed her back more roughly than before, and went on.</p>
<p>Now what would have happened if the baby hadn&rsquo;t cried I don&rsquo;t
know&mdash;but just at that moment it <em>did</em> cry. Peter felt his
way through the dark smoke, found something small and soft and warm and
alive, picked it up and backed out, nearly tumbling over Bobbie who was
close behind. A dog snapped at his leg&mdash;tried to bark, choked.</p>
<p>&ldquo;I&rsquo;ve got the kid,&rdquo; said Peter, tearing off the
handkerchief and staggering on to the deck.</p>
<p>Bobbie caught at the place where the bark came from, and her hands met
on the fat back of a smooth-haired dog. It turned and fastened its teeth on her hand, but very gently, as much as to say:&mdash;</p></div>

<div class="bookPage" id="p174" name="174">
<p>&ldquo;I&rsquo;m bound to bark and bite if strangers come into my
master&rsquo;s cabin, but I know you mean well, so I won&rsquo;t
<em>really</em> bite.&rdquo;</p>
<p>Bobbie dropped the dog.</p>
<p>&ldquo;All right, old man. Good dog,&rdquo; said she.
&ldquo;Here&mdash;give me the baby, Peter; you&rsquo;re so wet you&rsquo;ll
give it cold.&rdquo;</p>
<p>Peter was only too glad to hand over the strange little bundle that
squirmed and whimpered in his arms.</p>
<p>&ldquo;Now,&rdquo; said Bobbie, quickly, &ldquo;you run straight to the
Rose and Crown and tell them. Phil and I will stay here with the precious.
Hush, then, a dear, a duck, a darling! Go <em>now</em>, Peter!
Run!&rdquo;</p>
<p>&ldquo;I can&rsquo;t run in these things,&rdquo; said Peter, firmly,
&ldquo;they&rsquo;re as heavy as lead. I&rsquo;ll walk.&rdquo;</p>
<p>&ldquo;Then <em>I&rsquo;ll</em> run,&rdquo; said Bobbie. &ldquo;Get
on the bank, Phil, and I&rsquo;ll hand you the dear.&rdquo;</p>
<p>The baby was carefully handed. Phyllis sat down on the bank and tried to
hush the baby. Peter wrung the water from his sleeves and knickerbocker
legs as well as he could, and it was Bobbie who ran like the wind across
the bridge and up the long white quiet twilight road towards the Rose and Crown.</p></div>

<div class="bookPage" id="p175" name="175">
<p>There is a nice old-fashioned room at the Rose and Crown where Bargees
and their wives sit of an evening drinking their supper beer, and toasting
their supper cheese at a glowing basketful of coals that sticks out into
the room under a great hooded chimney and is warmer and prettier and more
comforting than any other fireplace <em>I</em> ever saw.</p>
<p>There was a pleasant party of barge people round the fire. You might not
have thought it pleasant, but they did; for they were all friends or
acquaintances, and they liked the same sort of things, and talked the same
sort of talk. This is the real secret of pleasant society. The Bargee Bill,
whom the children had found so disagreeable, was considered excellent
company by his mates. He was telling a tale of his own wrongs&mdash;always
a thrilling subject. It was his barge he was speaking about.</p>
<p>&ldquo;And &rsquo;e sent down word &lsquo;paint her inside hout,&rsquo;
not namin&rsquo; no colour, d&rsquo;ye see? So I gets a lotter green paint
and I paints her stem to stern, and I tell yer she looked A1. Then &rsquo;e
comes along and &rsquo;e says, &lsquo;Wot yer paint &rsquo;er all one colour
for?&rsquo; &rsquo;e says. And I says, says I, &lsquo;Cause I thought
she&rsquo;d look fust-rate,&rsquo; says I, &lsquo;and I think so
still.&rsquo; An&rsquo; he says, &lsquo;<em>Dew</em> yer? Then ye can just pay for the bloomin&rsquo; paint yerself,&rsquo; says he. An&rsquo; I &rsquo;ad to,
too.&rdquo; A murmur of sympathy ran round the room. Breaking noisily in on
it came Bobbie. She burst open the swing door&mdash;crying
breathlessly:&mdash;</p></div>

<div class="bookPage" id="p176" name="176">
<p>&ldquo;Bill! I want Bill the Bargeman.&rdquo;</p>
<p>There was a stupefied silence. Pots of beer were held in mid-air,
paralysed on their way to thirsty mouths.</p>
<p>&ldquo;Oh,&rdquo; said Bobbie, seeing the bargewoman and making for her.
&ldquo;Your barge cabin&rsquo;s on fire. Go quickly.&rdquo;</p>
<p>The woman started to her feet, and put a big red hand to her waist, on
the left side, where your heart seems to be when you are frightened or
miserable.</p>
<p>&ldquo;Reginald Horace!&rdquo; she cried in a terrible voice; &ldquo;my
Reginald Horace!&rdquo;</p>
<p>&ldquo;All right,&rdquo; said Bobbie, &ldquo;if you mean the baby; got
him out safe. Dog, too.&rdquo; She had no breath for more except, &ldquo;Go
on&mdash;it&rsquo;s all right.&rdquo;</p>
<p>Then she sank on the ale-house bench and tried to get that breath of
relief after running which people call the &ldquo;second wind.&rdquo; But
she felt as though she would never breathe again.</p>
<p>Bill the Bargee rose slowly and heavily. But his wife was a hundred yards up the road before he had quite understood what
was the matter. Phyllis, shivering by the canal side, had hardly heard the
quick approaching feet before the woman had flung herself on the railing,
rolled down the bank, and snatched the baby from her.</p></div>

<div class="bookPage" id="p177" name="177">
<p>&ldquo;Don&rsquo;t,&rdquo; said Phyllis, reproachfully; &ldquo;I&rsquo;d
just got him to sleep.&rdquo;</p>
<p>Bill came up later talking in a language with which the children were
wholly unfamiliar. He leaped on to the barge and dipped up pails of water.
Peter helped him and they put out the fire. Phyllis, the bargewoman, and
the baby and presently Bobby, too,&mdash;cuddled together in a heap on the
bank.</p>
<p>&ldquo;Lord help me, if it was me left anything as could catch
alight,&rdquo; said the woman again and again.</p>
<p>But it wasn&rsquo;t she. It was Bill the Bargeman, who had knocked his
pipe out and the red ash had fallen on the hearth-rug, and smouldered there
and at last broken into flame. Though a stern man he was just. He did not
blame his wife for what was his own fault, as many bargemen, and other men,
too, would have done.</p>
<p>Mother was half wild with anxiety when at last the three children turned
up at Three Chimneys, all very wet by now, for Peter seemed to have come
off on the others. But when she had disentangled the truth of what had
happened from their mixed and incoherent narrative, she owned that they had
done quite right, and could not possibly have done otherwise. Nor did she
put any obstacles in the way of their accepting the cordial invitation with
which the bargeman had parted from them.</p>
</div>

<div class="bookPage" id="p178" name="178">
<p>&ldquo;Ye be here at seven to-morrow,&rdquo; he had said, &ldquo;and
I&rsquo;ll take you the entire trip to Farley and back, so I will, and not a
penny to pay. Nineteen locks!&rdquo;</p>
<p>They did not know what locks were; but they were at the bridge at seven,
with bread and cheese and half a soda cake, and quite a quarter of a leg of
mutton in a basket.</p>
<p>It was a glorious day. The old white horse strained at the ropes, the
barge glided smoothly and steadily through the still water. The sky was
blue overhead. Mr. Bill was as nice as any one could possibly be. No one
would have thought that he could be the same man who had held Peter by the
