<h2>CHAPTER VII<br />
FOR VALOUR</h2>
<p><strong>I HOPE</strong> you don&rsquo;t mind my telling you a
good deal about Roberta. The fact is I am growing very fond of her. The
more I observe her the more I love her. And I notice all sorts of things
about her that I like.</p>
<p>For instance, she was quite oddly anxious to make other people happy.
And she could keep a secret, a tolerably rare accomplishment. Also she had
the power of silent sympathy. That sounds rather dull, I know, but
it&rsquo;s not so dull as it sounds. It just means that a person is able to
know that you are unhappy, and to love you extra on that account, without
bothering you by telling you all the time how sorry she is for you. That
was what Bobbie was like. She knew that Mother was unhappy&mdash;and that
Mother had not told her the reason. So she just loved Mother more and never
said a single word that could let Mother know how earnestly her little girl
wondered what Mother was unhappy about. This needs practice. It is not so easy as you might
think.</p></div>

<div class="bookPage" id="p139" name="139">
<p>Whatever happened,&mdash;and all sorts of nice pleasant ordinary things
happened,&mdash;such as picnics, games, and buns for tea, Bobbie always had
these thoughts at the back of her mind. &ldquo;Mother&rsquo;s unhappy. Why?
I don&rsquo;t know. She doesn&rsquo;t want me to know. I won&rsquo;t try to
find out. But she <em>is</em> unhappy. Why? I don&rsquo;t know. She
doesn&rsquo;t&mdash;&rdquo; and so on, repeating and repeating like a tune
that you don&rsquo;t know the stopping part of.</p>
<p>The Russian gentleman still took up a good deal of everybody&rsquo;s
thoughts. All the editors and secretaries of Societies and Members of
Parliament had answered Mother&rsquo;s letters as politely as they knew how;
but none of them could tell where the wife and children of Mr. Szezcpansky
would be likely to be. (Did I tell you that the Russian&rsquo;s very Russian
name was that?)</p>
<p>Bobbie had another quality which you will hear differently described by
different people. Some of them call it interfering in other people&rsquo;s
business&mdash;and some call it &ldquo;helping lame dogs over
stiles,&rdquo; and some call it &ldquo;loving-kindness.&rdquo; It just
means tracing to help people.</p>
<p>She racked her brains to think of some way of helping the Russian gentleman to find his wife and children. He had learned
a few words of English now. He could say &ldquo;Good</p></div>

<div class="bookPage" id="p140" name="140">
<p>morning,&rdquo; and
&ldquo;Good night,&rdquo; and &ldquo;Please,&rdquo; and &ldquo;Thank
you,&rdquo; and &ldquo;Pretty,&rdquo; when the children brought him
flowers, and &ldquo;Ver&rsquo; good,&rdquo; when they asked him how he had
slept.</p>
<p>The way he smiled when he &ldquo;said his English,&rdquo; was, Bobbie
felt, &ldquo;just too sweet for anything.&rdquo; She used to think of his
face because she fancied it would help her to some way of helping him. But
it did not. Yet his being there cheered her because she saw that it made
Mother happier.</p>
<p>&ldquo;She likes to have some one to be good to, even beside us,&rdquo;
said Bobbie. &ldquo;And I know she hated to let him have Father&rsquo;s
clothes. But I suppose it &lsquo;hurt nice&rsquo; or she wouldn&rsquo;t
have.&rdquo;</p>
<p>For many and many a night after the day when she and Peter and Phyllis
had saved the train from wreck by waving their little red flannel flags,
Bobbie used to wake screaming and shivering, seeing again that horrible
mound, and the poor dear trustful engine rushing on towards it&mdash;just
thinking that it was doing its swift duty, and that everything was clear
and safe. And then a warm thrill of pleasure used to run through her at the
remembrance of how she and Peter and Phyllis and the red flannel petticoats had really saved everybody.</p></div>

<div class="bookPage" id="p141" name="141">
<p>One morning a letter came. It was addressed to Peter and Bobbie and
Phyllis. They opened it with enthusiastic curiosity, for they did not often
get letters.</p>
<p>The letter said:&mdash;</p>
<p>&ldquo;DEAR SIR, AND LADIES,&mdash;It is proposed to make a small
presentation to you, in commemoration of your prompt and courageous action
in warning the train on the &mdash;inst., and thus averting what must,
humanly speaking, have been a terrible accident. The presentation will take
place at the &mdash; Station at three o&rsquo;clock on the 30th inst. if
this time and place will be convenient to you.</p>
<p>Yours faithfully,<br />
&ldquo;JABEZ INGLEWOOD.<br />
&ldquo;Secretary Great Northern and Southern Railway
Co.&rdquo;</p>
<p>There never had been a prouder moment in the lives of the three
children. They rushed to Mother with the letter and she also felt proud and
said so, and this made the children happier than ever.</p>
<p>&ldquo;But if the presentation is money, you must say, &lsquo;Thank you,
but you&rsquo;d rather not take it,&rsquo;&rdquo; said Mother. &ldquo;I&rsquo;ll wash your Indian muslins at once,&rdquo; she
added. &ldquo;You must look tidy on an occasion like this.&rdquo;</p>
<p>&ldquo;Phil and I can wash them,&rdquo; said Bobbie, &ldquo;if
you&rsquo;ll iron them, Mother.&rdquo;</p></div>

<div class="bookPage" id="p142" name="142">
<p>Washing is rather fun. I wonder whether you&rsquo;ve ever done it? This
particular washing took place in the back kitchen which had a stone floor
and a very big stone sink under its window.</p>
<p>&ldquo;Let&rsquo;s put the bath on the sink,&rdquo; said Phyllis;
&ldquo;then we can pretend we&rsquo;re out-of-doors washerwomen like Mother
saw in France.&rdquo;</p>
<p>&ldquo;But they were washing in the cold river,&rdquo; said Peter, his
hands in his pockets, &ldquo;not in hot water.&rdquo;</p>
<p>&ldquo;This is a <em>hot</em> river, then,&rdquo; said Phyllis;
&ldquo;lend a hand with the bath, there&rsquo;s a dear.&rdquo;</p>
<p>&ldquo;I should like to see a deer lending a hand,&rdquo; said Peter,
but he lent his.</p>
<p>&ldquo;Now to rub and scrub and scrub and rub,&rdquo; said Phyllis,
hopping joyously about as Bobbie carefully carried the heavy kettle from
the kitchen fire.</p>
<p>&ldquo;Oh, no!&rdquo; said Bobbie, greatly shocked; &ldquo;you
don&rsquo;t rub muslin. You put the boiled soap in the hot water and make it
all frothy-lathery&mdash;and then you shake the muslin and squeeze it, ever
so gently, and all the dirt comes out. It&rsquo;s only clumsy things like
tablecloths and sheets that have to be rubbed.&rdquo;</p>
<p>The lilac and the Gloire de Dijon roses outside the window swayed in the
soft breeze.</p></div>

<div class="bookPage" id="p143" name="143">
<p>&ldquo;It&rsquo;s a nice drying day&mdash;that&rsquo;s one thing,&rdquo;
said Bobbie, feeling very grown up. &rdquo;Oh, I do wonder what wonderful
feelings we shall have when we wear the Indian muslin dresses!&rdquo;</p>
<p>&ldquo;Yes, so do I,&rdquo; said Phyllis, shaking and squeezing the
muslin in quite a professional manner.</p>
<p>&ldquo;Now we squeeze out the soapy water. No&mdash;we mustn&rsquo;t
twist them&mdash;and then rinse them. I&rsquo;ll hold them while you and
Peter empty the bath and get clean water.&rdquo;</p>
<p>&ldquo;A presentation! That means presents,&rdquo; said Peter, as his
sisters, having duly washed the pegs and wiped the line, hung up the
dresses to dry. &ldquo;Whatever will it be?&rdquo;</p>
<p>&ldquo;It might be anything,&rdquo; said Phyllis; &ldquo;what I&rsquo;ve
always wanted is a Baby elephant&mdash;but I suppose they wouldn&rsquo;t
know that.&rdquo;</p>
<p>&ldquo;Suppose it was gold models of steam-engines?&rdquo; said
Bobbie.</p>
<p>&ldquo;Or a big model of the scene of the prevented accident,&rdquo;
suggested Peter, &ldquo;with a little model train, and dolls dressed like us and the engine-driver and fireman and
passengers.&rdquo;</p><p>&ldquo;Do you <em>like</em>,&rdquo; said Bobbie, doubtfully, drying
her hands on the rough towel that hung on a roller at the back of</p>
</div>

<div class="bookPage" id="p144" name="144">
<p>the scullery door, &ldquo;do you <em>like</em> us being rewarded for saving
a train?&rdquo;</p>
<p>&ldquo;Yes, I do,&rdquo; said Peter, downrightly; &ldquo;and don&rsquo;t
you try to come it over us that you don&rsquo;t like it, too. Because I know
you do.&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Bobbie, doubtfully, &ldquo;I know I do. But
oughtn&rsquo;t we to be satisfied with just having done it, and not ask for
anything more?&rdquo;</p>
<p>&ldquo;Who did ask for anything more, silly?&rdquo; said her brother.
&ldquo;Victoria Cross soldiers don&rsquo;t <em>ask</em> for it; but
they&rsquo;re glad enough to get it all the same. Perhaps it&rsquo;ll be
medals. Then when I&rsquo;m very old indeed, I shall show them to my
grandchildren and say, &lsquo;We only did our duty,&rsquo; and they&rsquo;ll
be awfully proud of me.&rdquo;</p>
<p>&ldquo;You have to be married,&rdquo; warned Phyllis, &ldquo;or you
don&rsquo;t have any grandchildren.&rdquo;</p>
<p>&ldquo;I suppose I shall <em>have</em> to be married some
day,&rdquo; said Peter, &ldquo;but it will be an awful bother having her
round all the time. I&rsquo;d like to marry a lady who had trances, and only
woke up once or twice a year.&rdquo;</p>
<p>&ldquo;Just to say you were the light of her life and then go to sleep
again. Yes. That wouldn&rsquo;t be bad,&rdquo; said Bobbie.</p></div>

<div class="bookPage" id="p145" name="145">
<p>&ldquo;When <em>I</em> get married,&rdquo; said Phyllis, &ldquo;I
shall want him to want me to be awake all the time, so that I can hear him
say how nice I am.&rdquo;</p>
<p>&ldquo;I think it would be nice,&rdquo; said Bobbie, &ldquo;to marry
some one very poor, and then you&rsquo;d do all the work and he&rsquo;d love
you most frightfully, and see the blue wood smoke curling up among the
trees from the domestic hearth as he came home from work every night. I say
we&rsquo;ve got to answer that letter and say that the time and place
<em>will</em> be convenient to us. There&rsquo;s the soap, Peter.
We&rsquo;re both as clean as clean. That pink box of writing paper you had
on your birthday, Phil.&rdquo;</p>
<p>It took some time to arrange what should be said. Mother had gone back
to her writing, and several sheets of pink paper with scalloped gilt edges
and green four-leaved shamrocks in the corner were spoiled before the three
had decided what to say. Then each made a copy and signed it with its own
name.</p>
<p>The threefold letter ran:</p>
<p>&ldquo;DEAR MR. JABEZ INGLEWOOD,&mdash;Thank you very much. We did not
want to be rewarded but only to save the train, but we are glad you think so and thank you very
much. The time and place you say will be quite convenient to us. Thank you
very much.</p></div>

<div class="bookPage" id="p146" name="146">
<p>Your affecate little friend,&rdquo;<br />
Then came the name, and after it:&mdash;<br />
&ldquo;P.S. Thank you very much.&rdquo;</p>
<p>&ldquo;Washing is much easier than ironing,&rdquo; said Bobbie, taking
the clean dry dresses off the line.</p>
<p>&ldquo;I do love to see things come clean. Oh&mdash;I don&rsquo;t know
how we shall wait till it&rsquo;s time to know what presentation
they&rsquo;re going to present!&rdquo;</p>
<p>When at last&mdash;it seemed a very long time. after&mdash;it was
<em>the</em> day, the three children went down to the station at the
proper time. And everything that happened was so odd that it seemed like a
dream. The Station Master came out to meet them&mdash;in his best clothes,
as Peter noticed at once&mdash;and led them into the waiting room where
once they had played the advertisement game. It looked quite different now.
A carpet had been put down&mdash;and there were pots of roses on the
mantelpiece and on the window ledges&mdash;green branches stuck up, like
holly and laurel are at Christmas, over the framed advertisement of Cook&rsquo;s Tours and the Beauties of Devon and the Paris Lyons
Railway. There were quite a number of people there besides the
Porter,&mdash;two or three ladies in smart dresses, and quite a crowd of
gentlemen in high hats and frock coats,&mdash;besides everybody who
belonged to the station. They recognized several people who had been in the
train on the red-flannel-petticoat day. Best of all their own old</p></div>

<div class="bookPage" id="p147" name="147">
<p>gentleman
was there, and his coat and hat and collar seemed more than ever different
from any one else&rsquo;s.</p>
<p>He shook hands with them and then everybody sat
down on chairs, and a gentleman in spectacles&mdash;they found out
afterwards that he was the District Superintendent&mdash;began quite a long
speech&mdash;very clever indeed. I am not going to write the speech down.
First, because you would think it dull; and secondly, because it made all
the children blush so, and get so hot about the ears that I am quite
anxious to get away from this part of the subject; and thirdly, because the
gentleman took so many words to say what he had to say that I really
haven&rsquo;t time to write them down. He said all sorts of nice things
about the children&rsquo;s bravery and presence of mind, and when he had
done he sat down, and every one who was there clapped and said,
&ldquo;Hear, hear.&rdquo;</p>
<p>And then the old gentleman got up and said things, too. It was very like
a prize-giving. And then he called the children one by one, by their names,
and gave each of them a beautiful gold watch and chain. And inside the
watches were engraved after the name of the watch&rsquo;s new
owner:&mdash;</p>
<p>&ldquo;From the Directors of the Northern and Southern Railway in
grateful recognition of the courageous and prompt action which averted an
accident on &mdash;1905.&rdquo;</p></div>

<div class="bookPage" id="p148" name="148">
<p>The watches were the most beautiful you can possibly imagine, and each
one had a blue leather case to live in when it was at home.</p>
<p>&ldquo;You must make a speech now and thank every one for their
kindness,&rdquo; whispered the Station Master in Peter&rsquo;s ear and
pushed him forward. &ldquo;Begin &lsquo;Ladies and Gentlemen,&rsquo;&rdquo;
he added.</p>
<p>Each of the children had already said, &ldquo;Thank you,&rdquo; quite
properly.</p>
<p>&ldquo;O dear,&rdquo; said Peter, but he did not resist the push.</p>
<p>&ldquo;Ladies and Gentlemen,&rdquo; he said in a rather husky voice.
Then there was a pause, and he heard his heart beating in his throat.
&ldquo;Ladies and gentlemen,&rdquo; he went on with a rush,
&ldquo;it&rsquo;s most awfully good of you, and we shall treasure the watches all our lives&mdash;but really we don&rsquo;t deserve it because
what we did wasn&rsquo;t anything, really. At least, I mean it was awfully
exciting, and what I mean to say&mdash;thank you all very, very
much.&rdquo;</p>
<p>The people clapped Peter more than they had done the District
Superintendent, and then everybody shook hands with them, and as soon as
politeness would let them, they got away, and tore up the hill to Three
Chimneys, with their watches in their hands.</p></div>

<div class="bookPage" id="p149" name="149">
<p>It was a wonderful day&mdash;the kind of day that very seldom happens to
anybody and to most of us not at all.</p>
<p>&ldquo;I did want to talk to the old gentleman about something
else,&rdquo; said Bobbie, &ldquo;but it was so public&mdash;like being in
church.&rdquo;</p>
<p>&ldquo;What did you want to say?&rdquo; asked Phyllis.</p>
<p>&ldquo;I&rsquo;ll tell you when I&rsquo;ve thought about it more,&rdquo;
said Bobbie.</p>
<p>So when she had thought a little more she wrote a letter.</p>
<p>&ldquo;My dearest old gentleman,&rdquo; it said; &ldquo;I want most
awfully to ask you something. If you could get out of the train and go by
the next, it would do. I do not want you to give me anything. Mother says we ought not to. And besides, we do not want any
<em>things</em>. Only to talk to you about a Prisoner and Captive. Your
loving little friend,</p>
<p>&ldquo;BOBBIE.&rdquo;</p>
<p>She got the Station Master to give the letter to the old gentleman, and
next day she asked Peter and Phyllis to come down to the station with her
at the time when the train that brought the old gentleman from town would
be passing through.</p>
<p>She explained her idea to them&mdash;and they approved thoroughly.</p></div>

<div class="bookPage" id="p150" name="150">
<p>They had all washed their hands and faces, and brushed their hair, and
were looking as tidy as they knew how. But Phyllis, always unlucky, had
upset a jug of lemonade down the front of her dress. There was no time to
change&mdash;and the wind happening to blow from the coal yard, her frock
was soon powdered with gray, which stuck to the sticky lemonade stains and
made her look, as Peter said, &ldquo;like any little gutter
child.&rdquo;</p>
<p>It was decided that she should keep behind the others as much as
possible.</p>
<p>&ldquo;Perhaps the old gentleman won&rsquo;t notice,&rdquo; said Bobbie.
&ldquo;The aged are often weak in the eye.&rdquo;</p>
<p>There was no sign of weakness, however, in the eyes, or in any other
part of the old gentleman, as he stepped from the train and looked up and down the platform.</p>
<p>The three children, now that it came to the point, suddenly felt that
rush of deep shyness which makes your ears red and hot, your hands warm and
wet, and the tip of your nose pink and shiny.</p>
<p>&ldquo;Oh,&rdquo; said Phyllis, &ldquo;my heart&rsquo;s thumping like a
steam-engine&mdash;right under my sash, too.&rdquo;</p>
<p>&ldquo;Nonsense,&rdquo; said Peter, &ldquo;people&rsquo;s hearts
aren&rsquo;t under their sashes.&rdquo;</p></div>

<div class="bookPage" id="p151" name="151">
<p>&ldquo;I don&rsquo;t care&mdash;mine is,&rdquo; said Phyllis.</p>
<p>&ldquo;If you&rsquo;re going to talk like a poetry-book,&rdquo; said
Peter, &ldquo;my heart&rsquo;s in my mouth.&rdquo;</p>
<p>&ldquo;My heart&rsquo;s in my boots&mdash;if you come to that,&rdquo;
said Roberta; &ldquo;but do come on&mdash;he&rsquo;ll think we&rsquo;re
idiots.&rdquo;</p>
<p>&ldquo;He won&rsquo;t be far wrong,&rdquo; said Peter, gloomily. And they
went forward to meet the old gentleman.</p>
<p>&ldquo;Hullo,&rdquo; he said, shaking hands with them all in turn.
&ldquo;This is a very great pleasure.&rdquo;</p>
<p>&ldquo;It <em>was</em> good of you to get out,&rdquo; Bobbie said,
perspiring and polite.</p>
<p>He took her arm and drew her into the waiting room where she and the
others had played the advertisement game the day they found the Russian.
Phyllis and Peter followed. &ldquo;Well,&rdquo; said the old gentleman, giving Bobbie&rsquo;s arm a kind little shake before
he let it go, &ldquo;Well? What is it?&rdquo;</p>
<p>&ldquo;Oh, please!&rdquo; said Bobbie.</p>
<p>&ldquo;Yes?&rdquo; said the old gentleman.</p>
<p>&ldquo;What I mean to say&mdash;&rdquo; said Bobbie.</p>
<p>&ldquo;Well?&rdquo; said the old gentleman.</p></div>

<div class="bookPage" id="p152" name="152">
<p>&ldquo;It&rsquo;s all very nice and kind,&rdquo; said she.</p>
<p>&ldquo;But?&rdquo; he said.</p>
<p>&ldquo;I wish I might say something,&rdquo; she said.</p>
<p>&ldquo;Say it,&rdquo; said he.</p>
<p>&ldquo;Well, then,&rdquo; said Bobbie&mdash;and out came the story of
the Russian who had written the beautiful book about poor people, and had
been sent to prison and to Siberia for just that.</p>
<p>&ldquo;And what we want more than anything in the world is to find his
wife and children for him,&rdquo; said Bobbie, &ldquo;but we don&rsquo;t
know how. But you must be most horribly clever, or you wouldn&rsquo;t be a
Direction of the Railway. And if <em>you</em> knew how&mdash;and would?
We&rsquo;d rather have that than anything else in the world. We&rsquo;d go
without the watches, even, if you could sell them and find his wife with
the money.&rdquo;</p>
<p>And the others said so, too, though not with so much enthusiasm.</p>
<p>&ldquo;Hum,&rdquo; said the old gentleman, pulling down the white
waistcoat that had the big gilt buttons on it, &ldquo;what did you say the
name was&mdash;Fryingpansky?</p>
<p>&ldquo;No, no,&rdquo; said Bobbie, earnestly. &ldquo;I&rsquo;ll write it
down for you. It doesn&rsquo;t really look at all like that except when you</p></div>

<div class="bookPage" id="p153" name="153">
<p>say it. Have you a bit of pencil and the back of an envelope?&rdquo; she
asked. The old gentleman got out a gold pencil-case and a beautiful,
sweet-smelling, green Russia leather note-book and opened it at a new
page.</p>
<p>&ldquo;Here,&rdquo; he said, &ldquo;write here.&rdquo;</p>
<p>She wrote down &lsquo;Szezcpansky&rsquo; and said:&mdash;</p>
<p>&ldquo;That&rsquo;s how you write it. You <em>call</em> it
Shepansky.&rdquo;</p>
<p>The old gentleman took out a pair of gold-rimmed spectacles and fitted
them on his nose. When he had read the name, he looked quite different.</p>
<p>&ldquo;<em>That</em> man? Bless my soul!&rdquo; he said. &ldquo;Why,
I&rsquo;ve read his book! It&rsquo;s translated into every European language.
A fine book&mdash;a noble book. And so your mother took him in&mdash;like
the good Samaritan. Well, well. I&rsquo;ll tell you what,
youngsters&mdash;your mother must be a very good woman.&rdquo;</p>
<p>&ldquo;Of course she is,&rdquo; said Phyllis, in astonishment.</p>
<p>&ldquo;And you&rsquo;re a very good man,&rdquo; said Bobbie, very shy,
but firmly resolved to be polite.</p>
<p>&ldquo;You flatter me,&rdquo; said the old gentleman, taking off his hat
with a flourish. &ldquo;And now am I to tell you what I think of
you?&rdquo;</p></div>

<div class="bookPage" id="p154" name="154">
<p>&ldquo;Oh, please don&rsquo;t,&rdquo; said Bobbie, hastily.</p>
<p>&ldquo;Why?&rdquo; asked the old gentleman.</p>
<p>&ldquo;I don&rsquo;t exactly know,&rdquo; said Bobbie.
&ldquo;Only&mdash;if it&rsquo;s horrid, I don&rsquo;t want you to; and if
it&rsquo;s nice, I&rsquo;d rather you didn&rsquo;t.&rdquo;</p>
<p>The old gentleman laughed.</p>
<p>&ldquo;Well, then,&rdquo; he said, &ldquo;I&rsquo;ll only just say that
I&rsquo;m very glad you came to me about this&mdash;very glad, indeed. And I
shouldn&rsquo;t be surprised if I found out something very soon. I know a
great many Russians in London, and every Russian knows <em>his</em>
name. Now tell me all about yourselves.&rdquo;</p>
<p>He turned to the others, but there was only one other, and that was
Peter. Phyllis had disappeared.</p>
<p>&ldquo;Tell me all about yourself,&rdquo; said the old gentleman again.
And, quite naturally, Peter was stricken dumb.</p>
<p>&ldquo;All right, we&rsquo;ll have an examination,&rdquo; said the old
gentleman; &ldquo;you two sit on the table, and I&rsquo;ll sit on the bench
and ask questions.&rdquo;</p>
<p>He did, and out came their names and ages&mdash;their Father&rsquo;s name
and business&mdash;how long they had lived at Three Chimneys and a great
deal more.</p></div>

<div class="bookPage" id="pf155" name="">
<p><img src="illustrations/railway/nesbit-railway155.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></p></div>

<div class="bookPage" id="p155" name="155">
<p>The questions were beginning to turn on a herring and a half for three
halfpence, and a pound of lead and a pound of feathers, when the door of
the waiting room was kicked open by a boot; as the boot entered every one
could see that its lace was coming undone&mdash;and in came Phyllis, very
slowly and carefully.</p>
<p>In one hand she carried a large tin can, and in the other a thick slice
of bread and butter.</p>
<p>&ldquo;Afternoon tea,&rdquo; she announced proudly, and held the can and
the bread and butter out to the old gentleman who took them and
said:&mdash;</p>
<p>&ldquo;Bless my soul!&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Phyllis.</p>
<p>&ldquo;It&rsquo;s very thoughtful of you,&rdquo; said the old gentleman,
&ldquo;very.&rdquo;</p>
<p>&ldquo;But you might have got a cup,&rdquo; said Bobbie, &ldquo;and a
plate.&rdquo;</p>
<p>&ldquo;Perks always drinks out of the can,&rdquo; said Phyllis, flushing red. &ldquo;I think it was very nice of him to give it me
at all&mdash;let alone cups and plates,&rdquo; she added.</p>
<p>&ldquo;So do I,&rdquo; said the old gentleman, and he drank some of the
tea and tasted the bread and butter.</p></div>

<div class="bookPage" id="p156" name="156">
<p>And then it was time for the next train, and he got into it with many
good-bys and kind last words.</p>
<p>&ldquo;Well,&rdquo; said Peter, when they were left on the platform, and
the tail-lights of the train disappeared round the corner, &ldquo;it&rsquo;s
my belief that we&rsquo;ve lighted a candle to-day,&mdash;like Latimer, you
know, when he was being burned,&mdash;and there&rsquo;ll be fireworks for
our Russian before long.&rdquo;</p>
<p>And so there were.</p>
<p>It wasn&rsquo;t ten days after the interview in the waiting room that the
three children were sitting on the top of the biggest rock in the field
