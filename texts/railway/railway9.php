<h2>CHAPTER IX<br />
THE PRIDE OF PERKS</h2>
<p><HI>IT</strong> was breakfast-time. Mother&rsquo;s face was very bright as
she poured the milk and ladled out the porridge.</p>
<p>&ldquo;I&rsquo;ve sold another story, Chickies,&rdquo; she said;
&ldquo;the one about the King of the Mussels, so there&rsquo;ll be buns for
tea. You can go and get them as soon as they&rsquo;re baked. About eleven,
isn&rsquo;t it?&rdquo;</p>
<p>Peter, Phyllis, and Bobbie exchanged glances with each other, six
glances in all. Then Bobbie said:&mdash;</p>
<p>&ldquo;Mother would you mind if we didn&rsquo;t have the buns for tea
to-night, but on the fifteenth? That&rsquo;s next Thursday.&rdquo;</p>
<p>&ldquo;<em>I</em> don&rsquo;t mind when you have them, dear,&rdquo;
said Mother, &ldquo;but why?&rdquo;</p>
<p>&ldquo;Because it&rsquo;s Perk&rsquo;s birthday,&rdquo; said Bobbie;
&ldquo;he&rsquo;s thirty-two, and he says he doesn&rsquo;t keep his birthday
any more, because he&rsquo;s got other things to keep&mdash;not rabbits or
secrets&mdash;but the kids and the missus.&rdquo;</p>
<p>&ldquo;You mean his wife and children,&rdquo; said Mother.</p>
<p>&ldquo;Yes,&rdquo; said Phyllis; &ldquo;it&rsquo;s the same thing,
isn&rsquo;t it?&rdquo;</p></div>

<div class="bookPage" id="p183" name="183">
<p>&ldquo;And we thought we&rsquo;d make a nice birthday for him. He&rsquo;s
been so awfully jolly decent to us, you know, Mother,&rdquo; said Peter,
&ldquo;and we agreed that next bun-day we&rsquo;d ask you if we
could.&rdquo;</p>
<p>&ldquo;But supposing there hadn&rsquo;t been a bun-day before the
fifteenth?&rdquo; said Mother.</p>
<p>&ldquo;Oh, then, we meant to ask you to let us anti&mdash;antipate it,
and go without when the bun-day came.&rdquo;</p>
<p>&ldquo;Anticipate,&rdquo; said Mother. &ldquo;I see. Certainly. It would
be nice to put his name on the buns with pink sugar, wouldn&rsquo;t
it?&rdquo;</p>
<p>&ldquo;Perks,&rdquo; said Peter, &ldquo;it&rsquo;s not a pretty
name.&rdquo;</p>
<p>&ldquo;His other name&rsquo;s Albert,&rdquo; said Phyllis; &ldquo;I asked
him once.&rdquo;</p>
<p>&ldquo;We might put A.P.,&rdquo; said Mother; &ldquo;I&rsquo;ll show you
how when the time comes.&rdquo;</p>
<p>This was all very well as far as it went. But even fourteen halfpenny
buns with A.P. on them in pink sugar do not of themselves make a very grand
celebration.</p>
<p>&ldquo;There are always flowers, of course,&rdquo; said Bobbie, later,
when a really earnest council was being held on the subject in the hay-loft where the broken chaff-cutting</p></div>

<div class="bookPage" id="p184" name="184">
<p>machine was, and the row of holes to drop hay through into the hay-racks
over the mangers of the stables below.</p>
<p>&ldquo;He&rsquo;s got lots of flowers of his own,&rdquo; said Peter.</p>
<p>&ldquo;But it&rsquo;s always nice to have them given you,&rdquo; said
Bobbie, &ldquo;however many you&rsquo;ve got of your own. We can use flowers
for trimmings to the birthday. But there must be something to trim besides
buns.&rdquo;</p>
<p>&ldquo;Let&rsquo;s all be quiet and think,&rdquo; said Phyllis; &ldquo;no
one&rsquo;s to speak until it&rsquo;s thought of something.&rdquo;</p>
<p>So they were all quiet and so very still that a brown rat thought that
there was no one in the loft and came out very boldly. When Bobbie sneezed
the rat was quite shocked and hurried away, for he saw that a hay-loft
where such things could happen was no place for a respectable middle-aged
rat that liked a quiet life.</p>
<p>&ldquo;Hooray!&rdquo; cried Peter, suddenly, &ldquo;I&rsquo;ve got
it.&rdquo; He jumped up and kicked at the loose hay.</p>
<p>&ldquo;What?&rdquo; said the others, eagerly.</p>
<p>&ldquo;Why, Perks is so nice to everybody. There must be lots of people
in the village who&rsquo;d like to help to make him a birthday. Let&rsquo;s
go round and ask everybody.&rdquo;</p>
<p>&ldquo;Mother said we weren&rsquo;t to ask people for things,&rdquo; said
Bobby, doubtfully.</p></div>

<div class="bookPage" id="p185" name="185">
<p>&ldquo;For ourselves, she meant, silly, not for other people. I&rsquo;ll
ask the old gentleman, too. You see if I don&rsquo;t,&rdquo; said Peter.</p>
<p>&ldquo;Let&rsquo;s ask Mother first,&rdquo; said Bobbie.</p>
<p>&ldquo;Oh, what&rsquo;s the use of bothering Mother about every little
thing?&rdquo; said Peter, &ldquo;especially when she&rsquo;s busy. Come on.
Let&rsquo;s go down to the village now and begin.&rdquo;</p>
<p>So they went. The old lady at the Post-office said she didn&rsquo;t see
why Perks should have a birthday any more than any one else.</p>
<p>&ldquo;No,&rdquo; said Bobbie, &ldquo;I should like every one to have
one. Only we know when his is.&rdquo;</p>
<p>&ldquo;Mine&rsquo;s to-morrow,&rdquo; said the old lady, &ldquo;and much
notice any one will take of it. Go along with you.&rdquo;</p>
<p>So they went.</p>
<p>And some people were kind, and some were crusty. And some would give and
some would not. It is rather difficult work asking for things, even for
other people, as you have no doubt found if you have ever tried it.</p>
<p>When the children got home and counted up what had been given and what
had been promised, they felt that for the first day it was not so bad. Peter wrote down the
lists of the things in the little pocket-book where he kept the numbers of
his engines. These were the lists.</p></div>

<div class="bookPage" id="p186" name="186">
<h3>Given.</h3>
<ul>
<li>A tobacco pipe from the sweet shop.</li>
<li>Half a pound of tea from the grocer&rsquo;s.</li>
<li>A woolen scarf slightly faded from the draper&rsquo;s, which was the
other side of the grocer&rsquo;s.</li>
<li>A stuffed squirrel from the Doctor.</li>
</ul>
<h3>Promised.</h3>
<ul>
<li>A piece of meat from the butcher.</li>
<li>Six fresh eggs from the woman who lived in the old turnpike
cottage.</li>
<li>A piece of honeycomb and six bootlaces from the cobbler, and an iron
shovel from the blacksmith&rsquo;s.</li>
</ul>
<p>Very early next morning Bobbie got up and woke Phyllis. This had been
agreed oil between them. They had not told Peter because they thought he
would think it silly. But they told him afterwards, when it had turned out
all right.</p>
<p>They cut a big bunch of roses. and put it in a basket with the
needle-book that Phyllis had</p>
<p>made for Bobbie on her birthday, and a very pretty blue necktie of
Phyllis&rsquo;s. Then they wrote on a paper, &ldquo;For Mrs. Ransome, with
our best love, because it is her</p></div>

<div class="bookPage" id="p187" name="187">
<p>birthday,&rdquo; and they put the paper in
the basket, and they took it to the Post-office, and went in and put it on
the counter and ran away before the old woman at the Post-office had time
to get into her shop.</p>
<p>When they got home Peter had grown confidential over helping Mother to
get the breakfast and had told her their plans.</p>
<p>&ldquo;There&rsquo;s no harm in it,&rdquo; said Mother, &ldquo;but it
depends <em>how</em> you do it. I only hope he won&rsquo;t be offended
and think it&rsquo;s <em>charity</em>. Poor people are very proud, you
know.&rdquo;</p>
<p>&ldquo;It isn&rsquo;t because he&rsquo;s poor,&rdquo; said Phyllis;
&ldquo;it&rsquo;s because we&rsquo;re fond of him.&rdquo;</p>
<p>&ldquo;I&rsquo;ll find some things that Phyllis has outgrown,&rdquo; said
Mother, &ldquo;if you&rsquo;re quite sure you can give them to him without
his being offended. I should like to do some little thing for Lim because
he&rsquo;s been so kind to you. I can&rsquo;t do much because we&rsquo;re poor
ourselves. What are you writing, Bobbie?&rdquo;</p>
<p>&ldquo;Nothing particular,&rdquo; said Bobbie, who had suddenly begun to
scribble. &ldquo;I&rsquo;m sure he&rsquo;d like the things,
Mother.&rdquo;</p>
<p>The morning of the fifteenth was spent very happily in getting the buns
and watching Mother make A.P. on them with pink sugar. You know how
it&rsquo;s done, of course? You beat up whites of eggs and mix powdered</p></div>

<div class="bookPage" id="p188" name="188">
<p>
sugar with them, and put in a few drops of cochineal. And then you make a
cone of clean, white paper with a little hole at the pointed end, and put
the pink egg-sugar in at the big end. It runs slowly out at the pointed
end, and you write the letters with it just as though it were a great fat
pen full of pink sugar-ink.</p>
<p>The buns looked beautiful with A.P. on every one, and, when they were
put in a cool oven to set the sugar, the children went up to the village to
collect the honey and the shovel and the other promised things.</p>
<p>The old lady at the Post-office was standing on her doorstep. The
children said &ldquo;Good morning,&rdquo; politely, as they passed.</p>
<p>&ldquo;Here, stop a bit,&rdquo; she said.</p>
<p>So they stopped.</p>
<p>&ldquo;Those roses,&rdquo; said she.</p>
<p>&ldquo;Did you like them?&rdquo; said Phyllis; &ldquo;they were as fresh
as fresh. <em>I</em> made the needle-book, but it was Bobbie&rsquo;s
present.&rdquo; She skipped joyously as she spoke.</p>
<p>&ldquo;Here&rsquo;s your basket,&rdquo; said the Post-office woman. She
event in and brought out the basket. It was full of fat, red
gooseberries.</p>
<p>&ldquo;I dare say Perks&rsquo;s children would like them,&rdquo; said
she.</p></div>

<div class="bookPage" id="pf189" name="">
<p><img src="illustrations/railway/nesbit-railway189.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></p></div>

<div class="bookPage" id="p189" name="189">
<p>&ldquo;You <em>are</em> an old dear,&rdquo; said Phyllis, throwing
her arms around the old lady&rsquo;s fat waist. &ldquo;Perks
<em>will</em> be pleased.&rdquo;</p>
<p>&ldquo;He won&rsquo;t be half so pleased as I was with your needle-book
and the tie and the pretty flowers and all,&rdquo; said the old lady,
patting Phyllis&rsquo;s shoulder. &ldquo;You&rsquo;re good little souls, that
you are. Look here. I&rsquo;ve got a pram round the back in the wood-lodge.
It was got for my Emmie&rsquo;s first, that didn&rsquo;t live but six months,
and she never had but that one. I&rsquo;d like Mrs. Perks to have it. It
&rsquo;ud be a help to her with that great boy of her&rsquo;s. Will you take
it along?&rdquo;</p>
<p>&ldquo;<em>Oh</em>!&rdquo; said all the children together.</p>
<p>When Mrs. Ransome had got out the perambulator and taken off the careful
papers that covered it, and dusted it all over, she said:&mdash;</p>
<p>&ldquo;Well, there it is. I don&rsquo;t know but what I&rsquo;d have given
it to her before if I&rsquo;d thought of it. Only I didn&rsquo;t quite know
if she&rsquo;d accept of it from me. You tell her it was my Emmie&rsquo;s
little one&rsquo;s pram&mdash;&rdquo;</p>
<p>&ldquo;Oh, <em>isn&rsquo;t</em> it nice to think there is going to be
a real live baby in it again!&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Mrs. Ransome, sighing, and then laughing;
&ldquo;here, I&rsquo;ll give you some peppermint cushions for the little</p></div>

<div class="bookPage" id="p190" name="190">
<p>ones, and then you run along before I give you the roof off my head and the
clothes off my back.&rdquo;</p>
<p>All the things that had been collected for Perks were packed into the
perambulator, and at half-past three Peter and Bobbie and Phyllis wheeled
it down to the little yellow house where Perks lived.</p>
<p>The house was very tidy. On the window ledge was a jug of wild-flowers,
big daisies, and red sorrel, and feathery, flowery grasses.</p>
<p>There was a sound of splashing from the wash-house, and a partly washed
boy put his head round the door.</p>
<p>&ldquo;Mother&rsquo;s a-changing of herself,&rdquo; he said.</p>
<p>&ldquo;Down in a minute,&rdquo; a voice sounded down the narrow, freshly
scrubbed stairs.</p>
<p>The children waited. Next moment the stairs creaked and Mrs. Perks came
down, buttoning her bodice. Her hair was brushed very smooth and tight, and
her face shone with soap and water.</p>
<p>&ldquo;I&rsquo;m a bit late changing, Miss,&rdquo; she said to Bobbie, &ldquo;owing to me having had a extry clean-up to-day, along o&rsquo;
Perks happening to name its being his birthday. I don&rsquo;t know what put
it into his head to think of such a thing. We keeps the children&rsquo;s
birthdays, of course; but him and me&mdash;we&rsquo;re too old for such
like, as a general rule.&rdquo;</p></div>

<div class="bookPage" id="p191" name="191">
<p>&ldquo;We knew it was his birthday,&rdquo; said Peter, &ldquo;and
we&rsquo;ve got some presents for him outside in the
perambulator.&rdquo;</p>
<p>As the presents were being unpacked Mrs. Perks gasped. When they were
all unpacked, she surprised and horrified the children by sitting suddenly
down on a wooden chair and bursting into tears.</p>
<p>&ldquo;Oh, don&rsquo;t!&rdquo; said everybody; &ldquo;oh, please
don&rsquo;t!&rdquo; And Peter added, perhaps a little impatiently:
&ldquo;What on earth is the matter? You don&rsquo;t mean to say you
don&rsquo;t like it?&rdquo;</p>
<p>Mrs. Perks only sobbed. The Perks children, now as shiny-faced as any
one could wish, stood at the wash-house door, and scowled at the intruders.
There was a silence, an awkward silence.</p>
<p>&ldquo;<em>Don&rsquo;t</em> you like it?&rdquo; said Peter, again,
while his sisters patted Mrs. Perks on the back.</p>
<p>She stopped crying as suddenly as she had begun.</p>
<p>&ldquo;There, there, don&rsquo;t you mind me. <em>I&rsquo;m</em> all
right!&rdquo; she said. &ldquo;Like it? Why, it&rsquo;s a birthday such as
Perks never &rsquo;ad, not even when &rsquo;e was a boy and stayed with his
uncle, who was a corn chandler in his own account. He failed afterwards.
Like it? Oh&mdash;&rdquo; and then she went on and said all sorts of things
that I won&rsquo;t write down, because I am sure that Peter and Bobbie and
Phyllis would not like me</p></div>

<div class="bookPage" id="p192" name="192"><p>to. Their ears got hotter and hotter, and their
faces redder and redder, at the kind things Mrs. Perks said. They felt they
had done nothing to deserve all this praise.</p>
<p>At last Peter said: &ldquo;Look here, we&rsquo;re glad you&rsquo;re
pleased. But if you go on saying things like that, we must go home. And we
did want to stay and see if Mr. Perks is pleased, too. But we can&rsquo;t
stand this.&rdquo;</p>
<p>&ldquo;I won&rsquo;t say another single word,&rdquo; said Mrs. Perks,
with a beaming face, &ldquo;but that needn&rsquo;t stop me thinking, need
it? For if ever&mdash;&rdquo;</p>
<p>&ldquo;Can we have a plate for the buns?&rdquo; Bobbie asked abruptly.
And then Mrs. Perks hastily laid the table for tea, and the buns and the
honey and the gooseberries were displayed on plates, and the roses were put
in two glass jam jars, and the tea table looked, as Mrs. Perks said, &ldquo;fit for a Prince.&rdquo;</p>
<p>&ldquo;To think!&rdquo; she said, &ldquo;me getting the place tidy
early, and the little &rsquo;uns getting the wild-flowers and all&mdash;when
never did I think there&rsquo;d be anything more for him except the ounce of
his pet particular that I got o&rsquo; Saturday and been saving up for
&rsquo;im ever since. Bless us! &rsquo;e <em>is</em> early!&rdquo;</p>
<p>Perks had indeed unlatched the latch of the little front gate.</p></div>

<div class="bookPage" id="p193" name="193">
<p>&ldquo;Oh,&rdquo; whispered Bobbie, &ldquo;let&rsquo;s hide in the back
kitchen, and <em>you</em> tell him about it. But give him the tobacco
first, because you got it for him. And when you&rsquo;ve told him,
we&rsquo;ll all come in and shout, &lsquo;Many happy
returns!&rsquo;&rdquo;</p>
<p>It was a very nice plan, but it did not quite come off. To begin with,
there was only just time for Peter and Bobbie and Phyllis to rush into the
wash-house, pushing the young and open-mouthed Perks children in front of
them. There was not time to shut the door, so that, without at all meaning
it, they had to listen to what went on in the kitchen. The wash-house was a
tight fit for the Perks children and the Three Chimneys children, as well
as all the wash-house&rsquo;s proper furniture, including the mangle and the
copper.</p>
<p>&ldquo;Hullo, old woman!&rdquo; they heard Mr. Perks&rsquo;s voice say;
&ldquo;here&rsquo;s a pretty set out!&rdquo;</p>
<p>&ldquo;It&rsquo;s your birthday tea, Bert,&rdquo; said Mrs. Perks,
&ldquo;and here&rsquo;s a ounce of your extry particular. I got it o&rsquo;
Saturday along o&rsquo; your happening to remember it was your birthday
to-day.&rdquo;</p>
<p>&ldquo;Good old girl!&rdquo; said Mr. Perks, and there was a sound of a
kiss.</p>
<p>&ldquo;But what&rsquo;s that pram doing here? And what&rsquo;s all these
bundles? And where did you get the sweetstuff, and&mdash;&rdquo;</p></div>

<div class="bookPage" id="p194" name="194">
<p>The children did not hear what Mrs. Perks replied, because just then
Bobbie gave a start, put her hand in her pocket, and all her body grew
stiff with horror.</p>
<p>&ldquo;Oh!&rdquo; she whispered to the others, &ldquo;whatever shall we
do? I forgot to put the labels on any of the things! He won&rsquo;t know
what&rsquo;s from who. He&rsquo;ll think it&rsquo;s all <em>us</em>, and
that we&rsquo;re trying to be grand or charitable or something
horrid.&rdquo;</p>
<p>&ldquo;Hush!&rdquo; said Peter.</p>
<p>And then they heard the voice of Mr. Perks, loud and rather angry.</p>
<p>&ldquo;I don&rsquo;t care,&rdquo; he said; &ldquo;I won&rsquo;t stand it,
and so I tell you straight.&rdquo;</p>
<p>&ldquo;But,&rdquo; said Mrs. Perks, &ldquo;it&rsquo;s them children you make such a fuss about&mdash;the children from the Three
Chimneys.&rdquo;</p>
<p>&ldquo;I don&rsquo;t care,&rdquo; said Perks, firmly, &ldquo;not if it
was a angel from Heaven. We&rsquo;ve got on all right all these years and no
favours asked. I&rsquo;m not going to begin these sort of charity goings-on
at my time of life, so don&rsquo;t you think it, Nell.&rdquo;</p>
<p>&ldquo;Oh, hush!&rdquo; said poor Mrs. Perks; &ldquo;Bert, shut your
silly tongue, for goodness&rsquo; sake. The all three of &rsquo;em&rsquo;s in
the wash-house a-listening to every word you speaks.&rdquo;</p></div>

<div class="bookPage" id="pf195" name="">
<img src="illustrations/railway/nesbit-railway195.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></div>

<div class="bookPage" id="p195" name="195">
<p>&ldquo;Then I&rsquo;ll give them something to listen to,&rdquo; said the
angry Perks; &ldquo;I&rsquo;ve spoke my mind to them afore now, and
I&rsquo;ll do it again,&rdquo; he added, and he took two strides to the
wash-house door, and flung it wide open&mdash;as wide, that is, as it would
go, with the tightly packed children behind it.</p>
<p>&ldquo;Come out,&rdquo; said Perks, &ldquo;come out and tell me what you
mean by it. &rsquo;Ave I ever complained to you of being short, as you comes
this charity lay over me?&rdquo;</p>
<p>&ldquo;<em>Oh</em>!&rdquo; said Phyllis, &ldquo;I thought you&rsquo;d
be so pleased; I&rsquo;ll never try to be kind to any one else as long as I
live. No, I won&rsquo;t, not never.&rdquo;</p>
<p>She burst into tears.</p>
<p>&ldquo;We didn&rsquo;t mean any harm,&rdquo; said Peter.</p>
<p>&ldquo;It ain&rsquo;t what you means so much as what you does,&rdquo;
said Perks.</p>
<p>&ldquo;Oh, <em>don&rsquo;t</em>!&rdquo; cried Bobbie, trying hard to
be braver than Phyllis, and to find more words than Peter had done for
explaining in. &ldquo;We thought you&rsquo;d love it. We always have things
on our birthdays.&rdquo;</p>
<p>&ldquo;Oh, yes,&rdquo; said Perks, &ldquo;your own relations;
that&rsquo;s different.&rdquo;</p></div>

<div class="bookPage" id="p196" name="196">
<p>&ldquo;Oh, no,&rdquo; Bobbie answered. &ldquo;Not our own relations. All
the servants always gave us things at home, and us to them when it was
their birthdays. And when it was mine, and Mother gave me the brooch like a
buttercup, Mrs. Viney gave me two lovely glass pots, and nobody thought she
was coming the charity lay over us.&rdquo;</p>
<p>&ldquo;If it had been glass pots here,&rdquo; said Perks, &ldquo;I
wouldn&rsquo;t ha&rsquo; said so much. It&rsquo;s there being all this heaps
and heaps of things I can&rsquo;t stand. No&mdash;nor won&rsquo;t
neither.&rdquo;</p>
<p>&ldquo;But they&rsquo;re not all from us&mdash;&rdquo; said Peter,
&ldquo;only we forgot to put the labels on. They&rsquo;re from all sorts of
people in the village.&rdquo;</p>
<p>&ldquo;Who put &rsquo;em up to it, I&rsquo;d like to know?&rdquo; asked
Perks.</p>
<p>&ldquo;Why, we did,&rdquo; sniffed Phyllis.</p>
<p>Perks sat down heavily in the elbow-chair and looked at them with what
Bobbie afterwards described as withering glances of gloomy despair.</p>
<p>&ldquo;So you&rsquo;ve been round telling the neighbours we can&rsquo;t
make both ends meet? Well, now you&rsquo;ve disgraced us as deep as you can
in the neighbourhood, you can just take the whole bag of tricks back
w&rsquo;ere it come from. Very much obliged, I&rsquo;m sure. I don&rsquo;t
doubt but what you meant it kind, but I&rsquo;d rather not be acquainted
with you any longer if it&rsquo;s all the same to you.&rdquo; He
deliberately</p></div>

<div class="bookPage" id="p197" name="197"><p>turned the chair round so that his back was turned to the
children. The legs of the chair grated on the brick floor, and that was the
only sound that broke the silence.</p>
<p>Then suddenly Bobbie spoke.</p>
<p>&ldquo;Look here,&rdquo; she said, &ldquo;this is most awful.&rdquo;</p>
<p>&ldquo;That&rsquo;s what I says,&rdquo; said Perks, not turning
round.</p>
<p>&ldquo;Look here,&rdquo; said Bobbie, desperately, &ldquo;we&rsquo;ll go
if you like&mdash;and you needn&rsquo;t be friends with us any more if you
don&rsquo;t want, but&mdash;&rdquo;</p>
<p>&ldquo;I shall always be friends with you, however nasty you are to
us,&rdquo; sniffed Phyllis, wildly.</p>
<p>&ldquo;Be quiet,&rdquo; said Peter, in a fierce aside.</p>
<p>&ldquo;But before we go,&rdquo; Bobbie went on desperately, &ldquo;do
let us show you the labels we wrote to put on the things.&rdquo;</p>
<p>&ldquo;I don&rsquo;t want to see no labels,&rdquo; said Perks,
&ldquo;except proper luggage ones in my own walk of life. Do you think
I&rsquo;ve kept respectable and outer debt on what I gets, and her having to
take in washing, to be give away for a laughingstock to all the
neighbours?&rdquo;</p>
<p>&ldquo;Laughing?&rdquo; said Peter; &ldquo;you don&rsquo;t
know.&rdquo;</p></div>

<div class="bookPage" id="p198" name="198">
<p>&ldquo;You&rsquo;re a very hasty gentleman,&rdquo; whined Phyllis;
&ldquo;you know you were wrong once before, about us not telling you the
secret about the Russian. Do let Bobbie tell you about the
labels?&rdquo;</p>
<p>&ldquo;Well. Go ahead!&rdquo; said Perks, grudgingly.</p>
<p>&ldquo;Well, then,&rdquo; said Bobbie, fumbling miserably, yet not
without hope, in her tightly stuffed pocket, &ldquo;we wrote down all the
things everybody said when they gave us the things, with the people&rsquo;s
names, because Mother said we ought to be careful&mdash;because&mdash;but I
wrote down what she said&mdash;and you&rsquo;ll see.&rdquo;</p>
<p>But Bobbie could not read the labels just at once. She had to swallow
once or twice before she could begin.</p>
<p>Mrs. Perks had been crying steadily ever since her husband had opened the wash-house door. Now she caught her breath,
choked, and said:&mdash;</p>
<p>&ldquo;Don&rsquo;t you upset yourself, Missy. I know you meant it kind if
he doesn&rsquo;t.&rdquo;</p>
<p>&ldquo;May I read the labels?&rdquo; said Bobbie, crying on to the slips
as she tried to sort them. &ldquo;Mother&rsquo;s first. It says:&mdash;</p></div>

