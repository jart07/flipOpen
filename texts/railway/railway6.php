<p>Bobbie said nothing. She was thinking of the horrible mound, and the
trustful train rushing towards it.</p>
<p>&ldquo;And it was <em>us</em> that saved them,&rdquo; said
Peter.</p>
<p>&ldquo;How dreadful if they&rsquo;d all been killed!&rdquo; said Phyllis,
