<h2>CHAPTER VI<br />
SAVIOURS OF THE TRAIN</h2>
<p><strong>THE</strong> Russian gentleman was better the next
day, and the day after that better still, and on the third day he was well
enough to come into the garden. A basket chair was put for him and he sat
there, dressed in clothes of Father&rsquo;s which were too big for him. But
when Mother had hemmed up the ends of the sleeves and the trousers, the
clothes did well enough. His was a kind face now that it was no longer
tired and frightened, and he smiled at the children whenever he saw them.
They wished very much that he could speak English. Mother wrote several
letters to people she thought might know whereabouts in England a Russian
gentleman&rsquo;s wife and family might possibly be; not to the people she
used to know before she came to live at Three Chimneys&mdash;she never
wrote to any of them&mdash;but strange people,&mdash;Members of Parliament
and Editors of papers, and Secretaries of Societies.</p>
<p>And she did not do much of her story-writing, only corrected proofs as she sat in the sun near the Russian, and talked to
him every now and then.</p></div>

<div class="bookPage" id="pf119" name="">
<img src="illustrations/railway/nesbit-railway119.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></div>

<div class="bookPage" id="p119" name="119">
<p>The children wanted very much to shew how kindly they felt to this man
who had been sent to prison and to Siberia just for writing a beautiful
book about poor people. They could smile at him, of course; they could and
they did. But if you smile too constantly, the smile is apt to get fixed
like the smile of the hy&aelig;na. And then it no longer looks friendly,
but simply silly. So they tried other ways, and brought him flowers till
the place where he sat was surrounded by little fading bunches of clover
and roses and Canterbury bells.</p>
<p>And then Phyllis had an idea. She beckoned mysteriously to the others
and drew them into the back yard, and there, in a concealed spot, between
the pump and the water-butt, she said:&mdash;</p>
<p>&ldquo;You remember Perks promising me the very first strawberries out
of his own garden?&rdquo; Perks, you will recollect, was the Porter.
&ldquo;Well, I should think they&rsquo;re ripe now. Let&rsquo;s go down and
see.&rdquo;</p>
<p>Mother had been down as she had promised to tell the Station Master the
story of the Russian Prisoner. But even the charms of the railway had been
unable to tear the children away from the neighbourhood of the interesting stranger. So they had not been to the
station for three days.</p>
<p>They went now.</p></div>

<div class="bookPage" id="p120" name="120">
<p>And, to their surprise and distress, were very coldly received by
Perks.</p>
<p>&ldquo;&rsquo;Ighly honoured, I&rsquo;m sure,&rdquo; he said when they
peeped in at the door of the Porter&rsquo;s room. And he went on reading his
newspaper.</p>
<p>There was an uncomfortable silence.</p>
<p>&ldquo;O dear,&rdquo; said Bobbie, with a sigh, &ldquo;I do believe
you&rsquo;re cross.&rdquo;</p>
<p>&ldquo;What, me? Not me!&rdquo; said Perks, loftily; &ldquo;it
ain&rsquo;t nothing to me.&rdquo;</p>
<p>&ldquo;What ain&rsquo;t nothing to you?&rdquo; said Peter, too anxious
and alarmed to change the form of words.</p>
<p>&ldquo;Nothing ain&rsquo;t nothing. What &rsquo;appens either &rsquo;ere or
helsewhere,&rdquo; said Perks, &ldquo;if you likes to &rsquo;ave your
secrets, &rsquo;ave &rsquo;em and welcome. That&rsquo;s what I say.&rdquo;</p>
<p>The secret-chamber of each heart was rapidly examined during the pause
that followed. Three heads were shaken.</p>
<p>&ldquo;We haven&rsquo;t got any secrets from <em>you</em>,&rdquo;
said Bobbie at last.</p>
<p>&ldquo;Maybe you &rsquo;ave, and maybe you &rsquo;aven&rsquo;t,&rdquo; said Perks; &ldquo;it ain&rsquo;t nothing to me. And I wish you all a very good
afternoon.&rdquo; He held up the paper between him and them and went on
reading.</p></div>

<div class="bookPage" id="pf120" name="">
<img src="illustrations/railway/nesbit-railway120.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></div>

<div class="bookPage" id="p121" name="121">
<p>&ldquo;Oh, <em>don&rsquo;t</em>!&rdquo; said Phyllis, in despair;
&ldquo;this is truly dreadful! Whatever it is, do tell us.&rdquo;</p>
<p>&ldquo;We didn&rsquo;t mean to do it whatever it was.&rdquo;</p>
<p>No answer. The paper was refolded and Perks began on another column.</p>
<p>&ldquo;Look here,&rdquo; said Peter, suddenly, &ldquo;it&rsquo;s not
fair. Even people who do crimes aren&rsquo;t punished without being told
what it&rsquo;s for&mdash;unless it&rsquo;s in Russia.&rdquo;</p>
<p>&ldquo;I don&rsquo;t know nothing about Russia.&rdquo;</p>
<p>&ldquo;Oh, yes, you do, when Mother came down on purpose to tell you and
Mr. Gills all about <em>our</em> Russian.&rdquo;</p>
<p>&ldquo;Can&rsquo;t you fancy it?&rdquo; said Perks, indignantly;
&ldquo;don&rsquo;t you see &rsquo;im a-asking of me to step into &rsquo;is
room and take a chair and listen to what &rsquo;er Ladyship &rsquo;as to
say?&rdquo;</p>
<p>&ldquo;Do you mean to say you&rsquo;ve not heard?&rdquo;</p>
<p>&ldquo;Not so much as a breath. I did go so far as to put a question.
And he shuts me up like a rat-trap. &lsquo;Affairs of State, Perks,&rsquo;
says he. But I did think one o&rsquo; you would &rsquo;a&rsquo; nipped down to
tell me&mdash;you&rsquo;re here sharp enough when you want to get anything out of old Perks,&rdquo;&mdash;Phyllis flushed purple as she thought of the strawberries,&mdash;&ldquo;information about
locomotives or signals or the likes,&rdquo; said Perks.</p></div>

<div class="bookPage" id="p122" name="122">
<p>&ldquo;We didn&rsquo;t know you didn&rsquo;t know.&rdquo;</p>
<p>&ldquo;We thought Mother had told you.&rdquo;</p>
<p>&ldquo;We wanted to tell you only we thought it would be stale
news.&rdquo;</p>
<p>The three spoke all at once.</p>
<p>Perks said it was all very well, and still held up the paper. Then
Phyllis suddenly snatched it away, and threw her arms round his neck.</p>
<p>&ldquo;Oh, let&rsquo;s kiss and be friends,&rdquo; she said;
&ldquo;we&rsquo;ll say we&rsquo;re sorry first, if you like, but we
didn&rsquo;t really know that you didn&rsquo;t know.&rdquo;</p>
<p>&ldquo;We are so sorry,&rdquo; said the others.</p>
<p>And Perks at last consented to accept their apologies.</p>
<p>Then they got him to come out and sit in the sun on the green Railway
Bank, where the grass was quite hot to touch, and there, sometimes speaking
one at a time, and sometimes all together, they told the Porter the story
of the Russian Prisoner.</p>
<p>&ldquo;Well, I must say,&rdquo; said Perks; but he did not say
it&mdash;whatever it was.</p></div>

<div class="bookPage" id="p123" name="123">
<p>&ldquo;Yes, it is pretty awful, isn&rsquo;t it?&rdquo; said Peter,
&ldquo;and I don&rsquo;t wonder you were curious about who the Russian
was.&rdquo;</p>
<p>&ldquo;I wasn&rsquo;t curious, not so much as interested,&rdquo; said the
Porter.</p>
<p>&ldquo;Well, I do think Mr. Gills might have told you about it. It was
horrid of him.&rdquo;</p>
<p>&ldquo;I don&rsquo;t keep no down on &rsquo;im for that, Missie,&rdquo;
said the Porter; &ldquo;cos why? I see &rsquo;is reasons. &rsquo;E&rsquo;s
Russian sides in this &rsquo;ere war. An&rsquo; I&rsquo;m Jap. Course &rsquo;e
wouldn&rsquo;t want to give away &rsquo;is own side with a tale like that
&rsquo;ere. It ain&rsquo;t human nature. A man&rsquo;s got to stand up for his
own side whatever they does. That&rsquo;s what it means by Party Politics. I
should &rsquo;a&rsquo; done the same myself if that long-&rsquo;aired chap
&rsquo;ad &rsquo;a&rsquo; been a Jap.&rdquo;</p>
<p>&ldquo;But the Japs don&rsquo;t do cruel wicked things like that,&rdquo;
said Bobbie.</p>
<p>&ldquo;P&rsquo;r&rsquo;aps not,&rdquo; said Perks, cautiously;
&ldquo;still you can&rsquo;t be sure with foreigners. My own belief is
they&rsquo;re all tarred with the same brush.&rdquo;</p>
<p>&ldquo;Then why are you on the side of the Japs?&rdquo; Peter asked.</p>
<p>&ldquo;Well, sir&mdash;you see you must take one side or the other. Same
as with Liberals and Conservatives. The great thing</p></div>

<div class="bookPage" id="p124" name="124">
<p>is to take your side and then stick to it, whatever
happens.&rdquo;</p>
<p>A signal sounded.</p>
<p>&ldquo;There&rsquo;s the 3.14 up,&rdquo; said Perks. &ldquo;You lie low
till she&rsquo;s through, and then we&rsquo;ll go up along to my place, and
see if there&rsquo;s any of them strawberries ripe what I told you
about.&rdquo;</p>
<p>&ldquo;If there are any ripe, and you <em>do</em> give them to
me,&rdquo; said Phyllis, &ldquo;you won&rsquo;t mind if I give them to the
poor Russian, will you?&rdquo;</p>
<p>Perks narrowed his eyes and then raised his eyebrows.</p>
<p>&ldquo;So it was them strawberries you come down for this afternoon,
eh?&rdquo; said he.</p>
<p>This was an awkward moment for Phyllis. To say &ldquo;yes&rdquo; would
seem rude and greedy, and unkind to Perks. But she knew if she said
&ldquo;no,&rdquo; she would not be pleased with herself afterwards.
So&mdash;</p>
<p>&ldquo;Yes,&rdquo; she said, &ldquo;it was.&rdquo;</p>
<p>&ldquo;Well done!&rdquo; said the Porter; &ldquo;speak the truth and
shame the &mdash;&rdquo;</p>
<p>&ldquo;But we&rsquo;d have come down the very next day if we&rsquo;d known
you hadn&rsquo;t heard the story,&rdquo; Phyllis added hastily.</p>
</div>

<div class="bookPage" id="p125" name="125">
<p>&ldquo;I believe you, Missie,&rdquo; said Perks, and sprang across the line six feet in front of the advancing train.</p>
<p>The girls hated to see him do this, but Peter liked it. It was so
exciting.</p>
<p>The Russian gentleman was so delighted with the strawberries that the
three racked their brains to find some other surprise for him. But all the
racking did not bring out any idea more novel than wild cherries. And this
idea occurred to them next morning. They had seen the blossom on the trees
in the spring, and they knew where to look for wild cherries now that
cherry time was here. The trees grew all up and along the rocky face of the
cliff out of which the mouth of the tunnel opened. There were all sorts of
trees there, birches and beeches and baby oaks and hazels, and among them
the cherry blossom had shone like snow and silver.</p>
<p>The mouth of the tunnel was some way from Three Chimneys, so Mother let
them take their lunch with them in a basket. And the basket would do to
bring the cherries back in if they found any. She also lent them her silver
watch so that they should not be late for tea. Peter&rsquo;s Waterbury had
taken it into its head not to go since the day when Peter dropped it into
the water-butt. And they started. When they got to the top of the cutting, they
leaned over the fence and looked down to where the </p></div>

<div class="bookPage" id="p126" name="126">
<p>railway lines lay at the
bottom of what, as Phyllis said, was exactly like a mountain gorge.</p>
<p>&ldquo;If it wasn&rsquo;t for the railway at the bottom, it would be as
though the foot of man had never, wouldn&rsquo;t it?&rdquo; she said.</p>
<p>The sides of the cutting were of gray stone, very roughly hewn. Indeed
the top part of the cutting had been a little natural glen that had been
cut deeper to bring it down to the level of the tunnel&rsquo;s mouth. Among
the rocks, grass and flowers grew, and seeds dropped by birds in the
crannies of the stone had taken root and grown into bushes and trees that
overhung the cutting. Near the tunnel was a flight of steps leading down to
the line&mdash;just wooden bars roughly fixed into the earth&mdash;a very
steep and narrow way, more like a ladder than a stair.</p>
<p>&ldquo;We&rsquo;d better get down,&rdquo; said Peter; &ldquo;I&rsquo;m
sure the cherries would be quite easy to get at from the side of the steps.
You remember it was there we picked the cherry blossoms that we put on the
rabbit&rsquo;s funeral grave.&rdquo;</p>
<p>So they went along the fence towards the little swing gate that is at the top of these steps. And they were almost at the
gate when Bobbie said:&mdash;</p>
<p>&ldquo;Hush. Stop! What&rsquo;s that?&rdquo;</p></div>

<div class="bookPage" id="p127" name="127">
<p>&ldquo;That&rdquo; was a very odd noise indeed,&mdash;a soft noise, but
quite plainly to be heard through the sound of the wind in the tree
branches, and the hum and whir of the telegraph wires. It was a sort of
rustling, whispering sound. As they listened it stopped, and then it began
again.</p>
<p>And this time it did not stop, but it grew louder and more rustling and
rumbling.</p>
<p>&ldquo;Look&mdash;&rdquo; cried Peter, suddenly&mdash;&ldquo;the tree
over there!&rdquo;</p>
<p>The tree he pointed at was one of those that have rough gray leaves and
white flowers. The berries, when they come, are bright scarlet, but if you
pick them, they disappoint you by turning black before you get them home.
And, as Peter pointed, the tree was moving,&mdash;not just the way trees
ought to move when the wind blows through them, but all in one piece, as
though it were a live creature and were walking down the side of the
cutting.</p>
<p>&ldquo;It&rsquo;s moving!&rdquo; cried Bobbie. &ldquo;Oh, look! and so
are the others. It&rsquo;s like the woods in Macbeth.&rdquo;</p>
<p>&ldquo;It&rsquo;s magic,&rdquo; said Phyllis, breathlessly. &ldquo;I
always knew this railway was enchanted.&rdquo;</p></div>

<div class="bookPage" id="p128" name="128">
<p>It really did seem a little like magic. For all the trees for about
twenty yards of the opposite bank seemed to be slowly walking down towards
the railway line, the tree with the gray leaves bringing up the rear like
some old shepherd driving a flock of green sheep.</p>
<p>&ldquo;What is it? Oh, what is it?&rdquo; said Phyllis; &ldquo;it&rsquo;s
much too magic for me. I don&rsquo;t like it. Let&rsquo;s go home.&rdquo;</p>
<p>But Bobby and Peter clung fast to the rail and watched breathlessly. And
Phyllis made no movement towards going home by herself.</p>
<p>The trees moved on and on. Some stones and loose earth fell down and
rattled on the railway metals far below.</p>
<p>&ldquo;It&rsquo;s all coming down,&rdquo; Peter tried to say, but he
found there was hardly any voice to say it with. And, indeed, just as he
spoke, the great rock, on the top of which the walking trees were, leaned
slowly forward. The trees, ceasing to walk, stood still and shivered.
Leaning with the rock, they seemed to hesitate a moment, and then rock and
trees and grass and bushes, with a rushing sound, slipped right away from
the face of the cutting and fell on the line with a blundering crash that could have been
heard half a mile off. A cloud of dust rose up.</p></div>

<div class="bookPage" id="p129" name="129">
<p>&ldquo;Oh,&rdquo; said Peter, in awestruck tones, &ldquo;isn&rsquo;t it
exactly like when coals come in?&mdash;if there wasn&rsquo;t any roof to the
cellar and you could see down.&rdquo;</p>
<p>&ldquo;Look what a great mound it&rsquo;s made!&rdquo; said Bobbie.</p>
<p>&ldquo;Yes, it&rsquo;s right across the down line,&rdquo; said
Phyllis.</p>
<p>That&rsquo;ll take some sweeping up,&rdquo; said Bobbie.</p>
<p>&ldquo;Yes,&rdquo; said Peter, slowly. He was still leaning on the
fence. &ldquo;Yes,&rdquo; he said again, still more slowly.</p>
<p>Then he stood upright.</p>
<p>&ldquo;The 11.29 down hasn&rsquo;t gone by yet. We must let them know at
the station, or there&rsquo;ll be a most frightful accident.&rdquo;</p>
<p>&ldquo;Let&rsquo;s run,&rdquo; said Bobbie, and began.</p>
<p>But Peter cried, &ldquo;Come back!&rdquo; and looked at Mother&rsquo;s
watch. He was very prompt and businesslike, and his face looked whiter than
they had ever seen it.</p>
<p>&ldquo;No time,&rdquo; he said; &ldquo;it&rsquo;s two miles away, and
it&rsquo;s past eleven.&rdquo;</p>
<p>&ldquo;Couldn&rsquo;t we,&rdquo; suggested Phyllis, breathlessly, &ldquo;couldn&rsquo;t we climb up a telegraph post and do something to the
wires?&rdquo;</p></div>

<div class="bookPage" id="p130" name="130">
<p>&ldquo;We don&rsquo;t know how,&rdquo; said Peter.</p>
<p>&ldquo;They do it in war,&rdquo; said Phyllis; &ldquo;I know I&rsquo;ve
heard of it.&rdquo;</p>
<p>&ldquo;They only <em>cut</em> them, silly,&rdquo; said Peter,
&ldquo;and that doesn&rsquo;t do any good. And we couldn&rsquo;t cut them
even if we got up, and we couldn&rsquo;t get up. If we had anything red, we
could get down on the line and wave it.&rdquo;</p>
<p>&ldquo;But the train wouldn&rsquo;t see us till it got round the corner,
and then it could see the mound just as well as us,&rdquo; said Phyllis;
&ldquo;better, because it&rsquo;s much bigger than us.&rdquo;</p>
<p>&ldquo;If we only had something red,&rdquo; Peter repeated, &ldquo;we
could go round the corner and wave to the train.&rdquo;</p>
<p>&ldquo;We might wave, anyway.&rdquo;</p>
<p>&ldquo;They&rsquo;d only think it was just <em>us</em>, as usual.
We&rsquo;ve waved so often before. Anyway, let&rsquo;s get down.&rdquo;</p>
<p>They got down the steep stairs. Bobbie was pale and shivering.
Peter&rsquo;s face looked thinner than usual. Phyllis was red-faced and damp
with anxiety.</p>
<p>&ldquo;Oh, how hot I am!&rdquo; she said; &ldquo;and I thought it was going to be cold; I wish we hadn&rsquo;t put on our&mdash;&rdquo; she
stopped short, and then ended in quite a different tone&mdash;&ldquo;our
flannel petticoats.&rdquo;</p></div>

<div class="bookPage" id="p131" name="131">
<p>Bobbie turned at the bottom of the stairs.</p>
<p>&ldquo;Oh, yes,&rdquo; she cried; &ldquo;<em>they&rsquo;re</em> red!
Let&rsquo;s take them off.&rdquo;</p>
<p>They did, and with the petticoats rolled up under their arms, ran along
the railway, skirting the newly fallen mound of stones and rock and earth,
and bent, crushed, twisted trees. They ran at their best pace. Peter led,
but the girls were not far behind. They reached the corner that hid the
mound from the straight line of railway that ran half a mile without curve
or corner.</p>
<p>&ldquo;Now,&rdquo; said Peter, taking hold of the largest flannel
petticoat.</p>
<p>&ldquo;You&rsquo;re not&mdash;&rdquo; Phyllis
faltered&mdash;&ldquo;you&rsquo;re not going to tear them?&rdquo;</p>
<p>&ldquo;Shut up,&rdquo; said Peter, with brief sternness.</p>
<p>&ldquo;Oh, yes,&rdquo; said Bobbie, &ldquo;tear them into little bits if
you like. Don&rsquo;t you see, Phil, if we can&rsquo;t stop the train,
there&rsquo;ll be a real live accident, with people <em>killed</em>. Oh,
horrible! Here, Peter, you&rsquo;ll never tear it through the
band!&rdquo;</p>
<p>She took the red flannel petticoat from him and tore it off an inch from
the band. Then she tore the other in the same way.</p></div>

<div class="bookPage" id="p132" name="132">
<p>&ldquo;There!&rdquo; said Peter, tearing in his turn. He divided each
petticoat into three pieces. &ldquo;Now, we&rsquo;ve got six flags.&rdquo;
He looked at the watch again. &ldquo;And we&rsquo;ve got seven minutes. We
must have flag-staffs.&rdquo;</p>
<p>The knives given to boys are, for some odd reason, seldom of the kind of
steel that keeps sharp. The young saplings had to be broken off. Two came
up by the roots. The leaves were stripped from them.</p>
<p>&ldquo;We must cut holes in the flags, and run the sticks through the
holes,&rdquo; said Peter. And the holes were cut. The knife was sharp
enough to cut flannel with. Two of the flags were set up in heaps of loose
stones between the sleepers of the down line. Then Phyllis and Roberta took
each a flag, and stood ready to wave it as soon as the train came in
sight.</p>
<p>&ldquo;I shall have the other two myself,&rdquo; said Peter,
&ldquo;because it was my idea if we waved something red.&rdquo;</p>
<p>&ldquo;They&rsquo;re our petticoats, though,&rdquo; Phyllis was
beginning, but Bobbie interrupted&mdash;</p>
<p>&ldquo;Oh, what does it matter who waves what, if we can only save the
train?&rdquo;</p>
<p>Perhaps Peter had not rightly calculated the number of minutes it would take the 11.29 to get from the station to</p></div>

<div class="bookPage" id="p133" name="133">
<p>the place where they were, or perhaps the train was late. Anyway, it seemed a
very long time that they waited.</p>
<p>Phyllis grew impatient. &ldquo;I expect the watch is wrong, and the
train&rsquo;s gone by,&rdquo; said she.</p>
<p>Peter relaxed the heroic attitude he had chosen to shew off his two
flags with. And Bobbie began to feel sick with suspense.</p>
<p>It seemed to her that they had been standing there for hours and hours,
holding those silly little red flannel flags that no one would ever notice.
The train wouldn&rsquo;t care. It would go rushing by them and tear round
the corner and go crashing into that awful mound. And every one would be
killed. Her hands grew very cold and trembled so that she could hardly hold
the flag. And then came the distant rumble and hum of the metals, and a
puff of white steam showed far away along the stretch of line.</p>
<p>&ldquo;Stand firm,&rdquo; said Peter, &ldquo;and wave like mad! When it
gets to that big furze bush step back, but go on waving! Don&rsquo;t stand
<em>on</em> the line, Bobbie!&rdquo;</p>
<p>The train came rattling along very very fast.</p>
<p>&ldquo;They don&rsquo;t see us! They won&rsquo;t see us! It&rsquo;s all no
good!&rdquo; cried Bobbie.</p></div>

<div class="bookPage" id="p134" name="134">
<p>The two little flags on the line swayed as the nearing train shook and
loosened the heaps of loose stones that held them up. One of them slowly
leaned over and fell on the line. Bobbie jumped forward and caught it up,
and waved it; her hands did not tremble now.</p>
<p>&ldquo;Keep off the line, you silly cuckoo!&rdquo; said Peter,
fiercely.</p>
<p>It seemed that the train came on as fast as ever. It was very near
now.</p>
<p>&ldquo;It&rsquo;s no good,&rdquo; Bobbie said again.</p>
<p>&ldquo;Stand back!&rdquo; cried Peter, suddenly, and he dragged Phyllis
back by the arm.</p>
<p>But Bobbie cried, &ldquo;Not yet, not yet!&rdquo; and waved her two
flags right over the line. The front of the engine looked black and
enormous. <SIC CORR="Its">It&rsquo;s</SIC> voice was loud and harsh.</p>
<p>&ldquo;Oh, stop, stop, stop!&rdquo; cried Bobbie. No one heard her. At
least Peter and Phyllis didn&rsquo;t, for the oncoming rush of the train
covered the sound of her voice with a mountain of sound. But afterwards she
used to wonder whether the engine itself had not heard her. It seemed
almost as though it had&mdash;for it slackened swiftly, slackened and
stopped, not twenty yards from the place where Bobbie&rsquo;s two flags
waved over the line. She saw the great black engine stop dead, but somehow she could not stop </p></div>

<div class="bookPage" id="pf135" name="">
<img src="illustrations/railway/nesbit-railway135.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></div>

<div class="bookPage" id="p135" name="135">
<p>waving the flags.
And when the driver and the fireman had got off the engine and Peter and
Phyllis had gone to meet them and pour out their excited tale of the awful
mound just round the corner, Bobbie still waved the flags but more and more
feebly and jerkily.</p>
<p>When the others turned towards her she was lying across the line with
her hands flung forward and still gripping the sticks of the little red
flannel flags.</p>
<p>The engine-driver picked her up, carried her to the train, and laid her
on the cushions of a first-class carriage.</p>
<p>&ldquo;Gone right off in a faint,&rdquo; he said, &ldquo; poor little
woman. And no wonder. I&rsquo;ll just &rsquo;ave a look at this &rsquo;ere
mound of yours, and then we&rsquo;ll run you back to the station and get her
seen to.&rdquo;</p>
<p>It was horrible to see Bobbie lying so white and quiet, with her lips
blue, and parted.</p>
<p>&ldquo;I believe that&rsquo;s what people look like when they&rsquo;re
dead,&rdquo; whispered Phyllis.</p>
<p>&ldquo;<em>Don&rsquo;t</em>!&rdquo; said Peter, sharply.</p>
<p>They sat by Bobbie on the blue cushions, and the train ran back. Before
it reached their station Bobbie had sighed and opened her eyes, and rolled herself over and begun to cry.</p></div>

<div class="bookPage" id="p136" name="136">
<p>This cheered the others wonderfully.
They had seen her cry before, but they had never seen her faint, nor any
one else, for the matter of that. They had not known what to do when she
was fainting, but now she was only crying they could thump her on the back
and tell her not to, just as they always did. And presently, when she
stopped crying, they were able to laugh at her for being such a coward as
to faint.</p>
<p>When the station was reached, the three were the heroes of an agitated
meeting on the platform.</p>
<p>The praises they got for their &ldquo;prompt action,&rdquo; their
&ldquo;common sense,&rdquo; their &ldquo;ingenuity,&rdquo; were enough to
have turned anybody&rsquo;s head. Phyllis enjoyed herself thoroughly. She
had never been a real heroine before, and the feeling was delicious.
Peter&rsquo;s ears got very red. Yet he, too, enjoyed himself. Only Bobbie
wished they all wouldn&rsquo;t. She wanted to get away.</p>
<p>&ldquo;You&rsquo;ll hear from the Company about this, I expect,&rdquo;
said the Station Master.</p>
<p>Bobbie wished she might never hear of it again. She pulled at
Peter&rsquo;s jacket.</p>
<p>&ldquo;Oh, come away, come away! I want to go home,&rdquo; she said.</p></div>

