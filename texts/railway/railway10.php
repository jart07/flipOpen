<div class="bookPage" id="p211" name="211">
<p>So next day she set to work, and that happened to be the day when Mother
had praised her and the others about not quarrelling. She moved the
rose-bushes and carried them to the other end of the garden, where the
rubbish heap was that they meant to make a bonfire of when Guy Fawkes day
came.</p>
<p>Meanwhile Peter had decided to flatten out all his forts and earthworks,
with a view to making a model of the railway-tunnel, cutting, embankment,
canal, aqueduct, bridges, and all.</p>
<p>So when Bobbie came back from her last thorny journey with the dead
rose-bushes, he had got the rake and was using it busily.</p>
<p>&ldquo;<em>I</em> was using the rake,&rdquo; said Bobbie.</p>
<p>&ldquo;Well, I&rsquo;m using it now,&rdquo; said Peter.</p>
<p>&ldquo;But I had it first,&rdquo; said Bobbie.</p>
<p>&ldquo;Then it&rsquo;s my turn now,&rdquo; said Peter. And that was how
the quarrel began.</p>
<p>&ldquo;You&rsquo;re always being disagreeable about nothing,&rdquo; said
Peter, after some heated argument.</p>
<p>&ldquo;I had the rake first,&rdquo; said Bobbie, flushed and defiant,
holding on to its handle.</p></div>

<div class="bookPage" id="p212" name="212">
<p>&ldquo;Don&rsquo;t I tell you I said this morning I meant to have it.
Didn&rsquo;t I, Phil?&rdquo;</p>
<p>Phyllis said she didn&rsquo;t want to be mixed up in their rows. And
instantly, of course, she was.</p>
<p>&ldquo;If you remember you ought to say.&rdquo;</p>
<p>&ldquo;Of course she doesn&rsquo;t remember&mdash;but she might say
so.&rdquo;</p>
<p>&ldquo;I wish I&rsquo;d had a brother instead of two whiny little kiddy
sisters,&rdquo; said Peter. This was always recognised as indicating the
high-water mark of Peter&rsquo;s rage.</p>
<p>Bobbie made the reply she always made to it.</p>
<p>&ldquo;I can&rsquo;t think why little boys were ever invented,&rdquo; and
just as she said it she looked up, and saw the three long windows of
Mother&rsquo;s workshop flashing in the red rays of the sun. The sight
brought back those words of praise:&mdash;</p>
<p>&ldquo;You don&rsquo;t quarrel like you used to do.&rdquo;</p>
<p>&ldquo;<em>Oh</em>!&rdquo; cried Bobbie, just as if she had been
hit, or had caught her finger in a door, or had felt the hideous sharp
beginnings of toothache.</p>
<p>&ldquo;What&rsquo;s the matter?&rdquo; said Phyllis.</p></div>

<div class="bookPage" id="p213" name="213">
<p>Bobbie wanted to say: &ldquo;Don&rsquo;t let&rsquo;s quarrel. Mother hates
it so,&rdquo; but though she tried hard, she couldn&rsquo;t. Peter was
looking too disagreeable and insulting.</p>
<p>&ldquo;Take the horrid rake, then,&rdquo; was the best she could manage.
And she suddenly let go her hold on the handle. Peter had been holding on
to it too, firmly and pullingly, and now that the pull the other way was
suddenly stopped, he staggered and fell over backward, the teeth of the
rake between his feet.</p>
<p>&ldquo;Serve you right,&rdquo; said Bobbie, before she could stop
herself.</p>
<p>Peter lay still for half a moment&mdash;long enough to frighten Bobbie a
little. Then he frightened her a little more, for he sat up&mdash;screamed
once&mdash;turned rather pale, and then lay back and began to shriek,
faintly but steadily. It sounded exactly like a pig being killed a quarter
of a mile off.</p>
<p>Mother put her head out of the window, and it wasn&rsquo;t half a minute
after that she was in the garden kneeling by the side of Peter who never
for an instant ceased to squeal.</p>
<p>&ldquo;What happened, Bobbie?&rdquo; Mother asked.</p>
<p>&ldquo;It was the rake,&rdquo; said Phyllis. &ldquo;Peter was pulling at
it, so was Bobbie, and she let go and he went over.&rdquo;</p></div>

<div class="bookPage" id="p214" name="214">
<p>&ldquo;Stop that noise, Peter,&rdquo; said Mother. &ldquo;Come. Stop at
once.&rdquo;</p>
<p>Peter used up what breath he had left in a last squeal and stopped.</p>
<p>&ldquo;Now,&rdquo; said Mother, &ldquo;are you hurt?&rdquo;</p>
<p>&ldquo;If he was really hurt, he wouldn&rsquo;t make such a fuss,&rdquo;
said Bobbie, still trembling with fury; &ldquo;he&rsquo;s not a
coward!&rdquo;</p>
<p>&ldquo;I think my foot&rsquo;s broken off, that&rsquo;s all,&rdquo; said
Peter, huffily, and sat up. Then he turned quite white. Mother put her arm
round him.</p>
<p>&ldquo;He <em>is</em> hurt,&rdquo; she said; &ldquo;he&rsquo;s
fainted. Here, Bobbie, sit down and take his head on your lap.&rdquo;</p>
<p>Then Mother undid Peter&rsquo;s boots. As she took the right one off
something dripped from his foot on to the ground. It was red blood. And
when the stocking came off there were three red wounds in Peter&rsquo;s foot
and ankle, where the teeth of the rake had bitten him, and his foot was
covered with red smears.</p>
<p>&ldquo;Run for water&mdash;a basinful,&rdquo; said Mother, and Phyllis
ran. She upset most of the water out of the basin in her haste, and had to fetch more in a jug.</p></div>

<div class="bookPage" id="p215" name="215">
<p>Peter did not open his eyes again till Mother had tied her handkerchief
round his foot, and she and Bobbie had carried him in and laid him on the
brown wooden settle in the dining room. By this time Phyllis was halfway to
the Doctor&rsquo;s.</p>
<p>Mother sat by Peter and bathed his foot and talked to him, and Bobbie
went out and got tea ready, and put in the kettle.</p>
<p>&ldquo;It&rsquo;s all I can do,&rdquo; she told herself. &ldquo;Oh,
suppose Peter should die, or be a helpless cripple for life, or have to
walk with crutches, or wear a boot with a sole like a log of
wood!&rdquo;</p>
<p>She stood by the back door reflecting on these gloomy possibilities, her
eyes fixed on the water-butt.</p>
<p>&ldquo;I wish I&rsquo;d never been born,&rdquo; she said, and she said it
out loud.</p>
<p>&ldquo;Why, lawk a mercy, what&rsquo;s that for?&rdquo; asked a voice,
and Perks stood before her with a wooden trug basket full of green-leaved
things and soft, loose earth.</p>
<p>&ldquo;Oh, it&rsquo;s you,&rdquo; she said. &ldquo;Peter&rsquo;s hurt his
foot with a rake&mdash;three great gaping wounds, like soldiers get. And it
was partly my fault.&rdquo;</p></div>

<div class="bookPage" id="p216" name="216">
<p>&ldquo;That it wasn&rsquo;t, I&rsquo;ll go bail,&rdquo; said Perks.
&ldquo;Doctor seen him?&rdquo;</p>
<p>&ldquo;Phyllis has gone for the Doctor.&rdquo;</p>
<p>&ldquo;He&rsquo;ll be all right; you see if he isn&rsquo;t,&rdquo; said
Perks. &ldquo;Why, my father&rsquo;s second cousin had a hay-fork run into
him, right into his inside, and he was right as ever in a few weeks, all
except his being a bit weak in the head afterwards, and they did say that
it was along of his getting a touch of the sun in the hay-field, and not
the fork at all. I remember him well. A kind-&rsquo;earted chap, but soft,
as you might say.&rdquo;</p>
<p>Bobbie tried to let herself be cheered by this heartening
reminiscence.</p>
<p>&ldquo;Well,&rdquo; said Perks, &ldquo;you won&rsquo;t want to be
bothered with gardening just this minute, I dare say. You shew me where
your garden is, and I&rsquo;ll pop the bits of stuff in for you. And
I&rsquo;ll hang about, if I may make so free, to see the Doctor as he comes
out and hear what he says. You cheer up, Missie. I lay a pound he
ain&rsquo;t hurt, not to speak of.&rdquo;</p>
<p>But he was. The Doctor came and looked at the foot and bandaged it
beautifully, and said that Peter must not put it to the ground for at least
a week.</p></div>

<div class="bookPage" id="p217" name="217">
<p>&ldquo;He won&rsquo;t be lame, or have to wear crutches or a lump on his
foot, will he?&rdquo; whispered Bobbie, breathlessly, at the door.</p>
<p>&ldquo;My aunt! no!&rdquo; said Dr. Forrest; &ldquo;he&rsquo;ll be as
nimble as ever on his pins in a fortnight. Don&rsquo;t you worry, little
Mother Goose.&rdquo;</p>
<p>It was when Mother had gone to the gate with the Doctor to take his last
instructions and Phyllis was filling the kettle for tea, that Peter and
Bobbie found themselves alone.</p>
<p>&ldquo;He says you won&rsquo;t be lame or anything,&rdquo; said
Bobbie.</p>
<p>&ldquo;Oh, course I shan&rsquo;t, silly,&rdquo; said Peter, very much
relieved all the same.</p>
<p>&ldquo;Oh, Peter, I <em>am</em> so sorry,&rdquo; said Bobbie, after
a pause.</p>
<p>&ldquo;That&rsquo;s all right,&rdquo; said Peter, gruffly.</p>
<p>&ldquo;It was <em>all</em> my fault,&rdquo; said Bobbie.</p>
<p>&ldquo;Rot,&rdquo; said Peter.</p>
<p>&ldquo;If we hadn&rsquo;t quarrelled, it wouldn&rsquo;t have happened. I
knew it was wrong to quarrel. I wanted to say so, but somehow I
couldn&rsquo;t.&rdquo;</p></div>

<div class="bookPage" id="p218" name="218">
<p>&ldquo;Don&rsquo;t drivel,&rdquo; said Peter. &ldquo;I shouldn&rsquo;t
have stopped if you &rdquo;had said it. Not likely. And besides, us rowing
hadn&rsquo;t anything to do with it. I might have caught my foot in the hoe,
or taken off my fingers in the chaff-cutting machine or blown my nose off with
fireworks. It would have been hurt just the same whether we&rsquo;d been
rowing or not.&rdquo;</p>
<p>&ldquo;But I knew it was wrong to quarrel,&rdquo; said Bobbie, in tears,
&ldquo;and now you&rsquo;re hurt and&mdash;&rdquo;</p>
<p>&ldquo;Now look here,&rdquo; said Peter, firmly, &ldquo;you just dry up.
If you&rsquo;re not careful, you&rsquo;ll turn into a beastly little
Sunday-school prig, so I tell you.&rdquo;</p>
<p>&ldquo;I don&rsquo;t mean to be a prig. But it&rsquo;s so hard not to be,
when you&rsquo;re really trying to be good.&rdquo;</p>
<p>(The Gentle Reader may perhaps have suffered from this difficulty.)</p>
<p>&ldquo;Not it,&rdquo; said Peter; &ldquo;it&rsquo;s a jolly good thing it
wasn&rsquo;t you was hurt. I&rsquo;m glad it was me. There! If it had been
you, you&rsquo;d have been lying on the sofa looking like a suffering angel
and being the light of the anxious household and all that. And I
couldn&rsquo;t have stood it.&rdquo;</p>
<p>&ldquo;No, I shouldn&rsquo;t,&rdquo; said Bobbie.</p>
<p>&ldquo;Yes, you would,&rdquo; said Peter.</p></div>

<div class="bookPage" id="p219" name="219">
<p>&ldquo;I tell you I shouldn&rsquo;t.&rdquo;</p>
<p>&ldquo;I tell you you would.&rdquo;</p>
<p>&ldquo;Oh, children,&rdquo; said Mother&rsquo;s voice at the door.
&ldquo;Quarrelling again? Already?&rdquo;</p>
<p>&ldquo;We aren&rsquo;t quarrelling&mdash;not really,&rdquo; said Peter. &ldquo;I wish you wouldn&rsquo;t think it&rsquo;s rows every time we
don&rsquo;t agree!&rdquo; When Mother had gone out again, Bobbie broke
out:&mdash;</p>
<p>&ldquo;Peter, I <em>am</em> sorry you&rsquo;re hurt. But you are a
beast to say I&rsquo;m a prig.&rdquo;</p>
<p>&ldquo;Well,&rdquo; said Peter, unexpectedly, &ldquo;perhaps I am. You
did say I wasn&rsquo;t a coward, even when you were in such a wax. The only
thing is&mdash;don&rsquo;t you be a prig, that&rsquo;s all. You keep your
eyes open and if you feel prigginess coming on just stop it in time.
See?&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Bobbie, &ldquo;I see.&rdquo;</p>
<p>&ldquo;Then let&rsquo;s call it Pax,&rdquo; said Peter, magnanimously:
&ldquo;bury the hatchet in the fathoms of the past. Shake hands on it. I
say, Bobbie, old chap, I am tired.&rdquo;</p>
<p>He was tired for many days after that, and the settle seemed hard and
uncomfortable in spite of all the pillows and bolsters and soft folded
rugs. It was terrible not to be able to go out. They moved the settle to
the window, and from there Peter could see the smoke of the trains winding
along the valley. But he could not see the trains.</p></div>

<div class="bookPage" id="p220" name="220">
<p>At first Bobbie found it quite hard to be as nice to him as she wanted
to be, for fear he should think her priggish. But that soon wore off, and both she and Phyllis were, as he observed, jolly good sorts. Mother sat
with him when his sisters were out. And the words, &ldquo;he&rsquo;s not a
coward,&rdquo; made Peter determined not to make any fuss about the pain in
his foot, though it was rather bad, especially at night.</p>
<p>Praise helps people very much, sometimes.</p>
<p>There were visitors, too. Mrs. Perks came up to ask how he was, and so
did the Station Master, and several of the village people. But the time
went slowly, slowly.</p>
<p>&ldquo;I do wish there was something to read,&rdquo; said Peter.
&ldquo;I&rsquo;ve read all our books fifty times over.&rdquo;</p>
<p>&ldquo;I&rsquo;ll go to the Doctor&rsquo;s,&rdquo; said Phyllis;
&ldquo;he&rsquo;s sure to have some.&rdquo;</p>
<p>&ldquo;Only about how to be ill, and about people&rsquo;s nasty insides,
I expect,&rdquo; said Peter.</p>
<p>&ldquo;Perks has a whole heap of Magazines that come out of trains when
people are tired of them,&rdquo; said Bobbie. &ldquo;I&rsquo;ll run down and
ask him.&rdquo;</p>
<p>So the girls went their two ways.</p>
<p>Bobbie found Perks busy cleaning lamps.</p></div>

<div class="bookPage" id="p221" name="221">
<p>&ldquo;And how&rsquo;s the young gent?&rdquo; said he.</p>
<p>&ldquo;Better, thanks,&rdquo; said Bobbie, &ldquo;but he&rsquo;s most
frightfully bored. I came to ask if you&rsquo;d got any Magazines you could
lend him.&rdquo;</p>
<p>&ldquo;There, now,&rdquo; said Perks, regretfully, rubbing his ear with
a black and oily lump of cotton waste, &ldquo;why didn&rsquo;t I think of
that, now? I was trying to think of something as &rsquo;ud amuse him only
this morning, and I couldn&rsquo;t think of anything better than a
guinea-pig. And a young chap I know&rsquo;s going to fetch that over for him
this tea-time.&rdquo;</p>
<p>&ldquo;How lovely! A real live guinea! He will be pleased. But he&rsquo;d
like the Magazines as well.&rdquo;</p>
<p>&ldquo;That&rsquo;s just it,&rdquo; said Perks. &ldquo;I&rsquo;ve just
sent the pick of &rsquo;em to Snigson&rsquo;s boy&mdash;him what&rsquo;s just
getting over the pewmonia. But I&rsquo;ve lots of illustrated papers
left.&rdquo;</p>
<p>He turned to the pile of papers in the corner and took up a heap six
inches thick.</p>
<p>&ldquo;There!&rdquo; he said. &ldquo;I&rsquo;ll just slip a bit of string
and a bit of paper round &rsquo;em.&rdquo;</p>
<p>He pulled an old newspaper from the pile and spread it on the table, and
made a neat parcel of it.</p>
<p>&ldquo;There,&rdquo; said he, &ldquo;there&rsquo;s lots of pictures, and
if he likes to mess &rsquo;em about with his paint-box, or coloured chalks
or what not, why, let him. <em>I</em> don&rsquo;t want
&rsquo;em.&rdquo;</p></div>

<div class="bookPage" id="p222" name="222">
<p>&ldquo;You&rsquo;re a dear,&rdquo; said Bobbie, took the parcel, and
started. The papers were heavy, and when she had to wait at the level crossing while a train went by, she
