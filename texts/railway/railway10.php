<h2>CHAPTER X<br />
THE TERRIBLE SECRET</h2>
<p><strong>WHEN</strong> they first went to live at Three
Chimneys, the children had talked a great deal about their Father, and had
asked a great many questions about him, and what he was doing and where he
was and when he would come home. Mother always answered their questions as
well as she could. But as the time went on they grew to speak less of him.
Bobbie had felt almost from the first that for some strange miserable
reason these questions hurt Mother and made her sad. And little by little
the others came to have this feeling, too, though they could not have put
it into words.</p>
<p>One day when Mother was working so hard that she could not leave off
even for ten minutes, Bobbie carried up her tea to the big bare room that
they called Mother&rsquo;s workshop. It had hardly any furniture. Just a
table and a chair and a rug. But always big pots of flowers on the window-sills and on the mantelpiece. The children saw to that. And from
the three long uncurtained windows the beautiful stretch of meadow and
moorland, the far violet of the hills, and the unchanging changefulness of
cloud and sky.</p></div>

<div class="bookPage" id="p205" name="205">

<p>&ldquo;Here&rsquo;s your tea, Mother-love,&rdquo; said Bobbie; &ldquo;do
drink it while it&rsquo;s hot.&rdquo;</p>
<p>Mother laid down her pen among the pages that were scattered all over
the table, pages covered with her writing, which was almost as plain as
print, and much prettier. She ran her hands into her hair, as if she were
going to pull it out by handfuls.</p>
<p>&ldquo;Poor dear head,&rdquo; said Bobbie, &ldquo;does it
ache?&rdquo;</p>
<p>&ldquo;No&mdash;yes&mdash;not much,&rdquo; said Mother. &ldquo;Bobbie,
do you think Peter and Phil are <em>forgetting</em> Father?&rdquo;</p>
<p>&ldquo;No,&rdquo; said Bobbie, indignantly. &ldquo;Why?&rdquo;</p>
<p>&ldquo;You none of you ever speak of him now.&rdquo;</p>
<p>Bobbie stood first on one leg and then on the other.</p>
<p>&ldquo;We often talk about him when we&rsquo;re by ourselves,&rdquo; she
said.</p>
<p>&ldquo;But not to me,&rdquo; said Mother. &ldquo;Why?&rdquo;</p>
<p>Bobbie did not find it easy to say why.</p>
<p>&ldquo;I&mdash;you&mdash;&rdquo; she said, and stopped. She went over to
the window and looked out.</p>
<p>&ldquo;Bobbie, come here,&rdquo; said her Mother, and Bobbie came.</p></div>

<div class="bookPage" id="p206" name="206">

<p>&ldquo;Now,&rdquo; said Mother, putting her arm round Bobbie and laying
her ruffled head against Bobbie&rsquo;s shoulder, &ldquo;try to tell me,
dear.&rdquo;</p>
<p>Bobbie fidgeted.</p>
<p>&ldquo;Tell Mother.&rdquo;</p>
<p>&ldquo;Well, then,&rdquo; said Bobbie, &ldquo;I thought you were so
unhappy about Daddy not being here, it made you worse when I talked about
him. So I stopped doing it.&rdquo;</p>
<p>&ldquo;And the others?&rdquo;</p>
<p>&ldquo;I don&rsquo;t know about the others,&rdquo; said Bobbie. &ldquo;I
never said anything about <em>that</em> to them. But I expect they felt
the same about it as me.&rdquo;</p>
<p>&ldquo;Bobbie dear,&rdquo; said Mother, still leaning her head against
her, &ldquo;I&rsquo;ll tell you. Besides parting from Father, he and I have
had a great sorrow,&mdash;oh, terrible&mdash;worse than anything you can
think of, and at first it did hurt to hear you all talking of him as if
everything were just the same. But it would be much more terrible if you
were to forget him. That would be worse than anything.&rdquo;</p>
<p>&ldquo;The trouble,&rdquo; said Bobbie, in a very little voice,&mdash;&ldquo;I promised I would never ask you any questions, and I
never have, have I? But&mdash;the trouble&mdash;it won&rsquo;t last
always?&rdquo;</p></div>

<div class="bookPage" id="p207" name="207">
<p>&ldquo;No,&rdquo; said Mother, &ldquo;the worst will be over when Father
comes home to us.&rdquo;</p>
<p>&ldquo;I wish I could comfort you,&rdquo; said Bobbie.</p>
<p>&ldquo;Oh, my dear, do you suppose you don&rsquo;t? What should I do
without you&mdash;you and the others? Do you think I haven&rsquo;t noticed
how good you&rsquo;ve all been, not quarrelling nearly as much as you used
to&mdash;and all the little kind things you do for me&mdash;the flowers,
and cleaning my shoes, and tearing up to make my bed before I get time to
do it myself?&rdquo;</p>
<p>Bobbie had sometimes wondered whether Mother noticed these things.</p>
<p>&ldquo;That&rsquo;s nothing,&rdquo; she said, &ldquo;to
what&mdash;&rdquo;</p>
<p>&ldquo;I <em>must</em> get on with my work,&rdquo; said Mother,
giving Bobbie one last squeeze. &ldquo;Don&rsquo;t say anything to the
others.&rdquo;</p>
<p>That evening in the hour before bed-time instead of reading to the
children Mother told them stories of the games she and Father used to have
when they were children and lived near each other in the
country&mdash;tales of the adventures of Father with Mother&rsquo;s brothers
when they were all boys together. Very funny stories they were, and the children laughed as they
listened.</p></div>

<div class="bookPage" id="p208" name="208">
<p>&ldquo;Uncle Edward died before he was grown up, didn&rsquo;t he?&rdquo;
said Phyllis, as Mother lighted the bedroom candles.</p>
<p>&ldquo;Yes, dear,&rdquo; said Mother, &ldquo;you would have loved him.
He was such a brave boy, and so adventurous. Always in mischief, and yet
friends with everybody in spite of it. And your Uncle Reggie&rsquo;s in
Ceylon&mdash;yes, and Father&rsquo;s away, too. But I think they&rsquo;d all
like to think we&rsquo;d enjoyed talking about the things they used to do.
Don&rsquo;t you think so?&rdquo;</p>
<p>&ldquo;Not Uncle Edward,&rdquo; said Phyllis, in a shocked tone;
&ldquo;he&rsquo;s in Heaven.&rdquo;</p>
<p>&ldquo;You don&rsquo;t suppose he&rsquo;s forgotten us and all the old
times, because God has taken him, any more than I forget him. Oh, no, he
remembers. He&rsquo;s only away for a little time. We shall see him some
day.&rdquo;</p>
<p>&ldquo;And Uncle Reggie&mdash;and Father, too?&rdquo; said Peter.</p>
<p>&ldquo;Yes,&rdquo; said Mother. &ldquo;Uncle Reggie and Father, too.
Good night, my darlings.&rdquo;</p>
<p>&ldquo;Good night,&rdquo; said every one. Bobbie hugged her mother more
closely even than usual, and whispered in her ear, &ldquo;Oh, I do love you so, Mummy&mdash;I do&mdash;I
do&mdash;&rdquo;</p></div>

<div class="bookPage" id="p209" name="209">
<p>When Bobbie came to think it all over, she tried not to wonder what the
great trouble was. But she could not always help it. Father was not
dead&mdash;like poor Uncle Edward&mdash;Mother had said so. And he was not
ill, or Mother would have been with him. Being poor wasn&rsquo;t the
trouble. Bobbie knew it was something nearer the heart than money could
be.</p>
<p>&ldquo;I mustn&rsquo;t try to think what it is,&rdquo; she told herself;
&ldquo;no, I mustn&rsquo;t. I <em>am</em> glad Mother noticed about us
not quarrelling so much. We&rsquo;ll keep that up.&rdquo;</p>
<p>And alas, that very afternoon she and Peter had what Peter called a
first-class shindy.</p>
<p>They had not been a week at Three Chimneys before they had asked Mother
to let them have a piece of garden each for their very own, and she had
agreed, and the south border under the peach trees had been divided into
three pieces and they were allowed to plant whatever they liked there.</p>
<p>Phyllis had planted mignonette and nasturtium and Virginia Stock in
hers. The seeds came up and though they looked just like weeds, Phyllis believed that they would bear flowers some day. The Virginia Stock
justified her faith quite soon, and her garden was gay with a band of
bright little flowers, pink and white and red and mauve.</p></div>

<div class="bookPage" id="p210" name="210">
<p>&ldquo;I can&rsquo;t weed for fear I pull up the wrong things,&rdquo; she
used to say comfortably; &ldquo;it saves such a lot of work.&rdquo;</p>
<p>Peter sowed vegetable seeds in his,&mdash;carrots and onions and
turnips. The seed was given to him by the farmer who lived in the nice
black-and-white, wood-and-plaster house just beyond the bridge. He kept
turkeys and guinea fowls, and was a most amiable man. But Peter&rsquo;s
vegetables never had much of a chance, because he liked to use the earth of
his garden for digging canals, and making forts and earthworks for his toy
soldiers. And the seeds of vegetables rarely come to much in a soil that is
constantly disturbed for the purposes of war and irrigation.</p>
<p>Bobbie planted rose-bushes in her garden, but all the little new leaves
of the rose-bushes shrivelled and withered, perhaps because she moved them
from the other part of the garden in May, which is not at all the right
time of year for moving roses. But she would not own that they were dead,
and hoped on against hope, until the day when Perks came up to see the garden, and told her quite plainly that all her roses were as dead as door nails.</p>
<p>&ldquo;Only good for bonfires, Miss,&rdquo; he said. &ldquo;You just dig
&rsquo;em up and burn &rsquo;em, and I&rsquo;ll give you some nice fresh roots
outer my garden; pansies, and stocks, and sweet willies, and
forget-me-nots. I&rsquo;ll bring &rsquo;em along to-morrow if you get the
ground ready.&rdquo;</p></div>

<div class="bookPage" id="pf210" name="">
