<p>&ldquo;Not at present,&rdquo; said the old gentleman. &ldquo;Your Mother
has most kindly consented to let him stay here. I thought of sending a
nurse, but your Mother is good enough to say that she will nurse him
herself.&rdquo;</p>
<p>&ldquo;But what about her writing?&rdquo; said Peter, before any one
could stop him. &ldquo;There won&rsquo;t be anything for him to eat if
Mother doesn&rsquo;t write.&rdquo;</p>
<p>&ldquo;That&rsquo;s all right,&rdquo; said Mother, hastily.</p>
<p>The old gentleman looked very kindly at Mother.</p>
<p>&ldquo;I see,&rdquo; he said, &ldquo;you trust your children, and
confide in them.&rdquo;</p>
<p>&ldquo;Of course,&rdquo; said Mother.</p>
