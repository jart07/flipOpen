<p>The old gentleman rose.</p>
<p>&ldquo;I&rsquo;m so glad,&rdquo; said Peter, &ldquo;that you&rsquo;re
going to keep him, Mother.&rdquo;</p>
<p>&ldquo;Take care of your Mother, my dears,&rdquo; said the old
