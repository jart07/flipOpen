politeness&mdash;much less to practise it.</p>
<p>&ldquo;It&rsquo;s our own old gentleman!&rdquo; said Phyllis.</p>
<p>&ldquo;Oh, it&rsquo;s you!&rdquo; said Bobbie. And then they remembered
themselves and their manners and said, &ldquo;How do you do?&rdquo; very
