<h2>CHAPTER XIII<br />
THE HOUND&rsquo;S GRANDFATHER</h2>
<p><strong>MOTHER</strong> did not get back to her writing all
that day, for the red-jerseyed hound whom the children had brought to Three
Chimneys had to be put to bed. And then the Doctor came, and hurt him most
horribly. Mother was with him all through it, and that made it a little
better than it would have been, but &ldquo;bad was the best,&rdquo; as Mrs.
Viney said.</p>
<p>The children sat in the parlour downstairs and heard the sound of the
Doctor&rsquo;s boots going backwards and forwards over the bedroom floor.
And once or twice there was a groan.</p>
<p>&ldquo;It&rsquo;s horrible,&rdquo; said Bobbie. &ldquo;Oh, I wish Dr.
Forrest would make haste. Oh, poor Jim!&rdquo;</p>
<p>&ldquo;It <em>is</em> horrible,&rdquo; said Peter, &ldquo;but
it&rsquo;s very exciting. I wish Doctors weren&rsquo;t so stuck-up about who
they&rsquo;ll have in the room when they&rsquo;re doing things. I should most
awfully like to see a leg set. I believe the bones crunch like
anything.&rdquo;</p>
<p>&ldquo;Don&rsquo;t!&rdquo; said the two girls at once.</p></div>

<div class="bookPage" id="p269" name="269">
<p>&ldquo;Rubbish!&rdquo; said Peter. &ldquo;How are you going to be Red
Cross Nurses, like you were talking of coming home, if you can&rsquo;t even
stand hearing me say about bones crunching? You&rsquo;d have to hear them
crunch on the field of battle&mdash;and be steeped in gore up to the elbows
as likely as not, and&mdash;&rdquo;</p>
<p>&ldquo;Stop it!&rdquo; cried Bobbie, with a white face; &ldquo;you
don&rsquo;t know how funny you&rsquo;re making me feel.&rdquo;</p>
<p>&ldquo;Me, too,&rdquo; said Phyllis, whose face was pink.</p>
<p>&ldquo;Cowards!&rdquo; said Peter.</p>
<p>&ldquo;I&rsquo;m not,&rdquo; said Bobbie. &ldquo;I helped Mother with
your rake-wounded foot, and so did Phil&mdash;you know we did.&rdquo;</p>
<p>&ldquo;Well, then!&rdquo; said Peter. &ldquo;Now look here. It would be
a jolly good thing for you if I were to talk to you every day for half an
hour about broken bones and people&rsquo;s insides, so as to get you used to
it.&rdquo;</p>
<p>A chair was moved above.</p>
<p>&ldquo;Listen,&rdquo; said Peter, &ldquo;that&rsquo;s the bone
crunching.&rdquo;</p>
<p>&ldquo;I do wish you wouldn&rsquo;t,&rdquo; said Phyllis. &ldquo;Bobbie
doesn&rsquo;t like it.&rdquo;</p>
<p>&ldquo;I&rsquo;ll tell you what they do,&rdquo; said Peter. I can&rsquo;t
think what made him so horrid. Perhaps it was because he had been</p></div>

<div class="bookPage" id="p270" name="270">
<p>so very nice and kind all the earlier part of
the day, and now he had to have a change. This is called reaction. One
notices it now and then in one&rsquo;s self. Sometimes when one has been
extra good for a longer time than usual, one is suddenly attacked by a
violent fit of not being good at all. &ldquo;I&rsquo;ll tell you what they
do,&rdquo; said Peter; &ldquo;they strap the broken man down so that he
can&rsquo;t resist or interfere with their doctorish designs, and then some
one holds his head, and some one holds his leg-the broken one, and pulls it
till the bones fit in&mdash;with a crunch, mind you! Then they strap it up
and&mdash;let&rsquo;s play at bone-setting!&rdquo;</p>
<p>&ldquo;Oh, no!&rdquo; said Phyllis.</p>
<p>But Bobbie said suddenly: &ldquo;All right&mdash;let&rsquo;s! I&rsquo;ll
be the doctor, and Phil can be the nurse. You can be the broken boner; we
can get at your legs more easily, because you don&rsquo;t wear
petticoats.&rdquo;</p>
<p>&ldquo;I&rsquo;ll get the splints and bandages,&rdquo; said Peter;
&ldquo;you get the couch of suffering ready.&rdquo;</p>
<p>The ropes that had tied up the boxes that had come from home were all in
a wooden packing-case in the cellar. When Peter brought in a trailing
tangle of them, and two boards for splints, Phyllis was excitedly
giggling.</p>
<p>&ldquo;Now, then,&rdquo; he said, and lay down on the settle, groaning
most grievously.</p></div>

<div class="bookPage" id="p271" name="271">
<p>&ldquo;Not so loud!&rdquo; said Bobbie, beginning to wind the ropes
round him and the settle. &ldquo;You pull, Phil.&rdquo;</p>
<p>&ldquo;Not so tight,&rdquo; moaned Peter. &ldquo;You&rsquo;ll break my
other leg.&rdquo;</p>
<p>Bobbie worked on in silence, winding more and more rope round him.</p>
<p>&ldquo;That&rsquo;s enough,&rdquo; said Peter. &ldquo;I can&rsquo;t move
at all. Oh, my poor leg!&rdquo; He groaned again.</p>
<p>&ldquo;Sure you can&rsquo;t move?&rdquo; asked Bobbie, in a rather
strange tone.</p>
<p>&ldquo;Quite sure,&rdquo; replied Peter. &ldquo;Shall we play it&rsquo;s
bleeding freely or not?&rdquo; he asked cheerfully.</p>
<p>&ldquo;<em>You</em> can play what you like,&rdquo; said Bobbie,
sternly, folding her arms and looking down at him where he lay all wound
round and round with cord. &ldquo;Phil and I are going away. And we
shan&rsquo;t untie you till you promise never never to talk to us about
blood and wounds unless we say you may. Come, Phil!&rdquo;</p>
<p>&ldquo;You beast!&rdquo; said Peter, writhing. &ldquo;I&rsquo;ll never
promise, never. I&rsquo;ll yell, and Mother will come.&rdquo;</p>
<p>&ldquo;Do,&rdquo; said Bobbie, &ldquo;and tell her why we tied you up!
Come on, Phil. No, I&rsquo;m not a beast, Peter. But you wouldn&rsquo;t stop when we asked you and&mdash;&rdquo;</p></div>

<div class="bookPage" id="p272" name="272">
<p>&ldquo;Yah,&rdquo; said Peter, &ldquo;it wasn&rsquo;t even your own idea.
You got it out of Stalky!&rdquo;</p>
<p>Bobbie and Phil, retiring in silent dignity, were met at the door by the
Doctor. He came in rubbing his hands and looking pleased with himself.</p>
<p>&ldquo;Well,&rdquo; he said, &ldquo;<em>that</em> job&rsquo;s done.
It&rsquo;s a nice clean fracture, and it&rsquo;ll go on all right, I&rsquo;ve
no doubt. Plucky young chap, too&mdash;hullo! what&rsquo;s all
this?&rdquo;</p>
<p>His eye had fallen on Peter who lay mousy-still in his bonds on the
settle.</p>
<p>&ldquo;Playing at prisoners, eh?&rdquo; he said; but his eyebrows had
gone up a little. Somehow he had not thought that Bobbie would be playing
while in the room above some one was having a broken bone set.</p>
<p>&ldquo;Oh, no!&rdquo; said Bobbie, &ldquo;not at <em>prisoners</em>.
We were playing at setting bones. Peter&rsquo;s the broken boner, and I was
the doctor.&rdquo;</p>
<p>&ldquo;I was the nurse,&rdquo; put in Phyllis, cheerfully.</p>
<p>The Doctor frowned.</p>
<p>&ldquo;Then I must say,&rdquo; he said, and he said it rather sternly,
&ldquo;that it&rsquo;s a very heartless game. Haven&rsquo;t you enough imagination even to faintly picture what&rsquo;s been going on</p></div>

<div class="bookPage" id="p273" name="273">
<p>upstairs? That poor chap, with the drops of sweat on his
forehead, and biting his lips so as not to cry out, and every touch on his
leg agony and&mdash;&rdquo;</p>
<p>&ldquo;You ought to be tied up,&rdquo; said Phyllis; &ldquo;you&rsquo;re
as bad as&mdash;&rdquo;</p>
<p>&ldquo;Hush,&rdquo; said Bobbie; &ldquo;I&rsquo;m sorry, but we
weren&rsquo;t heartless, really.&rdquo;</p>
<p>&ldquo;I was, I suppose,&rdquo; said Peter, crossly. &ldquo;All right,
Bobbie, don&rsquo;t you go on being noble and screening me, because I jolly
well won&rsquo;t have it. It was only that I kept on talking about blood and
wounds. I wanted to train them for Red Cross Nurses. And I wouldn&rsquo;t
stop when they asked me.&rdquo;</p>
<p>&ldquo;Well,&rdquo; said Dr. Forrest, sitting down.</p>
<p>&ldquo;Well&mdash;then I said, &lsquo;Let&rsquo;s play at setting
bones.&rsquo; It was all rot. I knew Bobbie wouldn&rsquo;t. I only said it
to tease her. And then when she said &lsquo;yes,&rsquo; of course I had to
go through with it. And they tied me up. They got it out of Stalky. And I
think it&rsquo;s a beastly shame.&rdquo;</p>
<p>He managed to writhe over and hide his face against the wooden back of
the settle.</p>
<p>&ldquo;I didn&rsquo;t think that any one would know but us,&rdquo; said Bobbie, indignantly answering Peter&rsquo;s unspoken
reproach. &ldquo;I never thought of your coming in. And</p></div>

<div class="bookPage" id="p274" name="274">
<p>hearing about blood
and wounds does really make me feel most awfully funny. It was only a joke
our tying him up. Let me untie you, Pete.&rdquo;</p>
<p>&ldquo;I don&rsquo;t care if you never untie me,&rdquo; said Peter;
&ldquo;and if that&rsquo;s your idea of a joke&mdash;&rdquo;</p>
<p>&ldquo;If I were you,&rdquo; said the Doctor, though really he did not
quite know what to say, &ldquo;I should be untied before your Mother comes
down. You don&rsquo;t want to worry her, just now, do you?&rdquo;</p>
<p>&ldquo;I don&rsquo;t promise anything about not saying about wounds,
mind,&rdquo; said Peter, in very surly tones, as Bobbie and Phyllis began
to untie the knots.</p>
<p>&ldquo;I&rsquo;m very sorry, Pete,&rdquo; Bobbie whispered, leaning close
to him as she fumbled with the big knot under the settle; &ldquo;but if you
only knew how sick you made me feel.&rdquo;</p>
<p>&ldquo;You&rsquo;ve made <em>me</em> feel pretty sick, I can tell
you,&rdquo; Peter rejoined. Then he shook off the loose cords, and stood
up.</p>
<p>&ldquo;I looked in,&rdquo; said Dr. Forrest, &ldquo;to see if one of you
would come along to the surgery. There are some things that your Mother
will want at once, and I&rsquo;ve given my man a day off to go and see the circus; will you
come, Peter?&rdquo;</p>
<p>Peter went without a word or a look to his sisters.</p></div>

<div class="bookPage" id="p275" name="275">
<p>The two walked in silence up to the gate that led from the Three
Chimneys field to the road. Then Peter said:&mdash;</p>
<p>&ldquo;Let me carry your bag. I say, it is heavy&mdash;what&rsquo;s in
it?&rdquo;</p>
<p>&ldquo;Oh, knives and lancets and different instruments for hurting
people. And the ether bottle. I had to give him ether, you know&mdash;the
agony was so intense.&rdquo;</p>
<p>Peter was silent.</p>
<p>&ldquo;Tell me all about how you found that chap,&rdquo; said Dr.
Forrest.</p>
<p>Peter told. And then Dr. Forrest told him stories of brave rescues; he
was a most interesting man to talk to, as Peter had often remarked.</p>
<p>Then in the surgery Peter had a better chance than he had ever had of
examining the Doctor&rsquo;s balance, and his microscope, and his scales and
measuring glasses. When all the things were ready that Peter was to take
back, the Doctor said suddenly:&mdash;</p>
<p>&ldquo;You&rsquo;ll excuse my shoving my oar in, won&rsquo;t you? But I
should like to say something to you.&rdquo;</p>
<p>&ldquo;Now for a rowing,&rdquo; thought Peter, who had been wondering
how it was that he had escaped one.</p>
<p>&ldquo;Something scientific,&rdquo; added the Doctor.</p></div>

<div class="bookPage" id="p276" name="276">
<p>&ldquo;Yes,&rdquo; said Peter, fiddling with the fossil ammonite that
the Doctor used for a paper-weight.</p>
<p>&ldquo;Well,&rdquo; said the Doctor, &ldquo;you know men have to do the
work of the world and not be afraid of anything&mdash;so they have to be
hardy and brave. But women have to take care of their babies and cuddle
them and nurse them and be very patient and gentle.&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Peter, wondering what was coming next.</p>
<p>&ldquo;Well, then, you see. Boys and girls are only little men and
women. And <em>we</em> are much harder and hardier than they
are&mdash;&rdquo; (Peter liked the &lsquo;we.&rsquo; Perhaps the Doctor had
known he would.)&mdash;&ldquo;and much stronger, and things that hurt them
don&rsquo;t hurt us. You know you mustn&rsquo;t hit a girl&mdash;&rdquo;</p>
<p>&ldquo;I should think not, indeed,&rdquo; muttered Peter,
indignantly.</p>
<p>&ldquo;Not even if she&rsquo;s your own sister. That&rsquo;s because girls
are so much softer and weaker than we are; they have to be, you
know,&rdquo; he added, &ldquo;because if they weren&rsquo;t, it
wouldn&rsquo;t be nice for the babies. And that&rsquo;s why all the animals are so good to the mother
animals. They never fight them, you know.&rdquo;</p>
<p>&ldquo;I know,&rdquo; said Peter, interested; &ldquo;two buck rabbits
will fight all day if you let them, but they won&rsquo;t hurt a
doe.&rdquo;</p></div>

<div class="bookPage" id="p277" name="277">
<p>&ldquo;No; and quite wild beasts&mdash;lions and
elephants&mdash;they&rsquo;re immensely gentle with the female beasts. And
we&rsquo;ve got to be, too.&rdquo;</p>
<p>&ldquo;I see,&rdquo; said Peter.</p>
<p>&ldquo;And their hearts are soft, too,&rdquo; the Doctor went on,
&ldquo;and things that we shouldn&rsquo;t think anything of hurt them
dreadfully. So that a man has to be very careful, not only of his fists,
but of his words. They&rsquo;re awfully brave, you know,&rdquo; he went on.
&ldquo;Think of Bobbie waiting alone in the tunnel with that poor chap.
It&rsquo;s an odd thing&mdash;the softer and more easily hurt a woman is the
better she can screw herself up to do what <em>has</em> to be done.
I&rsquo;ve seen some brave women&mdash;Your Mother&rsquo;s one,&rdquo; he
ended abruptly.</p>
<p>&ldquo;Yes,&rdquo; said Peter.</p>
<p>&ldquo;Well, that&rsquo;s all. Excuse my mentioning it. But nobody knows
everything without being told. And you see what I mean, don&rsquo;t
you?&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Peter. &ldquo;I&rsquo;m sorry. There!&rdquo;</p>
<p>&ldquo;Of course you are! People always are&mdash;directly they
understand. Every one ought to be taught these scientific facts. So
long!&rdquo;</p>
<p>They shook hands heartily. When Peter came home, his sisters looked at
him doubtfully.</p></div>

<div class="bookPage" id="p278" name="278">
<p>&ldquo;It&rsquo;s Pax,&rdquo; said Peter, dumping down the basket on the
table. &ldquo;Dr. Forrest has been talking scientific to me. No, it&rsquo;s
no use my telling you what he said; you wouldn&rsquo;t understand. But it
all comes to you girls being poor, soft, weakly, frightened things like
rabbits, so us men have just got to put up with them. He said you were
female beasts. Shall I take this up to Mother, or will you?&rdquo;</p>
<p>&ldquo;I know what <em>boys</em> are,&rdquo; said Phyllis, with
flaming cheeks; &ldquo;they&rsquo;re just the nastiest,
rudest&mdash;&rdquo;</p>
<p>&ldquo;They&rsquo;re very brave,&rdquo; said Bobbie,
&ldquo;sometimes.&rdquo;</p>
<p>&ldquo;Ah, you mean the chap upstairs? I see. Go ahead, Phil&mdash;I
shall put up with you whatever you say because you&rsquo;re a poor, weak,
frightened, soft&mdash;&rdquo;</p>
<p>&ldquo;Not if I pull your hair you won&rsquo;t,&rdquo; said Phyllis,
springing at him.</p>
<p>&ldquo;He said &lsquo;Pax,&rsquo;&rdquo; said Bobbie, pulling her away.
&ldquo;Don&rsquo;t you see,&rdquo; she whispered as Peter picked up the
basket and stalked out with it, &ldquo;he&rsquo;s sorry, really, only he won&rsquo;t say so? Let&rsquo;s say we&rsquo;re
sorry.&rdquo;</p>
<p>&ldquo;It&rsquo;s so goody goody,&rdquo; said Phyllis, doubtfully;
&ldquo;he said we were female beasts, and soft and
frightened&mdash;&rdquo;</p>
<p>&ldquo;Then let&rsquo;s show him we&rsquo;re not frightened of him
thinking us goody goody,&rdquo; said Bobbie; &ldquo;and we&rsquo;re not any
more beasts than he is.&rdquo;</p></div>

<div class="bookPage" id="p279" name="279">
<p>And when Peter came back, still with his chin in the air, Bobbie
said:&mdash;</p>
<p>&ldquo;We&rsquo;re sorry we tied you up, Pete.&rdquo;</p>
<p>&ldquo;I thought you would be,&rdquo; said Peter, very stiff and
superior.</p>
<p>This was hard to bear. But&mdash;</p>
<p>&ldquo;Well, so we are,&rdquo; said Bobbie. &ldquo;Now let honour be
satisfied on both sides.&rdquo;</p>
<p>&ldquo;I did call it Pax,&rdquo; said Peter, in an injured tone.</p>
<p>&ldquo;Then let it <em>be</em> Pax,&rdquo; said Bobbie. &ldquo;Come
on, Phil, let&rsquo;s get the tea. Pete, you might lay the cloth.&rdquo;</p>
<p>&ldquo;I say,&rdquo; said Phyllis, when peace was really restored, which
was not till they were washing up the cups after tea, &ldquo;Dr. Forrest
didn&rsquo;t really say we were female beasts, did he?&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Peter, firmly, &ldquo;but I think he meant we
men were wild beasts, too.&rdquo;</p>
<p>&ldquo;How funny of him!&rdquo; said Phyllis, breaking a cup.</p>
<p>&ldquo;May I come in, Mother?&rdquo; Peter was at the door of
Mother&rsquo;s writing room, where Mother sat at her table with two candles
in front of her. Their flames looked</p></div>

<div class="bookPage" id="p280" name="280">
<p>orange and violet against the clear
gray blue of the sky where already a few stars were twinkling.</p>
<p>&ldquo;Yes, dear,&rdquo; said Mother, absently, &ldquo;anything
wrong?&rdquo; She wrote a few more words and then laid down her pen and
began to fold up what she had written. &ldquo;I was just writing to
Jim&rsquo;s grandfather. He lives near here, you know.&rdquo;</p>
<p>&ldquo;Yes, you said so at tea. That&rsquo;s what I want to say. Must you
write to him, Mother? Couldn&rsquo;t we keep Jim, and not say anything to
his people till he&rsquo;s well? It would be such a surprise for
them.&rdquo;</p>
<p>&ldquo;Well, yes,&rdquo; said Mother, laughing, &ldquo;I think it
would.&rdquo;</p>
<p>&ldquo;You see,&rdquo; Peter went on, &ldquo;of course the girls are all
right and all that&mdash;I&rsquo;m not saying anything against
<em>them</em>. But I should like it if I had another chap to talk to
sometimes.&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Mother, &ldquo;I know it&rsquo;s dull for you, dear. But I can&rsquo;t help it. Next year perhaps I can send you to
school&mdash;you&rsquo;d like that, wouldn&rsquo;t you?&rdquo;</p>
<p>&ldquo;I do miss the other chaps, rather,&rdquo; Peter confessed;
&ldquo;but if Jim could stay after his leg was well, we could have awful
larks.&rdquo;</p>
<p>&ldquo;I&rsquo;ve no doubt of it,&rdquo; said Mother. &ldquo;Well perhaps
he could, but you know, dear, we&rsquo;re not rich. I can&rsquo;t afford</p></div>

<div class="bookPage" id="p281" name="281">
<p>to get him everything he&rsquo;ll want. And he must have a nurse.&rdquo;</p>
<p>&ldquo;Can&rsquo;t you nurse him, Mother? You do nurse people so
beautifully.&rdquo;</p>
<p>&ldquo;That&rsquo;s a pretty compliment, Pete&mdash;but I can&rsquo;t do
nursing and my writing as well. That&rsquo;s the worst of it.&rdquo;</p>
<p>&ldquo;Then you <em>must</em> send the letter to his
grandfather?&rdquo;</p>
<p>&ldquo;Of course&mdash;and to his schoolmaster, too. We telegraphed to
them both, but I must write as well. They&rsquo;ll be most dreadfully
anxious.&rdquo;</p>
<p>&ldquo;I say, Mother, why can&rsquo;t his grandfather pay for a
nurse?&rdquo; Peter suggested. &ldquo;That would be ripping. I expect the
old boy&rsquo;s rolling in money. Grandfathers in books always
are.&rdquo;</p>
<p>&ldquo;Well, this one isn&rsquo;t in a book,&rdquo; said Mother,
&ldquo;so we mustn&rsquo;t expect him to roll much.&rdquo;</p>
<p>&ldquo;I say,&rdquo; said Peter, musingly, &ldquo;wouldn&rsquo;t it be
jolly if we all <em>were</em> in a book, and you were writing it? Then
you could make all sorts of jolly things happen, and make Jim&rsquo;s legs
get well at once and be all right to-morrow, and Father come home soon
and&mdash;&rdquo;</p>
<p>&ldquo;Do you miss your Father very much?&rdquo; Mother asked, rather
coldly, Peter thought.</p></div>

<div class="bookPage" id="p282" name="282">
<p>&ldquo;Awfully,&rdquo; said Peter, briefly.</p>
<p>Mother was enveloping and addressing the second letter.</p>
<p>&ldquo;You see,&rdquo; Peter went on slowly, &ldquo;you see it&rsquo;s
not only him <em>being</em> Father, but now he&rsquo;s away there&rsquo;s
no other man in the house but me&mdash;that&rsquo;s why I want Jim to stay
so frightfully much. Wouldn&rsquo;t you like to be writing that book with us
all in it, Mother, and make Daddy come home soon?&rdquo;</p>
<p>Peter&rsquo;s Mother put her arm round him suddenly, and hugged him in
silence for a minute. Then she said:&mdash;</p>
<p>&ldquo;Don&rsquo;t you think it&rsquo;s rather nice to think that
we&rsquo;re in a book that God&rsquo;s writing? If I were writing the book, I
might make mistakes. But God knows how to make the story end just
right&mdash;in the way that&rsquo;s best for us.&rdquo;</p>
<p>&ldquo;Do you really believe that, Mother?&rdquo; Peter asked
quietly.</p>
<p>&ldquo;Yes,&rdquo; she said, &ldquo;I do believe it&mdash;almost
always&mdash;except when I&rsquo;m so sad that I can&rsquo;t believe
anything. But even when I can&rsquo;t believe it, I know it&rsquo;s
true&mdash;and I try to believe it. You don&rsquo;t know how I try, Peter.
Now take the letters to the post, and don&rsquo;t let&rsquo;s be sad any
more. Courage, courage! That&rsquo;s the finest of all the virtues! I dare
say Jim will be here for two or three weeks yet.&rdquo;</p></div>

<div class="bookPage" id="p283" name="283">
<p>For what was left of the evening Peter was so angelic that Bobbie feared
he was going to be ill. She was quite relieved in the morning to find him
plaiting Phyllis&rsquo;s hair on to the back of her chair in quite his old
manner.</p>
<p>It was soon after breakfast that a knock came at the door. The children
were hard at work cleaning the brass candlesticks in honour of Jim&rsquo;s
visit.</p>
<p>&ldquo;That&rsquo;ll be the Doctor,&rdquo; said Mother; &ldquo;I&rsquo;ll
go. Shut the kitchen door&mdash;you&rsquo;re not fit to be seen.&rdquo;</p>
<p>But it wasn&rsquo;t the Doctor. They knew that by the voice and by the
sound of the boots that went upstairs. They did not recognise the sound of
the boots, but every one was certain that it had heard the voice before.</p>
<p>There was a longish interval. The boots and the voice did not come down
again.</p>
<p>&ldquo;Who can it possibly be?&rdquo; they kept on asking themselves and
each other.</p>
<p>&ldquo;Perhaps,&rdquo; said Peter at last, &ldquo;Dr. Forrest has been
attacked by highwaymen and left for dead, and this is the man he&rsquo;s
telegraphed for to take his place. Mrs. Viney said he had a local tenant to
do his work when he went for a holiday, didn&rsquo;t you, Mrs.
Viney?&rdquo;</p></div>

<div class="bookPage" id="p284" name="284">
<p>&ldquo;I did so, my dear,&rdquo; said Mrs. Viney from the back
kitchen.</p>
<p>&ldquo;He&rsquo;s fallen down in a fit, more likely,&rdquo; said Phyllis,
&ldquo;all human aid despaired of. And this is his man come to break the
news to Mother.&rdquo;</p>
<p>&ldquo;Nonsense!&rdquo; said Peter, briskly; &ldquo;Mother wouldn&rsquo;t
have taken the man up into Jim&rsquo;s bedroom. Why should she?
Listen&mdash;the door&rsquo;s opening. Now they&rsquo;ll come down. I&rsquo;ll
open the door a crack.&rdquo;</p>
<p>He did.</p>
<p>&ldquo;It&rsquo;s not listening,&rdquo; he replied indignantly to
Bobbie&rsquo;s scandalised remarks; &ldquo;nobody in their senses would talk
secrets on the stairs. And Mother can&rsquo;t have secrets to talk with Dr. Forrest&rsquo;s
stable-man&mdash;and you said it was him.&rdquo;</p>
<p>&ldquo;Bobbie,&rdquo; called Mother&rsquo;s voice.</p>
<p>They opened the kitchen door, and Mother leaned over the stair
railing.</p>
<p>&ldquo;Jim&rsquo;s grandfather has come,&rdquo; she said; &ldquo;wash
your hands and faces and then you can see him. He wants to see you!&rdquo;
The bedroom door shut again.</p>
<p>&ldquo;There now!&rdquo; said Peter; &ldquo;fancy us not even thinking
of that! Let&rsquo;s have some hot water, Mrs. Viney. I&rsquo;m as black as
your hat.&rdquo;</p></div>

