<h2>CHAPTER XII<br />
WHAT BOBBIE BROUGHT HOME</h2>
<p>&ldquo;<strong>OH</strong>, look up! Speak to me! For my sake,
speak!&rdquo; The children said the words over and over again to the
unconscious hound in a red jersey, who sat with closed eyes and pale face
against the side of the tunnel.</p>
<p>&ldquo;Wet his ears with milk,&rdquo; said Bobbie. &ldquo;I know they do
it to people&rsquo;s that faint&mdash;with eau-de-cologne. But I expect
milk&rsquo;s just as good.&rdquo;</p>
<p>So they wetted his ears, and some of the milk ran down his neck under
the red jersey. It was very dark in the tunnel. The candle end Peter had
carried, and which now burned on a flat stone, gave hardly any light at
all.</p>
<p>&ldquo;Oh, <em>do</em> look up,&rdquo; said Phyllis. &ldquo;For
<em>my</em> sake! I believe he&rsquo;s dead.&rdquo;</p>
<p>&ldquo;For <em>my</em> sake,&rdquo; repeated Bobbie. &ldquo;No, he
isn&rsquo;t.&rdquo;</p>
<p>&ldquo;For <em>any</em> sake,&rdquo; said Peter; &ldquo;come out of
it.&rdquo; And he shook the sufferer by the arm.</p>
<p>And then the boy in the red jersey sighed, and opened his eyes, and shut them again and said in a very small voice,
&ldquo;Chuck it.&rdquo;</p></div>

<div class="bookPage" id="p250" name="250">
<p>&ldquo;Oh, he&rsquo;s <em>not</em> dead,&rdquo; said Phyllis.
&ldquo;I knew he wasn&rsquo;t,&rdquo; and she began to cry.</p>
<p>&ldquo;What&rsquo;s up? I&rsquo;m all right,&rdquo; said the boy.</p>
<p>&ldquo;Drink this,&rdquo; said Peter, firmly, thrusting the nose of the
milk bottle into the boy&rsquo;s mouth. The boy struggled, and some of the
milk was upset before he could get his mouth free to say:&mdash;</p>
<p>&ldquo;What is it?&rdquo;</p>
<p>&ldquo;It&rsquo;s milk,&rdquo; said Peter. &ldquo;Fear not, you are in
the hands of friends. Phil, you stop bleating this minute.&rdquo;</p>
<p>&ldquo;Do drink it,&rdquo; said Bobbie, gently; &ldquo;it&rsquo;ll do you
good.&rdquo;</p>
<p>So he drank. And the three stood by without speaking to him.</p>
<p>&ldquo;Let him be a minute,&rdquo; Peter whispered; &ldquo;he&rsquo;ll be
all right as soon as the milk begins to run like fire through his
veins.&rdquo;</p>
<p>He was.</p>
<p>&ldquo;I&rsquo;m better now,&rdquo; he announced. &ldquo;I remember all
about it.&rdquo; He tried to move, but the movement ended in a groan.
&ldquo;Bother! I believe I&rsquo;ve broken my leg,&rdquo; he said.</p>
<p>&ldquo;Did you tumble down?&rdquo; asked Phyllis, sniffing.</p></div>

<div class="bookPage" id="p251" name="251">
<p>&ldquo;Of course not&mdash;I&rsquo;m not a kiddie,&rdquo; said the boy,
indignantly; &ldquo;it was one of those beastly wires tripped me up, and
when I tried to get up again I couldn&rsquo;t stand, so I sat down. Gee
whillikins! it does hurt, though. How did <em>you</em> get
here?&rdquo;</p>
<p>&ldquo;We saw you all go into the tunnel and then we went across the
hill to see you all come out. And the others did&mdash;all but you, and you
didn&rsquo;t. So we are a rescue party,&rdquo; said Peter, with pride.</p>
<p>&ldquo;You&rsquo;ve got some pluck, I will say,&rdquo; remarked the
boy.</p>
<p>&ldquo;Oh, that&rsquo;s nothing,&rdquo; said Peter, with modesty.
&ldquo;Do you think you could walk if we helped you?&rdquo;</p>
<p>&ldquo;I could try,&rdquo; said the boy.</p>
<p>He did try. But he could only stand on one foot; the other dragged in a
very nasty way.</p>
<p>&ldquo;Here, let me sit down. I feel like dying,&rdquo; said the boy.
&ldquo;Let go of me&mdash;let go, quick&mdash;&rdquo; He lay down and
closed his eyes. The others looked at each other by the dim light of the
little candle.</p>
<p>&ldquo;What on earth!&rdquo; said Peter.</p>
<p>&ldquo;Look here,&rdquo; said Bobbie, quickly, &ldquo;you must go and
get help. Go to the nearest house.&rdquo;</p>
<p>&ldquo;Yes, that&rsquo;s the only thing,&rdquo; said Peter. &ldquo;Come
on.&rdquo;</p></div>

<div class="bookPage" id="p252" name="252">
<p>&ldquo;If you take his feet and Phil and I take his head, we could carry
him to the manhole.&rdquo;</p>
<p>They did it. It was perhaps as well for the sufferer that he had fainted
again.</p>
<p>&ldquo;Now,&rdquo; said Bobbie, &ldquo;I&rsquo;ll stay with him. You take
the longest bit of candle, and oh,&mdash;be quick, for this bit won&rsquo;t
burn long.&rdquo;</p>
<p>&ldquo;I don&rsquo;t think Mother would like me leaving you,&rdquo; said
Peter, doubtfully. &ldquo;Let me stay, and you and Phil go.&rdquo;</p>
<p>&ldquo;No, no,&rdquo; said Bobbie, &ldquo;you and Phil go&mdash;and lend
me your knife. I&rsquo;ll try to get his boot off before he wakes up
again.&rdquo;</p>
<p>&ldquo;I hope it&rsquo;s right what we&rsquo;re doing,&rdquo; said
Peter.</p>
<p>&ldquo;Of course it&rsquo;s right,&rdquo; said Bobbie, impatiently.
&ldquo;What else <em>would</em> you do? Leave him here all alone
because it&rsquo;s dark? Nonsense. Hurry up, that&rsquo;s all.&rdquo;</p>
<p>So they hurried up.</p>
<p>Bobbie watched their dark figures and the little light of the little
candle with an odd feeling of having come to the end of everything. She
knew now, she thought, what nuns who were bricked up alive in convent walls
felt like. Suddenly she gave herself a little shake.</p></div>

<div class="bookPage" id="p253" name="253">
<p>&ldquo;Don&rsquo;t be a silly little girl,&rdquo; she said. She was
always very angry when any one else called her a little girl, even if the
adjective that went first was not &ldquo;silly&rdquo; but
&ldquo;nice&rdquo; or &ldquo;good&rdquo; or &ldquo;clever.&rdquo; And it
was only when she was very angry with herself that she allowed Roberta to
use that expression to Bobbie.</p>
<p>She fixed the little candle end on a broken brick near the red-jerseyed
boy&rsquo;s feet. Then she opened Peter&rsquo;s knife. It was always hard to
manage&mdash;a halfpenny was generally needed to get it open at all. This
time Bobbie somehow got it open with her thumb nail. She broke the nail,
and it hurt horribly. Then she cut the boy&rsquo;s bootlace, and got the
boot off. She tried to pull off his stocking, but his leg was dreadfully
swollen, and it did not seem to be the proper shape. So she cut the
stocking down, very slowly and carefully. It was a brown, knitted stocking,
and she wondered who had knitted it, and whether it was the boy&rsquo;s
mother, and whether she was feeling anxious about him, and how she would
feel when he was brought home with his leg broken. When Bobbie had got the
stocking off and saw the poor leg, she felt as though the tunnel was
growing darker, and the ground felt unsteady, and nothing seemed quite
real.</p>
<p>&ldquo;<em>Silly</em> little girl!&rdquo; said Roberta to Bobbie,
and felt better.</p>
<p>&ldquo;The poor leg,&rdquo; she told herself; &ldquo;it ought to have a
cushion&mdash;ah!&rdquo;</p></div>

<div class="bookPage" id="p254" name="254">
<p>She remembered the day when she and Phyllis had torn up their red
flannel petticoats to make danger signals to stop the train and prevent an
accident. Her flannel petticoat to-day was white, but it would be quite as
soft as a red one. She took it off.</p>
<p>&ldquo;Oh, what useful things flannel petticoats are!&rdquo; she said;
&ldquo;the man who invented them ought to have a statue directed to
him.&rdquo; And she said it aloud, because it seemed that any voice, even
her own, would be a comfort in that darkness.</p>
<p>&ldquo;<em>What</em> ought to be directed? Who to?&rdquo; asked the
boy, suddenly and very feebly.</p>
<p>&ldquo;Oh,&rdquo; said Bobbie, &ldquo;now you&rsquo;re better! Hold your
teeth and don&rsquo;t let it hurt too much. Now!&rdquo;</p>
<p>She had folded the petticoat, and lifting his leg laid it on the cushion
of folded flannel.</p>
<p>&ldquo;Don&rsquo;t faint again, <em>please</em> don&rsquo;t,&rdquo;
said Bobbie, as he groaned. She hastily wetted her handkerchief with milk
and spread it over the poor leg.</p>
<p>&ldquo;Oh, that hurts,&rdquo; cried the boy, shrinking.
&ldquo;Oh&mdash;no, it doesn&rsquo;t&mdash;it&rsquo;s nice,
really.&rdquo;</p>
<p>&ldquo;What&rsquo;s your name?&rdquo; said Bobbie.</p></div>

<div class="bookPage" id="p255" name="255">
<p>&ldquo;Jim.&rdquo;</p>
<p>&ldquo;Mine&rsquo;s Bobbie.&rdquo;</p>
<p>&ldquo;But you&rsquo;re a girl, aren&rsquo;t you?&rdquo;</p>
<p>&ldquo;Yes, my long name&rsquo;s Roberta.&rdquo;</p>
<p>&ldquo;I say&mdash;Bobbie.&rdquo;</p>
<p>&ldquo;Yes?&rdquo;</p>
<p>&ldquo;Wasn&rsquo;t there some more of you just now?&rdquo;</p>
<p>&ldquo;Yes, Peter and Phil&mdash;that&rsquo;s my brother and sister.
They&rsquo;ve gone to get some one to carry you out.&rdquo;</p>
<p>&ldquo;What rum names. All boys.&rdquo;</p>
<p>&ldquo;Yes&mdash;I wish I was a boy, don&rsquo;t you?&rdquo;</p>
<p>&ldquo;I think you&rsquo;re all right as you are.&rdquo;</p>
<p>&ldquo;I didn&rsquo;t mean that&mdash;I meant don&rsquo;t you wish you
were a boy, but of course you are without wishing.&rdquo;</p>
<p>&ldquo;You&rsquo;re just as brave as a boy. Why didn&rsquo;t you go with
the others?&rdquo;</p>
<p>&ldquo;Somebody had to stay with you,&rdquo; said Bobbie.</p>
<p>&ldquo;Tell you what, Bobbie,&rdquo; said Jim, &ldquo;you&rsquo;re a
brick. Shake.&rdquo; He reached out a red-jerseyed arm and Bobbie squeezed
his hand.</p></div>

<div class="bookPage" id="p256" name="256">
<p>&ldquo;I won&rsquo;t shake it,&rdquo; she explained, &ldquo;because it
would shake <em>you</em>, and that would shake your poor leg, and that
would hurt. Have you got a hanky?&rdquo;</p>
<p>&ldquo;I don&rsquo;t expect I have.&rdquo; He felt in his pocket.
&ldquo;Yes, I have. What for?&rdquo;</p>
<p>She took it and wetted it with milk and put it on his forehead.</p>
<p>&ldquo;That&rsquo;s jolly,&rdquo; he said; &ldquo;what is it?&rdquo;</p>
<p>&ldquo;Milk,&rdquo; said Bobbie. &ldquo;We haven&rsquo;t any
water&mdash;&rdquo;</p>
<p>&ldquo;You&rsquo;re a jolly good little nurse,&rdquo; said Jim.</p>
<p>&ldquo;I do it for Mother sometimes,&rdquo; said Bobbie, &ldquo;not
milk, of course, but scent, or vinegar and water. I say, I must put the
candle out now, because there mayn&rsquo;t be enough of the other one to get
you out by.&rdquo;</p>
<p>&ldquo;By George,&rdquo; said he, &ldquo;you think of
everything.&rdquo;</p>
<p>Bobbie blew. Out went the candle. You have no idea how black-velvety the
darkness was.</p>
<p>&ldquo;I say, Bobbie,&rdquo; said a voice through the blackness,
&ldquo;aren&rsquo;t you afraid of the dark?&rdquo;</p>
<p>&ldquo;Not&mdash;not very, that is&mdash;&rdquo;</p></div>

<div class="bookPage" id="p257" name="257">
<p>&ldquo;Let&rsquo;s hold hands,&rdquo; said the boy, and it was really
rather good of him, because he was like most boys of his age and hated all
material tokens of affection, such as kissing and holding of hands. He
called all such things &ldquo;pawings,&rdquo; and detested them.</p>
<p>The darkness was more bearable to Bobbie now 
that her hand was held in the large rough hand of the red-jerseyed
sufferer; and he, holding her little smooth hot paw, was surprised to find
that he did not mind it so much as he expected. She tried to talk, to amuse
him, and &ldquo;take his mind off&rdquo; his sufferings, but it is very
difficult to go on talking in the dark, and presently they found themselves
in a silence, only broken now and then by a
<strong>&ldquo;You all right, Bobbie?&rdquo;</strong>
or an&mdash;
<strong>&ldquo;I&rsquo;m afraid it&rsquo;s hurting you most awfully, Jim. I am
so sorry.&rdquo;</strong>
</p><p>And it was very cold.</p>
<p>Peter and Phyllis tramped down the long way of the tunnel towards
daylight, the candle-grease dripping over Peter&rsquo;s fingers. There were
no accidents unless you count Phyllis&rsquo;s catching her frock on a wire,
and tearing a long, jagged slit in it, and tripping over her bootlace when
it came undone, or going down on her hands-and-knees, all four of which
were grazed.</p>
<p>&ldquo;There&rsquo;s no end to this tunnel,&rdquo; said Phyllis, and
indeed it did seem very very long.</p></div>

<div class="bookPage" id="p258" name="258">
<p>&ldquo;Stick to it,&rdquo; said Peter; &ldquo;everything has an end, and
you get to it if you only keep all on.&rdquo;</p>
<p>Which is quite true, if you come to think of it, and a useful thing to
remember in seasons of trouble,&mdash;such as measles, arithmetic,
impositions, and those times when you are in disgrace, and feel as though
no one would ever love you again, and you could never&mdash;never
again&mdash;love anybody.</p>
<p>&ldquo;Hurray,&rdquo; said Peter, suddenly, &ldquo;there&rsquo;s the end
of the tunnel&mdash;looks just like a pin-hole in a bit of black paper,
doesn&rsquo;t it?&rdquo;</p>
<p>The pin-hole got larger&mdash;blue lights lay along the sides of the
tunnel. The children could see the gravel way that lay in front of them;
the air grew warmer and sweeter. Another twenty steps and they were out in
the good glad sunshine with the green trees on both sides.</p>
<p>Phyllis drew a long breath.</p>
<p>&ldquo;I&rsquo;ll never go into a tunnel again as long as ever I
live,&rdquo; said she, &ldquo;not if there are twenty hundred thousand
million hounds inside with red jerseys and their legs broken.&rdquo;</p>
<p>&ldquo;Don&rsquo;t be a silly cuckoo,&rdquo; said Peter, as usual.
&ldquo;You&rsquo;d <em>have</em> to.&rdquo;</p></div>

<div class="bookPage" id="pf259" name="">
<img src="illustrations/railway/nesbit-railway259.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></div>

<div class="bookPage" id="p259" name="259">
<p>&ldquo;I think it was very brave and good of me,&rdquo; said
Phyllis.</p>
<p>&ldquo;Not it,&rdquo; said Peter; &ldquo;you didn&rsquo;t go because you
were brave, but because Bobbie and I aren&rsquo;t skunks. 
Now where&rsquo;s the nearest house, I wonder? You can&rsquo;t see anything
here for the trees.&rdquo;</p>
<p>&ldquo;There&rsquo;s a roof over there,&rdquo; said Phyllis, pointing
down the line.</p>
<p>&ldquo;That&rsquo;s the signal-box,&rdquo; said Peter, &ldquo;and you
know you&rsquo;re not allowed to speak to signalmen on duty. It&rsquo;s
wrong.&rdquo;</p>
<p>&ldquo;I&rsquo;m not near so afraid of doing wrong as I was of going into
that tunnel,&rdquo; said Phyllis. &ldquo;Come on,&rdquo; and she started to
run along the line. So Peter ran, too.</p>
<p>It was very hot in the sunshine, and both children were hot and
breathless by the time they stopped, and bending their heads back to look
up at the open windows of the signal-box, shouted &ldquo;Hi!&rdquo; as loud
as their breathless state allowed. But no one answered. The signal-box
stood quiet as an empty nursery, and the handrail of its steps was hot to
the hands of the children as they climbed softly up. They peeped in at the
open door. The signalman was sitting on a chair tilted back against the
wall. His head leaned sideways, and his mouth was open. He was fast
asleep.</p></div>

<div class="bookPage" id="p260" name="260">
<p>&ldquo;My hat!&rdquo; cried Peter; &ldquo;wake up!&rdquo; And he cried
it in a terrible voice, for he knew that if a signalman sleeps on duty, he
risks losing his situation, let alone all the other 
dreadful risks to trains which expect him to
tell them when it is safe for them to go their ways.</p>
<p>The signalman never moved. Then Peter sprang to him and shook him. And
slowly, yawning and stretching, the man awoke. But the moment he
<em>was</em> awake he leapt to his feet, put his hands to his head
&ldquo;like a mad maniac,&rdquo; as Phyllis said afterwards, and
shouted:&mdash;</p>
<p>&ldquo;Oh, my heavens&mdash;what&rsquo;s o&rsquo;clock?&rdquo;</p>
<p>&ldquo;Twelve thirteen,&rdquo; said Peter, and indeed it was by the
white-faced, round-faced clock on the wall of the signal-box.</p>
<p>The man looked at the clock, sprang to the levers, and wrenched them
this way and that. An electric bell tingled&mdash;the wires and cranks
creaked, and the man threw himself into a chair. He was very pale, and the
sweat stood on his forehead, &ldquo;like large dewdrops on a white
cabbage,&rdquo; as Phyllis remarked later. He was trembling, too; the
children could see his big hairy hands shake from side to side, &ldquo;with
quite extra-sized trembles,&rdquo; to use the subsequent words of Peter. He
drew long breaths. Then suddenly he cried, &ldquo;Thank God, thank God, you
come in when you did&mdash;oh, thank God!&rdquo;</p></div>

<div class="bookPage" id="p261" name="261">
<p>and his shoulders began to
heave and his face grew red again, and he hid it in those large hairy hands of
his.</p>
<p>&ldquo;Oh, don&rsquo;t cry&mdash;don&rsquo;t,&rdquo; said Phyllis,
&ldquo;it&rsquo;s all right now,&rdquo; and she patted him on one big, broad
shoulder, while Peter conscientiously thumped the other.</p>
<p>But the signalman seemed quite broken down, and the children had to pat
him and thump him for quite a long time before he found his
handkerchief&mdash;a red one with mauve and white horseshoes on
it&mdash;and mopped his face and spoke. During this patting and thumping
interval a train thundered by.</p>
<p>&ldquo;I&rsquo;m downright shamed, that I am,&rdquo; were the words of
the big signalman when he had stopped crying; &ldquo;snivelling like a
kid.&rdquo; Then suddenly he seemed to get cross. &ldquo;And what was you
doing up here, anyway?&rdquo; he said; &ldquo;you know it ain&rsquo;t
allowed.&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Phyllis, &ldquo;we knew it was wrong but I
wasn&rsquo;t afraid of doing wrong, and so it turned out right. You
aren&rsquo;t sorry we came.&rdquo;</p>
<p>&ldquo;Lor love you&mdash;if you hadn&rsquo;t &rsquo;a&rsquo;
come&mdash;&rdquo; he stopped and then went on. &ldquo;It&rsquo;s a
disgrace, so it is, sleeping on duty. If it was to come to be
known&mdash;even as it is, when no harm&rsquo;s come of it.&rdquo;</p></div>

<div class="bookPage" id="p262" name="262">
<p>&ldquo;It won&rsquo;t come to be known,&rdquo; said Peter; &ldquo;we
aren&rsquo;t sneaks. All the same you oughtn&rsquo;t to sleep on
duty&mdash;it&rsquo;s dangerous.&rdquo;</p>
<p>&ldquo;Tell me something I don&rsquo;t know,&rdquo; said the man,
&ldquo;but I can&rsquo;t help it. I know&rsquo;d well enough just how it
&rsquo;ud be. But I couldn&rsquo;t get off. They couldn&rsquo;t get no one to
take on my duty. I tell you I ain&rsquo;t had ten minutes&rsquo; sleep this
last five days. My little chap&rsquo;s ill,&mdash;pewmonia, the Doctor
says,&mdash;and there&rsquo;s no one but me and &rsquo;is little sister to do
for him. That&rsquo;s where it is. The gell must &rsquo;ave her sleep.
Dangerous? Yes, I believe you. Now go and split on me if you
like.&rdquo;</p>
<p>&ldquo;Of course we won&rsquo;t,&rdquo; said Peter, indignantly, but
Phyllis ignored the whole of the signalman&rsquo;s speech, except the first
six words.</p>
<p>&ldquo;You asked us,&rdquo; she said, &ldquo;to tell you something you
don&rsquo;t know. Well, I will. There&rsquo;s a boy in the tunnel over there
with a red jersey and his leg broken.&rdquo;</p>
<p>&ldquo;What did he want to go into the blooming tunnel for, then?&rdquo;
said the man.</p>
<p>&ldquo;Don&rsquo;t you be so cross,&rdquo; said Phyllis, kindly.
&ldquo;We haven&rsquo;t done anything wrong except coming and waking you up,
and that was right, as it happens.&rdquo;</p>
<p>Then Peter told how the boy came to be in the tunnel.</p></div>

<div class="bookPage" id="p263" name="263">
<p>&ldquo;Well,&rdquo; said the man, &ldquo;I don&rsquo;t see as I can do
anything. I can&rsquo;t leave the box.&rdquo;</p>
<p>&ldquo;You might tell us where to go after some one who isn&rsquo;t in a
box, though,&rdquo; said Phyllis.</p>
<p>&ldquo;There&rsquo;s Brigden&rsquo;s farm over yonder&mdash;where you see
the smoke a-coming up through the trees,&rdquo; said the man, more and more
grumpy, as Phyllis noticed.</p>
<p>&ldquo;Well&mdash;good-by, then,&rdquo; said Peter.</p>
<p>But the man said, &ldquo;Wait a minute.&rdquo; He put his hand in his
pocket and brought out some money&mdash;a lot of pennies and one or two
shillings and sixpences and half-a-crown. He picked out two shillings and
held them out.</p>
<p>&ldquo;Here,&rdquo; he said. &ldquo;I&rsquo;ll give you this to hold your
tongues about what&rsquo;s taken place to-day.&rdquo;</p>
<p>There was a short unpleasant pause. Then:&mdash;</p>
<p>&ldquo;You <em>are</em> a nasty man, though, aren&rsquo;t you?&rdquo;
said Phyllis.</p>
<p>Peter took a step forward and knocked the man&rsquo;s hand up, so that
the shillings leapt out of it and rolled on the floor.</p>
<p>&ldquo;If anything <em>could</em> make me sneak, <em>that</em>
would!&rdquo; he said. &ldquo;Come, Phil,&rdquo; and marched out of the
signal-box with flaming cheeks.</p></div>

<div class="bookPage" id="p264" name="264">
<p>Phyllis hesitated. Then she took the hand, still held out stupidly, that
the shillings had been in.</p>
<p>&ldquo;I forgive you,&rdquo; she said, &ldquo;even if Peter
doesn&rsquo;t. You&rsquo;re not in your proper senses, or you&rsquo;d never
have done that. I know want of sleep sends people mad. Mother told me. I
hope your little boy will soon be better, and&mdash;&rdquo;</p>
<p>&ldquo;Come on, Phil,&rdquo; cried Peter, angrily.</p>
<p>&ldquo;I give you my sacred honour-word we&rsquo;ll never tell any one.
Kiss and be friends,&rdquo; said Phyllis, feeling how noble it was of her
to try to make up a quarrel in which she was not to blame.</p>
<p>The signalman stooped and kissed her.</p>
<p>&ldquo;I do believe I&rsquo;m a bit off my head, Sissy,&rdquo; he said.
&ldquo;Now run along home to Mother. I didn&rsquo;t mean to put you
about&mdash;there.&rdquo;</p>
<p>So Phil left the hot signal-box and followed Peter across the fields to
the farm.</p>
<p>When the farm men, led by Peter and Phyllis and carrying a hurdle
covered with horse-cloths, reached the manhole in the tunnel, Bobbie was
fast asleep and so was Jim. Worn out with the pain, the Doctor said
afterwards.</p>
<p>&ldquo;Where does he live?&rdquo; the bailiff from the farm asked, when
Jim had been lifted on to the hurdle.</p></div>

<div class="bookPage" id="p265" name="265">
<p>&ldquo;In Northumberland,&rdquo; answered Bobbie.</p>
<p>&ldquo;I&rsquo;m at school at Maidbridge,&rdquo; said Jim. &ldquo;I
suppose I&rsquo;ve got to get back there, somehow.&rdquo;</p>
<p>&ldquo;Seems to me the Doctor ought to have a look in first,&rdquo; said
the bailiff.</p>
<p>&ldquo;Oh, bring him up to our house,&rdquo; said Bobbie.
&ldquo;It&rsquo;s only a little way by the road. I&rsquo;m sure Mother would
say we ought to.&rdquo;</p>
<p>&ldquo;Will your Ma like you bringing home strangers with broken
legs?&rdquo;</p>
<p>&ldquo;She took the poor Russian home herself,&rdquo; said Bobbie.
&ldquo;I know she&rsquo;d say we ought.&rdquo;</p>
<p>&ldquo;All right,&rdquo; said the bailiff, &ldquo;you ought to know what
your Ma &rsquo;ud like. I wouldn&rsquo;t take it upon me to fetch him up to
our place without I asked the Missus first, and they call me the Master,
too.&rdquo;</p>
<p>&ldquo;Are you sure your Mother won&rsquo;t mind?&rdquo; whispered
