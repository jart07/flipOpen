<div class="bookPage" id="p307" name="307">
<p>&ldquo;I knew something wonderful was going to happen,&rdquo; said
Bobbie, as they went up the road, &ldquo;but I didn&rsquo;t think it was
going to be this. Oh, my Daddy, my Daddy!&rdquo;</p>
<p>&ldquo;Then didn&rsquo;t Mother get my letter?&rdquo; Father asked.
<p>&ldquo;There weren&rsquo;t any letters this morning. Oh! Daddy it
<em>is</em> really you, isn&rsquo;t it?&rdquo;</p>
<p>The clasp of a hand she had not forgotten assured her that it was.</p>
<p>&ldquo;You must go in by yourself, Bobbie, and tell Mother quite quietly
that it&rsquo;s all right. They&rsquo;ve caught the man who did it. Every one
knows now that it wasn&rsquo;t your Daddy.&rdquo;</p>
<p>&ldquo;<em>I</em> always knew it wasn&rsquo;t,&rdquo; said Bobbie.
&ldquo;Me and Mother and the old gentleman.&rdquo;</p>
<p>&ldquo;Yes,&rdquo; he said, &ldquo;it&rsquo;s all his doing. Mother wrote
and told me you had found out. And she told me what you&rsquo;d been to her.
My own little girl!&rdquo; They stopped a minute then.</p>
<p>And now I see them crossing the field. Bobbie goes into the house,
trying to keep her eyes from speaking before her lips have found the right
words to &ldquo;tell Mother quite quietly&rdquo; that the sorrow and the
struggle and the parting are over and done, and that Father has come
home.</p></div>

<div class="bookPage" id="p308" name="308">
<p>I see Father walking in the garden, waiting&mdash;waiting. He is looking
at the flowers, and each flower is a miracle to eyes that all these months
of Spring and Summer have seen only flag-stones and gravel and a little
grudging grass. But his eyes keep turning towards the house. And presently
he leaves the garden and goes to stand outside the nearest door. It is the
back door, and across the yard the swallows are circling. They are getting
ready to fly away from cold winds and keen frost to the land where it is
always summer. They are the same swallows that the children built the
little clay nests for.</p>
<p>Now the house door opens. Bobbie&rsquo;s voice calls:&mdash;</p>
<p>&ldquo;Come in, Daddy, come in!&rdquo;</p>
<p>He goes in and the door is shut. I think we will not open the door or
follow him. I think that just now we are not wanted there. I think it will
be best for us to go quickly and quietly away. At the end of the field,
among the thin gold spikes of grass and the harebells and Gipsy roses and
St. John&rsquo;s Wort, we may just take one last look, over our shoulders,
at the white house where neither we nor any one else is wanted now.</p>

