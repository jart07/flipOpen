<h2>CHAPTER XIV<br />
THE END</h2>
<p><strong>LIFE</strong> at the Three Chimneys was never quite
the same again after the old gentleman came to see his grandson. Although
they now knew his name, the children never spoke of him by it,&mdash;at any
rate, when they were by themselves. To them he was always the old
gentleman, and I think he had better be the old gentleman to us, too. It
wouldn&rsquo;t make him seem any more real to you, would it, if I were to
tell you that his name was Snooks or Jenkins (which it wasn&rsquo;t)? and,
after all, I must be allowed to keep one secret. It&rsquo;s the only one; I
have told you everything else, except what I am going to tell you in this
chapter, which is the last. At least, of course I haven&rsquo;t told you
<em>everything</em>. If I were to do that, the book would never come to
an end, and that would be a pity, wouldn&rsquo;t it?</p>
<p>Well, as I was saying, life at Three Chimneys was never quite the same
again. The cook and the housemaid were very nice (I don&rsquo;t mind telling 
you their names&mdash;they were Clara and Ethelwyn), but they told Mother
they did not seem to want Mrs. Viney, and that she was an old muddler. So
Mrs. Viney only came two days a week to do washing and ironing, Then Clara
and Ethelwyn said they</p></div>

<div class="bookPage" id="p292" name="292">
<p>could do the work all right if they weren&rsquo;t
interfered with, and that meant that the children no longer got the tea and
cleared it away and washed up the tea-things and dusted the rooms.</p>
<p>This would have left quite a blank in their lives, although they had
often pretended to themselves and to each other that they hated housework.
But now that Mother had no writing and no housework to do, she had time for
lessons. And lessons the children had to do. However nice the person who is
teaching you may be, lessons are lessons all the world over, and at their
best are worse fun than peeling potatoes or lighting a fire.</p>
<p>On the other hand, if Mother now had time for lessons, she also had time
for play, and to make up little rhymes for the children as she used to do.
She had not had much time for rhymes since she came to Three Chimneys.</p>
<p>There was one very odd thing about these lessons. Whatever the children
were doing, they always wanted to be doing something else. When 
Peter was doing his Latin, he thought it would be nice to be learning
History like Bobbie. Bobbie would have preferred Arithmetic, which was what
Phyllis happened to be doing, and Phyllis of course thought Latin much the
most interesting kind of lesson. And so on.</p></div>

<div class="bookPage" id="p293" name="293">
<p>So, one day, when they sat down to lessons, each of them found a little
rhyme at its place. I put the rhymes in to shew you that their Mother
really did understand a little how children feel about things, and also the
kind of words they use, which is the case with very very few grown-up
people. I suppose most grown-ups have very bad memories, and have forgotten
how they felt when they were little. Of course the verses are supposed to
be spoken by the children.</p>
<h3>PETER</h3>
<pre>
I once thought C&aelig;sar easy pap
How very soft I must have been!
When they start C&aelig;sar with a chap
He little knows what that will mean.
Oh, verbs are silly stupid things.
I&rsquo;d rather learn the dates of kings!
</pre>

<h3>BOBBIE</h3>
<pre>
The worst of all my lesson things
Is learning who succeeded who
In all the rows of queens and kings,
With dates to everything they do:
With dates enough to make you sick;&mdash;
I wish it was Arithmetic!
</pre></div>

<div class="bookPage" id="p294" name="294">
<h3>PHYLLIS</h3>
<pre>
Such pounds and pounds of apples fill
My slate&mdash;what is the price you&rsquo;d spend?
You scratch the figures out until
You cry upon the dividend.
I&rsquo;d break the slate and scream for joy
If I did Latin like a boy!
</pre>
<p>This kind of thing, of course, made lessons much jollier. It is something
to know that the person who is teaching you sees that it is not all plain
sailing for you, and does not think that it is just your stupidness that
makes you not know your lessons till you&rsquo;ve learned them!</p>
<p>Then as Jim&rsquo;s leg got better it was very pleasant to go up and sit
with him and hear tales about his school life and the other boys. There was
one boy, named Parr, of whom Jim seemed to have formed the lowest possible
opinion, and another boy named Wigsby Manor, for whose views Jim had a
great respect. Also there were three brothers named Paley, and the youngest
was called Paley Terts, and was much given to fighting.</p>
<p>Peter drank in all this with deep joy, and Mother seemed to have listened with some interest, for one day she gave Jim a sheet of paper on which she had written a rhyme about Parr, bringing in
Paley and Wigsby by name in a most wonderful way, as well as all the
reasons Jim had for</p></div>

<div class="bookPage" id="p295" name="295">
<p>not liking Parr, and Wigsby&rsquo;s wise opinion on the
matter. Jim was immensely pleased. He had never had a rhyme written
expressly for him before. He read it till he knew it by heart and then he
sent it to Wigsby, who liked it almost as much as Jim did. Perhaps you may
like it, too.</p>

<h3>THE NEW BOY</h3>
<pre>
His name is Parr: he says that he
Is given bread and milk for tea.
He says his father killed a bear.
He says his mother cuts his hair.
</pre>
<pre>
He wears goloshes when it&rsquo;s wet.
I&rsquo;ve heard his people call him &ldquo;Pet&rdquo;!
He has no proper sense of shame;
He told the chaps his Christian name.
</pre>
<pre>
He cannot wicket-keep at all,
He&rsquo;s frightened of a cricket ball.
He reads, indoors, for hours and hours.
He knows the names of beastly flowers.
</pre>
<pre>
He says his French just like Mossoo&mdash;
A beastly stuck-up thing to do
He won&rsquo;t keep <em>cave</em>, shirks his turn
And says he came to school to learn!
</pre>
<pre>
He won&rsquo;t play football, says it hurts;
He wouldn&rsquo;t fight with Paley Terts;
He couldn&rsquo;t whistle if he tried,
And when we laughed at him he cried!
</pre></div>

<div class="bookPage" id="p296" name="296">
<pre>
Now, Wigsby Minor says that Parr
Is only like all new boys are.
I know when <em>I</em> first came to school
I wasn&rsquo;t such a jolly fool!
</pre>
<p>Jim could not understand how Mother could have been clever enough to do it.
To the others it seemed nice, but natural. You see they had always been
used to having a mother who could write verses just like the way people
talk, even to the shocking expression at the end of the rhyme, which was
Jim&rsquo;s very own.</p>
<p>Jim taught Peter to play chess and draughts and dominoes, and all
together it was a nice quiet time.</p>
<p>Only Jim&rsquo;s leg got better and better, and a general feeling began
to spring up among Bobbie, Peter, and Phyllis that something ought to be
done to amuse him; not just games, but something really handsome. But it
was extraordinarily difficult to think of anything.</p>
<p>&ldquo;It&rsquo;s no good,&rdquo; said Peter, when all of them had
thought and thought till their heads felt quite heavy and swollen; &ldquo;if we can&rsquo;t think of anything to amuse him,
we just can&rsquo;t, and there&rsquo;s an end of it. Perhaps something will
just happen of its own accord that he&rsquo;ll like.&rdquo;</p></div>

<div class="bookPage" id="p297" name="297">

<p>&ldquo;Things <em>do</em> happen by themselves sometimes, without
your making them,&rdquo; said Phyllis, rather as though, usually,
everything that happened in the world was her doing.</p>
<p>&ldquo;I wish something would happen,&rdquo; said Bobbie, dreamily,
&ldquo;something wonderful.&rdquo;</p>
<p>And something wonderful did happen exactly four days after she had said
this. I wish I could say it was three days after, because in fairy tales it
is always three days after that things happen. But this is not a fairy
story, and besides, it really was four and not three, and I am nothing if
not strictly truthful.</p>
<p>They seemed to be hardly Railway children at all in those days, and as
the days went on each had an uneasy feeling about this which Phyllis
expressed one day.</p>
<p>&ldquo;I wonder if the Railway misses us,&rdquo; she said plaintively.
&ldquo;We never go to see it now.&rdquo;</p>
<p>&ldquo;It seems ungrateful,&rdquo; said Bobbie; &ldquo;we loved it so
when we hadn&rsquo;t any one else to play with.&rdquo;</p>
<p>&ldquo;Perks is always coming up to ask after Jim,&rdquo; said Peter, &ldquo;and the signalman&rsquo;s little boy is better. He told
me so.&rdquo;</p>
<p>&ldquo;I didn&rsquo;t mean the people,&rdquo; explained Phyllis; &ldquo;I
meant the dear Railway itself.&rdquo;</p></div>

<div class="bookPage" id="p298" name="298">
<p>&ldquo;The thing I don&rsquo;t like,&rdquo; said Bobbie, on this fourth
day, which was a Tuesday, &ldquo;is our having stopped waving to the 9.15
and sending our love to Father by it.&rdquo;</p>
<p>&ldquo;Let&rsquo;s begin again,&rdquo; said Phyllis. And they did.</p>
<p>Somehow the change of everything that was made by having servants in the
house and Mother not doing any writing, made the time seem extremely long
since that strange morning at the beginning of things, when they had got up
so early and burnt the bottom out of the kettle and had apple pie for
breakfast and first seen the Railway.</p>
<p>It was September now, and the turf on the slope to the Railway was dry
and crisp. Little long grass spikes stood up like bits of gold wire, frail
blue harebells trembled on their tough, slender stalks, Gipsy roses opened
wide and flat their lilac-coloured discs, and the golden stars of St.
John&rsquo;s Wort shone at the edges of the pool that lay halfway to the
Railway. Bobbie gathered a generous handful of the flowers and thought how
pretty they would look lying on the green-and-pink blanket of silk-waste that now
covered Jim&rsquo;s poor broken leg.</p>
<p>&ldquo;Hurry up,&rdquo; said Peter, &ldquo;or we shall miss the
9.15!&rdquo;</p>
<p>&ldquo;I can&rsquo;t hurry more than I am doing,&rdquo; said Phyllis.
&ldquo;Oh, bother it! my bootlace has come undone
<em>again</em>!&rdquo;</p></div>

<div class="bookPage" id="p299" name="299">
<p>&ldquo;When you&rsquo;re married,&rdquo; said Peter, &ldquo;your bootlace
will come undone going up the church aisle, and your man that you&rsquo;re
going to get married to will tumble over it and smash his nose in on the
ornamented pavement; and then you&rsquo;ll say you won&rsquo;t marry him, and
you&rsquo;ll have to be an old maid.&rdquo;</p>
<p>&ldquo;I shan&rsquo;t,&rdquo; said Phyllis. &ldquo;I&rsquo;d much rather
marry a man with his nose smashed in than not marry anybody.&rdquo;</p>
<p>&ldquo;It would be horrid to marry a man with a smashed nose, all the
same,&rdquo; went on Bobbie. &ldquo;He wouldn&rsquo;t be able to smell the
flowers at the wedding. Wouldn&rsquo;t that be awful!&rdquo;</p>
<p>&ldquo;Bother the flowers at the wedding!&rdquo; cried Peter.
&ldquo;Look! the signal&rsquo;s down. We must run!&rdquo;</p>
<p>They ran. And once more they waved their 
handkerchiefs, without at all minding whether the handkerchiefs were clean
or not, to the 9.15.</p>
<p>&ldquo;Take our love to Father!&rdquo; cried Bobbie. And the others,
too, shouted:&mdash;</p>
<p>&ldquo;Take our love to Father!&rdquo;</p>
<p>The old gentleman waved from his first-class carriage window. Quite
violently he waved. And there was nothing odd in that, for he always had
waved. But what</p></div>

<div class="bookPage" id="p300" name="300">
<p>was really remarkable was that from every window
handkerchiefs fluttered, newspapers signalled, hands waved wildly. The
train swept by with a rustle and roar, the little pebbles jumped and danced
under it as it passed, and the children were left looking at each
other.</p>
<p>&ldquo;Well!&rdquo; said Peter.</p>
<p>&ldquo;<em>Well</em>!&rdquo; said Bobbie.</p>
<p>&ldquo;<em>WELL</em>!&rdquo; said Phyllis.</p>
<p>&ldquo;Whatever on earth does that mean?&rdquo; asked Peter, but he did
not expect any answer.</p>
<p>&ldquo;<em>I</em> don&rsquo;t know,&rdquo; said Bobbie.
&ldquo;Perhaps the old gentleman told the people at his station to look out
for us and wave. He knew we should like it!&rdquo;</p>
<p>Now, curiously enough, this was just what had happened. The old
gentleman, who was very well known and respected at his particular 
station, had got there early that morning, and he had waited at the door
where the young man stands holding the interesting machine that clips the
tickets, and he had said something to every single passenger who passed
through that door. And after nodding to what the old gentleman had
said,&mdash;and the nods expressed every shade of surprise, interest,
doubt, cheerful pleasure, and grumpy agreement,&mdash;each passenger had
gone on to the platform and read one</p></div>

<div class="bookPage" id="p301" name="301">
<p>certain part of his newspaper. And
when the passengers got into the train, they had told the other passengers
who were already there what the old gentleman had said, and then the other
passengers had also looked in their newspapers and seemed very astonished
and, mostly, pleased. Then, when the train passed the fence where the three
children were, newspapers and hands and handkerchiefs were waved madly,
till all that side of the train was fluttery with white like the pictures
of the King&rsquo;s Coronation in the biograph at Maskelyne and Cook&rsquo;s.
To the children it almost seemed as though the train itself was alive, and
was at last responding to the love that they had given it so freely and so
long.</p>
<p>&ldquo;It is most extraordinary rum!&rdquo; said Peter.</p>
<p>&ldquo;Most stronery!&rdquo; echoed Phyllis.</p>
<p>But Bobbie said, &ldquo;Don&rsquo;t you think the old gentleman&rsquo;s
waves seemed more significating than usual?&rdquo;</p>
<p>&ldquo;No,&rdquo; said the others.</p>
<p>&ldquo;I do,&rdquo; said Bobbie. &ldquo;I thought he was trying to
explain something to us with his newspaper.&rdquo;</p>
<p>&ldquo;Explain what?&rdquo; asked Peter, not unnaturally.</p>
<p>&ldquo;<em>I</em> don&rsquo;t know,&rdquo; Bobbie answered,
&ldquo;but I do feel most awfully funny. I feel just exactly as if
something was going to happen.&rdquo;</p></div>

<div class="bookPage" id="p302" name="302">
<p>&ldquo;What is going to happen,&rdquo; said Peter, &ldquo;is that
Phyllis&rsquo;s stocking is going to come down.&rdquo;</p>
<p>This was but too true. The suspender had given way in the agitation of
the waves to the 9.15. Bobbie&rsquo;s handkerchief served as first aid to
the injured, and they all went home.</p>
<p>Lessons were more than usually difficult to Bobbie that day. Indeed, she
disgraced herself so deeply over a quite simple sum about the division of
48 pounds of meat and 36 pounds of bread among 144 hungry children that
Mother looked at her anxiously.</p>
<p>&ldquo;Don&rsquo;t you feel quite well, dear?&rdquo; she asked.</p>
<p>&ldquo;I don&rsquo;t know,&rdquo; was Bobbie&rsquo;s unexpected answer.
&ldquo;I don&rsquo;t know how I feel. It isn&rsquo;t that I&rsquo;m lazy. 
Mother, will you let me off lessons to-day? I feel as if I wanted to be
quite alone by myself.&rdquo;</p>
<p>&ldquo;Yes, of course I&rsquo;ll let you off,&rdquo; said Mother;
&ldquo;but&mdash;&rdquo;</p>
<p>Bobbie dropped her slate. It cracked just across the little green mark
that is so useful for drawing patterns round, and it was never the same
slate again. Without waiting to pick it up she bolted. Mother caught her in
the hall feeling blindly among the waterproofs and umbrellas for her garden
hat.</p></div>

<div class="bookPage" id="p303" name="303">
<p>&ldquo;What is it, my sweetheart?&rdquo; said Mother. &ldquo;You
don&rsquo;t feel ill, do you?&rdquo;</p>
<p>&ldquo;I &rdquo;don&rsquo;t know,&rdquo; Bobbie answered, a little
breathless, &ldquo;but I want to be by myself and see if my head really
<em>is</em> all silly and my inside all squirmy-twisty.&rdquo;</p>
<p>&ldquo;Hadn&rsquo;t you better lie down?&rdquo; Mother said, stroking her
hair back from her forehead.</p>
<p>&ldquo;I&rsquo;d be more alive in the garden, I think,&rdquo; said
Bobbie.</p>
<p>But she could not stay in the garden. The hollyhocks and the asters and
the late roses all seemed to be waiting for something to happen. It was one
of those still shiny autumn days, when everything does seem to be
waiting.</p>
<p>Bobbie could not wait.</p>
<p>&ldquo;I&rsquo;ll go down to the station,&rdquo; she said, &ldquo;and
talk to Perks and ask about the signalman&rsquo;s little boy.&rdquo;</p>
<p>So she went down. On the way she passed the old lady from the
Post-office, who gave her a kiss and a hug, but, rather to Bobbie&rsquo;s
surprise, no words except:&mdash;</p>
<p>&ldquo;God bless you, love&mdash;&rdquo; and, after a pause, &ldquo;run
along&mdash;do.&rdquo;</p>
<p>The draper&rsquo;s boy, who had sometimes been a little less than civil
and a little more than contemptuous, now touched his cap, and uttered the
remarkable words:&mdash;</p></div>

<div class="bookPage" id="p304" name="304">
<p>&ldquo;&rsquo;Morning, Miss, I&rsquo;m sure&mdash;&rdquo;</p>
<p>The blacksmith, coming along with an open newspaper in his hand, was
even more strange in his manner. He grinned broadly, though, as a rule, he
was a man not given to smiles, and waved the newspaper long before he came
up to her. And as he passed her, he said, in answer to her &ldquo;Good
morning&rdquo;:&mdash;</p>
<p>&ldquo;Good morning to you, Missie, and many of them! I wish you joy,
that I do!&rdquo;</p>
<p>&ldquo;Oh!&rdquo; said Bobbie to herself, and her heart quickened its
beats, &ldquo;something <em>is</em> going to happen! I know it
is&mdash;every one is so odd, like people are in dreams.&rdquo;</p>
<p>The Station Master wrung her hand warmly. In fact he worked it up and
down like a pump-handle. But he gave her no reason for this unusually
enthusiastic greeting. He only said:&mdash;</p>
<p>&ldquo;The 11.54&rsquo;s a bit late, Miss&mdash;the extra luggage this
holiday time,&rdquo; and went away very quickly into that inner Temple of
his into which even Bobbie dared not follow him.</p>
<p>Perks was not to be seen, and Bobbie shared the solitude of the platform
with the Station Cat. This tortoise-shell lady, usually of a retiring
disposition, came to-day to rub</p></div>

<div class="bookPage" id="p305" name="305">
<p>herself against the brown stockings of
Bobbie with arched back, waving tail, and reverberating purrs.</p>
<p>&ldquo;Dear me!&rdquo; said Bobbie, stooping to stroke her, &ldquo;how
very kind everybody is to-day&mdash;even you, Pussy!&rdquo;</p>
<p>Perks did not appear until the 11.54 was signalled, and then he, like
everybody else that morning, had a newspaper in his hand.</p>
<p>&ldquo;Hullo!&rdquo; he said, &ldquo;&rsquo;ere you are. Well, if
<em>this</em> is the train, it&rsquo;ll be smart work! Well, God bless
you, my dear! I see it in the paper, and I don&rsquo;t think I was ever so
glad of anything in all my born!&rdquo; He looked at Bobbie a moment and
then said, &ldquo;One I must have, Miss, and no offence, 
I know, on a day like this &rsquo;ere!&rdquo; and with that he kissed her,
first on one cheek, and then on the other.</p>
<p>&ldquo;You ain&rsquo;t offended, are you?&rdquo; he asked anxiously.
&ldquo;I ain&rsquo;t took too great a liberty? On a day like this, you
know&mdash;&rdquo;</p>
<p>&ldquo;No, no,&rdquo; said Bobbie, &ldquo;of course it&rsquo;s not a
liberty, dear Mr. Perks; we love you quite as much as if you were an uncle
of ours&mdash;but&mdash;on a day like <em>what</em>?&rdquo;</p>
<p>&ldquo;Like this &rsquo;ere!&rdquo; said Perks. &ldquo;Don&rsquo;t I tell
you I see it in the paper?&rdquo;</p>
<p>&ldquo;Saw <em>what</em> in the paper?&rdquo; asked Bobbie, but
already the 11.54 was steaming into the station and the Station</p></div>

<div class="bookPage" id="p306" name="306">
<p>Master was looking at all the places where Perks was not and ought to have been.</p>
<p>Bobbie was left standing alone, the Station Cat watching her from under
the bench with friendly golden eyes.</p>
<p>Of course you know already exactly what was going to happen. Bobbie was
not so clever. She had the vague, confused, expectant feeling that comes to
one&rsquo;s heart in dreams. What her heart expected I can&rsquo;t
tell,&mdash;perhaps the very thing that you and I know as going to happen.
But her mind expected nothing; it was almost blank, and felt nothing but
tiredness and stupidness and an empty feeling like your body has when you
have been a long walk and it is very far indeed past your proper
dinner-time.</p>
<p>Only three people got out of the 11.54. The first was a countryman a
with two baskety boxes full of live chickens who stuck their russet heads
out anxiously through the wicker bars; the second was Miss Peckitt, the
grocer&rsquo;s wife&rsquo;s cousin, with a tin box and three brown paper
parcels; and the third&mdash;</p>
<p>&ldquo;Oh! my Daddy, my Daddy!&rdquo; That scream went like a knife into
the heart of every one in the train, and people put their heads out of the
windows to see a tall pale man with lips set in a thin close line, and a
little girl clinging to him with arms and legs, while his arms went tightly
round her.</p></div>

