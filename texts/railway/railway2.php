<h2>CHAPTER II<br />
PETER&rsquo;S COAL-MINE</h2>
<p>&ldquo;<strong>WHAT</strong> fun!&rdquo; said Mother, in the
dark, feeling for the matches on the table. &ldquo;How frightened the poor
mice were&mdash;I don&rsquo;t believe they were rats at all.&rdquo;</p>
<p>She struck a match and relighted the candle and every one looked at each
other by its winky, blinky light.</p>
<p>&ldquo;Well,&rdquo; she said, &ldquo;you&rsquo;ve often wanted something
to happen, and now it has. This is quite an adventure, isn&rsquo;t it? I
told Mrs. Viney to get us some bread and butter, and meat and things, and
to have supper ready. I suppose she&rsquo;s laid it in the dining room. So
let&rsquo;s go and see.&rdquo;</p>
<p>The dining room opened out of the kitchen. It looked much darker than
the kitchen when they went in with the one candle. Because the kitchen was
whitewashed, but the dining room was dark wood from floor to ceiling, and
across the ceiling there were heavy black beams. There was a muddled maze
of dusty furniture&mdash;the breakfast-room furniture from the old home where they had lived all their lives. It seemed a very long time ago, and a very long way off.</p></div>

<div class="bookPage" id="p24" name="24">
<p>There was the table certainly, and there were chairs, but there was no
supper.</p>
<p>&ldquo;Let&rsquo;s look in the other rooms,&rdquo; said Mother; and they
looked. And in each room was the same kind of blundering half-arrangement
of furniture, and fire-irons and crockery, and all sorts of odd things on
the floor, but there was nothing to eat; and even in the pantry there was
only a rusty cake-tin and a broken plate with whitening mixed in it.</p>
<p>&ldquo;What a horrid old woman!&rdquo; said Mother; &ldquo;she&rsquo;s
just walked off with the money and not got us anything to eat at
all.&rdquo;</p>
<p>&ldquo;Then shan&rsquo;t we have any supper at all?&rdquo; asked Phyllis,
dismayed, stepping back on to a soap-dish that cracked responsively.</p>
<p>&ldquo;Oh, yes,&rdquo; said Mother, &ldquo;only it&rsquo;ll mean
unpacking one of those big cases that we put in the cellar. Phil, do mind
where you&rsquo;re walking to, there&rsquo;s a dear. Peter, hold the
light.&rdquo;</p>
<p>The cellar door opened out of the kitchen. There were five wooden steps
leading down. It wasn&rsquo;t a proper cellar at all, the children thought, because its ceiling went up as high as the kitchen&rsquo;s. A bacon-rack
hung under its ceiling. There was wood in it, and coal. Also the big
cases.</p></div>

<div class="bookPage" id="p25" name="25">

<p>Peter held the candle, all on one side, while Mother tried to open the
great packing-case. It was very securely nailed down.</p>
<p>&ldquo;Where&rsquo;s the hammer?&rdquo; asked Peter.</p>
<p>&ldquo;That&rsquo;s just it,&rdquo; said Mother. &ldquo;I&rsquo;m afraid
it&rsquo;s inside the box. But there&rsquo;s a coal-shovel&mdash;and
there&rsquo;s the kitchen poker.&rdquo;</p>
<p>And with these she tried to get the case open.</p>
<p>&ldquo;Let me do it,&rdquo; said Peter, thinking he could do it better
himself. Every one thinks this when he sees another person stirring a fire,
or opening a box, or untying a knot in a bit of string.</p>
<p>&ldquo;You&rsquo;ll hurt your hands, Mammy,&rdquo; said Roberta;
&ldquo;let me.&rdquo;</p>
<p>&ldquo;I wish Father was here,&rdquo; said Phyllis; &ldquo;he&rsquo;d get
it open in two shakes. What are you kicking me for, Bobbie?&rdquo;</p>
<p>&ldquo;I wasn&rsquo;t,&rdquo; said Roberta.</p>
<p>Just then the first of the long nails in the packing-case began to come
out with a scrunch. Then a lath was raised and then another, till all four
stood up with the long nails in them shining fiercely like iron teeth in
the candle-light.</p></div>

<div class="bookPage" id="p26" name="26">
<p>&ldquo;Hooray!&rdquo; said Mother; &ldquo;here are some
candles&mdash;the very first thing! You girls go and light them.
You&rsquo;ll find some saucers and things. Just drop a little candle-grease
in the saucer and stick the candle upright in it.&rdquo;</p>
<p>&ldquo;How many shall we light?&rdquo;</p>
<p>&ldquo;As many as ever you like,&rdquo; said Mother, gaily. &ldquo;The
great thing is to be cheerful. Nobody can be cheerful in the dark except
owls and dormice.&rdquo;</p>
<p>So the girls lighted candles. The head of the first match flew off and
stuck to Phyllis&rsquo;s finger; but, as Roberta said, it was only a little
burn, and she might have had to be a Roman martyr and be burned whole if
she had happened to live in the days when those things were
fashionable.</p>
<p>Then when the dining room was lighted by fourteen candles, Roberta
fetched coal and wood, and lighted a fire.</p>
<p>&ldquo;It&rsquo;s very cold for May,&rdquo; she said, feeling what a
grown-up thing it was to say.</p>
<p>The fire-light and the candle-light made the dining room look very
different, for now you could see that the dark walls were of wood, carved
here and there into little wreaths and loops.</p>
<p>The girls hastily &ldquo;tidied&rdquo; the room, which meant putting the chairs against the wall, and piling all the odds and</p></div>

<div class="bookPage" id="p27" name="27">
<p>ends
into a corner and partly hiding them with the big leather arm-chair that
Father used to sit in after dinner.</p>
<p>&ldquo;Bravo!&rdquo; cried Mother, coming in with a tray full of things.
&ldquo;This is something like! I&rsquo;ll just get a table-cloth and
then&mdash;&rdquo;</p>
<p>The table-cloth was in a box with a proper lock that was opened with a
key and not with a shovel, and when the cloth was spread on the table, a
real feast was laid out on it.</p>
<p>Every one was very very tired, but every one cheered up at the sight of
the funny and delightful supper. There were biscuits, the Marie and the
plain kind, sardines, preserved ginger, cooking raisins, and candied peel
and marmalade.</p>
<p>&ldquo;What a good thing Aunt Emma packed up all the odds and ends out
of the Store cupboard,&rdquo; said Mother. &ldquo;Now, Phil,
<em>don&rsquo;t</em> put the marmalade spoon in among the
sardines.&rdquo;</p>
<p>&ldquo;No, I won&rsquo;t, Mother,&rdquo; said Phyllis, and put it down
among the Marie biscuits.</p>
<p>&ldquo;Let&rsquo;s drink Aunt Emma&rsquo;s health,&rdquo; said Roberta,
suddenly; &ldquo;what should we have done if she hadn&rsquo;t packed up
these things? Here&rsquo;s to Aunt Emma!&rdquo;</p>
</div>

<div class="bookPage" id="p28" name="28">
<p>And the toast was drunk in ginger wine and water, out of
willow-patterned tea-cups, because the glasses couldn&rsquo;t be found.</p>
<p>They all felt that they had been a little hard on Aunt Emma. She
wasn&rsquo;t a nice cuddly person like Mother, but after all it was she who
had thought of packing up the odds and ends of things to eat.</p>
<p>It was Aunt Emma, too, who had aired all the sheets ready; and the men
who had moved the furniture had put the bedsteads together, so the beds
were soon made.</p>
<p>&ldquo;Good night, chickies,&rdquo; said Mother. &ldquo;I&rsquo;m sure
there aren&rsquo;t any rats. But I&rsquo;ll leave my door open, and then if a
mouse comes, you need only scream, and I&rsquo;ll come and tell it exactly
what I think of it.&rdquo;</p>
<p>Then she went to her own room. Roberta woke to hear the little
travelling clock chime two. It sounded like a church clock ever so far
away, she always thought. And she heard, too, Mother still moving about in
her room.</p>
<p>Next morning Roberta woke Phyllis by pulling her hair gently, but quite
enough for her purpose.</p>
<p>&ldquo;Wassermarrer?&rdquo; asked Phyllis, still almost wholly
asleep.</p>
<p>&ldquo;Wake up! wake up!&rdquo; said Roberta. &ldquo;We&rsquo;re in the
new house&mdash;don&rsquo;t you remember? No servants or anything.
Let&rsquo;s get up and begin to be useful. We&rsquo;ll just creep down</p></div>

<div class="bookPage" id="p29" name="29">
<p>mouse-quietly, and have everything beautiful before Mother gets up.
I&rsquo;ve woke Peter. He&rsquo;ll be dressed as soon as we are.&rdquo;</p>
<p>So they dressed quietly and quickly. Of course there was no water in
their room, so when they got down they washed as much as they thought was
necessary under the spout of the pump in the yard. One pumped and the other
washed. It was splashy but interesting.</p>
<p>&ldquo;It&rsquo;s much more fun than basiny washing,&rdquo; said Roberta.
&ldquo;How sparkly the weeds are between the stones, and the moss on the
roof&mdash;oh, and the flowers!&rdquo;</p>
<p>The roof of the back kitchen sloped down quite low. It was made of
thatch and it had moss on it, and house-leeks and stone crop and
wall-flowers, and even a clump of purple flag-flowers, at the far
corner.</p>
<p>&ldquo;This is far far farandaway prettier than Edgecombe Villa,&rdquo;
said Phyllis. &ldquo;I wonder what the garden&rsquo;s like.&rdquo;</p>
<p>&ldquo;We mustn&rsquo;t think of the garden yet,&rdquo; said Roberta, with earnest energy. &ldquo;Let&rsquo;s go in and begin to
work.&rdquo;</p>
<p>They lighted the fire and put the kettle on, and they arranged the
crockery for breakfast; they could not find all the right things, but a
glass ash-tray made an excellent salt-cellar, and a newish baking-tin
seemed as if it would do to put bread on, if they had any.</p></div>

<div class="bookPage" id="p30" name="30">

<p>When there seemed to be nothing more that they could do, they went out
again into the fresh bright morning.</p>
<p>&ldquo;We&rsquo;ll go into the garden now,&rdquo; said Peter. But somehow
they couldn&rsquo;t find the garden. They went round the house and round the
house. The yard occupied the back, and across it were stables and
outbuildings. On the other three sides the house stood simply in a field,
without a yard of garden to divide it from the short smooth turf. And yet
they had certainly seen the garden wall the night before.</p>
<p>It was a hilly country. Down below they could see the line of the
railway, and the black yawning mouth of a tunnel. The station was out of
sight. There was a great bridge with tall arches running across one end of
the valley.</p>
<p>&ldquo;Never mind the garden,&rdquo; said Peter; &ldquo;let&rsquo;s go down and look at the railway. There might be trains passing.&rdquo;</p>
<p>&ldquo;We can see them from here,&rdquo; said Roberta, slowly;
&ldquo;let&rsquo;s sit down a bit.&rdquo;</p>
<p>So they all sat down on a great flat gray stone that had pushed itself
up out of the grass; it was one of many that lay about on the hillside, and
when mother came out to look for them at eight o&rsquo;clock, she found them
deeply asleep in a contented sun-warmed bunch.</p></div>

<div class="bookPage" id="pf31" name="">
<img src="illustrations/railway/nesbit-railway031.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></div>

<div class="bookPage" id="p31" name="31">
<p>They had made an excellent fire, and had set the kettle on it at about
half-past five. So that by eight the fire had been out for some time, the
water had all boiled away, and the bottom was burned out of the kettle.
Also they had not thought of washing the crockery before they set the
table.</p>
<p>&ldquo;But it doesn&rsquo;t matter&mdash;the cups and saucers, I
mean,&rdquo; said Mother. &ldquo;Because I&rsquo;ve found another
room&mdash;I&rsquo;d quite forgotten there was one. And it&rsquo;s magic! And
I&rsquo;ve boiled the water for tea in a saucepan.&rdquo;</p>
<p>The forgotten room opened out of the kitchen. In the agitation and half
darkness the night before its door had been mistaken for a cupboard&rsquo;s.
It was a little square room, and on its table, all nicely set out, was a joint of cold roast beef, with bread, butter, cheese,
and a pie.</p>
<p>&ldquo;Pie for breakfast!&rdquo; cried Peter; &ldquo;how perfectly
ripping!&rdquo;</p>
<p>&ldquo;It isn&rsquo;t pigeon-pie,&rdquo; said Mother; &ldquo;it&rsquo;s
only apple. Well, this is the supper we ought to have had last night. And
there was a note from Mrs. Viney. Her son-in-law has broken his arm, and
she had to get home early. She&rsquo;s coming this morning at
ten.&rdquo;</p>
<p>That was a wonderful breakfast. It is unusual to begin the day with cold
apple pie, but the children all said they would rather have it than
meat.</p></div>

<div class="bookPage" id="p32" name="32">
<p>&ldquo;You see it&rsquo;s more like dinner than breakfast to us,&rdquo;
said Peter, passing his plate for more, &ldquo;because we were up so
early.&rdquo;</p>
<p>The day passed in helping Mother to unpack and arrange things. Six small
legs quite ached with running about while their owners carried clothes and
crockery and all sorts of things to their proper places. It was not till
quite late in the afternoon that Mother said:&mdash;</p>
<p>&ldquo;There! That&rsquo;ll do for to-day. I&rsquo;ll lie down for an
hour, so as to be as fresh as a lark by supper-time.&rdquo;</p>
<p>Then they all looked at each other. Each of the three expressive
countenances expressed the same thought. That thought was double, and
consisted, like the bits of information in the Child&rsquo;s Guide to
Knowledge, of a question, and an answer.</p>
<p>Q. Where shall we go?</p>
<p>A. To the railway.</p>
<p>So to the railway they went, and as soon as they started for the railway
they saw where the garden had hidden itself. It was right behind the
stables, and it had a high wall all round.</p>
<p>&ldquo;Oh, never mind about the garden now!&rdquo; cried Peter.
&ldquo;Mother told me this morning where it was. It&rsquo;ll keep till
to-morrow. Let&rsquo;s get to the railway.&rdquo;</p></div>

<div class="bookPage" id="p33" name="33">

<p>The way to the railway was all down hill over smooth, short turf with
here and there furze bushes and gray and yellow rocks sticking out like
candied peel out of the top of a cake.</p>
<p>The way ended in a steep run and a wooden fence,&mdash;and there was the
railway with the shining metals and the telegraph wires and posts and
signals.</p>
<p>They all climbed on to the top of the fence, and then suddenly there was
a rumbling sound that made them look along the line to the right, where the dark mouth of a tunnel opened itself in the face of a rocky cliff; next moment a train had
rushed out of the tunnel with a shriek and a snort, and had slid noisily
past them. They felt the rush of its passing, and the pebbles on the line
jumped and rattled under it as it went by.</p>
<p>&ldquo;Oh!&rdquo; said Roberta, drawing a long breath; &ldquo;it was
like a great dragon tearing by. Did you feel it fan us with its hot
wings?&rdquo;</p>
<p>&ldquo;I suppose a dragon&rsquo;s lair might look very like that tunnel
from the outside,&rdquo; said Phyllis.</p>
<p>But Peter said:&mdash;</p>
<p>&ldquo;I never thought we should ever get as near to a train as this.
It&rsquo;s the most ripping sport!&rdquo;</p></div>

<div class="bookPage" id="p34" name="34">

<p>&ldquo;Better than toy-engines, isn&rsquo;t it?&rdquo; said Roberta.</p>
<p>(I am tired of calling Roberta by her name. I don&rsquo;t see why I
should. No one else did. Every one else called her Bobbie, and I don&rsquo;t
see why I shouldn&rsquo;t.)</p>
<p>&ldquo;I don&rsquo;t know; it&rsquo;s different,&rdquo; said Peter.
&ldquo;It seems so odd to see <em>all</em> of a train. It&rsquo;s
awfully tall, isn&rsquo;t it?&rdquo;</p>
<p>&ldquo;We&rsquo;ve always seen them cut in half by platforms,&rdquo; said
Phyllis.</p>
<p>&ldquo;I wonder if that train was going to London,&rdquo; Bobbie said.
&ldquo;London&rsquo;s where Father is.&rdquo;</p>
<p>&ldquo;Let&rsquo;s go down to the station and find out,&rdquo; said
Peter.</p>
<p>So they went.</p>
<p>They walked along the edge of the line, and heard the telegraph wires
humming over their heads. When you are in the train, it seems such a little
way between post and post, and one after another the posts seem to catch up
the wires almost more quickly than you can count them. But when you have to
walk, the posts seem few and far between.</p>
<p>But the children got to the station at last.</p>
<p>Never before had any of them been at a station, except for the purpose
of catching trains,&mdash;or perhaps waiting for them,&mdash;and always
with grown-ups in attendance,</p></div>

<div class="bookPage" id="p35" name="35">
<p>grown-ups who were not themselves interested
in stations, except as places from which they wished to get away.</p>
<p>Never before had they passed close enough to a signal box to be able to
notice the wires, and to hear the mysterious &ldquo;ping ping,&rdquo;
followed by the strong firm clicking of machinery.</p>
<p>The very sleepers on which the rails lay were a delightful path to
travel by&mdash;just far enough apart to serve as the stepping stones in a game of foaming torrents hastily organised by Bobbie.</p>
<p>Then to arrive at the station, not through the booking office, but in a
freebooting sort of way by the sloping end of the platform. This in itself
was joy.</p>
<p>Joy, too, it was to peep into the porter&rsquo;s room, where the lamps
are, and the Railway almanac on the wall, and one porter half asleep behind
<em>Lloyd&rsquo;s Weekly News</em>.</p>
<p>There were a great many crossing lines at the station; some of them just
ran into a yard and stopped short, as though they were tired of business
and meant to retire for good. Trucks stood on the rails here, and on one
side was a great heap of coal&mdash;not a loose heap, such as you see in
your coal cellar, but a sort of solid building of coals, with large square
blocks of coal outside used just as though they were brick, and built up
till the heap looked like the</p></div>

<div class="bookPage" id="p36" name="36">
<p>picture of the Cities of the Plain in
&ldquo;Bible Stories for Infants.&rdquo; There was a line of whitewash near
the top of the coaly wall.</p>
<p>When, presently, the Porter lounged out of his room at the
twice-repeated tingling thrill of a gong over the station door, Peter said,
&ldquo;How do you do?&rdquo; in his best manner, and hastened to ask what the white mark
was on the coal for.</p>
<p>&ldquo;To mark how much coal there be,&rdquo; said the Porter, &ldquo;so
as we&rsquo;ll know if any one nicks it. So don&rsquo;t you go off with none
in your pockets, young gentleman!&rdquo;</p>
<p>This seemed, at the time, but a merry jest, and Peter felt at once that
the Porter was a friendly sort, with no nonsense about him. But later the
words came back to Peter with a new meaning.</p>
<p>Have you ever gone into a farm-house kitchen on a baking day, and seen
the great crock of dough set by the fire to rise? If you have, and if you
were at that time still young enough to be interested in everything you
saw, you will remember that you found yourself quite unable to resist the
temptation to poke your finger into the soft round of dough that curved
inside the pan like a giant mushroom. And you will remember that your
finger made a dent in the dough, and that slowly, but quite surely, the
dent disappeared, and the dough looked quite</p></div>

<div class="bookPage" id="p37" name="37">
<p>the same as it did before you
touched it. Unless, of course, your hand was extra dirty, in which case, naturally, there would be a little black mark.</p>
<p>Well, it was just like that with the sorrow the children had felt at
Father&rsquo;s going away, and at Mother&rsquo;s being so unhappy. It made a
deep impression, but the impression did not last long.</p>
<p>They soon got used to being without Father, though they did not forget
him; and they got used to not going to school, and to seeing very little of
Mother, who was now almost all day shut up in her upstairs room writing,
writing, writing. She used to come down at tea-tine and read aloud the
stories she had written. They were lovely stories.</p>
<p>The rocks and hills and valleys and trees, the canal, and above all, the
railway, were so new and so perfectly pleasing that the remembrance of the
old life in the villa grew to seem almost like a dream.</p>
<p>Mother had told them more than once that they were &ldquo;quite poor
now,&rdquo; but this did not seem to be anything but a way of speaking.
Grown-up people, even Mothers, often make remarks that don&rsquo;t seem to
mean anything in particular, just for the sake of saying something,
seemingly. There was always enough to eat, and they wore the same kind of nice clothes they had always worn.</p>
</div>

<div class="bookPage" id="p38" name="38">
<p>But in June came three wet days; the rain came down, straight as lances,
and it was very very cold. Nobody could go out, and everybody shivered.
They all went up to the door of Mother&rsquo;s room and knocked.</p>
<p>&ldquo;Well, what is it?&rdquo; asked Mother from inside.</p>
<p>&ldquo;Mother,&rdquo; said Bobbie, &ldquo;mayn&rsquo;t I light a fire? I
do know how.&rdquo;</p>
<p>And Mother said: &ldquo;No, my ducky-love. We mustn&rsquo;t have fires in
June&mdash;coal is so dear. If you&rsquo;re cold, go and have a good romp in
the attic. That&rsquo;ll warm you.&rdquo;</p>
<p>&ldquo;But, Mother, it only takes such a very little coal to make a
fire.&rdquo;</p>
<p>&ldquo;It&rsquo;s more than we can afford, chickeny-love,&rdquo; said
Mother, cheerfully. &ldquo;Now run away, there&rsquo;s
darlings&mdash;I&rsquo;m madly busy!&rdquo;</p>
<p>&ldquo;Mother&rsquo;s always busy now,&rdquo; said Phyllis, in a whisper
to Peter. Peter did not answer. He shrugged his shoulders. He was
thinking.</p>
<p>Thought, however, could not long keep itself from the suitable
furnishing of a bandit&rsquo;s lair in the attic. Peter was the bandit, of
course. Bobbie was his lieutenant, his band of trusty robbers, and, in due course,
the parent of</p>
</div>

<div class="bookPage" id="p39" name="39">
<p>Phyllis, who was the captured maiden for whom a magnificent
ransom&mdash;in horse-beans&mdash;was unhesitatingly paid.</p>
<p>They all went down to tea flushed and joyous as any mountain
brigands.</p>
<p>But when Phyllis was going to add jam to her bread and butter, Mother
said:&mdash;</p>
<p>&ldquo;Jam <em>or</em> butter, dear&mdash;not jam <em>and</em>
butter. We can&rsquo;t afford that sort of reckless luxury
nowadays.&rdquo;</p>
<p>Phyllis finished the slice of bread and butter in silence, and followed
it up by bread and jam. Peter mingled thought and weak tea.</p>
<p>After tea they went back to the attic and he said to his
sisters:&mdash;</p>
<p>&ldquo;I have an idea.&rdquo;</p>
<p>&ldquo;What&rsquo;s that?&rdquo; they asked politely.</p>
<p>&ldquo;I shan&rsquo;t tell you,&rdquo; was Peter&rsquo;s unexpected
rejoinder.</p>
<p>&ldquo;Oh, very well,&rdquo; said Bobbie; and Phil said,
&ldquo;Don&rsquo;t, then.&rdquo;</p>
<p>&ldquo;Girls,&rdquo; said Peter, &ldquo;are always so hasty
tempered.&rdquo;</p>

</div>

<div class="bookPage" id="p40" name="40">
<p>&ldquo;I should like to know what boys are,&rdquo; said Bobbie, with fine disdain. &ldquo;I don&rsquo;t want to know about your
silly ideas.&rdquo;</p>
<p>&ldquo;You&rsquo;ll know some day,&rdquo; said Peter, keeping his own
temper by what looked exactly like a miracle; &ldquo;if you hadn&rsquo;t
been so keen on a row, I might have told you about it being only
noble-heartedness that made me not tell you my idea. But now I shan&rsquo;t
tell you anything at all about it&mdash;so there!&rdquo;</p>
<p>And it was, indeed, some time before he could be induced to say
anything, and when he did it wasn&rsquo;t much. He said:&mdash;</p>
<p>&ldquo;The only reason why I won&rsquo;t tell you my idea that I&rsquo;m
going to do is because it <em>may</em> be wrong, and I don&rsquo;t want
to drag you into it.&rdquo;</p>
<p>&ldquo;Don&rsquo;t you do it if it&rsquo;s wrong, Peter,&rdquo; said
Bobbie; &ldquo;let me do it.&rdquo; But Phyllis said:&mdash;</p>
<p>&ldquo;<em>I</em> should like to do wrong if
<em>you&rsquo;re</em> going to!&rdquo;</p>
<p>&ldquo;No,&rdquo; said Peter, rather touched by this devotion;
&ldquo;it&rsquo;s a forlorn hope, and I&rsquo;m going to lead it. All I ask
is that if Mother asks where I am, you won&rsquo;t blab.&rdquo;</p>
<p>&ldquo;We haven&rsquo;t got anything <em>to</em> blab,&rdquo; said
Bobbie, indignantly.</p>

</div>

<div class="bookPage" id="p41" name="41">
<p>&ldquo;Oh, yes, you have!&rdquo; said Peter, dropping horsebeans through
his fingers. &ldquo;I&rsquo;ve trusted you to the death. You know I&rsquo;m going to do a lone adventure&mdash;and some
people might think it wrong&mdash;I don&rsquo;t. And if Mother asks where I
am, say I&rsquo;m playing at mines.&rdquo;</p>
<p>&ldquo;What sort of mines?&rdquo;</p>
<p>&ldquo;You just say mines.&rdquo;</p>
<p>&ldquo;You might tell <em>us</em>, Pete.&rdquo;</p>
<p>&ldquo;Well, then, <em>coal</em>-mines. But don&rsquo;t you let the
word pass your lips on pain of torture.&rdquo;</p>
<p>&ldquo;You needn&rsquo;t threaten,&rdquo; said Bobbie, &ldquo;and I do
think you might let us help.&rdquo;</p>
<p>&ldquo;If I find a coal-mine, you shall help cart the coal,&rdquo; Peter
condescended to promise.</p>
