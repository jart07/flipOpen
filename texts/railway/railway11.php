<h2>CHAPTER XI<br />
THE HOUND IN THE RED JERSEY</h2>
<p><strong>BOBBIE</strong> knew the secret now. A sheet of old
newspaper wrapped round a parcel&mdash;just a little chance like
that&mdash;had given the secret to her. And she had to go down to tea and
pretend that there was nothing the matter. The pretence was bravely made,
but it wasn&rsquo;t very successful.</p>
<p>For when she came in, every one looked up from its tea and saw her
pink-lidded eyes and her pale face with red tear-blotches on it.</p>
<p>&ldquo;My darling,&rdquo; cried Mother, jumping up from the tea-tray,
&ldquo;whatever <em>is</em> the matter?&rdquo;</p>
<p>&ldquo;My head aches, rather,&rdquo; said Bobbie. And indeed it did.</p>
<p>&ldquo;Has anything gone wrong?&rdquo; Mother asked.</p>
<p>&ldquo;I&rsquo;m all right, really,&rdquo; said Bobbie, and she
telegraphed to her Mother from her swollen eyes this brief, imploring
message&mdash;&ldquo;<em>Not</em> before the others!&rdquo;</p>
<p>Tea was not a cheerful meal. Peter was so distressed by the obvious fact
that something horrid had happened to</p></div>

<div class="bookPage" id="p225" name="225">
<p>Bobbie that he limited his speech to repeating, &ldquo;More bread and butter, please,&rdquo; at
startlingly short intervals. Phyllis stroked her sister&rsquo;s hand under
the table to express sympathy, and knocked her cup over as she did it.
Fetching a cloth and wiping up the spilt milk helped Bobbie a little. But
she thought that tea would never end. Yet at last it did end, as all things
do at last, and when Mother took out the tray, Bobbie followed her.</p>
<p>&ldquo;She&rsquo;s gone to own up,&rdquo; said Phyllis to Peter; &ldquo;I
wonder what she&rsquo;s done.&rdquo;</p>
<p>&ldquo;Broken something, I suppose,&rdquo; said Peter, &ldquo;but she
needn&rsquo;t be so silly over it. Mother never rows for accidents. Listen!
Yes, they&rsquo;re going upstairs. She&rsquo;s taking Mother up to shew
her,&mdash;the water-jug with storks on it, I expect it is.&rdquo;</p>
<p>Bobbie, in the kitchen, had caught hold of Mother&rsquo;s hand as she set
down the tea-things.</p>
<p>&ldquo;What is it?&rdquo; Mother asked.</p>
<p>But Bobbie only said, &ldquo;Come upstairs, come up where nobody can
hear us.&rdquo;</p>
<p>When she had got Mother alone in her room she locked the door and then
stood quite still, and quite without words.</p></div>

<div class="bookPage" id="p226" name="226">
<p>All through tea she had been thinking of what to say; she had decided
that &ldquo;I know all,&rdquo; or &ldquo;All is known to me,&rdquo; or &ldquo;The terrible secret is a secret no longer,&rdquo; would be the proper thing. But now that she and her
Mother and that awful sheet of newspaper were alone in the room together,
she found that she could say nothing.</p>
<p>Suddenly she went to Mother and put her arms round her and began to cry
again. And still she could find no words, only, &ldquo;Oh, Mammy, oh,
Mammy, oh, Mammy,&rdquo; over and over again.</p>
<p>Mother held her very close and waited.</p>
<p>Suddenly Bobbie broke away from her and went to her bed. From under its
mattress she pulled out the paper she had hidden there, and held it out,
pointing to her Father&rsquo;s name with a finger that shook.</p>
<p>&ldquo;Oh, Bobbie,&rdquo; Mother cried, when one little quick look had
shown her what it was, &ldquo;you don&rsquo;t believe it? You don&rsquo;t
believe Daddy did it?&rdquo;</p>
<p>&ldquo;<em>No</em>,&rdquo; Bobbie almost shouted. She had stopped
crying.</p>
<p>&ldquo;That&rsquo;s all right,&rdquo; said Mother. &ldquo;It&rsquo;s not
true. And they&rsquo;ve shut him up in prison, but he&rsquo;s done nothing
wrong. He&rsquo;s good and noble and honourable, and he belongs to us. We
have to think of that, and be proud of him, and wait.&rdquo;</p></div>

<div class="bookPage" id="pf227" name="">
<img src="illustrations/railway/nesbit-railway227.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></div>

<div class="bookPage" id="p227" name="227">
<p>Again Bobbie clung to her Mother, and again only one word came to her, but now that word was &ldquo;Daddy,&rdquo; and
&ldquo;Oh, Daddy, oh, Daddy, oh, Daddy!&rdquo; again and again.</p>
<p>&ldquo;Why didn&rsquo;t you tell me, Mammy?&rdquo; she asked
presently.</p>
<p>&ldquo;Are you going to tell the others?&rdquo; Mother asked.</p>
<p>&ldquo;No.&rdquo;</p>
<p>&ldquo;Why?</p>
<p>&ldquo;Because&mdash;&rdquo;</p>
<p>&ldquo;Exactly,&rdquo; said Mother; &ldquo;so you understand why I
didn&rsquo;t tell you. We two must help each other to be brave.&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Bobbie; &ldquo;Mother, will it make you more
unhappy if you tell me all about it? I want to understand.&rdquo;</p>
<p>So then, sitting cuddled up close to her Mother, Bobbie heard &ldquo;all
about it.&rdquo; She heard how those men, who had asked to see Father on
that remembered last night when the Engine was being mended, had come to
arrest him, charging him with selling State secrets to the
Russians&mdash;with being, in fact, a spy and a traitor. She heard about
the trial, and about the evidence&mdash;letters, found in Father&rsquo;s
desk at the office, letters that convinced the jury that Father was
guilty.</p></div>

<div class="bookPage" id="p228" name="228">
<p>&ldquo;Oh, how could they look at him and believe it!&rdquo; cried
Bobbie; &ldquo;and how could <em>any</em> one do such a
thing!&rdquo;</p>
<p>&ldquo;<em>Some one</em> did it,&rdquo; said Mother, &ldquo;and all
the evidence was against Father. Those letters&mdash;&rdquo;</p>
<p>&ldquo;Yes. How did the letters get into his desk?&rdquo;</p>
<p>&ldquo;Some one put them there. And the person who put them there was
the person who was really guilty.&rdquo;</p>
<p>&ldquo;He must be feeling pretty awful all this time,&rdquo; said
Bobbie, thoughtfully.</p>
<p>&ldquo;I don&rsquo;t believe he had any feelings,&rdquo; Mother said
hotly; &ldquo;he couldn&rsquo;t have done a thing like that if he
had.&rdquo;</p>
<p>&ldquo;Perhaps he just shoved the letters into the desk to hide them
when he thought he was going to be found out. Why don&rsquo;t you tell the
lawyers, or some one, that it must have been that person? There wasn&rsquo;t
any one that would have hurt Father on purpose, was there?&rdquo;</p>
<p>&ldquo;I don&rsquo;t know&mdash;I don&rsquo;t know. The man under him who
got Daddy&rsquo;s place when he&mdash;when the awful thing happened&mdash;he
was always jealous of your Father because Daddy was so clever and every one
thought such a lot of him. And Daddy never quite trusted that
man.&rdquo;</p>
<p>&ldquo;Couldn&rsquo;t we explain all that to some one?&rdquo;</p></div>

<div class="bookPage" id="p229" name="229">
<p>&ldquo;Nobody will listen,&rdquo; said Mother, very bitterly,
&ldquo;nobody at all. Do you suppose I&rsquo;ve not tried everything? No, my
dearest, there&rsquo;s nothing to be done. All we can do, you and I and
Daddy, is to be brave, and patient, and&mdash;&rdquo; she spoke very
softly&mdash;&ldquo;to pray, Bobbie dear.&rdquo;</p>
<p>&ldquo;Mother, you&rsquo;ve got very thin,&rdquo; said Bobbie,
abruptly.</p>
<p>&ldquo;A little, perhaps.&rdquo;</p>
<p>&ldquo;And oh,&rdquo; said Bobbie, &ldquo;I do think you&rsquo;re the
bravest person in the world as well as the nicest!&rdquo;</p>
<p>&ldquo;We won&rsquo;t talk of all this any more, will we, dear?&rdquo;
said Mother, &ldquo;we must bear it and be brave. And, darling, try not to
think of it. Try to be cheerful, and to amuse yourself and the others.
It&rsquo;s much easier for me if you can be a little bit happy and enjoy
things. Wash your poor little round face, and let&rsquo;s go out into the
garden for a bit.&rdquo;</p>
<p>The other two were very gentle and kind to Bobbie. And they did not ask
her what was the matter. This was Peter&rsquo;s idea, and he had drilled
Phyllis, who would have asked a hundred questions if she had been left to
herself.</p>
<p>A week later Bobbie managed to get away alone. And once more she wrote a letter. And once more it was to the old gentleman.</p></div>

<div class="bookPage" id="p230" name="230">
<p>&ldquo;MY DEAR FRIEND,&rdquo; she said, &ldquo;you see what is in this
paper. It is not true. Father never did it. Mother says some one put the
papers in Father&rsquo;s desk, and she says the man under him that got
Father&rsquo;s place afterwards was jealous of Father, and Father suspected
him a long time. But nobody listens to a word she says, but you are so good
and clever, and you found out about the Russian gentleman&rsquo;s wife
directly. Can&rsquo;t you find out who did the treason because it
wasn&rsquo;t Father upon my honour; he is an Englishman and uncapable to do
such things, and then they would let Father out of prison. It is dreadful,
and Mother is getting so thin. She told us once to pray for all prisoners
and captives. I see now. Oh, do help me&mdash;there is only just Mother and
me know, and we can&rsquo;t do anything. Peter and Phil don&rsquo;t know.
I&rsquo;ll pray for you twice every day as long as I live if you&rsquo;ll
only try&mdash;just try to find out. Think if it was <em>your</em>
Daddy, what you would feel. Oh, do, do, <em>do</em> help me. With
love</p>
<p>&ldquo;I remain Your affectionately little friend<br />
&ldquo;ROBERTA.</p>
<p><strong>&ldquo;P.S. Mother would send her kind regards if she knew I am
writing&mdash;but it is no use telling her I am, in case you can&rsquo;t do
anything. But I know you will. Bobbie with best love.&rdquo;</strong></p></div>

<div class="bookPage" id="pf231" name="">
<p><img src="illustrations/railway/nesbit-railway231.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></p></div>

<div class="bookPage" id="p231" name="231">
<p>She cut the account of her Father&rsquo;s trial out of the newspaper with
Mother&rsquo;s big cutting-out scissors, and put it in the envelope with her
letter.</p>
<p>Then she took it down to the station, going out the back way and round
by the road, so that the others should not see her and offer to come with
her, and she gave the letter to the Station Master to give to the old
gentleman next morning.</p>
<p>&ldquo;Where <em>have</em> you been?&rdquo; shouted Peter, from the
top of the yard wall where he and Phyllis were.</p>
<p>&ldquo;To the station, of course,&rdquo; said Bobbie; &ldquo;give us a
hand, Pete.&rdquo;</p>
<p>She set her foot on the lock of the yard door. Peter reached down a
hand.</p>
<p>&ldquo;What on earth?&rdquo; she asked as she reached the
wall-top&mdash;for Phyllis and Peter were very very muddy. A lump of wet
clay lay between them on the wall, they had each a slip of slate in a very
dirty hand, and behind Peter, out of the reach of accidents, were several strange rounded objects rather like very fat sausages, hollow, but closed up at one end.</p>
<p>&ldquo;It&rsquo;s nests,&rdquo; said Peter, &ldquo;swallows&rsquo; nests.
We&rsquo;re going to dry them in the oven and hand them up with string under
the eaves of the coachhouse.&rdquo;</p></div>

<div class="bookPage" id="p232" name="232">
<p>&ldquo;Yes,&rdquo; said Phyllis; &ldquo;and then we&rsquo;re going to
save up all the wool and hair we can get, and in the spring we&rsquo;ll line
them, and then how pleased the swallows will be!&rdquo;</p>
<p>&ldquo;I&rsquo;ve often thought people don&rsquo;t do nearly enough for
dumb animals,&rdquo; said Peter, with an air of virtue. &ldquo;I do think
people might have thought of making nests for poor little swallows before
this.&rdquo;</p>
<p>&ldquo;Oh,&rdquo; said Bobbie, vaguely, &ldquo;if everybody thought of
everything, there&rsquo;d be nothing left for anybody else to think
about.&rdquo;</p>
<p>&ldquo;Look at the nests&mdash;aren&rsquo;t they pretty?&rdquo; said
Phyllis, reaching across Peter to grasp a nest.</p>
<p>&ldquo;Look out, Phil, you goat,&rdquo; said her brother. But it was too
late; her strong little fingers had crushed the nest.</p>
<p>&ldquo;There now,&rdquo; said Peter.</p>
<p>&ldquo;Never mind,&rdquo; said Bobbie.</p>
<p>&ldquo;It is one of my own,&rdquo; said Phyllis, &ldquo;so you
needn&rsquo;t jaw, Pete. Yes, we&rsquo;ve put our initial names on the ones
we&rsquo;ve done, so that the swallows will know who they&rsquo;ve got to be
so grateful to and fond of.&rdquo;</p>
<p>&ldquo;Swallows can&rsquo;t read, silly,&rdquo; said Peter.</p>
<p>&ldquo;Silly yourself,&rdquo; retorted Phyllis; &ldquo;how do you
know?&rdquo;</p></div>

<div class="bookPage" id="p233" name="233">
<p>&ldquo;Who thought of making the nests, anyhow?&rdquo; shouted
Peter.</p>
<p>&ldquo;I did,&rdquo; screamed Phyllis.</p>
<p>&ldquo;Nya,&rdquo; rejoined Peter, &ldquo;you only thought of making hay
ones and sticking them in the ivy for the sparrows, and they&rsquo;d have
been sopping long before egg-laying time. It was me said clay and
swallows.&rdquo;</p>
<p>&ldquo;I don&rsquo;t care what you said.&rdquo;</p>
<p>&ldquo;Look,&rdquo; said Bobbie, &ldquo;I&rsquo;ve made the nest all
right again. Give me the bit of stick to mark your initial name on it. But
how can you? Your letter and Peter&rsquo;s are the same. P. for Peter, P.
for Phyllis.&rdquo;</p>
<p>&ldquo;I put F. for Phyllis,&rdquo; said the child of that name.
&ldquo;That&rsquo;s how it sounds. The swallows wouldn&rsquo;t spell Phyllis
with a P, I&rsquo;m certain-sure.&rdquo;</p>
<p>&ldquo;They can&rsquo;t spell at all,&rdquo; Peter was still
insisting.</p>
<p>&ldquo;Then why do you see them always on Christmas cards and valentines
with letters round their necks? How would they know where to go if they
couldn&rsquo;t read?&rdquo;</p>
<p>&ldquo;That&rsquo;s only in pictures. You never saw one really with
letters round its neck.&rdquo;</p></div>

<div class="bookPage" id="p234" name="234">
<p>&ldquo;Well, I have a pigeon, then; at least Daddy told me they did.
Only it was under their wings and not round their necks, but it comes to
the same thing, and&mdash;&rdquo;</p>
<p>&ldquo;I say,&rdquo; interrupted Bobbie, &ldquo;there&rsquo;s to be a
paper-chase to-morrow.&rdquo;</p>
<p>&ldquo;Who?&rdquo; Peter asked.</p>
<p>&ldquo;Grammar School. Perks thinks the hare will go along by the line
at first. We might go along the cutting. You can see a long way from
there.&rdquo;</p>
<p>The paper-chase was found to be a more amusing subject of conversation
than the reading powers of swallows. Bobbie had hoped it might be. And next
morning Mother let them take their lunch and go out for the day to see the
paper-chase.</p>
<p>&ldquo;If we go to the cutting,&rdquo; said Peter, &ldquo;we shall see
the workmen, even if we miss the paper-chase.&rdquo;</p>
<p>Of course it had taken some time to get the line clear from the rocks
and earth and trees that had fallen on it when the great landslip happened. That was the occasion,
you will remember, when the three children saved the train from being
wrecked by waving six little red-flannel-petticoat flags. It is always
interesting to watch people working, especially when they work with such
interesting things as spades and picks and shovels and</p></div>

<div class="bookPage" id="p235" name="235">
<p>planks and barrows, when they have cindery red fires in iron pots with round holes in them, and
red lamps hanging near the works at night. Of course the children were never out at night; but once, at dusk, when Peter had got out of his bedroom skylight on to the roof, he had seen the red lamp shining far away at the edge of the cutting. The children had often been down to watch the
work, and this day the interest of picks and spades and barrows being
wheeled along planks completely put the paper-chase out of their heads, so
that they quite jumped when a voice just behind them panted, &ldquo;Let me
pass, please.&rdquo; It was the hare&mdash;a big-boned, loose-limbed boy,
with dark hair lying flat on a very damp forehead. The bag of torn paper
under his arm was fastened across one shoulder by a strap. The children
stood back. The hare ran along by the line. and the workmen leaned on their
picks to watch him. He ran on steadily and disappeared into the mouth of the tunnel.</p>
<p>&ldquo;That&rsquo;s against the by-laws,&rdquo; said the foreman.</p>
<p>&ldquo;Why worry?&rdquo; said the oldest workman; &ldquo;live and let
live&rsquo;s what I always say. Ain&rsquo;t you never been young yourself,
Mr. Bates?&rdquo;</p>
<p>&ldquo;I ought to report him,&rdquo; said the foreman.</p>
<p>&ldquo;Why spoil sport&rsquo;s what I always say.&rdquo;</p></div>

<div class="bookPage" id="p236" name="236">
<p>&ldquo;Passengers are forbidden to cross the line on any
pretence,&rdquo; murmured the foreman, doubtfully.</p>
<p>&ldquo;He ain&rsquo;t no passenger,&rdquo; said one of the workmen.</p>
<p>&ldquo;Nor &rsquo;e ain&rsquo;t crossed the line, not where we could see
&rsquo;im do it,&rdquo; said another.</p>
<p>&ldquo;Nor yet &rsquo;e ain&rsquo;t made no pretences,&rdquo; said a
third.</p>
<p>&ldquo;And,&rdquo; said the oldest workman, &ldquo;&rsquo;e&rsquo;s outer
sight now. What the eye don&rsquo;t see the &rsquo;art needn&rsquo;t take no
notice of&rsquo;s what I always say.&rdquo;</p>
<p>And now, following the track of the hare by the little white blots of
scattered paper, came the hounds. There were thirty of them, and they all
came down the steep, ladder-like steps by ones and twos and threes and
sixes and sevens. Bobbie and Phyllis and Peter counted them as they 
passed. The foremost ones hesitated a moment at the foot of the ladder,
then their eyes caught the gleam of scattered whiteness along the line and
they turned towards the tunnel, and, by ones and twos and threes and sixes
and sevens, disappeared in the dark mouth of it. The last one, in a red
jersey, seemed to be extinguished by the darkness like a candle that is
blown out.</p>
<p>&ldquo;They don&rsquo;t know what they&rsquo;re in for,&rdquo; said the
foreman; &ldquo;it isn&rsquo;t so easy running in the dark. The tunnel takes
two or three turns.&rdquo;</p></div>

<div class="bookPage" id="p237" name="237">
<p>&ldquo;They&rsquo;ll take a long time to get through, you think?&rdquo;
Peter asked.</p>
<p>&ldquo;An hour or more, I shouldn&rsquo;t wonder.&rdquo;</p>
<p>&ldquo;Then let&rsquo;s cut across the top and see them come out at the
other end,&rdquo; said Peter; &ldquo;we shall get there long before they
do.&rdquo;</p>
<p>The counsel seemed good, and they went.</p>
<p>They climbed the steep steps from which they had picked the wild cherry
blossom for the grave of the little wild rabbit, and reaching the top of
the cutting, set their faces towards the hill through which the tunnel was
cut. It was stiff work.</p>
<p>&ldquo;It&rsquo;s like Alps,&rdquo; said Bobbie, breathlessly.</p>
<p>&ldquo;Or Andes,&rdquo; said Peter.</p>
<p>&ldquo;It&rsquo;s like Himmy what&rsquo;s its names?&rdquo; gasped
Phyllis. &ldquo;Mount Everlasting. Do let&rsquo;s stop.&rdquo;</p>
<p>&ldquo;Stick to it,&rdquo; panted Peter; &ldquo;you&rsquo;ll get your
second wind in a minute.&rdquo;</p>
<p>Phyllis consented to stick to it&mdash;and on they went, running when
the turf was smooth and the slope easy, climbing over stones, helping
themselves up rocks by the branches of trees, creeping through narrow
openings</p> </div>

<div class="bookPage" id="p238" name="238">
<p>between tree trunks and rocks, and so on and on, up and up, till
at last they stood on the very top of the hill where they had so often
wished to be.</p>
<p>&ldquo;Halt!&rdquo; cried Peter, and threw himself flat on the grass.
For the very top of the hill was a smooth, turfed table-land, dotted with
mossy rocks and little mountain-ash trees.</p>
<p>The girls also threw themselves down flat.</p>
<p>&ldquo;Plenty of time,&rdquo; Peter panted; &ldquo;the rest&rsquo;s all
down hill.&rdquo;</p>
<p>When they were rested enough to sit up and look round them, Bobbie
cried:&mdash;</p>
<p>&ldquo;Oh, look!&rdquo;</p>
<p>&ldquo;What at?&rdquo; said Phyllis.</p>
<p>&ldquo;The view,&rdquo; said Bobbie.</p>
<p>&ldquo;I hate views,&rdquo; said Phyllis, &ldquo;don&rsquo;t you,
Peter?&rdquo;</p>
<p>&ldquo;Let&rsquo;s get on,&rdquo; said Peter.</p>
<p>&ldquo;But this isn&rsquo;t a view like they take you to in carriages
when you&rsquo;re at the seaside, all sea and sand and bare hills. It&rsquo;s
like the &lsquo;coloured counties&rsquo; in one of Mother&rsquo;s poetry
books.&rdquo;</p></div>

<div class="bookPage" id="p239" name="239">
<p>&ldquo;It&rsquo;s not so dusty,&rdquo; said Peter; &ldquo;look at the
Aqueduct straddling slap across the valley like a giant centipede, and then
the towns sticking their church spires up out of the trees like pens out of
an inkstand. <em>I</em> think it&rsquo;s more like
<Q>&ldquo;There could he see the bannersOf twelve fair cities
shine.&rdquo;</Q></p>
<p>&ldquo;I love it,&rdquo; said Bobbie; &ldquo;it&rsquo;s worth the
climb.&rdquo;</p>
<p>&ldquo;The paper-chase is worth the climb,&rdquo; said Phyllis,
&ldquo;if we don&rsquo;t lose it. Let&rsquo;s get on. It&rsquo;s all down hill
now.&rdquo;</p>
<p>&ldquo;<em>I</em> said that ten minutes ago,&rdquo; said Peter.</p>
<p>&ldquo;Well, <em>I&rsquo;ve</em> said it now,&rdquo; said Phyllis;
&ldquo;come on.&rdquo;</p>
<p>&ldquo;Loads of time,&rdquo; said Peter. And there was. For when they
had got down to a level with the top of the tunnel&rsquo;s mouth,&mdash;they
were a couple of hundred yards out of their reckoning and had to creep
along the face of the hill,&mdash;there was no sign of the hare or the
hounds.</p>
<p>&ldquo;They&rsquo;ve gone long ago, of course,&rdquo; said Phyllis, as
they leaned on the brick parapet above the tunnel.</p>
<p>&ldquo;I don&rsquo;t think so,&rdquo; said Bobbie, &ldquo;but even if
they had, it&rsquo;s ripping here, and we shall see the trains come out of
the tunnel like dragons out of lairs. We&rsquo;ve never seen that from the
top side before.&rdquo;</p>
<p>&ldquo;No more we have,&rdquo; said Phyllis, partially appeased.</p></div>

<div class="bookPage" id="p240" name="240">
<p>It was really a most exciting place to be in. The top of the tunnel
seemed ever so much farther from the line than they had expected, and it
was like being on a bridge, but a bridge overgrown with bushes and creepers
and grass and wild-flowers.</p>
<p>&ldquo;I know the paper-chase has gone long ago,&rdquo; said Phyllis
every two minutes, and she hardly knew whether she was pleased or
disappointed when Peter, leaning over the parapet, suddenly
cried:&mdash;</p>
<p>&ldquo;Look out. Here he comes!&rdquo;</p>
<p>They all leaned over the sun-warmed brick wall in time to see the hare,
going very slowly, come out from the shadow of the tunnel.</p>
<p>&ldquo;There, now,&rdquo; said Peter, &ldquo;what did I tell you? Now
for the hounds!&rdquo;</p>
<p>Very soon came the hounds&mdash;by ones and twos and threes and sixes
and sevens&mdash;and they also were going slowly and seemed very tired. Two or three who lagged far
behind came out long after the others.</p>
<p>&ldquo;There,&rdquo; said Bobbie, &ldquo;that&rsquo;s all&mdash;now what
shall we do?&rdquo;</p>
<p>&ldquo;Go along into the tulgy wood over there and have lunch,&rdquo;
said Phyllis; &ldquo;we can see them for miles from up here.&rdquo;</p></div>

<div class="bookPage" id="p241" name="241">
<p>&ldquo;Not yet,&rdquo; said Peter. &ldquo;That&rsquo;s not the last.
There&rsquo;s the one in the red jersey to come yet. Let&rsquo;s see the last
of them come out.&rdquo;</p>
<p>But though they waited and waited and waited, the boy in the red jersey
did not appear.</p>
<p>&ldquo;Oh, let&rsquo;s have lunch,&rdquo; said Phyllis; &ldquo;I&rsquo;ve
got a pain in my front with being so hungry. You must have missed seeing
the red-jerseyed one when he came out with the others&mdash;&rdquo;</p>
<p>But Bobbie and Peter agreed that he had not come out with the
others.</p>
<p>&ldquo;Let&rsquo;s get down to the tunnel mouth,&rdquo; said Peter;
&ldquo;then perhaps we shall see him coming along from the inside. I expect
he felt spun-chuck, and rested in one of the manholes. You stay up here and
watch, Bob, and when I signal from below, you come down. We might miss
seeing him on the way down, with all these trees.&rdquo;</p>
<p>So the others climbed down and Bobbie waited till they signalled to her
from the line below. And then she, too, scrambled down the roundabout
slippery path among tree roots and moss till she stepped out between two
dogwood trees and joined the others on the line. And still there was no
sign of the hound with the red jersey.</p>
<p>&ldquo;Oh, do, <em>do</em> let&rsquo;s have something to eat,&rdquo;
wailed Phyllis. &ldquo;I shall die if you don&rsquo;t, and then you&rsquo;ll
be sorry.&rdquo;</p></div>

<div class="bookPage" id="p242" name="242">
<p>&ldquo;Give her the sandwiches, for goodness&rsquo; sake, and stop her
silly mouth,&rdquo; said Peter, not quite unkindly. &ldquo;Look
here,&rdquo; he added, turning to Bobbie, &ldquo;perhaps we&rsquo;d better
have one each, too. We may need all our strength. Not more than one,
though. There&rsquo;s no time.&rdquo;</p>
<p>&ldquo;What?&rdquo; asked Bobbie, her mouth already full, for she was
just as hungry as Phyllis.</p>
<p>&ldquo;Don&rsquo;t you see,&rdquo; replied Peter, impressively,
&ldquo;that red-jerseyed hound has had an accident&mdash;that&rsquo;s what
it is. Perhaps even as we speak he&rsquo;s lying with his head on the
metals, an unresisting prey to any passing express&mdash;&rdquo;</p>
<p>&ldquo;Oh, don&rsquo;t try to talk like a book,&rdquo; cried Bobbie,
bolting what was left of her sandwich; &ldquo;come on, Phil, keep close
behind me, and if a train comes, stand flat against the tunnel wall and hold your petticoats close to
you.&rdquo;</p>
<p>&ldquo;Give me one more sandwich,&rdquo; pleaded Phyllis, &ldquo;and I
will.&rdquo;</p>
<p>&ldquo;I&rsquo;m going first,&rdquo; said Peter; &ldquo;it was my
idea,&rdquo; and he went.</p>
<p>Of course you know what going into a tunnel is like? The engine gives a
scream, and then suddenly the noise of the running, rattling train changes
and grows different and much louder. Grown-up people pull up the windows
and hold them by the strap. The railway carriage suddenly</p> </div>

<div class="bookPage" id="p243" name="243">
<p>grows like night&mdash;with lamps, of course, unless you are in a slow local train, in
which case lamps are not always provided. Then by and by the darknessoutside the carriage window is touched by puffs of cloudy whiteness, then you see a blue light on the walls of the tunnel, then the sound of the
moving train changes once more, and you are out in the good open air again,
and grown-ups let the straps go. The windows, all dim with the yellow
breath of the tunnel, rattle down into their places, and you see once more
the dip and catch of the telegraph wires beside the line, and the
straight-cut hawthorn hedges with the tiny baby trees growing up out of
them every thirty yards.</p>
<p>All this, of course, is what a tunnel means when you are in a train. But
everything is quite different when you walk into a tunnel on your own feet,
and tread on shifting sliding stones and gravel on a path that curves
downwards from the shining metals to the wall. Then you see slimy, oozy
trickles of water running down the inside of the tunnel, and you notice
that the bricks are not red or brown, as they are at the tunnel&rsquo;s
mouth, but dull, sticky, sickly green. Your voice, when you speak, is quite
changed from what it was out in the sunshine, and it is a long time before
the tunnel is quite dark.</p></div>

<div class="bookPage" id="p244" name="244">
<p>It was not yet quite dark in the tunnel when Phyllis caught at
Bobbie&rsquo;s skirt, ripping out half a yard of gathers, but no one noticed
this at the time.</p>
<p>&ldquo;I want to go back,&rdquo; she said, &ldquo; I don&rsquo;t like it.
It&rsquo;ll be pitch dark in a minute. I <em>won&rsquo;t</em> go on in
the dark. I don&rsquo;t care what you say, I
<em>won&rsquo;t</em>.&rdquo;</p>
<p>&ldquo;Don&rsquo;t be a silly cuckoo,&rdquo; said Peter; &ldquo;I&rsquo;ve
got a candle end and matches, and &mdash; what&rsquo;s that?&rdquo;</p>
<p>&ldquo;That&rdquo; was a low, humming sound on the railway line, a
trembling of the wires beside it, a buzzing, humming sound that grew louder
and louder as they listened.</p>
<p>&ldquo;It&rsquo;s a train,&rdquo; said Bobbie.</p>
<p>&ldquo;Which line?&rdquo;</p>
<p>Nobody knew.</p>
<p>&ldquo;Let me go back,&rdquo; cried Phyllis, struggling to get away from
the hand by which Bobbie held her.</p>
<p>&ldquo;Don&rsquo;t be a coward,&rdquo; said Bobbie; &ldquo;it&rsquo;s
quite safe. Stand back.&rdquo;</p>
<p>&ldquo;Come on,&rdquo; shouted Peter, who was a few yards ahead.
&ldquo;Quick! Manhole!&rdquo;</p>
<p>The roar of the advancing train was now louder than the noise you hear
when your head is under water in the</p></div>

<div class="bookPage" id="p245" name="245">
<p>bath and both taps are running, and
you are kicking with your heels against the bath&rsquo;s tin sides. But
Peter had shouted for all he was worth, and Bobbie heard him. She dragged Phyllis along to the manhole. Phyllis, of course, stumbled over the wires
and grazed both her legs. But they dragged her in, and all three stood in
the dark, damp, arched recess while the train roared louder and louder. It
seemed as if it would deafen them. And, in the distance, they could see its
eyes of fire growing bigger and brighter every instant.</p>
<p>&ldquo;It <em>is</em> a dragon&mdash;I always knew it was&mdash;it
takes its own shape in here, in the dark,&rdquo; shouted Phyllis. But nobody heard her. You see the train was shouting, too, and its voice was bigger than hers.</p>
<p>And now, with a rush and a roar and a rattle and a long dazzling flash
of lighted carriage windows, a smell of smoke, and blast of hot air, the
train hurtled by, clanging and jangling and echoing in the vaulted roof of
the tunnel. Phyllis and Bobbie clung to each other. Even Peter caught hold
of Bobbie&rsquo;s arm, &ldquo;in case she should be frightened,&rdquo; as he
explained afterwards.</p>
<p>And now, slowly and gradually, the tail-lights grew smaller and smaller,
and so did the noise, till with one last <em>whiz</em> the train got
itself out of the tunnel, and silence settled again on its damp walls and
dripping roof.</p></div>

<div class="bookPage" id="p246" name="246">
<p>&ldquo;<em>Oh</em>!&rdquo; said the children, all together in a
whisper.</p>
<p>Peter was lighting the candle end with a hand that trembled.</p>
<p>&ldquo;Come on,&rdquo; he said; but he had to clear his throat before he
could speak in his natural voice.</p>
<p>&ldquo;Oh,&rdquo; said Phyllis, &ldquo;if the red-jerseyed one was in
the way of the train!&rdquo;</p>
<p>&ldquo;We&rsquo;ve got to go and see,&rdquo; said Peter.</p>
<p>&ldquo;Couldn&rsquo;t we go and send some one from the station?&rdquo;
said Phyllis.</p>
<p>&ldquo;Would you rather wait here for us?&rdquo; asked Bobbie, severely,
and of course that settled the question.</p>
<p>So the three went on into the deeper darkness of the tunnel. Peter led,
holding his candle end high to light the way. The grease ran down his
fingers, and some of it right up his sleeve. He found a long streak from
wrist to elbow when he went to bed that night.</p>
<p>It was not more than a hundred and fifty yards from the spot where they
had stood while the train went by that Peter stood still, shouted
&ldquo;Hullo,&rdquo; and then went on much quicker than before. When the
others caught him up, he stopped. And he stopped within a yard of what they
had come into the tunnel to look for. Phyllis saw a</p> </div>

<div class="bookPage" id="p247" name="247">
<p>gleam of red, and shut her eyes tight. There, by the curved, pebbly down line, was the
red-jerseyed hound. His back was against the wall, his arms hung limply by
his sides, and his eyes were shut.</p>
<p>&ldquo;Was the red, blood? Is he all killed?&rdquo; asked Phyllis,
screwing her eyelids more tightly together.</p>
<p>&ldquo;Killed? Nonsense!&rdquo; said Peter. &ldquo;There&rsquo;s 
nothing red about him except his jersey. He&rsquo;s only fainted. What on
earth are we to do?&rdquo;</p>
<p>&ldquo;Can we move him?&rdquo; asked Bobby.</p>
<p>&ldquo;I don&rsquo;t know; he&rsquo;s a big chap.&rdquo;</p>
<p>&ldquo;Suppose we bathed his forehead with water. No, I know we
haven&rsquo;t any, but milk&rsquo;s just as wet. There&rsquo;s a whole
bottle.&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Peter, &ldquo;and they rub people&rsquo;s hands,
I believe, and say, &lsquo;Oh, look up, speak to me! For my sake,
speak!&rsquo;&rdquo;</p>
<p>&ldquo;They burn feathers, I know,&rdquo; said Phyllis.</p>
<p>&ldquo;What&rsquo;s the good of saying that when we haven&rsquo;t any
feathers?&rdquo;</p>
<p>&ldquo;As it happens,&rdquo; said Phyllis, in a tone of exasperated
