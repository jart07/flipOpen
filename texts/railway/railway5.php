<h2>CHAPTER V</h2>
<h2>PRISONERS AND CAPTIVES</h2>
<p><strong>IT</strong> was one day when Mother had gone to
Maidbridge. She had gone alone, but the children were to go to the station
to meet her. And, loving the station as they did, it was only natural that
they should be there a good hour before there was any chance of
Mother&rsquo;s train arriving, even if the train were punctual, which was
most unlikely. No doubt they would have been just as early, even if it had
been a fine day, and all the delights of woods and fields and rocks and
rivers had been open to them. But it happened to be a very wet day and, for
July, very cold. There was a wild wind that drove flocks of dark purple
clouds across the sky &ldquo;like herds of dream-elephants,&rdquo; as
Phyllis said. And the rain stung sharply, so that the way to the station
was finished at a run. Then the rain fell faster and harder, and beat
slantwise against the windows of the booking office and of the chill place
that has General Waiting Room on its door.</p></div>

<div class="bookPage" id="p100" name="100">
<p>&ldquo;It&rsquo;s like being in a besieged castle,&rdquo; Phyllis said;
&ldquo;look at the arrows of the foe striking against the
battlements!&rdquo;</p>
<p>&ldquo;It&rsquo;s much more like a great garden-squirt,&rdquo; said
Peter.</p>
<p>They decided to wait on the up side, for the down platform looked very
wet indeed, and the rain was driving right into the little bleak shelter
where down-passengers have to wait for their trains.</p>
<p>The hour would be full of incident and of interest, for there would be
two up trains, and one down to look at before the one that should bring
Mother back.</p>
<p>&ldquo;Perhaps it&rsquo;ll have stopped raining by then,&rdquo; said
Bobbie; &ldquo;anyhow, I&rsquo;m glad I brought Mother&rsquo;s waterproof and
umbrella.&rdquo;</p>
<p>They went into the desert spot labelled General Waiting Room, and the
time passed pleasantly enough in a game of advertisements. You know the
game, of course? It is something like dumb Crambo. The players take it in
turns to go out, and then come back and look as like some advertisement as
they can, and the others have to guess what advertisement it is meant to
be. Bobbie came in and sat down under Mother&rsquo;s umbrella and made a
sharp face, and every one knew she</p></div>

<div class="bookPage" id="p101" name="101">
<p>was the fox who sits under the umbrella in the advertisement. Phyllis tried
to make a Magic Carpet of Mother&rsquo;s waterproof, but it would not stand
out stiff and raft-like as a Magic Carpet should, and nobody could guess
it. Every one thought Peter was carrying things a little too far when he
blacked his face all over with coal-dust and struck a spidery attitude and
said he was the blot that advertises somebody&rsquo;s Blue Black Writing
Fluid.</p>
<p>It was Phyllis&rsquo;s turn again, and she was trying to look like the
Sphinx that advertises What&rsquo;s-his-name&rsquo;s Personally Conducted
Tours up the Nile when the sharp ting of the signal announced the up train.
The children rushed out to see it pass. On its engine were the particular
driver and fireman who were now numbered among the children&rsquo;s dearest
friends. Courtesies passed between them. Jim asked after the toy engine,
and Bobbie pressed on his acceptance a moist greasy package of toffee that
she had made herself.</p>
<p>Charmed by this attention, the engine-driver consented to consider her
request that some day he would take Peter for a ride on the engine.</p>
<p>&ldquo;Stand back, Mates,&rdquo; cried the engine-driver, suddenly,
&ldquo;and horf she goes.&rdquo;</p></div>

<div class="bookPage" id="p102" name="102">
<p>And sure enough, off the train went. The children watched the
tail-lights of the train till it disappeared round the curve of the line,
and then turned to go back to the dusty freedom of the General Waiting Room
and the joys of the advertisement game.</p>
<p>They expected to see just one or two people, the end of the procession
of passengers who had given up their tickets and gone away. Instead, the
platform round the door of the station had a dark blot round it, and the
dark blot was a crowd of people.</p>
<p>&ldquo;Oh!&rdquo; cried Peter, with a thrill of joyous excitement,
&ldquo;something&rsquo;s happened! Come on!&rdquo;</p>
<p>They ran down the platform. When they got to the crowd, they could, of
course, see nothing but the damp backs and elbows of the people on the
crowd&rsquo;s outside. Everybody was talking at once. It was evident that
something had happened.</p>
<p>&ldquo;It&rsquo;s my belief he&rsquo;s nothing worse than a
natural,&rdquo; said a farmerish looking person. Peter saw his red,
clean-shaven face as he spoke.</p>
<p>&ldquo;If you ask me, I should say it was a Police Court case,&rdquo;
said a young man with a black bag.</p>
<p>&ldquo;Not it; the Infirmary more like&mdash;&rdquo;</p></div>

<div class="bookPage" id="p103" name="103">
<p>Then the voice of the Station Master was heard, firm and
official:&mdash;</p>
<p>&ldquo;Now, then&mdash;move along there. I&rsquo;ll attend to this, if
<em>you</em> please.&rdquo;</p>
<p>But the crowd did not move. And then came a voice that thrilled the
children through and through. For it spoke in a foreign language. And, what
is more, it was a language that they had never heard. They had heard French
spoken and German. Aunt Emma knew German, and used to sing a song about
<em>bedeuten</em> and
<em>zeiten</em> and <em>bin</em>
and <em>sin</em>. Nor was it Latin. Peter had been in
Latin for four terms.</p>
<p>It was some comfort, anyhow, to find that none of the crowd understood
the foreign language any, better than the children did.</p>
<p>&ldquo;What&rsquo;s that he&rsquo;s saying?&rdquo; asked the farmer,
heavily.</p>
<p>&ldquo;Sounds like French to me,&rdquo; said the Station Master, who had
once been to Boulogne for the day.</p>
<p>&ldquo;It isn&rsquo;t French!&rdquo; cried Peter.</p>
<p>&ldquo;What is it, then?&rdquo; asked more than one voice. The crowd
fell back a little to see who had spoken, and Peter pressed forward, so
that when the crowd closed up again he was in the front rank.</p></div>

<div class="bookPage" id="p104" name="104">
<p>&ldquo;I don&rsquo;t know what it is,&rdquo; said Peter, &ldquo;but it
isn&rsquo;t French. I know that.&rdquo; Then he saw what it was that the
crowd had for its centre. It was a man&mdash;the man, Peter did not doubt,
who had spoken in that strange tongue. A man with long hair and wild eyes,
with shabby clothes of a cut Peter had not seen before&mdash;a man whose
hands and lips trembled, and who spoke again as his eyes fell on Peter.</p>
<p>&ldquo;No, it&rsquo;s not French,&rdquo; said Peter.</p>
<p>&ldquo;Try him with French if you know so much about it,&rdquo; said the
farmer-man.</p>
<p>&ldquo;<em LANG="fre">Parlay voo Frongsay</em>?&rdquo; began
Peter, boldly, and the next moment the crowd recoiled again, for the man
with the wild eyes had left leaning against the wall, and had sprung
forward and caught Peter&rsquo;s hands, and begun to pour forth a flood of
words which, though he could not understand a word of them, Peter knew the
sound of.</p>
<p>&ldquo;There!&rdquo; said he, and turned, his hands still clasped in the
hands of the strange shabby figure, to throw a glance of triumph at the
crowd; &ldquo;there; <em>that&rsquo;s</em> French.&rdquo;</p>
<p>&ldquo;What does he say?&rdquo;</p>
<p>&ldquo;I don&rsquo;t know.&rdquo; Peter was obliged to own it.</p></div>

<div class="bookPage" id="pf104" name="">
<p><img src="illustrations/railway/nesbit-railway104.jpg" alt="The Railway Children, Illustrations by C.E. Brock" /></p></div>

<div class="bookPage" id="p105" name="105">
<p>&ldquo;Here,&rdquo; said the Station Master again; &ldquo;you move on if
you please. I&rsquo;ll deal with this case.&rdquo;</p>
<p>A few of the more timid or less inquisitive travellers moved slowly and
reluctantly away. And Phyllis and Bobbie got near to Peter. All three had
been <em>taught</em> French at school. How deeply they now wished that
they had <em>learned</em> it! Peter shook his head at the stranger, but
he also shook his hands as warmly and looked at him as kindly as he could.
A person in the crowd, after some hesitation, said suddenly, &ldquo;No
comprenny!&rdquo; and then, blushing deeply, backed out of the press and
went away.</p>
<p>&ldquo;Take him into your room,&rdquo; whispered Bobbie to the Station
Master. &ldquo;Mother can talk French. She&rsquo;ll be here by the next
train from Maidbridge.&rdquo;</p>
<p>The Station Master took the arm of the stranger, suddenly but not
unkindly. But the man wrenched his arm away, and cowered back coughing and
trembling and trying to push the Station Master away.</p>
<p>&ldquo;Oh, don&rsquo;t!&rdquo; said Bobbie; &ldquo;don&rsquo;t you see how
frightened he is? He thinks you&rsquo;re going to shut him up. I know he
does,&mdash;look at his eyes!&rdquo;</p>
<p>&ldquo;They&rsquo;re like a fox&rsquo;s eyes when the beast&rsquo;s in a
trap,&rdquo; said the farmer.</p></div>

<div class="bookPage" id="p106" name="106">
<p>&ldquo;Oh, let me try!&rdquo; Bobbie went on; &ldquo;I do really know
one or two French words if I could only think of them.&rdquo;</p>
<p>Sometimes, in moments of great need, we can do wonderful
things&mdash;things that in ordinary life we could hardly even dream of
doing. Bobbie had never been anywhere near the top of her French class, but
she must have learned something without knowing it, for now, looking at
those wild, hunted eyes, she actually remembered, and what is more, spoke,
some French words. She said:</p>
<p>&ldquo;<em LANG="fre">Vous attendre. Ma mere parlez Francais.
Nous</em>&mdash;what&rsquo;s the French for &lsquo;being
kind&rsquo;?&rdquo;</p>
<p>Nobody knew.</p>
<p>&ldquo;<em LANG="fre">Bong</em> is &lsquo;good&rsquo;&rdquo;
said Phyllis.</p>
<p>&ldquo;<em LANG="fre">Nous &ecirc;tre bong pour
vous</em>.&rdquo;</p>
<p>I do not know whether the man understood her words, but he understood
the touch of the hand she thrust into his, and the kindness of the other
hand that stroked his shabby sleeve.</p>
<p>She pulled him gently towards the inmost sanctuary of the Station
Master. The other children followed, and the Station Master shut the door
in the face of the crowd, which stood a little while in the booking office
talking and looking at the fast closed yellow door, and then by</p></div>

<div class="bookPage" id="p107" name="107">
<p>ones and
twos went its way, grumbling.</p>
<p>Inside the Station Master&rsquo;s room Bobbie still held the
stranger&rsquo;s hand and stroked his sleeve.</p>
<p>&ldquo;Here&rsquo;s a go,&rdquo; said the Station Master; &ldquo;no
ticket&mdash;doesn&rsquo;t even know where he wants to go. I&rsquo;m not sure
now but what I ought to send for the police.&rdquo;</p>
<p>&ldquo;Oh, <em>don&rsquo;t</em>!&rdquo; all the children pleaded at
once. And suddenly Bobbie got between the others and the stranger, for she
had seen that he was crying.</p>
<p>By a most unusual piece of good fortune she had a handkerchief in her
pocket. By a still more uncommon accident the handkerchief was moderately
clean. Standing in front of the stranger, she got out the handkerchief and
passed it to him so that the others did not see.</p>
<p>&ldquo;Wait till Mother comes,&rdquo; Phyllis was saying; &ldquo;she
does speak French beautifully. You&rsquo;d just love to hear her.&rdquo;</p>
<p>&ldquo;I&rsquo;m sure he hasn&rsquo;t done anything like you&rsquo;re sent
to prison for,&rdquo; said Peter.</p>
<p>&ldquo;Looks like without visible means to me,&rdquo; said the Station
Master. &ldquo;Well, I don&rsquo;t mind giving him the benefit of the doubt
till your Mamma comes. I <em>should</em> like to know what
nation&rsquo;s got the credit of <em>him</em>, that I should.&rdquo;</p>
<p>Then Peter had an idea. He pulled an envelope out of his</p></div>

<div class="bookPage" id="p108" name="108">
<p>pocket, and showed that it was half full of foreign stamps.
&ldquo;Look here,&rdquo; he said, &ldquo;let&rsquo;s shew him
these&mdash;&rdquo;</p>
<p>Bobbie looked and saw that the stranger had dried his eyes with her
handkerchief. So she said, &ldquo;All right.&rdquo;</p>
<p>They shewed him an Italian stamp, and pointed from him to it and back
again, and made signs of question with their eyebrows. He shook his head.
Then they shewed him a Norwegian stamp&mdash;the common blue kind it
was&mdash;and again he signed No. Then they shewed him a Spanish one, and
at that he took the envelope from Peter&rsquo;s hand and searched among the
stamps with a hand that trembled. The hand that he reached out at last,
with a gesture as of one answering a question, contained a
<em>Russian</em> stamp.</p>
<p>&ldquo;He&rsquo;s Russian,&rdquo; cried Peter, &ldquo;or else he&rsquo;s
like &lsquo;the man who was&rsquo;&mdash;in Kipling, you know.
Russia&rsquo;s an awful place. That&rsquo;s why he&rsquo;s so frightened. They
do dreadful things to you there just for nothing at all&mdash;Mother told
me.&rdquo;</p>
<p>The train from Maidbridge was signalled.</p>
<p>&ldquo;I&rsquo;ll stay with him till you bring Mother in,&rdquo; said
Bobbie.</p>
<p>&ldquo;You&rsquo;re not afraid, Missie?&rdquo;</p></div>

<div class="bookPage" id="p109" name="109">
<p>&ldquo;Oh, no,&rdquo; said Bobbie, looking at the stranger, as she might
have looked at a strange dog of doubtful temper. &ldquo;You wouldn&rsquo;t
hurt me, would you?&rdquo;</p>
<p>She smiled at him, and he smiled back, a queer crooked smile. And then
he coughed again. And the heavy rattling swish of the incoming train swept
past, and the Station Master and Peter and Phyllis went out to meet it.
Bobbie was still holding the stranger&rsquo;s hand when they came back with
Mother.</p>
<p>The Russian rose and bowed very ceremoniously.</p>
<p>Then Mother spoke in French, and he replied, haltingly at first, but
presently in longer and longer sentences.</p>
<p>The children, watching his face and Mother&rsquo;s, knew that he was
telling her things that made her angry and pitying, and sorry and indignant
all at once.</p>
<p>&ldquo;Well, Mum, what&rsquo;s it all about?&rdquo; The Station Master
could not restrain his curiosity any longer.</p>
<p>&ldquo;Oh,&rdquo; said Mother, &ldquo;it&rsquo;s all right. He&rsquo;s a
Russian, and he&rsquo;s lost his ticket. And I&rsquo;m afraid he&rsquo;s very
ill. If you don&rsquo;t mind, I&rsquo;ll take him home with me now. He&rsquo;s really quite worn out, and I&rsquo;ll run down and
tell you all about him to-morrow.&rdquo;</p></div>

<div class="bookPage" id="p110" name="110">
<p>&ldquo;I hope you won&rsquo;t find you&rsquo;re taking home a frozen
viper,&rdquo; said the Station Master, doubtfully.</p>
<p>&ldquo;Oh, no,&rdquo; Mother said brightly, and she smiled;
&ldquo;I&rsquo;m quite sure I&rsquo;m not. Why, he&rsquo;s a great man in his
own country, writes books&mdash;beautiful books&mdash;-I&rsquo;ve read some
of them; but I&rsquo;ll tell you all about it to-morrow.&rdquo;</p>
<p>She spoke again in French to the Russian, and every one could see the
surprise and pleasure and gratitude in his eyes. He got up and politely
bowed to the Station Master, and offered his arm most ceremoniously to
Mother. She took it, but anybody could have seen that she was helping him
along, and not he her.</p>
<p>&ldquo;You girls run home and light a fire in the sitting room,&rdquo;
Mother said, &ldquo;and Peter had better go for the Doctor.&rdquo;</p>
<p>But it was Bobbie who went for the Doctor.</p>
<p>&ldquo;I hate to tell you,&rdquo; she said breathlessly when she came
upon him in his shirt sleeves, weeding his pansy-bed, &ldquo;but
Mother&rsquo;s got a very shabby Russian, and I&rsquo;m sure he&rsquo;ll have
to belong to your Club. I&rsquo;m certain he hasn&rsquo;t got any money. We
found him at the station.&rdquo;</p><p>&ldquo;Found him&mdash;was he lost, then?&rdquo; asked the Doctor,
reaching for his coat.</p></div>

<div class="bookPage" id="p111" name="111">
<p>&ldquo;Yes,&rdquo; said Bobbie, unexpectedly, &ldquo;that&rsquo;s just
what he was. He&rsquo;s been telling Mother the sad, sweet story of his life
in French; and she said would you be kind enough to come directly if you
were at home. He has a dreadful cough, and he&rsquo;s been
crying.&rdquo;</p>
<p>The Doctor smiled.</p>
<p>&ldquo;Oh, don&rsquo;t,&rdquo; said Bobbie; &ldquo;please don&rsquo;t. You
wouldn&rsquo;t if you&rsquo;d seen him. I never saw a man cry before. You
don&rsquo;t know what it&rsquo;s like.&rdquo;</p>
<p>Dr. Forrest wished then that he hadn&rsquo;t smiled.</p>
<p>When Bobbie and the Doctor got to Three Chimneys, the Russian was
sitting in the armchair that had been Father&rsquo;s, stretching his feet to
the blaze of a bright wood fire, and sipping the tea Mother had made
him.</p>
<p>&ldquo;The man seems worn out, mind and body,&rdquo; was what the Doctor
said; &ldquo;the cough&rsquo;s bad, but there&rsquo;s nothing that can&rsquo;t
be cured. He ought to go straight to bed, though&mdash;and let him have a
fire at night.&rdquo;</p>
<p>&ldquo;I&rsquo;ll make one in my room; it&rsquo;s the only one with a
fireplace,&rdquo; said Mother. She did, and presently the Doctor helped the
stranger to bed.</p></div>

<div class="bookPage" id="p112" name="112">
<p>There was a big black trunk in Mother&rsquo;s room that none of the
children had ever seen unlocked. Now, when she had lighted the fire, she
unlocked it and took some clothes out,&mdash;men&rsquo;s clothes,&mdash;and
set them to air by the newly lighted fire. Bobbie, coming in with more wood
for the fire, saw the mark on the night-shirt, and looked over to the open
trunk. All the things she could see were men&rsquo;s clothes. And the name
marked on the shirt was Father&rsquo;s name. Then Father hadn&rsquo;t taken
his clothes with him. And that night-shirt was one of Father&rsquo;s new
ones. Bobbie remembered its being made, just before Peter&rsquo;s birthday.
Why hadn&rsquo;t Father taken his clothes? Bobbie slipped from the room. As
she went she heard the key turned in the lock of the trunk. Her heart was
beating horribly. <em>Why</em> hadn&rsquo;t Father taken his clothes?
When Mother came out of the room, Bobbie flung tightly clasping arms round
her waist, and whispered:&mdash;</p>
<p>&ldquo;Mother&mdash;Daddy isn&rsquo;t&mdash;isn&rsquo;t dead, is
he?&rdquo;</p>
<p>&ldquo;My darling, no! What made you think of anything so
horrible?&rdquo;</p>
<p>&ldquo;I&mdash;I don&rsquo;t know,&rdquo; said Bobbie, angry with
herself, but still clinging to that resolution of hers, not to see anything
that Mother didn&rsquo;t mean her to see.</p></div>

<div class="bookPage" id="p113" name="113">
<p>Mother gave her a hurried hug. &ldquo;Daddy was quite,
<em>quite</em> well when I heard from him last,&rdquo; she said,
&ldquo;and he&rsquo;ll come back to us some day. Don&rsquo;t fancy such
horrible things, darling!&rdquo;</p>
<p>Later on, when the Russian Stranger had been made comfortable for the
night, Mother came into the girls&rsquo; room. She was to sleep there in
Phyllis&rsquo;s bed, and Phyllis was to have a mattress on the floor, a most
amusing adventure for Phyllis. Directly Mother came in, two white figures
started up, and two eager voices called:&mdash;</p>
<p>&ldquo;Now, Mother, tell us all about the Russian gentleman.&rdquo;</p>
<p>A white shape hopped into the room. It was Peter, dragging his quilt
behind him like the tail of a white peacock.</p>
<p>&ldquo;We have been patient,&rdquo; he said, &ldquo;and I had to bite my
tongue not to go to sleep, and I just nearly went to sleep and bit it too
hard, and it hurts ever so. <em>Do</em> tell us. Make a nice long story
of it.&rdquo;</p>
<p>&ldquo;I can&rsquo;t make a long story of it to-night,&rdquo; said
Mother; &ldquo;I&rsquo;m very tired.&rdquo;</p>
<p>Bobbie knew by her voice that Mother had been crying, but the others
didn&rsquo;t know.</p>
<p>&ldquo;Well, make it as long as you can,&rdquo; said Phil,</p></div>

<div class="bookPage" id="p114" name="114">
<p>and Bobbie got her arms around Mother&rsquo;s waist and snuggled close to
her.</p>
<p>&ldquo;Well, it&rsquo;s a story long enough to make a whole book of.
He&rsquo;s a writer; he&rsquo;s written beautiful books. But you know in
Russia you mustn&rsquo;t say anything about the rich people doing wrong, or
about the things that ought to be done to make poor people better and
happier. If you do, they send you to prison.&rdquo;</p>
<p>&ldquo;But they <em>can&rsquo;t</em>,&rdquo; said Peter;
&ldquo;people only go to prison when they&rsquo;ve done wrong.&rdquo;</p>
<p>&ldquo;Or when the Judges <em>think</em> they&rsquo;ve done
wrong,&rdquo; said Mother. &ldquo;Yes, that&rsquo;s so in England. But in
Russia it&rsquo;s different. And he wrote a beautiful book about poor people
and how to help them. I&rsquo;ve read it. There&rsquo;s nothing in it but
goodness and kindness. And they sent him to prison for it. He was three
years in a horrible dungeon, with hardly any light at all, and all damp and
dreadful. In prison all alone for three years.&rdquo;</p>
<p>Mother&rsquo;s voice trembled a little and stopped suddenly.</p>
<p>&ldquo;But, Mother,&rdquo; said Peter, &ldquo;that can&rsquo;t be true
<em>now</em>. It sounds like something out of a history book&mdash;the
Inquisition, or something.&rdquo;</p></div>

<div class="bookPage" id="p115" name="115">
<p>&ldquo;It <em>is</em> true,&rdquo; said Mother; &ldquo;it&rsquo;s all
horribly true. Well, then they took him out and sent him to Siberia, a
convict chained to other convicts&mdash;wicked men who&rsquo;d done all
sorts of crimes&mdash;a long chain of them, and they walked, and walked,
and walked, for days and weeks, till he thought they&rsquo;d never stop
walking. And overseers went behind them with whips&mdash;yes,
whips&mdash;to beat them if they got tired. And some of them went lame, and
some fell down, and when they couldn&rsquo;t get up and go on, they beat
them, and then left them to die. Oh, it&rsquo;s all too terrible! And at
last he got to the mines, and he was condemned to stay there for
life&mdash;for life, just for writing a good, noble, splendid
book.&rdquo;</p>
<p>&ldquo;How did he get away?&rdquo;</p>
<p>&ldquo;When the war came, some of the Russian prisoners were allowed to
volunteer as soldiers. And he volunteered. But he deserted at the first
chance he got and &mdash;&rdquo;</p>
<p>&ldquo;But that&rsquo;s very cowardly, isn&rsquo;t it&mdash;&rdquo; said
Peter&mdash;&ldquo;to desert? Especially when it&rsquo;s war.&rdquo;</p>
<p>&ldquo;Do you think he owed anything to a country that had done
<em>that</em> to him? If he did, he owed more to his wife and children.
He didn&rsquo;t know what had become of them.&rdquo;</p></div>

<div class="bookPage" id="p116" name="116">
<p>&ldquo;Oh,&rdquo; cried Bobbie, &ldquo;he had <em>them</em> to think
about and be miserable about <em>too</em>, then, all the time he was in
prison?&rdquo;</p>
<p>&ldquo;Yes, he had them to think about and be miserable about all the
tune he was in prison. For anything he knew they might have been sent to
prison, too. They do those things in Russia. But while he was in the mines
some friends managed to get a message to him that his wife and children had
escaped and come to England. So when he deserted he came here to look for
them.&rdquo;</p>
<p>&ldquo;Has he got their address?&rdquo; said practical Peter.</p>
<p>&ldquo;No; just England. He was going to London, and he thought he had
to change at our station, and then he found he&rsquo;d lost his ticket and
his purse.&rdquo;</p>
<p>&ldquo;Oh, <em>do</em> you think he&rsquo;ll find them?&mdash;I mean
his wife and children, not the ticket and things.&rdquo;</p>
<p>&ldquo;I hope so. Oh, I hope and pray that he&rsquo;ll find his wife and
children again.&rdquo;</p>
<p>Even Phyllis now perceived that Mother&rsquo;s voice was very
unsteady.</p>
<p>&ldquo;Why, Mother,&rdquo; she said, &ldquo;how very sorry you seem to
be for him!&rdquo;</p>
<p>Mother didn&rsquo;t answer for a minute. Then she just said, &ldquo;Yes,&rdquo; and then she seemed to be thinking. The
children were quiet.</p></div>

<div class="bookPage" id="p117" name="117">
<p>Presently she said, &ldquo;Dears, when you say your prayers, I think you
might ask God to show His pity upon all prisoners and captives.&rdquo;</p>
<p>&ldquo;To show His pity,&rdquo; Bobbie repeated slowly, &ldquo;upon all
prisoners and captives. Is that right, Mother?&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Mother, &ldquo;upon all prisoners and captives.
All prisoners and captives.&rdquo;</p>

