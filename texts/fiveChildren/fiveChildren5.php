<h2>CHAPTER V<br />
NO WINGS</h2>
<p><strong>WHETHER</strong> anyone cried or not, there was certainly an interval
during which none of the party was quite itself. When they grew calmer,
Anthea put her handkerchief in her pocket and her arm round Jane, and
said&mdash;</p>
<p>&ldquo;It can&rsquo;t be for more than one night. We can signal with our
handkerchiefs in the morning. They&rsquo;ll be dry then. And someone will
come up and let us out&rdquo;&mdash;</p>
<p>&ldquo;And find the syphon,&rdquo; said Cyril gloomily; &ldquo;and we
shall be sent to prison for stealing&rdquo;&mdash;</p>
<p>&ldquo;You said it wasn&rsquo;t stealing. You said you were sure it
wasn&rsquo;t.&rdquo;</p>
<p>&ldquo;I&rsquo;m not sure <em>now</em>,&rdquo; said Cyril
shortly.</p>
<p>&ldquo;Let&rsquo;s throw the beastly thing slap away among the
trees,&rdquo; said Robert, &ldquo;then no one can do anything to
us.&rdquo;</p>
<p>&ldquo;Oh yes&rdquo;&mdash;Cyril&rsquo;s laugh was not a lighthearted
one&mdash;&ldquo;and hit some chap on the head, and be murderers as well
as&mdash;as the other thing.&rdquo;</p>
</div>

<div class="bookPage" id="p116" name="116">
<p>&ldquo;But we can&rsquo;t stay up here all night,&rdquo; said Jane;
&ldquo;and I want my tea.&rdquo;</p>
<p>&ldquo;You <em>can&rsquo;t</em> want your tea,&rdquo; said Robert;
&ldquo;you&rsquo;ve only just had your dinner.&rdquo;</p>
<p>&ldquo;But I <em>do</em> want it,&rdquo; she said; &ldquo;especially
when you begin talking about stopping up here all night. Oh,
Panther&mdash;I want to go home! I want to go home!&rdquo;</p>
<p>&ldquo;Hush, hush,&rdquo; Anthea said. &ldquo;Don&rsquo;t, dear.
It&rsquo;ll be all right, somehow. Don&rsquo;t,
don&rsquo;t&rdquo;&mdash;</p>
<p>&ldquo;Let her cry,&rdquo; said Robert desperately; &ldquo;if she howls
loud enough, someone may hear and come and let us out.&rdquo;</p>
<p>&ldquo;And see the soda&ndash;water thing,&rdquo; said Anthea swiftly.
&ldquo;Robert, don&rsquo;t be a brute. Oh, Jane, do try to be a man!
It&rsquo;s just the same for all of us.&rdquo;</p>
<p>Jane did try to &ldquo;be a man&rdquo;&mdash;and reduced her howls to
sniffs.</p>
<p>There was a pause. Then Cyril said slowly, &ldquo;Look here. We must
risk that syphon. I&rsquo;ll 
button it up inside my jacket&mdash;perhaps no one will notice it. You
others keep well in front of me. There are lights in the clergyman&rsquo;s
house. They&rsquo;ve not gone to bed yet. We must just yell as loud as ever
we can. Now all scream when I say three.</p>
</div>

<div class="bookPage" id="p117" name="117">
<p>Robert, you do the yell like the
railway engine, and I&rsquo;ll do the coo&ndash;ee like father&rsquo;s.
The girls can do as they please. One, two, three!&rdquo;</p>
<p>A fourfold yell rent the silent peace of the evening, and a maid at one
of the Vicarage windows paused with her hand on the blind&ndash;cord.</p>
<p>&ldquo;One, two, three!&rdquo; Another yell, piercing and complex,
startled the owls and starlings to a flutter of feathers in the belfry
below. The maid fled from the Vicarage window and ran down the Vicarage
stairs and into the Vicarage kitchen, and fainted as soon as she had
explained to the man&ndash;servant and the cook and the cook&rsquo;s
cousin that she had seen a ghost. It was quite untrue, of course, but I
suppose the girl&rsquo;s nerves were a little upset by the yelling.</p>
<p>&ldquo;One, two, three!&rdquo; The Vicar was on his 
doorstep by this time, and there was no mistaking the yell that greeted
him.</p>
<p>&ldquo;Goodness me,&rdquo; he said to his wife, &ldquo;my dear,
someone&rsquo;s being murdered in the church! Give me my hat and a thick
stick, and tell Andrew to come after me. I expect it&rsquo;s the lunatic
who stole the tongue.&rdquo;</p>
<p>The children had seen the flash of light when the Vicar opened his front
door. They had seen his dark form on the doorstep, and they had paused for
breath, and also to see what he would do.</p>
</div>

<div class="bookPage" id="p118" name="118">
<p>When he turned back for his hat, Cyril said hastily&mdash;</p>
<p>&ldquo;He thinks he only fancied he heard something. You don&rsquo;t
half yell; Now! One, two, three!&rdquo;</p>
<p>It was certainly a whole yell this time, and the Vicar&rsquo;s wife
flung her arms round her husband and screamed a feeble echo of it.</p>
<p>&ldquo;You shan&rsquo;t go!&rdquo; she said, &ldquo;not alone.
Jessie!&rdquo;&mdash;the maid unfainted and came out of the
kitchen&mdash;&ldquo;send Andrew at once. There&rsquo;s a dangerous lunatic
in the church, and he must go immediately and catch it.&rdquo;</p>
<p>&ldquo;I expect he <em>will</em> catch it too,&rdquo; said Jessie to
herself as she went through the kitchen door. &ldquo;Here, Andrew,&rdquo;
she said, &ldquo;there&rsquo;s someone screaming like mad in the church,
and the missus says you&rsquo;re to go along and catch it.&rdquo;</p>
<p>&ldquo;Not alone, I don&rsquo;t,&rdquo; said Andrew in low firm tones.
To his master he merely said, &ldquo;Yis, sir.&rdquo;</p>
<p>&ldquo;You heard those screams?&rdquo;</p>
<p>&ldquo;I did think I noticed a sort of something,&rdquo; said
Andrew.</p>
<p>&ldquo;Well, come on, then,&rdquo; said the Vicar. &ldquo;My dear, I
<em>must</em> go!&rdquo; He pushed her gently into the
sitting&ndash;room, banged the door, and rushed out, dragging Andrew by
the arm.</p>
</div>

<div class="bookPage" id="p119" name="119">
<p>A volley of yells greeted them. As it died into silence Andrew shouted,
&ldquo;Hullo, you there ! Did you call?&rdquo;</p>
<p>&ldquo;Yes,&rdquo; shouted four far&ndash;away voices.</p>
<p>&ldquo;They seem to be in the air,&rdquo; said the Vicar. &ldquo;Very
remarkable.&rdquo;</p>
<p>&ldquo;Where are you?&rdquo; shouted Andrew: and Cyril replied in
his deepest voice, very slowly and loud&mdash;</p>
<p>&ldquo;CHURCH! TOWER! TOP!&rdquo;</p>
<p>&ldquo;Come down, then !&rdquo; said Andrew; and the same voice
replied&mdash;</p>
<p>&ldquo;<em>Can&rsquo;t! Door locked!</em>&rdquo;</p>
<p>&ldquo;My goodness!&rdquo; said the Vicar. &ldquo;Andrew, fetch the
stable lantern. Perhaps it would be as well to fetch another man from the
village.&rdquo;</p>
<p>&ldquo;With the rest of the gang about, very likely. No sir; if this
&rsquo;ere ain&rsquo;t a trap&mdash;well, may I never! There&rsquo;s
cook&rsquo;s cousin at the back door now. He&rsquo;s a keeper, sir, and
used to dealing with vicious characters. And he&rsquo;s got his gun,
sir.&rdquo;</p>
<p>&ldquo;Hullo there!&rdquo; shouted Cyril from the church&ndash;tower;
&ldquo;come up and let us out.&rdquo;</p>
<p>&ldquo;We&rsquo;re a&ndash;coming,&rdquo; said Andrew. &ldquo;I&rsquo;m
a&ndash;going to get a policeman and a gun.&rdquo;</p>
</div>

<div class="bookPage" id="p120" name="120">
<p>&ldquo;Andrew, Andrew,&rdquo; said the Vicar, &ldquo;that&rsquo;s not
the truth.&rdquo;</p>
<p>&ldquo;It&rsquo;s near enough, sir, for the likes of them.&rdquo;</p>
<p>So Andrew fetched the lantern and the cook&rsquo;s cousin; and the
Vicar&rsquo;s wife begged them all to be very careful.</p>
<p>They went across the churchyard&mdash;it was 
quite dark now &mdash; and as they went they talked. The Vicar was certain
a lunatic was on the church&ndash;tower&mdash;the one who had written the
mad letter, and taken the cold tongue and things. Andrew thought it was a
&ldquo;trap&rdquo;; the cook&rsquo;s cousin alone was calm. &ldquo;Great
cry, little wool,&rdquo; said he; &ldquo;dangerous chaps is quieter.&rdquo;
He was not at all afraid. But then he had a gun. That was why he was asked
to lead the way up the worn steep dark steps of the church&ndash;tower. He
did lead the way, with the lantern in one hand and the gun in the other.
Andrew went next. He pretended afterwards that this was because he was
braver than his master, but really it was because he thought of traps, and
he did not like the idea of being behind the others for fear someone should
come softly up behind him and catch hold of his legs in the dark. They went
on and on, and round and round the little corkscrew staircase&mdash;then
through the bell&ndash;ringers&rsquo; loft, where the bell&ndash;ropes
hung with soft furry ends like giant caterpillars&mdash;then up another
stair into the belfry, where the big quiet bells are&mdash;and then on, up
a ladder with</p>
</div>

<div class="bookPage" id="p121" name="121">
<p>broad steps &mdash;and then up a little stone stair. And at 
the top of that there was a
little door. And the door was bolted on the stair side.</p>
<p>The cook&rsquo;s cousin, who was a gamekeeper, kicked at the door, and
said&mdash;</p>
<p>&ldquo;Hullo, you there!&rdquo;</p>
<p>The children were holding on to each other on the other side of the
door, and trembling with anxiousness&mdash;and very hoarse with their
howls. They could hardly speak, but Cyril managed to reply
huskily&mdash;</p>
<p>&ldquo;Hullo, you there!&rdquo;</p>
<p>&ldquo;How did you get up there?&rdquo;</p>
<p>It was no use saying &ldquo;We flew up,&rdquo; so Cyril said&mdash;</p>
<p>&ldquo;We got up&mdash;and then we found the door was locked and we
couldn&rsquo;t get down. Let us out&mdash;do.&rdquo;</p>
<p>&ldquo;How many of you are there?&rdquo; asked the keeper.</p>
<p>&ldquo;Only four,&rdquo; said Cyril.</p>
<p>&ldquo;Are you armed?&rdquo;</p>
<p>&ldquo;Are we what?&rdquo;</p>
</div>

