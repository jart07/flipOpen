</div>

<div class="bookPage" id="p122" name="122">
<p>&ldquo;I&rsquo;ve got my gun handy&mdash;so you&rsquo;d best not try any
tricks,&rdquo; said the keeper. &ldquo;If we open
the door, will you promise to come quietly down, and no
nonsense?&rdquo;</p>
<p>&ldquo;Yes&mdash;oh YES!&rdquo; said all the children together.</p>
<p>&ldquo;Bless me,&rdquo; said the Vicar, &ldquo;surely that was a female
voice?&rdquo;</p>
<p>&ldquo;Shall I open the door, sir?&rdquo; said the keeper. Andrew went
down a few steps, &ldquo;to leave room for the others&rdquo; he said
afterwards.</p>
<p>&ldquo;Yes,&rdquo; said the Vicar, &ldquo;open the door.
Remember,&rdquo; he said through the keyhole, &ldquo;we have come to
release you. You will keep your promise to refrain from
violence?&rdquo;</p>
<p>&ldquo;How this bolt do stick,&rdquo; said the keeper; &ldquo;anyone
&rsquo;ud think it hadn&rsquo;t been drawed for half a year.&rdquo; As a
matter of fact it hadn&rsquo;t.</p>
<p>When all the bolts were drawn, the keeper spoke deep&ndash;chested
words through the keyhole.</p>
<p>&ldquo;I don&rsquo;t open,&rdquo; said he, &ldquo;till you&rsquo;ve gone
over to the other side of the tower. And if one of you comes at me I fire.
Now!&rdquo;</p>
<p>&ldquo;We&rsquo;re all over on the other side,&rdquo; said the
voices.</p>
</div>

<div class="bookPage" id="p123" name="123">
<p>The keeper felt pleased with himself, and owned himself a bold man when
he threw open that door, and, stepping out into the leads, flashed the full light of
the stable lantern on to the group of desperadoes standing against the
parapet on the other side of the tower.</p>
<p>He lowered his gun, and he nearly dropped the lantern.</p>
<p>&ldquo;So help me,&rdquo; he cried, &ldquo;if they ain&rsquo;t a pack of
kiddies!&rdquo;</p>
<p>The Vicar now advanced.</p>
<p>&ldquo;How did you come here?&rdquo; he asked severely. &ldquo;Tell me
at once.&rdquo;</p>
<p>&ldquo;Oh, take us down,&rdquo; said Jane, catching at his coat,
&ldquo;and we&rsquo;ll tell you anything you like. You won&rsquo;t believe
us, but it doesn&rsquo;t matter. Oh, take us down!&rdquo;</p>
<p>The others crowded round him, with the same entreaty. All but Cyril. He
had enough to do with the soda&ndash;water syphon, which would keep
slipping down under his jacket. It needed both hands to keep it steady in
its place.</p>
<p>But he said, standing as far out of the lantern light as
possible&mdash;</p>
<p>&ldquo;Please do take us down.&rdquo;</p>
</div>

<div class="bookPage" id="p124" name="124">
<p>So they were taken down. It is no joke to go down a strange
church&ndash;tower in the dark, but the keeper helped them&mdash;only
Cyril had to be independent because of the soda&ndash;water syphon. It
would keep trying to get away. Half&ndash;way down the ladder it all but
escaped. Cyril just caught it by its spout, and as nearly as possible lost
his footing. He was trembling and pale when at last they reached the bottom
of the winding stair and stepped out on to the flags of the
church&ndash;porch.</p>
<p>Then suddenly the keeper caught Cyril and Robert each by an arm.</p>
<p>&ldquo;You bring along the gells, sir,&rdquo; said he; &ldquo;you and
Andrew can manage them.&rdquo;</p>
<p>&ldquo;Let go!&rdquo; said Cyril; &ldquo;we aren&rsquo;t running away.
We haven&rsquo;t hurt your old church. Leave go!&rdquo;</p>
<p>&ldquo;You just come along,&rdquo; said the keeper; and Cyril dared not
oppose him with violence, because just then the syphon began to slip
again.</p>
<p>So they were all marched into the Vicarage study, and the Vicar&rsquo;s
wife came rushing in.</p>
<p>&ldquo;Oh, William, <em>are</em> you safe?&rdquo; she cried.</p>
<p>Robert hastened to allay her anxiety.</p>
</div>

<div class="bookPage" id="p125" name="125">
<p>&ldquo;Yes,&rdquo; he said, &ldquo;he&rsquo;s quite safe. We
haven&rsquo;t hurt him at all. And please, we&rsquo;re very late, and
they&rsquo;ll be anxious at home. Could you send us home in your
carriage?&rdquo;</p>
<p>&ldquo;Or perhaps there&rsquo;s a hotel near where we could get a
carriage from,&rdquo; said Anthea. &ldquo;Martha will be very anxious as it
is.&rdquo;</p>
<p>The Vicar had sunk into a chair, overcome by emotion and amazement.</p>
<p>Cyril had also sat down, and was leaning forward with his elbows on his
knees because of that soda&ndash;water syphon.</p>
<p>&ldquo;But how did you come to be locked up in the
church&ndash;tower?&rdquo; asked the Vicar.</p>
<p>&ldquo;We went up,&rdquo; said Robert slowly, &ldquo;and we were tired,
and we all went to sleep, and when we woke up we found the door was locked,
so we yelled.&rdquo;</p>
<p>&ldquo;I should think you did!&rdquo; said the Vicar&rsquo;s wife.
&ldquo;Frightening everybody out of their wits like this! You ought to be
ashamed of yourselves.&rdquo;</p>
<p>&ldquo;We <em>are</em>,&rdquo; said Jane gently.</p>
<p>&ldquo;But who locked the door?&rdquo; asked the Vicar.</p>
</div>

<div class="bookPage" id="p126" name="126">
<p>&ldquo;I don&rsquo;t know at all,&rdquo; said Robert, with perfect
truth. &ldquo;Do please send us home.&rdquo;</p>
<p>&ldquo;Well, really,&rdquo; said the Vicar, &ldquo;I suppose we&rsquo;d
better. Andrew, put the horse to, and you can take them home.&rdquo;</p>
<p>&ldquo;Not alone, I don&rsquo;t,&rdquo; said Andrew to himself.</p>
<p>&ldquo;And,&rdquo; the Vicar went on, &ldquo;let this be a lesson to
you&rdquo;&mdash; He went on talking, and the children listened miserably.
But the keeper was not listening. He was looking at the unfortunate Cyril.
He knew all about poachers of course, so he knew how people look when
they&rsquo;re hiding something. The Vicar had just got to the part about
trying to grow up to be a blessing to your parents, and not a trouble and a
disgrace, when the keeper suddenly said&mdash;</p>
<p>&ldquo;Arst him what he&rsquo;s got there under his jacket;&rdquo; and
Cyril knew that concealment was at an end. So he stood up, and squared his
shoulders and tried to look noble, like the boys in books that no one can
look in the face of and doubt that they come of brave and noble 
families, and will be faithful to the death, and he pulled out the
soda&ndash;water syphon and said&mdash;</p>
<p>&ldquo;Well, there you are, then.&rdquo;</p>
<p>There was a silence, Cyril went on&mdash;there was nothing else for
it&mdash;</p>
</div>

<div class="bookPage" id="p127" name="127">
<p>&ldquo;Yes, we took this out of your larder, and some chicken and tongue
and bread. We were very hungry, and we didn&rsquo;t take the custard or
jam. We only took bread and meat and water,&mdash;and we couldn&rsquo;t
help its being the soda kind,&mdash;just the necessaries of life; and we
left half&ndash;a&ndash;crown to pay for it, and we left a letter. And
we&rsquo;re very sorry. And my father will pay a fine or anything you like,
but don&rsquo;t send us to prison. Mother would be so vexed. You know what
you said about not being a disgrace. Well, don&rsquo;t you go and do it to
us&mdash;that&rsquo;s all! We&rsquo;re as sorry as we can be.
There!&rdquo;</p>
<p>&ldquo;However did you get up to the larder window?&rdquo; said Mrs.
Vicar.</p>
<p>&ldquo;I can&rsquo;t tell you that,&rdquo; said Cyril firmly.</p>
<p>&ldquo;Is this the whole truth you&rsquo;ve been telling me?&rdquo;
asked the clergyman.</p>
<p>&ldquo;No,&rdquo; answered Jane suddenly; &ldquo;it&rsquo;s all true,
but it&rsquo;s not the whole truth. We can&rsquo;t tell you that.
It&rsquo;s no good asking. Oh, do forgive us and take us home!&rdquo; She
ran to the Vicar&rsquo;s wife and threw her arms round her. The
Vicar&rsquo;s wife put her arms round Jane, and the keeper whispered behind
his hand to the Vicar&mdash;</p>
</div>

<div class="bookPage" id="p128" name="128">
<p>&ldquo;They&rsquo;re all right, sir&mdash;I expect it&rsquo;s a pal
they&rsquo;re standing by. Someone put &rsquo;em up to it, and they
won&rsquo;t peach. Game little kids.&rdquo;</p>
<p>&ldquo;Tell me,&rdquo; said the Vicar kindly, &ldquo;are you screening
someone else? Had anyone else anything to do with this?&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Anthea, thinking of the Psammead, &ldquo;but it
wasn&rsquo;t their fault.&rdquo;</p>
<p>&ldquo;Very well, my dears,&rdquo; said the Vicar, &ldquo;then
let&rsquo;s say no more about it. Only just tell us why you wrote such an
odd letter.&rdquo;</p>
<p>&ldquo;I don&rsquo;t know,&rdquo; said Cyril. &ldquo;You see, Anthea
wrote it in such a hurry, and it really didn&rsquo;t seem like stealing
then. But afterwards, when we found we couldn&rsquo;t get down off the
church&ndash;tower, it seemed just exactly like it. We are all very
sorry&rdquo;&mdash;</p>
<p>&ldquo;Say no more about it,&rdquo; said the Vicar&rsquo;s wife;
&ldquo;but another time just think before you take other people&rsquo;s
tongues. Now&mdash;some cake and milk before you go home?&rdquo;</p>
<p>When Andrew came to say that the horse was put to, and was he expected
to be led alone into the trap that he had plainly seen from the first, he
found the children eating cake and drinking milk and laughing at the
Vicar&rsquo;s jokes. Jane was sitting on the Vicar&rsquo;s wife&rsquo;s
lap.</p>
</div>

<div class="bookPage" id="p129" name="129">
<p>So you see they got off better than they deserved.</p>
<p>The gamekeeper, who was the cook&rsquo;s cousin, asked leave to drive
home with them, and Andrew was only too glad to have someone to protect him
from the trap he was so certain of.</p>
<p>When the waggonette reached their own house, between the
chalk&ndash;quarry and the gravel&ndash;pit, the children were very
sleepy, but they felt that they and the keeper were friends for life.</p>
<p>Andrew dumped the children down at the iron gate without a word.</p>
<p>&ldquo;You get along home,&rdquo; said the Vicarage cook&rsquo;s cousin,
who was a gamekeeper. &ldquo;I&rsquo;ll get me home on Shanks&rsquo;
mare.&rdquo;</p>
<p>So Andrew had to drive off alone, which he did not like at all, and it
was the keeper that was cousin to the Vicarage cook who went with the
children to the door, and, when they had been swept to bed in a whirlwind
of reproaches, remained to explain to Martha and the cook and the housemaid
exactly what had happened. He explained so well that Martha was quite
amicable the next morning.</p>
<p>After that he often used to come over and see Martha, and in the
end&mdash;but that is another story, as dear Mr. Kipling says.</p>
</div>

<div class="bookPage" id="p130" name="130">

