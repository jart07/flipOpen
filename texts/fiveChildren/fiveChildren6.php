<h2>CHAPTER VI<br />
A CASTLE AND NO DINNER</h2>
<p><strong>THE</strong> others were to be kept in as a punishment for the
misfortunes of the day before. Of course Martha thought it was naughtiness,
and not misfortune&mdash;so you must not blame her. She only thought she
was doing her duty. You know grown&ndash;up people often say they do not
like to punish you, and that they only do it for your own good, and that it
hurts them as much as it hurts you&mdash;and this is really very often the
truth.</p>
<p>Martha certainly hated having to punish the children quite as much as
they hated to be punished. For one thing, she knew what a noise there would
be in the house all day. And she had other reasons.</p>
<p>&ldquo;I declare,&rdquo; she said to the cook, &ldquo;it seems almost a
shame keeping of them indoors this
lovely day; but they are that audacious, they&rsquo;ll be walking in with
their heads knocked off some of these days, if I don&rsquo;t put my foot
down. You make them a cake for tea to&ndash;morrow, dear. And we&rsquo;ll
have Baby along of us as we&rsquo;ve got a bit forrard with our work. Then they can have a good romp
with him, out of the way. Now, Eliza, come, get on with them beds.
Here&rsquo;s ten o&rsquo;clock nearly, and no rabbits caught!&rdquo;</p>
</div>

<div class="bookPage" id="p132" name="132">
<p>People say that in Kent when they mean &ldquo;and no work
done.&rdquo;</p>
<p>So all the others were kept in, but Robert, as I have said, was allowed
to go out for half an hour to get something they all wanted. And that, of
course, was the day&rsquo;s wish.</p>
<p>He had no difficulty in finding the Sand&ndash;fairy, for the day was
already so hot that it had actually, for the first time, come out of its
own accord, it was sitting in a sort of pool of soft sand, stretching
itself, and trimming its whiskers, and turning its snail&rsquo;s eyes round
and round.</p>
<p>&ldquo;Ha!&rdquo; it said when its left eye saw Robert;
&ldquo;I&rsquo;ve been looking out for you? Where are
the rest of you? Not smashed themselves up with those wings, I
hope?&rdquo;</p>
<p>&ldquo;No,&rdquo; said Robert; &ldquo;but the wings got us into a row,
just like all the wishes always do. So the others are kept indoors, and I
was only let out for half an hour&mdash;to get the wish. So please let me
wish as quickly as I can.&rdquo;</p>
<p>&ldquo;Wish away,&rdquo; said the Psammead, twisting itself round in the
sand. But Robert couldn&rsquo;t wish away. He forgot all the things he had
been thinking about, and nothing would come into his head but little things
for himself, like toffee, a foreign stamp album, or a clasp&ndash;knife
with three blades and a corkscrew. He sat down to think better, but it was
no use. He could only think of things the others</p>
</div>

<div class="bookPage" id="p133" name="133">
<p>would not have cared
for&mdash;such as a football, or a pair of leg&ndash;guards, or to be able
to lick Simpkins minor thoroughly when he went back to school.</p>
<p>&ldquo;Well,&rdquo; said the Psammead at last, &ldquo;you&rsquo;d better
hurry up with that wish of yours. Time flies.&rdquo;</p>
<p>&ldquo;I know it does,&rdquo; said Robert. &ldquo;<em>I</em>
can&rsquo;t think what to wish for. I wish you could
give one of the others their wish without their having to come here to ask
for it. Oh, <em>don&rsquo;t!</em>&rdquo;</p>
<p>But it was too late. The Psammead had blown itself out to about three
times its proper size, and now it collapsed like a pricked bubble, and with
a deep sigh leaned back against the edge of its sand&ndash;pool, quite
faint with the effort.</p>
<p>&ldquo;There!&rdquo; it said in a weak voice; &ldquo;it was tremendously
hard&mdash;but I did it. Run along home, or they&rsquo;re sure to wish for
something silly before you get there.&rdquo;</p>
<p>They were&mdash;quite sure; Robert felt this, and as he ran home his
mind was deeply occupied with the sort of wishes he might find they had
wished in his absence. They might wish for rabbits, or white mice or
chocolate, or a fine day to&ndash;morrow, or even&mdash;and that was most
likely&mdash;someone might have said, &ldquo;I do wish to goodness Robert
would hurry up.&rdquo; Well, he <em>was</em> hurrying up, and</p>
</div>

<div class="bookPage" id="pf157" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil157.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p134" name="134">
<p>so they
would have their wish, and the day would be wasted. Then he tried to think
what they could wish for&mdash;something that would be amusing indoors.
That had been his own difficulty from the beginning. So few things are
amusing indoors when the sun is shining outside and you mayn&rsquo;t go out,
however much you want to.</p>
<p>Robert was running as fast as he could, but when he turned the corner
that ought to have brought him within sight of the architect&rsquo;s
nightmare&mdash;the ornamental iron&ndash;work on the top of the
house&mdash;he opened his eyes so wide that he had to drop into a walk; for
you cannot run with your eyes wide open. Then suddenly he stopped short,
for there was no house to be seen. The front&ndash;garden railings were
gone too, and where the house had stood&mdash;Robert rubbed his eyes and
looked again. Yes, the others <em>had</em> wished,&mdash;there was no
doubt about that,&mdash;and they must have wished that they lived in a
castle; for there the castle stood, black and stately, and very tall and
broad, with battlements and lancet windows, and eight great towers; and,
where the garden and the orchard had been, there were white things dotted
like mushrooms. Robert walked slowly on, and as he got nearer he saw that
these were tents, and men in
armour were walking about among the tents&mdash;crowds and crowds of
them.</p>
</div>

<div class="bookPage" id="p135" name="135">
<p>&ldquo;Oh, crikey!&rdquo; said Robert fervently. &ldquo;They
<em>have!</em> They&rsquo;ve wished for a castle, and it&rsquo;s being
besieged! It&rsquo;s just like that Sand&ndash;fairy! I wish we&rsquo;d
never seen the beastly thing!&rdquo;</p>
<p>At the little window above the great archway, across the moat that now
lay where the garden had been but half an hour ago, someone was waving
something pale dust&ndash;coloured. Robert thought it was one of
Cyril&rsquo;s handkerchiefs. They had never been white since the day when
he had upset the bottle of &ldquo;Combined Toning and Fixing
Solution&rdquo; into the drawer where they were. Robert waved back, and
immediately felt that he had been unwise. For his signal had been seen by
the besieging force, and two men in steel&ndash;caps were coming towards
him. They had high brown boots on their long legs, and they came towards
him with such great strides that Robert remembered the shortness of his own
legs and did not run away. He knew it would be useless to
himself, and he feared it might be irritating to the foe. So he stood
still&mdash;and the two men seemed quite pleased with him.</p>
<p>&ldquo;By my halidom,&rdquo; said one, &ldquo;a brave varlet
this!&rdquo;</p>
<p>Robert felt pleased at being <em>called</em> brave, and somehow it
made him <em>feel</em> brave. He passed over the &ldquo;varlet&rdquo;.
It was the way people talked in historical romances for the young, he knew,
and it was evidently not meant for</p>
</div>

<div class="bookPage" id="p136" name="136">
<p>rudeness. He only hoped he would be able
to understand what they said to him. He had not always been able quite to
follow the conversations in the historical romances for the young.</p>
<p>&ldquo;His garb is strange,&rdquo; said the other. &ldquo;Some
outlandish treachery, belike.&rdquo;</p>
<p>&ldquo;Say, lad, what brings thee hither?&rdquo;</p>
<p>Robert knew this meant, &ldquo;Now then, youngster, what are you up to
here, eh?&rdquo;&mdash;so he said&mdash;</p>
<p>&ldquo;If you please, I want to go home.&rdquo;</p>
<p>&ldquo;Go, then!&rdquo; said the man in the longest boots; &ldquo;none
hindereth, and nought lets us to follow. Zooks!&rdquo; he added in a
cautious undertone, &ldquo;I misdoubt me but he beareth tidings to the
besieged.&rdquo;</p>
<p>&ldquo;Where dwellest thou, young knave?&rdquo; inquired the man with
the largest steel&ndash;cap.</p>
<p>&ldquo;Over there,&rdquo; said Robert; and directly he had said it he
knew he ought to have said &ldquo;Yonder!&rdquo;</p>
<p>&ldquo;Ha&mdash;sayest so?&rdquo; rejoined the longest boots.
&ldquo;Come hither, boy. This is a matter for our leader.&rdquo;</p>
<p>And to the leader Robert was dragged forthwith&mdash;by the reluctant
ear.</p>
</div>

<div class="bookPage" id="p137" name="137">
<p>The leader was the most glorious creature Robert had ever seen. He was
exactly like the pictures Robert had so often admired in the historical
romances. He had armour, and a helmet, and a horse, and a crest, and
feathers, and a shield, and a lance, and a sword. His armour and his
weapons were all, I am almost sure, of quite different periods. The shield
was thirteenth&ndash;century, while the sword was of the pattern used in
the Peninsular War. The cuirass was of the time of Charles I, and the
helmet dated from the Second Crusade. The arms on the shield were very
grand&mdash;three red running lions on a blue ground. The tents were of the latest brand and
the whole appearance of camp, army, and leader might have been a shock to
some. But Robert was dumb with admiration, and it all seemed to him
perfectly correct, because he knew no more of heraldry or archaeology than
the gifted artists who usually drew the pictures for the historical
romances. The scene was indeed &ldquo;exactly like a picture.&rdquo; He
admired it all so much that he felt braver than ever.</p>
<p>&ldquo;Come hither, lad,&rdquo; said the glorious leader, when the men
in Cromwellian steel&ndash;caps had said a few low eager words. And he
took off his helmet, because he could not see properly with it on. He had a
kind face, and long fair hair. &ldquo;Have no fear; thou shalt take no
scathe,&rdquo; he said.</p>
<p>Robert was glad of that. He wondered what &ldquo;scathe&rdquo; was, and
if it was nastier than the senna&ndash;tea which he had to take
sometimes.</p>
</div>

<div class="bookPage" id="pf160" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil160.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p138" name="138">
<p>&ldquo;Unfold thy tale without alarm,&rdquo; said the leader kindly.
&ldquo;Whence comest thou, and what is thine intent?&rdquo;</p>
<p>&ldquo;My what?&rdquo; said Robert.</p>
<p>&ldquo;What seekest thou to accomplish? What is thine errand, that thou
wanderest here alone among these rough men&ndash;at&ndash;arms? Poor
child, thy mother&rsquo;s heart aches for thee e&rsquo;en now, I&rsquo;ll
warrant me.&rdquo;</p>
<p>&ldquo;I don&rsquo;t think so,&rdquo; said Robert; &ldquo;you see, she
doesn&rsquo;t know I&rsquo;m out.&rdquo;</p>
<p>The leader wiped away a manly tear, exactly as a leader in a historical
romance would have done, and said&mdash;</p>
<p>&ldquo;Fear not to speak the truth, my child; thou has nought to fear
from Wulfric de Talbot.&rdquo;</p>
<p>Robert had a wild feeling that this glorious leader of the besieging
party&mdash;being himself part of a wish&mdash;would be able to understand
better than Martha, or the gipsies, or the policeman in Rochester, or the
clergyman of yesterday, the true tale of the wishes and the Psammead. The
only difficulty was that he knew he could never remember enough
&ldquo;quothas&rdquo; and &ldquo;beshrew me&rsquo;s&rdquo;, and things like
that, to make his talk sound like the talk of a boy in a historical
romance. However, he began boldly enough, with a sentence straight out of <em>Ralph de Courcy;
or, The Boy Crusader</em>. He said&mdash;</p>
</div>

<div class="bookPage" id="pf162" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil162.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p139" name="139">
<p>&ldquo;Grammercy for the courtesy, fair sir knight. The fact is,
it&rsquo;s like this&mdash;and I hope you&rsquo;re not in a hurry, because
the story&rsquo;s rather a breather. Father and mother were away, and when
we were down playing in the sand&ndash;pits we found a
Psammead.&rdquo;</p>
<p>&ldquo;I cry thee mercy! A Sammyadd?&rdquo; said the knight.</p>
<p>&ldquo;Yes, a sort of&mdash;of fairy, or enchanter&mdash;yes,
that&rsquo;s it, an enchanter; and he said we could have a wish every day,
and we wished first to be beautiful.&rdquo;</p>
<p>&ldquo;Thy wish was scarce granted,&rdquo; muttered one of the
men&ndash;at&ndash;arms, looking at Robert, who went on as if he had not
heard, though he thought the remark very rude indeed.</p>
<p>&ldquo;And then we wished for money&mdash;treasure, you know; but we
couldn&rsquo;t spend it. And yesterday we wished for wings, and we got
them, and we had a ripping time to begin with&rdquo;&mdash;</p>
<p>&ldquo;Thy speech is strange and uncouth,&rdquo; said
Sir Wulfric de Talbot. &ldquo;Repeat thy words&mdash;what hadst
thou?&rdquo;</p>
<p>&ldquo;A ripping&mdash;I mean a jolly&mdash;no&mdash;we were contented
with our lot&mdash;that&rsquo;s what I mean; only after that we got into an
awful fix.&rdquo;</p>
<p>&ldquo;What is a fix? A fray, mayhap?&rdquo;</p>
<p>&ldquo;No&mdash;not a fray. A&mdash;a&mdash;a tight place.&rdquo;</p>
</div>

<div class="bookPage" id="p140" name="140">
<p>&ldquo;A dungeon? Alas for thy youthful fettered limbs!&rdquo; said the
knight, with polite sympathy.</p>
<p>&ldquo;It wasn&rsquo;t a dungeon. We just&mdash;just encountered
undeserved misfortunes,&rdquo; Robert explained, &ldquo;and to&ndash;day
we are punished by not being allowed to go out. That&rsquo;s where I
live&rdquo;&mdash;he pointed to the castle. &ldquo;The others are in there,
and they&rsquo;re not allowed to go out. It&rsquo;s all the
Psammead&rsquo;s&mdash;I mean the enchanter&rsquo;s fault. I wish
we&rsquo;d never seen him.&rdquo;</p>
<p>&ldquo;He is an enchanter of might?&rdquo;</p>
<p>&ldquo;Oh yes&mdash;of might and main. Rather!&rdquo;</p>
<p>&ldquo;And thou deemest that it is the spells of the enchanter whom thou
hast angered that have lent strength to the besieging party,&rdquo; said
the gallant leader; &ldquo;but know thou that Wulfric de Talbot needs no
enchanter&rsquo;s aid to lead his followers to victory.&rdquo;</p>
<p>&ldquo;No, I&rsquo;m sure you don&rsquo;t,&rdquo; said Robert, with
hasty courtesy; &ldquo;of course not&mdash;you wouldn&rsquo;t you know.
But, all the same, it&rsquo;s partly his fault, but we&rsquo;re most to
blame. You couldn&rsquo;t have done anything if it hadn&rsquo;t been for
us.&rdquo;</p>
<p>&ldquo;How now, bold boy?&rdquo; asked Sir Wulfric haughtily. &ldquo;Thy
speech is dark, and eke scarce courteous. Unravel me this
riddle!&rdquo;</p>
</div>

<div class="bookPage" id="p141" name="141">
<p>&ldquo;Oh,&rdquo; said Robert desperately, &ldquo;of course you
don&rsquo;t know it, but you&rsquo;re not <em>real</em> at all.
You&rsquo;re only here because the others must have been idiots enough to
wish for a castle&mdash; and when the sun sets you&rsquo;ll just vanish
away, and it&rsquo;ll be all right.&rdquo;</p>
<p>The captain and the men&ndash;at&ndash;arms exchanged glances, at
first pitying, and then sterner, as the longest&ndash;booted man said,
&ldquo;Beware, noble my lord; the urchin doth but feign madness to escape
from our clutches. Shall we not bind him?&rdquo;</p>
<p>&ldquo;I&rsquo;m no more mad than you are,&rdquo; said Robert angrily.
&ldquo;perhaps not so much&mdash;only, I was an idiot to think you&rsquo;d
understand anything. Let me go&mdash;I haven&rsquo;t done anything to
you.&rdquo;</p>
<p>&ldquo;Whither?&rdquo; asked the knight, who seemed to have believed all
the enchanter story till it came to his own share in it. &ldquo;Whither
wouldst thou wend?&rdquo;</p>
<p>&ldquo;Home, of course,&rdquo; Robert pointed to the castle.</p>
<p>&ldquo;To carry news of succour? Nay!&rdquo;</p>
<p>&ldquo;All right then,&rdquo; said Robert, struck by a sudden idea;
&ldquo;then let me go somewhere else.&rdquo; His mind sought eagerly among
his memories of the historical romance.</p>
</div>

<div class="bookPage" id="p142" name="142">
<p>&ldquo;Sir Wulfric de Talbot,&rdquo; he said slowly, &ldquo;should think
foul scorn to&mdash;to keep a chap&mdash;I mean one who has done him no
hurt&mdash;when he wants to cut off quietly&mdash;I mean to depart without
violence.&rdquo;</p>
<p>&ldquo;This to my face! Beshrew thee for a knave!&rdquo; replied Sir
Wulfric. But the appeal seemed to have gone home. &ldquo;Yet thou sayest
sooth,&rdquo; he added thoughtfully. &ldquo;Go where thou wilt,&rdquo; he
added nobly, &ldquo;thou art free. Wulfric de Talbot warreth not with
babes, and Jakin here shall bear thee company.&rdquo;</p>
<p>&ldquo;All right,&rdquo; said Robert wildly. &ldquo;Jakin will 
enjoy himself, I think. Come on, Jakin. Sir Wulfric, I salute
thee.&rdquo;</p>
<p>He saluted after the modern military manner, and set off running to the
sand&ndash;pit, Jakin&rsquo;s long boots keeping up easily.</p>
<p>He found the Fairy. He dug it up, he woke it up, he implored it to give
him one more wish.</p>
<p>&ldquo;I&rsquo;ve done two to&ndash;day already,&rdquo; it grumbled,
&ldquo;and one was as stiff a bit of work as ever I did.&rdquo;</p>
<p>&ldquo;Oh, do, do, do, do, <em>do!</em>&rdquo; said Robert, while
Jakin looked on with an expression of open&ndash;mouthed horror at the
strange beast that talked, and gazed with its snail&rsquo;s eyes at
him.</p>
</div>

<div class="bookPage" id="pf167" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil167.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p143" name="143">
<p>&ldquo;Well, what is it?&rdquo; snapped the Psammead, with cross
sleepiness.</p>
<p>&ldquo;I wish I was with the others,&rdquo; said Robert. And the
Psammead began to swell. Robert never thought of wishing the castle and the
siege away. Of course he knew they had all come out of a wish, but swords
and daggers and pikes and lances seemed much too real to be wished away.
Robert lost consciousness
for an instant. When he opened his eyes the others were crowding round
him.</p>
<p>&ldquo;We never heard you come in,&rdquo; they said. &ldquo;How awfully
jolly of you to wish it to give us our wish!&rdquo;</p>
<p>&ldquo;Of course we understood that was what you&rsquo;d
done.&rdquo;</p>
<p>&ldquo;But you ought to have told us. Suppose we&rsquo;d wished
something silly.&rdquo;</p>
<p>&ldquo;Silly?&rdquo; said Robert, very crossly indeed. &ldquo;How much
sillier could you have been, I&rsquo;d like to know? You nearly settled
<em>me</em>&mdash;I can tell you.&rdquo;</p>
<p>Then he told his story, and the others admitted that it certainly had
been rough on him. But they praised his courage and cleverness so much that
he presently got back his lost temper, and felt braver than ever, and
consented to be captain of the besieged force.</p>
</div>

<div class="bookPage" id="p144" name="144">
<p>&ldquo;We haven&rsquo;t done anything yet,&rdquo; said Anthea
comfortably; &ldquo;we waited for you. We&rsquo;re going to shoot at them
through these little loopholes with the bow and arrows uncle gave you, and
you shall have first shot.&rdquo;</p>
<p>&ldquo;I don&rsquo;t think I would,&rdquo; said Robert
cautiously; &ldquo;you don&rsquo;t know what they&rsquo;re like near to.
They&rsquo;ve got <em>real</em> bows and arrows&mdash;an awful
length&mdash;and swords and pikes and daggers, and all sorts of sharp
things. They&rsquo;re all quite, quite real. It&rsquo;s not just a&mdash;a
picture, or a vision, or anything; they can <em>hurt</em> us &mdash; or
kill us even, I shouldn&rsquo;t wonder. I can feel my ear all sore still.
Look here&mdash;have you explored the castle? Because I think we&rsquo;d
better let them alone as long as they let us alone. I heard that Jakin man
say they weren&rsquo;t going to attack till just before sun&ndash;down. We
can be getting ready for the attack. Are there any soldiers in the castle
to defend it?&rdquo;</p>
<p>&ldquo;We don&rsquo;t know,&rdquo; said Cyril. &ldquo;You see, directly
I&rsquo;d wished we were in a besieged castle, everything seemed to go
upside down, and when it came straight we looked out of</p>
<p>the window, and saw
the camp and things and you&mdash;and of course we kept on looking at
everything. Isn&rsquo;t this room jolly? It&rsquo;s as real as
real!&rdquo;</p>
</div>

<div class="bookPage" id="p145" name="145">
<p>It was. It was square, with stone walls four feet thick, and great beams
for ceiling. A low door at the corner led to a flight of steps, up and down. The children
went down; they found themselves in a great arched gatehouse&mdash;the
enormous doors were shut and barred. There was a window in a little room at
the bottom of the round turret up which the stair wound, rather larger than
the other windows, and looking through it they saw that the drawbridge was
up and the portcullis down; the moat looked very wide and deep. Opposite
the great door that led to the moat was another great door, with a little
door in it. The children went through this, and found themselves in a big
paved courtyard, with the great grey walls of the castle rising dark and
heavy on all four sides.</p>
<p>Near the middle of the courtyard stood Martha, moving her right hand
backwards and forwards in the air. The cook was stooping down and moving
her hands, also in a very curious way. But the oddest and at the same time
most terrible thing was the Lamb, who was sitting on nothing, about three
feet from the ground, laughing happily.</p>
<p>The children ran towards him. Just as
Anthea was reaching out her arms to take him, Martha said crossly,
&ldquo;Let him alone &mdash; do, miss, when he <em>is</em>
good.&rdquo;</p>
<p>&ldquo;But what&rsquo;s he <em>doing</em>?&rdquo; said Anthea.</p>
</div>

<div class="bookPage" id="p146" name="146">
<p>&ldquo;Doing? Why, a&ndash;setting in his high chair as good as gold, a
precious, watching me doing of the ironing. Get along with you, do&mdash;my
iron&rsquo;s cold again.&rdquo;</p>
<p>She went towards the cook and seemed to poke an invisible fire with an
unseen poker&mdash;the cook seemed to be putting an unseen dish into an
invisible oven.</p>
<p>&ldquo;Run along with you do,&rdquo; she said; &ldquo;I&rsquo;m
behindhand as it is. You won&rsquo;t get no dinner if you come
a&ndash;hindering of me like this. Come, off you goes, or I&rsquo;ll pin a
dishcloth to some of your tails.&rdquo;</p>
<p>&ldquo;You&rsquo;re <em>sure</em> the Lamb&rsquo;s all right?&rdquo;
asked Jane anxiously.</p>
<p>&ldquo;Right as ninepence, if you don&rsquo;t come unsettling of him. I
thought you&rsquo;d like to be rid of him for to&ndash;day; but take him,
if you want, for gracious&rsquo; sake.&rdquo;</p>
<p>&ldquo;No, no,&rdquo; they said, and hastened away. They would have to
defend the castle presently,
and the Lamb was safer even suspended in mid&ndash;air in an invisible
kitchen than in the guard&ndash;room of a besieged castle. They went
through the first doorway they came to and sat down helplessly on a wooden
bench that ran along the room inside.</p>
<p>&ldquo;How awful!&rdquo; said Anthea and Jane together; and Jane added,
&ldquo;I feel as if I was in a mad asylum.&rdquo;</p>
</div>

<div class="bookPage" id="p147" name="147">
<p>&ldquo;What does it mean?&rdquo; Anthea said. &ldquo;It&rsquo;s creepy;
I don&rsquo;t like it. I wish we&rsquo;d wished for something plain&mdash;a
rocking&ndash;horse, or a donkey, or something.&rdquo;</p>
<p>&ldquo;It&rsquo;s no use wishing <em>now</em>,&rdquo; said Robert
bitterly; and Cyril said&mdash;</p>
<p>&ldquo;Do dry up a sec; I want to think.&rdquo;</p>
<p>He buried his face in his hands, and the others looked about them. They
were in a long room with an arched roof. There were wooden tables along it,
and one across at the end of the room, on a sort of raised platform. The
room was very dim and dark. The floor was strewn with dry things like
sticks, and they did not smell nice.</p>
<p>Cyril sat up suddenly and said&mdash;</p>
<p>&ldquo;Look here&mdash;it&rsquo;s all right. I think it&rsquo;s like
this. You know, we wished that the servants shouldn&rsquo;t notice any
