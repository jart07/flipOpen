<h2>CHAPTER IV<br />
WINGS</h2>
<p><strong>THE</strong> next day was very wet&mdash;too wet to go out, and far too
wet to think of disturbing a Sand&ndash;fairy so sensitive to water that
he still, after thousands of years, felt the pain of once having had his
left whisker wetted. It was a long day, and it was not till the afternoon
that all the children suddenly decided to write letters to their mother. It
was Robert who had the misfortune to upset the ink&ndash;pot&mdash;an
unusually deep and full one&mdash; straight into that part of
Anthea&rsquo;s desk where she had long pretended that an arrangement of gum
and cardboard painted with Indian ink was a secret drawer. It was not
exactly Robert&rsquo;s fault; it was only his misfortune that he chanced to
be lifting the ink across the desk just at the moment when Anthea had got
it open, and that that same moment should have been the one chosen by the Lamb to get under
the table and break his squeaking bird. There was a sharp convenient wire
inside the bird, and of course the Lamb ran the wire into Robert&rsquo;s
leg at once; and so, without anyone&rsquo;s meaning to, the secret drawer
was flooded with ink. At the same time a stream was poured over
Anthea&rsquo;s half&ndash;finished letter.</p>
</div>

<div class="bookPage" id="p90" name="90">
<p>So that her letter was something like this&mdash;</p>
<p>&ldquo;DARLING MOTHER, I hope you are quite well, and I hope Granny is
better. The other day we . . .&rdquo;</p>
<p>Then came a flood of ink, and at the bottom these words in
pencil&mdash;</p>
<p>&ldquo;It was not me upset the ink, but it took such a time clearing up,
so no more as it is post&ndash;time. From your loving daughter,</p>
<p>&ldquo;ANTHEA.&rdquo;</p>
<p>Robert&rsquo;s letter had not even been begun. He had been drawing a
ship on the blotting paper while he was trying to think of what to say. And
of course after the ink was upset he had to help Anthea to clean out her desk, and he promised to make her
another secret drawer, better than the other. And she said, &ldquo;Well,
make it now.&rdquo; So it was post&ndash;time and his letter wasn&rsquo;t
done. And the secret drawer wasn&rsquo;t done either.</p>
<p>Cyril wrote a long letter, very fast, and then went to set a trap for
slugs that he had read about in the <em>Home&ndash;made Gardener</em>,
and when it was post&ndash;time the letter could not be found, and it
never was found. Perhaps the slugs ate it.</p>
</div>

<div class="bookPage" id="p91" name="91">
<p>Jane&rsquo;s letter was the only one that went. She meant to tell her
mother all about the Psammead&mdash;in fact they had all meant to do
this&mdash;but she spent so long thinking how to spell the word that there
was no time to tell the story properly, and it is useless to tell a story
unless you <em>do</em> tell it properly, so she had to be contented
with this&mdash;</p>
<p>&ldquo;MY DEAR MOTHER DEAR,&mdash;We are all as good as we can, like you
told us to, and the Lamb has a little cold, but Martha says it is nothing,
only he upset the gold&ndash;fish into 
himself yesterday morning. When we were up at the sand&ndash;pit the other
day we went round by the safe way where the carts go, and we found
a&rdquo;&mdash;</p>
<p>Half an hour went by before Jane felt quite sure that they could none of
them spell Psammead. And they could not find it in the dictionary either,
though they looked. Then Jane hastily finished her letter:</p>
<p>&ldquo;We found a strange thing, but it is nearly post&ndash;time, so
no more at present from your little girl,</p>
<p>&ldquo;JANE.&rdquo;</p>
<p>&ldquo;P.S. If you could have a wish come true, what would you
have?&rdquo;</p>
<p>Then the postman was heard blowing his horn, and Robert rushed out in
the rain to stop his cart and give</p>
</div>

<div class="bookPage" id="p92" name="92">
<p>him the letters. And that was how it
happened that, though all the children meant to tell their mother about the
Sand&ndash;fairy, somehow or other she never got to know. There were other
reasons why she never got to know, but these come later.</p>
<p>The next day Uncle Richard came and took 
them all to Maidstone in a waggonette&mdash;all except the Lamb. Uncle
Richard was the very best kind of uncle. He bought them toys at Maidstone.
He took them into a shop and let them choose exactly what they wanted,
without any restrictions about price, and no nonsense about things being
instructive. It is very wise to let children choose exactly what they like,
because they are very foolish and inexperienced, and sometimes they will
choose a really instructive thing without meaning to. This happened to
Robert, who chose, at the last moment, and in a great hurry, a box with
pictures on it of winged bulls with men&rsquo;s heads and winged men with
eagles&rsquo; heads. He thought there would be animals inside, the same as
on the box. When he got it home it was a Sunday puzzle about ancient
Nineveh! The others chose in haste, and were happy at leisure. Cyril had a
model engine, and the girls had two dolls, as well as a china
tea&ndash;set with forget&ndash;me&ndash;nots on it, to be
&ldquo;between them.&rdquo; The boys&rsquo; &ldquo;between them&rdquo; was
bow and arrows.</p>
</div>

<div class="bookPage" id="p93" name="93">
<p>Then Uncle Richard took them on the beautiful Medway in a boat, and then
they all had tea at a beautiful pastry&ndash;cook&rsquo;s, and when they
reached home it was far too late to have any wishes that day.</p>
<p>They did not tell Uncle Richard anything about the Psammead. I do not
know why. And they do not know why. But I daresay you can guess.</p>
<p>The day after Uncle Richard had behaved so handsomely was a very hot day
indeed. The people who decide what the weather is to be, and puts its
orders down for it in the newspapers every morning, said afterwards that it
was the hottest day there had been for years. They had ordered it to be
&ldquo;warmer&mdash;some showers,&rdquo; and warmer it certainly was. In
fact it was so busy being warmer that it had no time to attend to the order
about showers, so there weren&rsquo;t any.</p>
<p>Have you ever been up at five o&rsquo;clock on a fine summer morning? It
is very beautiful. The sunlight is pinky and yellowy, and all the grass and
trees are covered with dew&ndash;diamonds. And all the shadows go the
opposite way to the way they do in the evening, which is very interesting and makes you
feel as though you were in a new other world.</p>
</div>

<div class="bookPage" id="p94" name="94">
<p>Anthea awoke at five. She had made herself wake, and I must tell you how
it is done, even if it keeps you waiting for the story to go on.</p>
<p>You get into bed at night, and lie down quite flat on your little back,
with your hands straight down by your sides. Then you say &ldquo;I
<em>must</em> wake up at five&rdquo; (or six, or seven, or eight, or
nine, or whatever the time is that you want), and as you say it you push
your chin down on to your chest and then bang your head back on the pillow.
And you do this as many times as there are ones in the time you want to
wake up at. (It is quite an easy sum.) Of course everything depends on your
really wanting to get up at five (or six, or seven, or eight, or nine); if
you don&rsquo;t really want to, it&rsquo;s all of no use. But if you
do&mdash;well, try it and see. Of course in this, as in doing Latin proses
or getting into mischief, practice makes perfect.</p>
<p>Anthea was quite perfect.</p>
<p>At the very moment when she opened her eyes she heard the
black&ndash;and&ndash;gold clock down in the dining&ndash;room strike
eleven. So she knew it was three minutes to five. The
black&ndash;and&ndash;gold clock always struck wrong, but was all right
when you knew what it meant. It was like a person talking a foreign
language. If you know the language it is just as easy to understand as
English. And Anthea knew the clock language. </p>
</div>

<div class="bookPage" id="p95" name="95">
<p>She was very sleepy, but she
jumped out of bed and put her face and hands into a basin of cold water.
This is a fairy charm that prevents your wanting to get back into bed
again. Then she dressed, and folded up her nightgown. She did not tumble it
together by the sleeves, but folded it by the seams from the hem, and that
will show you the kind of well&ndash;brought&ndash;up little girl she
was.</p>
<p>Then she took her shoes in her hand and crept softly down the stairs.
She opened the dining&ndash;room window and climbed out. It would have
been just as easy to go out by the door, but the window was more romantic,
and less likely to be noticed by Martha.</p>
<p>&ldquo;I will always get up at five,&rdquo; she said to herself.
&ldquo;It was quite too awfully pretty for anything.&rdquo;</p>
<p>Her heart was beating very fast, for she was carrying out a plan quite
her own. She could not be sure that it was a good plan, but she was quite
sure that it would not be any better if she were to tell the others about
it. And she had a feeling that, right or wrong, she would rather go through
with it alone. She put on her shoes under the iron verandah, on the
red&ndash;and&ndash;yellow shining tiles, and then she ran straight to
the sand&ndash;pit, and found the Psammead&rsquo;s place, and dug it out;
it was very cross indeed.</p>
</div>

<div class="bookPage" id="pf111" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil111.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p96" name="96">
<p>&ldquo;It&rsquo;s too bad,&rdquo; it said, fluffing up its fur like
pigeons do their feathers at Christmas time. &ldquo;The weather&rsquo;s
arctic, and it&rsquo;s the middle of the night.&rdquo;</p>
<p>&ldquo;I&rsquo;m so sorry,&rdquo; said Anthea gently, and she took off
her white pinafore and covered the Sand&ndash;fairy up with it, all but
its head, its bat&rsquo;s ears, and its eyes that were like a snail&rsquo;s
eyes.</p>
<p>&ldquo;Thank you,&rdquo; it said, &ldquo;that&rsquo;s better.
What&rsquo;s the wish this morning?&rdquo;</p>
<p>&ldquo;I don&rsquo;t know,&rdquo; said she; &ldquo;that&rsquo;s just it.
You see we&rsquo;ve been very unlucky, so far. I wanted to talk to you
about it. But&mdash;would you mind not giving me any wishes till after
breakfast? It&rsquo;s so hard to talk to anyone if they jump out at you
with wishes you don&rsquo;t really want!&rdquo;</p>
<p>&ldquo;You shouldn&rsquo;t say you wish for things if you don&rsquo;t
wish for them. In the old days people almost always knew whether it was
Megatherium or Ichthyosaurus they really wanted for dinner.&rdquo;</p>
<p>&ldquo;I&rsquo;ll try not,&rdquo; said Anthea, &ldquo;but I do
wish&rdquo;&mdash;</p>
<p>&ldquo;Look out!&rdquo; said the Psammead in a warning voice, and it
began to blow itself out.</p>
</div>

<div class="bookPage" id="p97" name="97">
<p>&ldquo;Oh, this isn&rsquo;t a magic wish&mdash;it&rsquo;s just&mdash;I
should be go glad if you&rsquo;d not swell yourself out and nearly burst to
give me any thing just now. Wait till the others are here.&rdquo;</p>
<p>&ldquo;Well, well,&rdquo; it said indulgently, but it shivered.</p>
<p>&ldquo;Would you,&rdquo; asked Anthea kindly&mdash; &ldquo;would you
like to come and sit on my lap? You&rsquo;d be warmer, and I could turn the 
skirt of my frock up round you. I&rsquo;d be very careful.&rdquo;</p>
<p>Anthea had never expected it would, but it did.</p>
<p>&ldquo;Thank you,&rdquo; it said, &ldquo;you really are rather
thoughtful.&rdquo; It crept on to her lap and snuggled down, and she put
her arms round it with a rather frightened gentleness. &ldquo;Now
then!&rdquo; it said.</p>
<p>&ldquo;Well then,&rdquo; said Anthea, &ldquo;everything we have wished
has turned out rather horrid. I wish you would advise us. You are so old,
you must be very wise.&rdquo;</p>
<p>&ldquo;I was always generous from a child,&rdquo; said the
Sand&ndash;fairy. &ldquo;I&rsquo;ve spent the whole of my waking hours in
giving. But one thing I won&rsquo;t give&mdash;that&rsquo;s
advice.&rdquo;</p>
<p>&ldquo;You see,&rdquo; Anthea went on, &ldquo;it&rsquo;s such a
wonderful thing&mdash;such a splendid, glorious chance. It&rsquo;s so good
and kind and dear of you to give us our wishes, and it seems such a pity it
should all be wasted just because we are too silly to know what to wish
for.&rdquo;</p>
</div>

<div class="bookPage" id="p98" name="98">
<p>Anthea had meant to say that&mdash; and she had not wanted to say it
before the others. It&rsquo;s one thing to say you&rsquo;re silly, and quite another to say
that other people are.</p>
<p>&ldquo;Child,&rdquo; said the Sand&ndash;fairy sleepily, &ldquo;I can
only advise you to think before you speak&rdquo;&mdash;</p>
<p>&ldquo;But I thought you never gave advice.&rdquo;</p>
<p>&ldquo;That piece doesn&rsquo;t count,&rdquo; it said.
&ldquo;You&rsquo;ll never take it! Besides, it&rsquo;s not original.
It&rsquo;s in all the copy&ndash;books.&rdquo;</p>
<p>&ldquo;But won&rsquo;t you just say if you think wings would be a silly
wish?&rdquo;</p>
<p>&ldquo;Wings?&rdquo; it said. &ldquo;I should think you might do worse.
Only, take care you aren&rsquo;t flying high at sunset. There was a little
Ninevite boy I heard of once. He was one of King Sennacherib&rsquo;s sons,
and a traveller brought him a Psammead. He used to keep it in a box of sand
on the palace terrace. It was a dreadful degradation for one of us, of
course; still the boy <em>was</em> the Assyrian King&rsquo;s son. And
one day he wished for wings and got them. But he forgot that they would
turn into stone at sunset, and when they did he fell slap on to one of the
winged lions at the top of his father&rsquo;s great staircase; 
and what with <em>his</em> stone wings and the lions&rsquo; stone
wings&mdash;well, it&rsquo;s not a pretty story! But I believe the boy
enjoyed himself very much till then.&rdquo;</p>
</div>

<div class="bookPage" id="p99" name="99">
<p>&ldquo;Tell me,&rdquo; said Anthea, &ldquo;why don&rsquo;t our wishes
turn into stone now? Why do they just vanish?&rdquo;</p>
<p>&ldquo;<em>Autres temps, autres
m&oelig;urs</em>,&rdquo; said the creature.</p>
<p>&ldquo;It that the Ninevite language?&rdquo; asked Anthea, who had
learned no foreign language at school except French.</p>
<p>&ldquo;What I mean is,&rdquo; the Psammead went on, &ldquo;that in the
old days people wished for good solid everyday gifts&mdash;Mammoths and
Pterodactyls and things&mdash;and those could be turned into stone as easy
as not, But people wish such high&ndash;flying fanciful things nowadays.
How are you going to turn being beautiful as the day, or being wanted by
everybody, into stone? You see it can&rsquo;t be done. And it would never
do to have two rules, so they simply vanish. If being beautiful as the day
<em>could</em> be turned into stone it would last an awfully long time,
you know&mdash;much longer than you would. Just look at the Greek statues. It&rsquo;s just as well as
it is. Good&ndash;bye. I <em>am</em> so sleepy.&rdquo;</p>
<p>It jumped off her lap&mdash;dug frantically, and vanished.</p>
<p>Anthea was late for breakfast. It was Robert who quietly poured a
spoonful of treacle down the Lamb&rsquo;s frock, so that he had to be taken
away and washed thoroughly directly after breakfast. And it was of course a
very naughty thing to do; yet it served two purposes&mdash;it delighted the
Lamb, who loved above all things to be</p>
</div>

<div class="bookPage" id="p100" name="100">
<p>completely sticky, and it engaged
Martha&rsquo;s attention so that the others could slip away to the
sand&ndash;pit without the Lamb.</p>
<p>They did it, and in the lane Anthea, breathless from the skurry of that
slipping, panted out&mdash;</p>
<p>&ldquo;I want to propose we take turns to wish. Only, nobody&rsquo;s to
have a wish if the others don&rsquo;t think it&rsquo;s a nice wish. Do you
agree?&rdquo;</p>
<p>&ldquo;Who&rsquo;s to have first wish?&rdquo; asked Robert
cautiously.</p>
<p>&ldquo;Me, if you don&rsquo;t mind,&rdquo; said Anthea 
apologetically. &ldquo;And I&rsquo;ve thought about it&mdash;and it&rsquo;s
<em>wings</em>.&rdquo;</p>
<p>There was a silence. The others rather wanted to find fault, but it was
hard, because the word &ldquo;wings&rdquo; raised a flutter of joyous
excitement in every breast.</p>
<p>&ldquo;Not so dusty,&rdquo; said Cyril generously; and Robert added,
&ldquo;Really, Panther, you&rsquo;re not quite such a fool as you
look.&rdquo;</p>
<p>Jane said, &ldquo;I think it would be perfectly lovely. It&rsquo;s like
a bright dream of delirium.&rdquo;</p>
<p>They found the Sand&ndash;fairy easily. Anthea said&mdash;</p>
<p>&ldquo;I wish we all had beautiful wings to fly with.&rdquo;</p>
</div>

<div class="bookPage" id="p101" name="101">
<p>The Sand&ndash;fairy blew himself out, and next moment each child felt
a funny feeling, half heaviness and half lightness, on its shoulders. The
Psammead put its head on one side and turned its snail&rsquo;s eyes from
one to the other.</p>
<p>&ldquo;Not so dusty,&rdquo; it said dreamily. &ldquo;But really, Robert,
you&rsquo;re not quite such an angel as you look.&rdquo; Robert almost
blushed.</p>
<p>The wings were very big, and more beautiful than you can possibly
imagine&mdash;for they were soft and smooth, and every feather lay neatly in its place. And the
feathers were of the most lovely mixed changing colours, like the rainbow,
or iridescent glass, or the beautiful scum that sometimes floats on water
that is not at all nice to drink.</p>
<p>&ldquo;Oh&mdash;but can we fly?&rdquo; Jane said, standing anxiously
first on one foot and then on the other.</p>
<p>&ldquo;Look out!&rdquo; said Cyril; &ldquo;you&rsquo;re treading on my
wing.&rdquo;</p>
<p>&ldquo;Does it hurt?&rdquo; asked Anthea with interest; but no one
answered, for Robert had spread his wings and jumped up, and now he was
slowly rising in the air. He looked very awkward in his knickerbocker
suit&mdash;his boots in particular hung helplessly, and seemed much larger
than when he was standing in them. But the others cared but little how he
looked,&mdash;or how they looked, for that matter. For now they all spread
out their wings and rose</p>
</div>

<div class="bookPage" id="p102" name="102">
<p>in the air. Of course you all know what flying
feels like, because everyone has dreamed about flying, and it seems so
beautifully easy&mdash;only, you can never remember how you did 
it; and as a rule you have to do it without wings,</p>
<p>in your dreams, which is
more clever and uncommon, but not so easy to remember the rule for. Now the
four children rose flapping from the ground, and you can&rsquo;t think how
good the air felt running against their faces. Their wings were
tremendously wide when they were spread out, and they had to fly quite a
long way apart so as not to get in each other&rsquo;s way. But little
things like this are easily learned.</p>
<p>All the words in the English Dictionary, and in the Greek Lexicon as
well, are, I find, of no use at all to tell you exactly what it feels like
to be flying, so I will not try. But I will say that to look
<em>down</em> on the fields and woods, instead of <em>along</em> at
them, is something like looking at a beautiful live map, where, instead of
silly colours on paper, you have real moving sunny woods and green fields
laid out one after the other. As Cyril said, and I can&rsquo;t think where
he got hold of such a strange expression, &ldquo;It does you a fair
treat!&rdquo; It was almost wonderful and more like real magic than any
wish the children had had yet. They flapped and flew and sailed on their great rainbow wings, between
green earth and blue sky; and they flew right over Rochester and then
swerved</p>
</div>

<div class="bookPage" id="p103" name="103">
<p>round towards Maidstone, and presently they all began to feel
extremely hungry. Curiously enough this happened when they were flying
rather low, and just as they were crossing an orchard where some early
plums shone red and ripe.</p>
<p>They paused on their wings. I cannot explain to you how this is done,
but it is something like treading water when you are swimming, and hawks do
it extremely well.</p>
<p>&ldquo;Yes, I daresay,&rdquo; said Cyril, though no one had spoken.
&ldquo;But stealing is stealing even if you&rsquo;ve got wings.&rdquo;</p>
<p>&ldquo;Do you really think so?&rdquo; said Jane briskly. &ldquo;If
you&rsquo;ve got wings you&rsquo;re a bird, and no one minds birds breaking
the commandments. At least, they may <em>mind</em>, but the birds
always do it, and no one scolds them or sends them to prison.&rdquo;</p>
<p>It was not so easy to perch on a plum&ndash;tree as you might think,
because the rainbow wings were so <em>very</em> large; but somehow 
they all managed to do it, and the plums were certainly very sweet and
juicy.</p>
<p>Fortunately, it was not till they had all had quite as many plums as
were good for them that they saw a stout man, who looked exactly as though
he owned the plum&ndash;trees, come hurrying through the orchard gate with
a thick stick, and with one accord they disentangled their wings from the
plum&ndash;laden branches and began to fly.</p>
</div>

<div class="bookPage" id="pf120" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil120.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p104" name="104">
<p>The man stopped short, with his mouth open. For he had seen the boughs
of his trees moving and twitching, and he had said to himself, &ldquo;Them
young varmint&mdash;at it again!&rdquo; And he had come out at once, for
the lads of the</p>
<p>village had taught him in past seasons that plums want
looking after. But when he saw the rainbow wings flutter up out of the
plum&ndash;tree he felt that he must have gone quite mad, and he did not
like the feeling at all. And when Anthea looked down and saw his mouth go
slowly open, and stay so, and his face become green and mauve in patches,
she called out&mdash;</p>
<p>&ldquo;Don&rsquo;t be frightened,&rdquo; and felt hastily in 
her pocket for a threepenny&ndash;bit with a hole in it, which she had
meant to hang on a ribbon round her neck, for luck. She hovered round the
unfortunate plum&ndash;owner, and said: &ldquo;We have had some of
your plums; we thought it wasn&rsquo;t stealing, but now I am not so sure.
So here&rsquo;s some money to pay for them.&rdquo;</p>
<p>She swooped down towards the terror&ndash;stricken grower of plums, and
slipped the coin into the pocket of his jacket, and in a few flaps she had
rejoined the others.</p>
<p>The farmer sat down on the grass, suddenly and heavily.</p>
</div>

<div class="bookPage" id="p105" name="105">
<p>&ldquo;Well&mdash;I&rsquo;m blessed!&rdquo; he said. &ldquo;This here is
what they call delusions, I suppose. But this here
threeepenny&rdquo;&mdash;he had pulled it out and bitten
it,&mdash;&ldquo;<em>that&rsquo;s</em> real enough. Well, from this day
forth I&rsquo;ll be a better man. It&rsquo;s the kind of thing to sober a
chap for life, this is. I&rsquo;m glad it was only wings, though. I&rsquo;d
rather see birds as aren&rsquo;t there, and couldn&rsquo;t be, even if they
pretend to talk, than some things as I could name.&rdquo;</p>
<p>He got up slowly and heavily, and went indoors, and he was so nice to
his wife that day that she felt quite happy, and said to herself, &ldquo;Law, whatever
have a&ndash;come to the man!&rdquo; and smartened herself up and put a
blue ribbon bow at the place where her collar fastened on, and looked so
pretty that he was kinder than ever. So perhaps the winged children really
did do one good thing that day. If so, it was the only one; for really
there is nothing like wings for getting you into trouble. But, on the other
hand, if you are in trouble, there is nothing like wings for getting you
out of it.</p>
<p>This was the case in the matter of the fierce dog who sprang out at them
when they had folded up their wings as small as possible and were going up
to a farm door to ask for a crust of bread and cheese, for in spite of the
plums they were soon just as hungry as ever again.</p>
</div>

<div class="bookPage" id="p106" name="106">
<p>Now there is no doubt whatever that, if the four had been ordinary
wingless children, that black and fierce dog would have had a good bite out
of the brown&ndash;stockinged leg of Robert, who was the nearest. But at
its first growl there was a flutter of wings, and the dog was 
left to strain at his chain and stand on his hind&ndash;legs as if he were
trying to fly too.</p>
<p>They tried several other farms, but at those where there were no dogs
the people were far too frightened to do anything but scream; and at last,
when it was nearly four o&rsquo;clock and their wings were getting
miserably stiff and tired, they alighted on a church&ndash;tower and held
a council of war.</p>
<p>&ldquo;We can&rsquo;t possibly fly all the way home without dinner
<em>or</em> tea,&rdquo; said Robert with desperate decision.</p>
<p>&ldquo;And nobody will give us any dinner, or even lunch, let alone
tea,&rdquo; said Cyril.</p>
<p>&ldquo;Perhaps the clergyman here might,&rdquo; suggested Anthea.
&ldquo;He must know all about angels&rdquo;&mdash;</p>
<p>&ldquo;Anybody could see we&rsquo;re not that,&rdquo; said Jane.
&ldquo;Look at Robert&rsquo;s boot and Squirrel&rsquo;s plaid
necktie.&rdquo;</p>
</div>

