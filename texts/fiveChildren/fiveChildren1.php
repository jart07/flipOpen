<h2>CHAPTER I<br />
BEAUTIFUL AS THE DAY</h2>
<p><strong>THE</strong> house was three miles from the station, but before the
dusty hired fly had rattled along for five minutes, the children began to
put their heads out of the carriage window and to say, &ldquo;Aren&rsquo;t
we nearly there?&rdquo; And every time they passed a house, which was not
very often, they all said, &ldquo;Oh, <em>is</em> this it?&rdquo; But
it never was, till they reached the very top of the hill, just past the
chalk&ndash;quarry and before you come to the gravel&ndash;pit. And then
there was a white house with a green garden and an orchard beyond, and
mother said, &ldquo;Here we are!&rdquo;</p>
<p>&ldquo;How white the house is,&rdquo; said Robert.</p>
<p>&ldquo;And look at the roses,&rdquo; said Anthea.</p>
<p>&ldquo;And the plums,&rdquo; said Jane.</p>
<p>&ldquo;It is rather decent,&rdquo; Cyril admitted.</p>
<p>The Baby said, &ldquo;Wanty go walky&rdquo;; and the fly stopped with a
last rattle and jolt.</p>
</div>

<div class="bookPage" id="p2" name="2">
<p>Everyone got its legs kicked or its feet trodden on in the scramble to
get out of the carriage that very minute, but no one seemed to mind.
Mother, curiously enough, was in no hurry to get out; and even when she had
come down slowly and by the step, and with no jump at all, she seemed to
wish to see the boxes carried in, and even to pay the driver, instead of
joining in that first glorious rush round the garden and the orchard and
the thorny, thistly, briery, brambly wilderness beyond the broken gate and
the dry fountain at the side of the house. But the children were wiser, for
once. It was not really a pretty house at all; it was quite ordinary, and
mother thought it was rather inconvenient, and was quite annoyed at there
being no shelves, to speak of, and hardly a cupboard in the place. Father
used to say that the iron&ndash;work on the roof and coping was
like an architect&rsquo;s nightmare. But the house was deep in the country,
with no other house in sight, and the children had been in London for two
years, without so much as once going to the seaside even for a day by an
excursion train, and so the White House seemed to them a sort of Fairy
Palace set down in an Earthly Paradise. For London is like prison for
children, especially if their relations are not rich.</p>
</div>

<div class="bookPage" id="pf2" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil002.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p3" name="3">
<p>Of course there are the shops and the theatres, and Maskelyne and
Cook&rsquo;s, and things, but if your people are rather poor you
don&rsquo;t get taken to the theatres, and you can&rsquo;t buy things out
of the shops; and London has none of those nice things that children may
play with without hurting the things or themselves&mdash;such as trees and
sand and woods and waters. And nearly everything in London is the wrong
sort of shape&mdash;all straight lines and flat streets, instead of being
all sorts of odd shapes, like things are in the country. Trees are all
different, as you know, and I am sure some tiresome person must have told
you that there are no two blades of grass exactly alike. But in streets,
where the blades of grass don&rsquo;t grow,
everything is like everything else. This is why so many children who live
in towns are so extremely naughty. They do not know what is the matter with
them, and no more do their fathers and mothers, aunts, uncles, cousins,
tutors, governesses, and nurses; but I know. And so do you, now. Children
in the country are naughty sometimes, too, but that is for quite different
reasons.</p>
<p>The children had explored the gardens and the outhouses thoroughly
before they were caught and cleaned for tea, and they saw quite well that
they were certain to be happy at the White House. They thought so from the
first moment, but when they found the back of the house covered with
jasmine, all in white flower, and smelling</p>
</div>

<div class="bookPage" id="p4" name="4">
<p> like a bottle of the most
expensive scent that is ever given for a birthday present; and when they
had seen the lawn, all green and smooth, and quite different from the brown
grass in the gardens at Camden Town; and when they had found the stable
with a loft over it and some old hay still left, they were almost certain;
and when Robert had found the broken swing and tumbled out of it
and got a lump on his head the size of an egg, and Cyril had nipped his
finger in the door of a hutch that seemed made to keep rabbits in, if you
ever had any, they had no longer any doubts whatever.</p>
<p>The best part of it all was that there were no rules about not going to
places and not doing things. In London almost everything is labelled
&ldquo;You mustn&rsquo;t touch,&rdquo; and though the label is invisible
it&rsquo;s just as bad, because you know it&rsquo;s there, or if you
don&rsquo;t you jolly soon get told.</p>
<p>The White House was on the edge of a hill, with a wood behind
it&mdash;and the chalk&ndash;quarry on one side and the gravel&ndash;pit
on the other. Down at the bottom of the hill was a level plain, with
queer&ndash;shaped white buildings where people burnt lime, and a big red
brewery and other houses; and when the big chimneys were smoking and the
sun was setting, the valley looked as if it was filled with golden mist,
and the limekilns and oast&ndash;houses glimmered and glittered till they
were like an enchanted city out of the <em>Arabian Nights</em>.</p>
</div>

<div class="bookPage" id="pf5" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil005.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p5" name="5">
<p>Now that I have begun to tell you about 
the place, I feel that I could go on and make this into a most interesting
story about all the ordinary things that the children did,&mdash;just the
kind of things you do yourself, you know,&mdash;and you would believe every
word of it; and when I told about the children&rsquo;s being tiresome, as
you are sometimes, your aunts would perhaps write in the margin of the
story with a pencil, &ldquo;How true!&rdquo; or &ldquo;How like
life!&rdquo; and you would see it and very likely be annoyed. So I will
only tell you the really astonishing things that happened, and you may
leave the book about quite safely, for no aunts and uncles either are
likely to write &ldquo;How true!&rdquo; on the edge of the story.
Grown&ndash;up people find it very difficult to believe really wonderful
things, unless they have what they call proof. But children will believe
almost anything, and grown&ndash;ups know this. That is why they tell you
that the earth is round like an orange, when you can see perfectly well
that it is flat and lumpy; and why they say that the earth goes round the
sun, when you can see for yourself any day that the sun gets up in the
morning and goes to bed at night like a good
sun as it is, and the earth knows its place, and lies as still as a mouse.
Yet I daresay you believe all that about the earth and the sun, and if so
you will find it quite easy to believe that before Anthea and Cyril and the
others had been a week in the country they had found a fairy. At least they</p>
</div>

<div class="bookPage" id="p6" name="6">
<p>
called it that, because that was what it called itself; and of course it
knew best, but it was not at all like any fairy you ever saw or heard of or
read about.</p>
<p>It was at the gravel&ndash;pits. Father had to go away suddenly on
business, and mother had gone away to stay with Granny, who was not very
well. They both went in a great hurry, and when they were gone the house
seemed dreadfully quiet and empty, and the children wandered from one room
to another and looked at the bits of paper and string on the floors left
over from the packing, and not yet cleared up, and wished they had
something to do. It was Cyril who said&mdash;</p>
<p>&ldquo;I say, let&rsquo;s take our Margate spades and go and dig in the
gravel&ndash;pits. We can pretend it&rsquo;s seaside.&rdquo;</p>
<p>&ldquo;Father said it was once,&rdquo; Anthea said; &ldquo;he
says there are shells there thousands of years old.&rdquo;</p>
<p>So they went. Of course they had been to the edge of the
gravel&ndash;pit and looked over, but they had not gone down into it for
fear father should say they mustn&rsquo;t play there, and the same with the
chalk&ndash;quarry. The gravel&ndash;out is not really dangerous if you
don&rsquo;t try to climb down the edges, but go the slow safe way round by
the road, as if you were a cart.</p>
</div>

<div class="bookPage" id="p7" name="7">
<p>Each of the children carried its own spade, and took it in turns to
carry the Lamb. He was the baby, and they called him that because
&ldquo;Baa&rdquo; was the first thing he ever said. They called Anthea
&ldquo;Panther,&rdquo; which seems silly when you read it, but when you say
it it sounds a little like her name.</p>
<p>The gravel&ndash;pit is very large and wide, with grass growing round
the edges at the top, and dry stringy wild&ndash;flowers, purple and
yellow. It is like a giant&rsquo;s wash&ndash;hand basin. And there are
mounds of gravel, and holes in the sides of the basin where gravel has been
taken out, and high up in the steep sides there are the
little holes that are the little front doors of the little
sand&ndash;martins&rsquo; little houses.</p>
<p>The children built a castle, of course, but castle&ndash;building is
rather poor fun when you have no hope of the swishing tide ever coming in
to fill up the moat and wash away the drawbridge, and, at the happy last,
to wet everybody up to the waist at least.</p>
<p>Cyril wanted to dig out a cave to play smugglers in, but the others
thought it might bury him alive, so it ended in all spades going to work to
dig a hole through the castle to Australia. The children, you see, believed
that the world was round, and that on the other side the little Australian
boys and girls were really walking wrong way up, like flies on the ceiling,
with their heads hanging down into the air.</p>
</div>

<div class="bookPage" id="p8" name="8">
<p>The children dug and they dug and they dug, and their hands got sandy
and hot and red, and their faces got damp and shiny. The Lamb had tried to
eat the sand, and had cried so hard when he found that it was not, as he
had supposed, brown sugar, that he was now tired out, and was lying asleep
in a warm fat
bunch in the middle of the half&ndash;finished castle. This left his
brothers and sisters free to work really hard, and the hole that was to
come out in Australia soon grew so deep that Jane, who was called Pussy for
short, begged the others to stop.</p>
<p>&ldquo;Suppose the bottom of the hole gave way suddenly,&rdquo; she
said, &ldquo;and you tumbled out among the little Australians, all the sand
would get in their eyes.&rdquo;</p>
<p>&ldquo;Yes,&rdquo; said Robert; &ldquo;and they would hate us, and throw
stones at us, and not let us see the kangaroos, or opossums, or
blue&ndash;gums, or Emu Brand birds, or anything.&rdquo;</p>
<p>Cyril and Anthea knew that Australia was not quite so near as all that,
but they agreed to stop using their spades and go on with their hands. This
was quite easy, because the sand at the bottom of the hole was very soft
and fine and dry, like sea&ndash;sand. And there were little shells in
it.</p>
<p>&ldquo;Fancy it having been wet sea here once, all sloppy and
shiny,&rdquo; said Jane, &ldquo;with fishes and conger&ndash;eels and
coral and mermaids.&rdquo;</p>
</div>

<div class="bookPage" id="p9" name="9">
<p>&ldquo;And masts of ships and wrecked Spanish
treasure. I wish we could find a gold doubloon, or something,&rdquo; Cyril
said.</p>
<p>&ldquo;How did the sea get carried away?&rdquo; Robert asked.</p>
<p>&ldquo;Not in a pail, silly,&rdquo; said his brother. &ldquo;Father says
the earth got too hot underneath, like you do in bed sometimes, so it just
hunched up it shoulders, and the sea had to slip off, like the blankets do
off us, and the shoulder was left sticking out, and turned into dry land.
Let&rsquo;s go and look for shells; I think that little cave looks likely,
and I see something sticking out there like a bit of wrecked ship&rsquo;s
anchor, and it&rsquo;s beastly hot in the Australian hole.&rdquo;</p>
<p>The others agreed, but Anthea went on digging. She always liked to
finish a thing when she had once begun it. She felt it would be a disgrace
to leave that hole without getting through to Australia.</p>
<p>The cave was disappointing, because there were no shells, and the
wrecked ship&rsquo;s anchor turned out to be only the broken end of a
pickaxe handle, and the cave party were just making up their minds that
sand makes
you thirstier when it is not by the seaside, and someone had suggested
going home for lemonade, when Anthea suddenly screamed:</p>
<p>&ldquo;Cyril! Come here! Oh, come quick! It&rsquo;s alive! It&rsquo;ll
get away! Quick!&rdquo;</p>
</div>

<div class="bookPage" id="p10" name="10">
<p>They all hurried back.</p>
<p>&ldquo;It&rsquo;s a rat, I shouldn&rsquo;t wonder,&rdquo; said Robert.
&ldquo;Father says they infest old places&mdash;and this must be pretty old
if the sea was here thousands of years ago.&rdquo;&mdash;</p>
<p>&ldquo;Perhaps it is a snake,&rdquo; said Jane, shuddering.</p>
<p>&ldquo;Let&rsquo;s look,&rdquo; said Cyril, jumping into the hole.
&ldquo;I&rsquo;m not afraid of snakes. I like them. If it&rsquo;s a snake
I&rsquo;ll tame it, and it will follow me everywhere, and I&rsquo;ll let it
sleep round my neck at night.&rdquo;</p>
<p>&ldquo;No, you won&rsquo;t,&rdquo; said Robert firmly. He shared
Cyril&rsquo;s bedroom. &ldquo;But you may if it&rsquo;s a rat.&rdquo;</p>
<p>&ldquo;Oh, don&rsquo;t be silly!&rdquo; said Anthea; &ldquo;it&rsquo;s
not a rat, it&rsquo;s <em>much</em> bigger. And it&rsquo;s not a snake.
It&rsquo;s got feet; I saw them; and fur! No&mdash;not the spade.
You&rsquo;ll hurt it! Dig with your hands.&rdquo;</p>
<p>&ldquo;And let <em>it</em> hurt <em>me</em> instead!
That&rsquo;s so likely, isn&rsquo;t it?&rdquo; said Cyril, seizing a
spade.</p>
<p>&ldquo;Oh, don&rsquo;t!&rdquo; said Anthea. &ldquo;Squirrel,
<em>don&rsquo;t</em>. I&mdash;it sounds silly, but it said something.
It really and truly did.&rdquo;</p>
<p>&ldquo;What?&rdquo;</p>
<p>&ldquo;It said, &lsquo;You let me alone.&rsquo;&rdquo;</p>
</div>

<div class="bookPage" id="p11" name="11">
<p>But Cyril merely observed that his sister must have gone off her nut,
and he and Robert dug with spades while Anthea sat on the edge of the hole,
jumping up and down with hotness and anxiety. They dug carefully, and
presently everyone could see that there really was something moving in the
bottom of the Australian hole.</p>
<p>Then Anthea cried out. &ldquo;<em>I&rsquo;m</em> not afraid. Let me
dig,&rdquo; and fell on her knees and began to scratch like a dog does when
he has suddenly remembered where it was that he buried his bone.</p>
<p>&ldquo;Oh, I felt fur,&rdquo; she cried, half laughing and half crying.
&ldquo;I did indeed! I did!&rdquo; when suddenly a dry husky voice in the
sand made them all jump back, and their hearts jumped nearly as fast as
they did.</p>
<p>&ldquo;Let me alone,&rdquo; it said. And now everyone heard the voice
and looked at the others to see if they had too.</p>
<p>&ldquo;But we want to see you,&rdquo; said Robert bravely.</p>
<p>&ldquo;I wish you&rsquo;d come out,&rdquo; said Anthea, also taking
courage.</p>
<p>&ldquo;Oh, well&mdash;if that&rsquo;s your wish,&rdquo; the voice said,
and the sand stirred and spun and scattered, and something brown and furry
and fat came rolling out into the hole, and the sand fell off it, and it
sat there yawning and rubbing the ends of its eyes with its hands.</p>
</div>

<div class="bookPage" id="p12" name="12">
<p>&ldquo;I believe I must have dropped asleep,&rdquo; it said, stretching
itself.</p>
<p>The children stood round the hole in a ring, looking at the creature
they had found. It was worth looking at. Its eyes were on long horns like a
snail&rsquo;s eyes, and it could move them in and out like telescopes; it
had ears like a bat&rsquo;s ears, and its tubby body was shaped like a
spider&rsquo;s and covered with thick soft fur; its legs and arms were
furry too, and it had hands and feet like a monkey&rsquo;s.</p>
<p>&ldquo;What on earth is it?&rdquo; Jane said. &ldquo;Shall we take it
home?&rdquo;</p>
<p>The thing turned its long eyes to look at her, and
said:&mdash;</p>
<p>&ldquo;Does she always talk nonsense, or is it only the rubbish on her
head that makes her silly?&rdquo;</p>
<p>It looked scornfully at Jane&rsquo;s hat as it spoke.</p>
<p>&ldquo;She doesn&rsquo;t mean to be silly,&rdquo; Anthea said gently;
&ldquo;we none of us do, whatever you may think! Don&rsquo;t be frightened;
we don&rsquo;t want to hurt you, you know.&rdquo;</p>
<p>&ldquo;Hurt <em>me</em>!&rdquo; it said. &ldquo;<em>Me</em>
frightened! Upon my word! Why, you talk as if I were nobody in
particular.&rdquo; All its fur stood out like a cat&rsquo;s when it is
going to fight.</p>
</div>

<div class="bookPage" id="pf12" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil012.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p13" name="13">
<p>&ldquo;Well,&rdquo; said Anthea, still kindly, &ldquo;perhaps if we knew
who you are in particular we could think of something to say that
wouldn&rsquo;t make you cross. Everything we&rsquo;ve said so far seems to
have. Who are you? And don&rsquo;t get angry! Because really we don&rsquo;t
know.&rdquo;</p>
<p>&ldquo;You don&rsquo;t know?&rdquo; it said. &ldquo;Well, I knew the
world had changed&mdash;but&mdash;well, really&mdash;do you mean to tell me seriously you don&rsquo;t know a Psammead when you
see one?&rdquo;</p>
<p>&ldquo;A Sammyadd? That&rsquo;s Greek to me.&rdquo;</p>
<p>&ldquo;So it is to everyone,&rdquo; said the creature sharply.
&ldquo;Well, in plain English, then, a <em>Sand&ndash;fairy</em>.
Don&rsquo;t you know a Sand&ndash;fairy when you see one?&rdquo;</p>
<p>It looked so grieved and hurt that Jane hastened to say, &ldquo;Of
course I see you are, <em>now</em>. It&rsquo;s quite plain now one
comes to look at you.&rdquo;</p>
<p>&ldquo;You came to look at me several sentences ago,&rdquo; it said
crossly, beginning to curl up again in the sand.</p>
<p>&ldquo;Oh&mdash;don&rsquo;t go away again! Do talk some more,&rdquo;
Robert cried. &ldquo;I didn&rsquo;t know you were a Sandy&ndash;fairy, but
I knew directly I saw you that you were much the wonderfullest thing
I&rsquo;d ever seen.&rdquo;</p>
<p>The Sand&ndash;fairy seemed a shade less disagreeable after this.</p>
</div>

<div class="bookPage" id="p14" name="14">
<p>&ldquo;It isn&rsquo;t talking I mind,&rdquo; it said, &ldquo;as long as
you&rsquo;re reasonably civil. But I&rsquo;m not going to make polite
conversation for you. If you talk nicely to me, perhaps I&rsquo;ll answer
you, and perhaps I won&rsquo;t. Now say something.&rdquo;</p>
<p>Of course no one could think of anything to say, but at last Robert
thought of &ldquo;How long have you lived here?&rdquo; and he said it at
once.</p>
<p>&ldquo;Oh, ages&mdash;several thousand years,&rdquo; replied the
Psammead.</p>
<p>&ldquo;Tell us all about it. Do.&rdquo;</p>
<p>&ldquo;It&rsquo;s all in books.&rdquo;</p>
<p>&ldquo;<em>You</em> aren&rsquo;t!&rdquo; Jane said. &ldquo;Oh, tell
us everything you can about yourself! We don&rsquo;t know anything about
you, and you <em>are</em> so nice.&rdquo;</p>
<p>The Sand&ndash;fairy smoothed his long rat&ndash;like whiskers and
smiled between them.</p>
<p>&ldquo;Do please tell!&rdquo; said the children all together.</p>
<p>It is wonderful how quickly you get used to things, even the most
astonishing. Five minutes before, the children had had no more idea than
you that there was such a thing as a sand&ndash;fairy in the world, and
now they were talking to it as though they had known it all their
lives.</p>
<p>It drew its eyes in and said&mdash;</p>
</div>

<div class="bookPage" id="p15" name="15">
<p>&ldquo;How very sunny it is&mdash;quite like old times!
Where do you get your Megatheriums from now?&rdquo;</p>
<p>&ldquo;What?&rdquo; said the children all at once. It is very difficult
always to remember that &ldquo;what&rdquo; is not polite, especially in
moments of surprise or agitation.</p>
<p>&ldquo;Are Pterodactyls plentiful now?&rdquo; the Sand&ndash;fairy went
on.</p>
<p>The children were unable to reply.</p>
<p>&ldquo;What do you have for breakfast? &rdquo; the Fairy said
impatiently, &ldquo;and who gives it you?&rdquo;</p>
<p>&ldquo;Eggs and bacon, and bread and milk, and porridge and things.
Mother gives it us. What are
Mega&ndash;what&rsquo;s&ndash;it&rsquo;s&ndash;names and
Ptero&ndash;what&ndash;do&ndash;you&ndash;call&ndash;thems? And does
anyone have them for breakfast?&rdquo;</p>
<p>&ldquo;Why, almost everyone had Pterodactyl for breakfast in my time!
Pterodactyls were something like crocodiles and something like
birds&mdash;I believe they were very good grilled. You see it was like
this: of course there were heaps of sand&ndash;fairies then, and in
the morning early you went out and hunted for them, and when you&rsquo;d
found one it gave you your wish. People used to send their little boys down
to
the seashore early in the morning before breakfast to get the day&rsquo;s
wishes, and very often the eldest boy in the family would be told to</p>
</div>

<div class="bookPage" id="p16" name="16">
<p>wish for a Megatherium, ready jointed for cooking. It was as big as an elephant,
you see, so there was a good deal of meat on it. And if they wanted fish,
the Ichthyosaurus was asked for,&mdash;he was twenty to forty feet long, so
there was plenty of him. And for poultry there was the Plesiosaurus; there
were nice pickings on that too. Then the other children could wish for
other things. But when people had dinner&ndash;parties it was nearly
always Megatheriums; and Ichthyosaurus, because his fins were a great
delicacy and his tail made soup.&rdquo;</p>
<p>&ldquo;There must have been heaps and heaps of cold meat left
over,&rdquo; said Anthea, who meant to be a good housekeeper some day.</p>
<p>&ldquo;Oh no,&rdquo; said the Psammead, &ldquo;that would never have
done. Why, of course at sunset what was left over turned into stone. You
find the stone bones of the Megatherium and things all over the place even
now, they tell me.&rdquo;</p>
<p>&ldquo;Who tell you?&rdquo; asked Cyril; but the Sand&ndash;fairy
frowned and began to dig very fast with its furry hands.</p>
<p>&ldquo;Oh, don&rsquo;t go!&rdquo; they all cried; &ldquo;tell us more
about it when it was Megatheriums for breakfast! Was the world like this
then?&rdquo;</p>
<p>It stopped digging.</p>
</div>

<div class="bookPage" id="p17" name="17">
<p>&ldquo;Not a bit,&rdquo; it said; &ldquo;it was nearly all sand where I
lived, and coal grew on trees, and the periwinkles were as big as
tea&ndash;trays&mdash;you find them now; they&rsquo;re turned into stone.
We sand&ndash;fairies used to live on the seashore, and the children used
to come with their little flint&ndash;spades and flint&ndash;pails and
make castles for us to live in. That&rsquo;s thousands of years ago, but I
hear that children still build castles on the sand. It&rsquo;s difficult to
break yourself off a habit.&rdquo;</p>
<p>&ldquo;But why did you stop living in the castles?&rdquo; asked
Robert.</p>
<p>&ldquo;It&rsquo;s a sad story,&rdquo; said the Psammead gloomily.
&ldquo;It was because they <em>would</em> build moats to the castles,
and the nasty wet bubbling sea used to come in, and of course as soon as a
sand&ndash;fairy got wet it caught cold, and
generally died. And so there got to be fewer and fewer, and, whenever you
found a fairy and had a wish, you used to wish for a Megatherium, and eat
twice as much as you wanted, because it might be weeks before you got
another wish.&rdquo;</p>
<p>&ldquo;And did <em>you</em> get wet?&rdquo; Robert inquired.</p>
</div>

<div class="bookPage" id="p18" name="18">
<p>The Sand&ndash;fairy shuddered. &ldquo;Only once,&rdquo; it said;
&ldquo;the end of the twelfth hair of my top left whisker&mdash;I feel the
place still in damp weather. It was only once, but it was quite enough for
me. I went away as soon as the sun had dried my poor dear whisker. I
skurried away to the back of the beach, and dug myself a house deep in warm
dry sand, and there I&rsquo;ve been ever since. And the sea changed its
lodgings afterwards. And now I&rsquo;m not going to tell you another
thing.&rdquo;</p>
<p>&ldquo;Just one more, please,&rdquo; said the children. &ldquo;Can you
give wishes now?&rdquo;</p>
<p>&ldquo;Of course,&rdquo; said it, &ldquo;didn&rsquo;t I give you yours a
few minutes ago? You said, &lsquo;I wish you&rsquo;d come out,&rsquo; and I
did.&rdquo;</p>
<p>&ldquo;Oh, please, mayn&rsquo;t we have another?&rdquo;</p>
<p>&ldquo;Yes, but be quick about it. I&rsquo;m tired of you.&rdquo;</p>
<p>I daresay you have often thought what you would do if you had three
wishes given you, and have despised the old man and his wife in the
black&ndash;pudding story, and felt certain that if you had the chance you
could think of three really useful wishes without a moment&rsquo;s
hesitation. These children had often talked this matter over, but, now the
chance had suddenly come to them, they could not make up their minds.</p>

</div>

<div class="bookPage" id="p19" name="19"><p>&ldquo;Quick,&rdquo; said the Sand&ndash;fairy crossly. No one could
think of anything, only Anthea did manage to remember a private wish of her
own and Jane&rsquo;s which they had never told the boys. She knew the boys
would not care about it&mdash;but still it was better than nothing.</p>
<p>&ldquo;I wish we were all as beautiful as the day,&rdquo; she said in a
great hurry.</p>
<p>The children looked at each other, but each could see that the others
were not any better&ndash;looking than usual. The Psammead pushed out its
long eyes, and seemed to be holding
its breath and swelling itself out till it was twice as fat and furry as
before. Suddenly it lets its breath go in a long sigh.</p>
<p>&ldquo;I&rsquo;m really afraid I can&rsquo;t manage it,&rdquo; it said
apologetically, &ldquo;I must be out of practice.&rdquo;</p>
<p>The children were horribly disappointed.</p>
<p>&ldquo;Oh, <em>do</em> try again!&rdquo; they said.</p>
<p>&ldquo;Well,&rdquo; said the Sand&ndash;fairy, &ldquo;the fact is, I
was keeping back a little strength to give the rest of you your wishes
with. If you&rsquo;ll be contented with one wish a day amongst the lot of
you I daresay I can screw myself up to do it. Do you agree to
that?&rdquo;</p>

</div>

<div class="bookPage" id="p20" name="20">
<p>&ldquo;Yes, oh yes!&rdquo; said Jane and Anthea. The boys nodded. They
did not believe the Sand&ndash;fairy could do it. You can always make
girls believe things much easier than you can boys.</p>
<p>It stretched out its eyes farther than ever, and swelled and swelled and
swelled.</p>
<p>&ldquo;I do hope it won&rsquo;t hurt itself,&rdquo; said Anthea.</p>
<p>&ldquo;Or crack its skin,&rdquo; Robert said anxiously.</p>
<p>Everyone was very much relieved when the Sand&ndash;fairy, after
getting so big that it almost
filled up the hole in the sand, suddenly let out its breath and went back
to its proper size.</p>
<p>&ldquo;That&rsquo;s all right,&rdquo; it said, panting heavily.
&ldquo;It&rsquo;ll come easier to&ndash;morrow.&rdquo;</p>
<p>&ldquo;Did it hurt much?&rdquo; asked Anthea.</p>
<p>&ldquo;Only my poor whisker, thank you,&rdquo; said he, &ldquo;but
you&rsquo;re a kind and thoughtful child. Good day.&rdquo;</p>
<p>It scratched suddenly and fiercely with its hands and feet, and
disappeared in the sand. Then the children looked at each other, and each
child suddenly found itself alone with three perfect strangers, all
radiantly beautiful.</p>
</div>

<div class="bookPage" id="p21" name="21">
<p>They stood for some moments in perfect silence. Each thought that its
brothers and sisters had wandered off, and that these strange children had
stolen up unnoticed while it was watching the swelling form of the
Sand&ndash;fairy. Anthea spoke first&mdash; </p>
<p>&ldquo;Excuse me,&rdquo; she said very politely to Jane, who now had
enormous blue eyes and a cloud of russet hair, &ldquo;but have you seen two
little boys and a little girl anywhere about?&rdquo;</p>
<p>&ldquo;I was just going to ask you that,&rdquo; said Jane. And then
Cyril cried&mdash;</p>
<p>&ldquo;Why, it&rsquo;s <em>you</em>! I know the hole in your
pinafore! You <em>are</em> Jane, aren&rsquo;t you? And you&rsquo;re the
Panther; I can see your dirty handkerchief that you forgot to change after
you&rsquo;d cut your thumb! Crikey! The wish <em>has</em> come off,
after all. I say, am I as handsome as you are?&rdquo;</p>
<p>&ldquo;If you&rsquo;re Cyril, I liked you much better as you were
before,&rdquo; said Anthea decidedly. &ldquo;You look like the picture of
the young chorister, with your golden hair; you&rsquo;ll die young, I
shouldn&rsquo;t wonder. And if that&rsquo;s Robert, he&rsquo;s like an
Italian organ&ndash;grinder. His hair&rsquo;s all black.&rdquo;</p>
<p>&ldquo;You two girls are like Christmas cards, then&mdash;that&rsquo;s
all&mdash;silly Christmas cards,&rdquo; said Robert angrily. &ldquo;And
Jane&rsquo;s hair is simply carrots.&rdquo;</p>
</div>

<div class="bookPage" id="p22" name="22">
<p>It was indeed of that Venetian tint so much admired by artists.</p>
<p>&ldquo;Well, it&rsquo;s no use finding fault with each other,&rdquo;
said Anthea; &ldquo;let&rsquo;s get the Lamb and lug it home to dinner. The
servants will admire us most awfully, you&rsquo;ll see.&rdquo;</p>
<p>Baby was just waking when they got to
him, and not one of the children but was relieved to find that he at least
was not as beautiful as the day, but just the same as usual.</p>
<p>&ldquo;I suppose he&rsquo;s too young to have wishes naturally,&rdquo;
said Jane. &ldquo;We shall have to mention him specially next
time.&rdquo;</p>
<p>Anthea ran forward and held out her arms.</p>
<p>&ldquo;Come to own Panther, ducky,&rdquo; she said.</p>
<p>The Baby looked at her disapprovingly, and put a sandy pink thumb in his
mouth. Anthea was his favourite sister.</p>
<p>&ldquo;Come then,&rdquo; she said.</p>
<p>&ldquo;G&rsquo;way long!&rdquo; said the Baby.</p>
<p>&ldquo;Come to own Pussy,&rdquo; said Jane.</p>
<p>&ldquo;Wants my Panty,&rdquo; said the Lamb dismally, and his lip
trembled.</p>
</div>

<div class="bookPage" id="p23" name="23">
<p>&ldquo;Here, come on, Veteran,&rdquo; said Robert, &ldquo;come and have
a yidey on Yobby&rsquo;s back.&rdquo;</p>
<p>&ldquo;Yah, narky narky boy,&rdquo; howled the Baby, giving way
altogether. Then the children knew the worst. <em>he Baby did not know
them!</em></p>
<p>They looked at each other in despair, and it was terrible to each, in
this dire emergency,
to meet only the beautiful eyes of perfect strangers, instead of the merry,
friendly, commonplace, twinkling, jolly little eyes of its own brothers and
sisters.</p>
<p>&ldquo;This is most truly awful,&rdquo; said Cyril when he had tried to
lift up the Lamb, and the Lamb had scratched like a cat and bellowed like a
bull. &ldquo;We&rsquo;ve got to <em>make friends</em> with him! I
can&rsquo;t carry him home screaming like that. Fancy having to make
friends with our own baby!&mdash;it&rsquo;s too silly.&rdquo;</p>
<p>That, however, was exactly what they had to do. It took over an hour,
and the task was not rendered any easier by the fact that the Lamb was by
this time as hungry as a lion and as thirsty as a desert.</p>
<p>At last he consented to allow these strangers to carry him home by
turns, but as he refused to hold on to such new acquaintances he was a dead
weight, and most exhausting.</p>
</div>

<div class="bookPage" id="p24" name="24">
<p>&ldquo;Thank goodness, we&rsquo;re home!&rdquo; said Jane, staggering
through the iron gates to where Martha, the nursemaid, stood at the front
door shading her eyes with her hand and looking out anxiously. &ldquo;Here!
Do take Baby!&rdquo;</p>
<p>Martha snatched the Baby from her arms.</p>
<p>&ldquo;Thanks be, <em>he&rsquo;s</em> safe back,&rdquo; she said.
&ldquo;Where are the others, and whoever to goodness gracious are all of
you?&rdquo;</p>
<p>&ldquo;We&rsquo;re <em>us</em>, of course,&rdquo; said Robert.</p>
<p>&ldquo;And who&rsquo;s Us, when you&rsquo;re at home?&rdquo; asked
Martha scornfully.</p>
<p>&ldquo;I tell you it&rsquo;s <em>us</em>, only we&rsquo;re beautiful
as the day,&rdquo; said Cyril. &ldquo;I&rsquo;m Cyril, and these are the
others, and we&rsquo;re jolly hungry. Let us in, and don&rsquo;t be a silly
idiot.&rdquo;</p>
<p>Martha merely dratted Cyril&rsquo;s impudence and tried to shut the door
in his face.</p>
<p>&ldquo;I know we <em>look</em> different, but I&rsquo;m Anthea, and
we&rsquo;re so tired, and it&rsquo;s long past
dinner&ndash;time.&rdquo;</p>
<p>&ldquo;Then go home to your dinners, whoever you are; and if our
children put you up to this play&ndash;acting you can tell them from me
they&rsquo;ll catch it, so they know what to expect!&rdquo; With that she
did bang the door. Cyril rang the bell violently. No answer. Presently cook
put her head out of a bedroom window and said&mdash;</p>
</div>

<div class="bookPage" id="p25" name="25">
<p>&ldquo;If you don&rsquo;t take yourself off, and that
precious sharp, I&rsquo;ll go and fetch the police.&rdquo; And she slammed
down the window.</p>
<p>&ldquo;It&rsquo;s no good,&rdquo; said Anthea. &ldquo;Oh, do, do come
away before we get sent to prison!&rdquo;</p>
<p>The boys said it was nonsense, and the law of England couldn&rsquo;t put
you in prison for just being as beautiful as the day, but all the same they
followed the others out into the lane.</p>
<p>&ldquo;We shall be our proper selves after sunset, I suppose,&rdquo;
said Jane.</p>
<p>&ldquo;I don&rsquo;t know,&rdquo; Cyril said sadly; &ldquo;it
mayn&rsquo;t be like that now&mdash;things have changed a good deal since
Megatherium times.&rdquo;</p>
<p>&ldquo;Oh,&rdquo; cried Anthea suddenly, &ldquo;perhaps we shall turn
into stone at sunset, like the Megatheriums did, so that there mayn&rsquo;t
be any of us left over for the next day.&rdquo;</p>
<p>She began to cry, so did Jane. Even the boys turned pale. No one had the
heart to say anything.</p>
<p>It was a horrible afternoon. There was no house near where the children
could beg a crust of bread or even a glass of water. They were afraid to go
to the village, because they had seen Martha go down there with a</p>
</div>

<div class="bookPage" id="p26" name="26">
<p>basket,
and there was a local constable. True, they were all as beautiful as the
day, but that is a poor comfort when you are as hungry as a hunter and as
thirsty as a sponge.</p>
<p>Three times they tried in vain to get the servants in the White House to
let them in and listen to their tale. And then Robert went alone, hoping to
be able to climb in at one of the back windows and so open the door to the
others. But all the windows were out of reach, and Martha emptied a
toilet&ndash;jug of cold water over him from a top window, and
said&mdash;</p>
<p>&ldquo;Go along with you, you nasty little Eyetalian monkey.&rdquo;</p>
<p>It came at last to their sitting down in a row under the hedge, with
their feet in a dry ditch, waiting for sunset, and wondering whether, when
the sun <em>did</em> set, they would turn into stone, or only into
their own old natural selves; and each of them still felt lonely and among
strangers and tried not to look at the others, for, though their voices
were their own, their faces were so radiantly beautiful as to be quite
irritating to look at.</p>
<p>&ldquo;I don&rsquo;t believe we <em>shall</em> turn to stone,&rdquo;
said
Robert, breaking a long miserable silence, &ldquo;because the
Sand&ndash;fairy said he&rsquo;d give us another wish to&ndash;morrow,
and he couldn&rsquo;t if we were stone, could he?&rdquo;</p>
</div>

<div class="bookPage" id="pf26" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil026.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p27" name="27">
<p>The others said &ldquo;No,&rdquo; but they weren&rsquo;t at all
comforted.</p>
<p>Another silence, longer and more miserable, was broken by Cyril&rsquo;s
suddenly saying, &ldquo;I don&rsquo;t want to frighten you girls, but I
believe it&rsquo;s beginning with me already. My foot&rsquo;s quite dead.
I&rsquo;m turning to stone, I know I am, and so will you in a
minute.&rdquo;</p>
<p>&ldquo;Never mind,&rdquo; said Robert kindly, &ldquo;perhaps
you&rsquo;ll be the only stone one, and the rest of us will be all right,
and we&rsquo;ll cherish your statue and hang garlands on it.&rdquo;</p>
<p>But when it turned out that Cyril&rsquo;s foot had only gone to sleep
through his sitting too long with it under him, and when it came to life in
an agony of pins and needles, the others were quite cross.</p>
<p>&ldquo;Giving us such a fright for nothing!&rdquo; said Anthea.</p>
<p>The third and miserablest silence of all was broken by Jane. She
said&mdash;</p>
<p>&ldquo;If we <em>do</em> come out of this all right we&rsquo;ll ask
the Sammyadd to make it so that the servants don&rsquo;t notice anything
different, no matter what wishes we have.&rdquo;</p>
<p>The others only grunted. They were too wretched even to make good
resolutions.</p>
</div>

<div class="bookPage" id="p28" name="28">
<p>At last hunger and fright and crossness and tiredness&mdash;four very
nasty things&mdash;all joined together to bring one nice thing, and that
was sleep. The children lay asleep in a row, with their beautiful eyes shut
and their beautiful mouths open. Anthea woke first. The sun had set, and
the twilight was coming on.</p>
<p>Anthea pinched herself very hard, to make sure, and when she found she
could still feel pinching she decided that she was not stone, and then she
pinched the others. They, also, were soft.</p>
<p>&ldquo;Wake up,&rdquo; she said, almost in tears for joy;
&ldquo;it&rsquo;s all right, we&rsquo;re not stone. And oh, Cyril, how nice
and ugly you do look, with your old freckles and your brown hair and your
little eyes. And so do you all!&rdquo; she added, so that they might not
feel jealous.</p>
<p>When they got home they were very much
scolded by Martha, who told them about the strange children.</p>
<p>&ldquo;A good&ndash;looking lot, I must say, but that
impudent.&rdquo;</p>
<p>&ldquo;I know,&rdquo; said Robert, who knew by experience how hopeless
it would be to try to explain things to Martha.</p>
<p>&ldquo;And where on earth have you been all this time, you naughty
little things, you?&rdquo;</p>
<p>&ldquo;In the lane.&rdquo;</p>
</div>

<div class="bookPage" id="p29" name="29">
<p>&ldquo;Why didn&rsquo;t you come home hours ago?&rdquo;</p>
<p>&ldquo;We couldn&rsquo;t because of <em>them</em>,&rdquo; said
Anthea.</p>
<p>&ldquo;Who?&rdquo;</p>
<p>&ldquo;The children who were as beautiful as the day. They kept us there
till after sunset. We couldn&rsquo;t come back till they&rsquo;d gone. You
don&rsquo;t know how we hated them! Oh, do, do give us some supper&mdash;we
are so hungry.&rdquo;</p>
<p>&ldquo;Hungry! I should think so,&rdquo; said Martha angrily; &ldquo;out
all day like this. Well, I hope it&rsquo;ll be a lesson to you not to go
picking up with strange children&mdash;down here after measles, as likely
as not. Now mind, if you see them again, don&rsquo;t you speak to
them&mdash;not one word nor so much as a look&mdash;but come straight
away and tell me. I&rsquo;ll spoil their beauty for them!&rdquo;</p>
<p>&ldquo;If ever we <em>do</em> see them again we&rsquo;ll tell
you,&rdquo; Anthea said; and Robert, fixing his eyes fondly on the cold
beef that was being brought in on a tray by cook, added in heartfelt
undertones&mdash;</p>
<p>&ldquo;And we&rsquo;ll take jolly good care we never <em>do</em> see
them again.&rdquo;</p>
<p>And they never have.</p>

