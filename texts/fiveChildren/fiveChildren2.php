<img src="illustrations/fiveChildren/nesbit-fivechil045.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p40" name="40"><p>
heaps of gold, and Robert began to bury her, as you bury your father in
sand when you are at the seaside and he has gone to sleep on the beach with
his newspaper over his face. But Jane was not half buried before she cried
out, &ldquo;Oh, stop, it&rsquo;s too heavy! It hurts!&rdquo;</p>
<p>Robert said &ldquo;Bosh!&rdquo; and went on.</p>
<p>&ldquo;Let me out, I tell you,&rdquo; cried Jane, and was taken out,
very white, and trembling a little.</p>
<p>&ldquo;You&rsquo;ve no idea what it&rsquo;s like,&rdquo; she said;
&ldquo;it&rsquo;s like stones on you&mdash;or like chains.&rdquo;</p>
<p>&ldquo;Look here,&rdquo; Cyril said, &ldquo;if this is to do us any
good, it&rsquo;s no good our staying gasping at it like this. Let&rsquo;s
fill our pockets and go and buy things. Don&rsquo;t you forget, it
won&rsquo;t last after sunset. I wish we&rsquo;d asked the Sammyadd why
things don&rsquo;t turn to stone. Perhaps this will. I&rsquo;ll tell you
what, there&rsquo;s a pony and cart in the village.&rdquo;</p>
<p>&ldquo;Do you want to buy that?&rdquo; asked Jane.</p>
<p>&ldquo;No, silly,&mdash;we&rsquo;ll <em>hire</em> it. And then
we&rsquo;ll go to Rochester and buy heaps and heaps of things. Look here,
let&rsquo;s each take as much as we can carry. But it&rsquo;s not
sovereigns. They&rsquo;ve got a man&rsquo;s head on one side and a thing
like the ace of spades on the other. Fill your pockets with it, I tell you,
and come along. You can jaw as we go&mdash;if you <em>must</em>
jaw.&rdquo;</p>
</div>

<div class="bookPage" id="p41" name="41">
<p>Cyril sat down and began to fill his pockets.</p>
<p>&ldquo;You made fun of me for getting father to have nine pockets in my
Norfolks,&rdquo; said he, &ldquo;but now you see!&rdquo;</p>
<p>They did. For when Cyril had filled his
nine pockets and his handkerchief and the space between himself and his
shirt front with the gold coins, he had to stand up. But he staggered, and
had to sit down again in a hurry.</p>
<p>&ldquo;Throw out some of the cargo,&rdquo; said Robert.
&ldquo;You&rsquo;ll sink the ship, old chap. That comes of nine
pockets.&rdquo;</p>
<p>And Cyril had to.</p>
<p>Then they set off to walk to the village. It was more than a mile, and
the road was very dusty indeed, and the sun seemed to get hotter and
hotter, and the gold in their pockets got heavier and heavier.</p>
<p>It was Jane who said, &ldquo;I don&rsquo;t see how we&rsquo;re to spend
it all. There must be thousands of pounds among the lot of us. I&rsquo;m
going to leave some of mine behind this stump in the hedge. And directly we
get to the village we&rsquo;ll buy some biscuits; I know it&rsquo;s long
past dinner&ndash;time.&rdquo; She took out a handful or two of gold and
hid it in the hollows of an old hornbeam. &ldquo;How round and yellow they
are,&rdquo; she said. &ldquo;Don&rsquo;t you wish they were gingerbread
nuts and we were going to eat them?&rdquo;</p>
</div>

<div class="bookPage" id="p42" name="42">
<p>&ldquo;Well, they&rsquo;re not, and we&rsquo;re not,&rdquo; said Cyril.
&ldquo;Come on!&rdquo;</p>
<p>But they came on heavily and wearily. Before they reached the village,
more than one stump in the hedge concealed its little hoard of hidden
treasure. Yet they reached the village with about twelve hundred guineas in
their pockets. But in spite of this inside wealth they looked quite
ordinary outside, and no one would have thought they could have more than a
half&ndash;crown each at the outside. The haze of heat, the blue of the
wood smoke, made a sort of dim misty cloud over the red roofs of the
village. The four sat down heavily on the first bench they came to. It
happened to be outside the Blue Boar Inn.</p>
<p>It was decided that Cyril should go into the Blue Boar and ask for
ginger&ndash;beer, because, as Anthea said, &ldquo;It is not wrong for men
to go into public houses, only for children. And Cyril is nearer to being a
man than us, because he is the eldest.&rdquo; So he went. The others sat in
the sun and waited.</p>
<p>&ldquo;Oh, hats, how hot it is!&rdquo; said Robert. &ldquo;Dogs put
their tongues out when they&rsquo;re hot; I
wonder if it would cool us at all to put out ours?&rdquo;</p>
<p>&ldquo;We might try,&rdquo; Jane said; and they all put their tongues
out as far as ever they could go, so that it quite stretched their throats,
but it only seemed to make them thirstier</p>
</div>

<div class="bookPage" id="p43" name="43">
<p>than ever, besides annoying
everyone who went by. So they took their tongues in again, just as Cyril
came back with the ginger&ndash;beer.</p>
<p>&ldquo;I had to pay for it out of my own
two&ndash;and&ndash;sevenpence, though, that I was going to buy rabbits
with,&rdquo; he said. &ldquo;They wouldn&rsquo;t change the gold. And when
I pulled out a handful the man just laughed and said it was
card&ndash;counters. And I got some sponge&ndash;cakes too, out of a
glass jar on the bar&ndash;counter. And some biscuits with caraways
in.&rdquo;</p>
<p>The sponge&ndash;cakes were both soft and dry and the biscuits were dry
too, and yet soft, which biscuits ought not to be. But the
ginger&ndash;beer made up for everything.</p>
<p>&ldquo;It&rsquo;s my turn now to try to buy something with the
money,&rdquo; Anthea said; &ldquo;I&rsquo;m next eldest. Where is the
pony&ndash;cart kept?&rdquo;</p>
<p>It was at The Chequers, and Anthea went
in the back way to the yard, because they all knew that little girls ought
not to go into the bars of public&ndash;houses. She came out, as she
herself said, &ldquo;pleased but not proud&rdquo;.</p>
<p>&ldquo;He&rsquo;ll be ready in a brace of shakes, he says,&rdquo; she
remarked. &ldquo;and he&rsquo;s to have one sovereign&mdash;or whatever it
is&mdash;to drive us in to Rochester and back, besides waiting there till
we&rsquo;ve got everything we want. I think I managed very well.&rdquo;</p>
</div>

<div class="bookPage" id="pf48" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil048.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p44" name="44">
<p>&ldquo;You think yourself jolly clever, I daresay,&rdquo; said Cyril
moodily. &ldquo;How did you do it?&rdquo;</p>
<p>&ldquo;I wasn&rsquo;t jolly clever enough to go taking handfuls of money
out of my pocket, to make it seem cheap, anyway,&rdquo; she retorted.
&ldquo;I just found a young man doing something to a horse&rsquo;s leg with
a sponge and a pail. And I held out one sovereign, and I said, &lsquo;Do
you know what this is?&rsquo; He said, &lsquo;No,&rsquo; and he&rsquo;d
call his father. And the old man came, and he said it was a spade guinea;
and he said was it my own to do as I liked with, and I said
&lsquo;Yes&rsquo;; and I asked about the pony&ndash;cart, and I said he
could have the guinea if he&rsquo;d
drive us in to Rochester. And his name is S. Crispin. And he said,
&lsquo;Right oh.&rsquo;&rdquo;</p>
<p>It was a new sensation to be driven in a smart pony&ndash;trap along
pretty country roads; it was very pleasant too (which is not always the
case with new sensations), quite apart from the beautiful plans of spending
the money which each child made as they went along, silently of course and
quite to itself, for they felt it would never have done to let the old
innkeeper hear them talk in the affluent sort of way they were thinking in.
The old man put them down by the bridge at their request.</p>
<p>&ldquo;If you were going to buy a carriage and horses, where would you
go?&rdquo; asked Cyril, as if he were only asking for the sake of something
to say.</p>
</div>

<div class="bookPage" id="p45" name="45">
<p>&ldquo;Billy Peasemarsh, at the Saracen&rsquo;s Head,&rdquo; said the
old man promptly. &ldquo;Though all forbid I should recommend any man where
it&rsquo;s a question of horses, no more than I&rsquo;d take anybody
else&rsquo;s recommending if I was a&ndash;buying one. But if your
pa&rsquo;s thinking of a turnout of any sort, there ain&rsquo;t a
straighter
man in Rochester, nor a civiller spoken, than Billy, though I says
it.&rdquo;</p>
<p>&ldquo;Thank you,&rdquo; said Cyril. &ldquo;The Saracen&rsquo;s
Head.&rdquo;</p>
<p>And now the children began to see one of the laws of nature turn upside
down and stand on its head like an acrobat. Any grown&ndash;up person
would tell you that money is hard to get and easy to spend. But the fairy
money had been easy to get, and spending it was not only hard, it was
almost impossible. The tradespeople of Rochester seemed to shrink, to a
trades&ndash;person, from the glittering fairy gold (&ldquo;furrin
money&rdquo; they called it, for the most part). To begin with, Anthea, who
had had the misfortune to sit on her hat earlier in the day, wished to buy
another. She chose a very beautiful one, trimmed with pink roses and the
blue breasts of peacocks. It was marked in the window, &ldquo;Paris Model,
three guineas.&rdquo;</p>
<p>&ldquo;I&rsquo;m glad,&rdquo; she said, &ldquo;because, if it says
guineas, it means guineas, and not sovereigns, which we haven&rsquo;t
got.&rdquo;</p>
</div>

<div class="bookPage" id="pf50" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil050.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p46" name="46">
<p>But when she took three of the spade
guineas in her hand, which was by this time rather dirty owing to her not
having put on gloves before going to the gravel&ndash;pit, the
black&ndash;silk young lady in the shop looked very hard at her, and went
and whispered something to an older and uglier lady, also in black silk,
and then they gave her back the money and said it was not current coin.</p>
<p>&ldquo;It&rsquo;s good money,&rdquo; said Anthea, &ldquo;and it&rsquo;s
my own.&rdquo;</p>
<p>&ldquo;I daresay,&rdquo; said the lady, &ldquo;but it&rsquo;s not the
kind of money that&rsquo;s fashionable now, and we don&rsquo;t care about
taking it.&rdquo;</p>
<p>&ldquo;I believe they think we&rsquo;ve stolen it,&rdquo; said Anthea,
rejoining the others in the street; &ldquo;if we had gloves they
wouldn&rsquo;t think we were so dishonest. It&rsquo;s my hands being so
dirty fills their minds with doubts.&rdquo;</p>
<p>So they chose a humble shop, and the girls bought cotton gloves, the
kind at sixpence three&ndash;farthings, but when they offered a guinea the
woman looked at it through her spectacles and said she had no change; so
the gloves had to be paid for out of Cyril&rsquo;s
two&ndash;and&ndash;sevenpence that he meant to buy rabbits with,
