<p>&ldquo;I daresay you would.&rdquo;</p>
<p>&ldquo;Will you show us a few, please? To choose from.&rdquo;</p>
<p>&ldquo;Who are you a&ndash;kidden of?&rdquo; inquired Mr. Billy
