<h2>CHAPTER II<br />
GOLDEN GUINEAS</h2>
<p><strong>ANTHEA</strong> woke in the morning from a very real sort of dream, in
which she was walking in the Zoological Gardens on a pouring wet day
without any umbrella. The animals seemed desperately unhappy because of the
rain, and were all growling gloomily. When she awoke, both the growling and
the rain went on just the same. The growling was the heavy regular
breathing of her sister Jane, who had a slight cold and was still asleep.
The rain fell in slow drops on to Anthea&rsquo;s face from the wet corner
of a bath&ndash;towel which her brother Robert was gently squeezing the
water out of, to wake her up, as he now explained.</p>
<p>&ldquo;Oh, drop it!&rdquo; she said rather crossly; so he did, for he
was not a brutal brother, though very ingenious in apple&ndash;pie beds,
booby&ndash;traps,
original methods of awakening sleeping relatives, and the other little
accomplishments which made home happy.</p>
<p>&ldquo;I had such a funny dream,&rdquo; Anthea began.</p>
</div>

<div class="bookPage" id="p31" name="31">
<p>&ldquo;So did I,&rdquo; said Jane, wakening suddenly and without
warning. &ldquo;I dreamed we found a Sand&ndash;fairy in the
gravel&ndash;pits, and it said it was a Sammyadd, and we might have a new
wish every day, and&rdquo;&mdash;</p>
<p>&ldquo;But that&rsquo;s what <em>I</em> dreamed,&rdquo; said Robert;
&ldquo;I was just going to tell you&mdash;and we had the first wish
directly it said so. And I dreamed you girls were donkeys enough to ask for
us all to be beautiful as the day, and we jolly well were, and it was
perfectly beastly.&rdquo;</p>
<p>&ldquo;But <em>can</em> different people all dream the same
thing?&rdquo; said Anthea, sitting up in bed, &ldquo;because I dreamed all
that as well as about the Zoo and the rain; and Baby didn&rsquo;t know us
in my dream, and the servants shut us out of the house because the
radiantness of our beauty was such a complete disguise,
and&rdquo;&mdash;</p>
<p>The voice of the eldest brother sounded from across the landing.</p>
<p>&ldquo;Come on Robert,&rdquo; it said, &ldquo;you&rsquo;ll be late for
breakfast again&mdash;unless you mean to shirk your bath like you did on
Tuesday.&rdquo;</p>
<p>&ldquo;I say, come here a sec,&rdquo; Robert replied. &ldquo;I
didn&rsquo;t shirk it; I had it after brekker in father&rsquo;s
dressing&ndash;room, because ours was emptied away.&rdquo;</p>
</div>

<div class="bookPage" id="p32" name="32">
<p>Cyril appeared in the doorway, partially clothed.</p>
<p>&ldquo;Look here,&rdquo; said Anthea, &ldquo;we&rsquo;ve all had such an
odd dream. We&rsquo;ve all dreamed we found a Sand&ndash;fairy.&rdquo;</p>
<p>Her voice died away before Cyril&rsquo;s contemptuous glance.
&ldquo;Dream?&rdquo; he said; &ldquo;you little sillies, it&rsquo;s
<em>true</em>. I tell you it all happened. That&rsquo;s why I&rsquo;m
so keen on being down early. We&rsquo;ll go up there directly after
brekker, and have another wish. Only we&rsquo;ll make up our minds, solid,
before we go, what it is we do want, and no one must ask for anything
unless the others agree first. No more peerless beauties for this child,
thank you. Not if I know it!&rdquo;</p>
<p>The other three dressed, with their mouths open. If all that dream about
the Sand&ndash;fairy
was real, this real dressing seemed very like a dream, the girls thought.
Jane felt that Cyril was right, but Anthea was not sure, till after they
had seen Martha and heard her full and plain reminders about their naughty
conduct the day before. Then Anthea was sure. &ldquo;Because,&rdquo; said
she, &ldquo;servants never dream anything but the things in the
Dream&ndash;book, like snakes and oysters and going to a wedding&mdash;
that means a funeral, and snakes are a false female friend, and oysters are
babies.&rdquo;</p>
<p>&ldquo;Talking of babies,&rdquo; said Cyril, &ldquo;where&rsquo;s the
Lamb?&rdquo;</p>
</div>

<div class="bookPage" id="p33" name="33">
<p>&ldquo;Martha&rsquo;s going to take him to Rochester to see her cousins.
Mother said she might. She&rsquo;s dressing him now,&rdquo; said Jane,
&ldquo;in his very best coat and hat. Bread&ndash;and&ndash;butter,
please.&rdquo;</p>
<p>&ldquo;She seems to like taking him too,&rdquo; said Robert in a tone of
wonder.</p>
<p>&ldquo;Servants <em>do</em> like taking babies to see their
relations,&rdquo; Cyril said; &ldquo;I&rsquo;ve noticed it
before&mdash;especially in their best things.&rdquo;</p>
<p>&ldquo;I expect they pretend they&rsquo;re their own babies, and that
they&rsquo;re not servants at all, but
married to noble dukes of high degree, and they say the babies are the
little dukes and duchesses,&rdquo; Jane suggested dreamily, taking more
marmalade. &ldquo;I expect that&rsquo;s what Martha&rsquo;ll say to her
cousin. She&rsquo;ll enjoy herself most frightfully.&rdquo;</p>
<p>&ldquo;She won&rsquo;t enjoy herself most frightfully carrying our
infant duke to Rochester,&rdquo; said Robert; &ldquo;not if she&rsquo;s
anything like me&mdash;she won&rsquo;t.&rdquo;</p>
<p>&ldquo;Fancy walking to Rochester with the Lamb on your back! Oh,
crikey!&rdquo; said Cyril in full agreement.</p>
<p>&ldquo;She&rsquo;s going by carrier,&rdquo; said Jane.
&ldquo;Let&rsquo;s see them off, then we shall have done a polite and
kindly act, and we shall be quite sure we&rsquo;ve got rid of them for the
day.&rdquo;</p>
</div>

<div class="bookPage" id="p34" name="34">
<p>So they did.</p>
<p>Martha wore her Sunday dress of two shades of purple, so tight in the
chest that it made her stoop, and her blue hat with the pink cornflowers
and white ribbon. She had a yellow&ndash;lace collar with a green bow. And
the Lamb had indeed his very best cream&ndash;coloured silk coat and hat.
It was a smart party that the
carrier&rsquo;s cart picked up at the Cross Roads. When its white tilt and
red wheels had slowly vanished in a swirl of chalkdust&mdash;</p>
<p>&ldquo;And now for the Sammyadd!&rdquo; said Cyril, and off they
went.</p>
<p>As they went they decided on the wish they would ask for. Although they
were all in a great hurry they did not try to climb down the sides of the
gravel&ndash;pit, but went round by the safe lower road, as if they had
been carts. They had made a ring of stones round the place where the
Sand&ndash;fairy had disappeared, so they easily found the spot. The sun
was burning and bright, and the sky was deep blue&mdash;without a cloud.
The sand was very hot to touch.</p>
<p>&ldquo;Oh&mdash;suppose it was only a dream, after all,&rdquo; Robert
said as the boys uncovered their spades from the sand&ndash;heap where
they had buried them and began to dig.</p>
<p>&ldquo;Suppose you were a sensible chap,&rdquo; said Cyril;
&ldquo;one&rsquo;s quite as likely as the other!&rdquo;</p>
</div>

<div class="bookPage" id="p35" name="35">
<p>&ldquo;Suppose you kept a civil tongue in your head,&rdquo; Robert
snapped.</p>
<p>&ldquo;Suppose we girls take a turn,&rdquo; said Jane,
laughing. &ldquo;You boys seem to be getting very warm.&rdquo;</p>
<p>&ldquo;Suppose you don&rsquo;t come shoving your silly oar in,&rdquo;
said Robert, who was now warm indeed.</p>
<p>&ldquo;We won&rsquo;t,&rdquo; said Anthea quickly. &ldquo;Robert dear,
don&rsquo;t be so grumpy&mdash;we won&rsquo;t say a word, you shall be the
one to speak to the Fairy and tell him what we&rsquo;ve decided to wish
for. You&rsquo;ll say it much better than we shall.&rdquo;</p>
<p>&ldquo;Suppose you drop being a little humbug,&rdquo; said Robert, but
not crossly. &ldquo;Look out&mdash;dig with your hands, now!&rdquo;</p>
<p>So they did, and presently uncovered the spider&ndash;shaped brown
hairy body, long arms and legs, bat&rsquo;s ear and snail&rsquo;s eyes of
the Sand&ndash;fairy himself. Everyone drew a deep breath of satisfaction,
for now of course it couldn&rsquo;t have been a dream.</p>
<p>The Psammead sat up and shook the sand out of its fur.</p>
<p>&ldquo;How&rsquo;s your left whisker this morning?&rdquo; said Anthea
politely.</p>
<p>&ldquo;Nothing to boast of,&rdquo; said it; &ldquo;it had rather a
restless night. But thank you for asking.&rdquo;</p>
</div>

<div class="bookPage" id="p36" name="36">
<p>&ldquo;I say,&rdquo; said Robert, &ldquo;do you feel up to giving wishes
to&ndash;day, because we very much want an extra besides the regular one?
The extra&rsquo;s a very little one,&rdquo; he added reassuringly.</p>
<p>&ldquo;Humph!&rdquo; said the Sand&ndash;fairy. (If you read this story
aloud, please pronounce &ldquo;humph&rdquo; exactly as it is spelt, for
that is how he said it.) &ldquo;Humph! Do you know, until I heard you being
disagreeable to each other just over my head, and so loud too, I really
quite thought I had dreamed you all. I do have very odd dreams
sometimes.&rdquo;</p>
<p>&ldquo;Do you?&rdquo; Jane hurried to say, so as to get away from the
subject of disagreeableness. &ldquo;I wish,&rdquo; she added politely,
&ldquo;you&rsquo;d tell us about your dreams&mdash;they must be awfully
interesting.&rdquo;</p>
<p>&ldquo;Is that the day&rsquo;s wish?&rdquo; said the Sand&ndash;fairy,
yawning.</p>
<p>Cyril muttered something about &ldquo;just like a girl,&rdquo; and the
rest stood silent. If they said &ldquo;Yes,&rdquo; then good&ndash;bye to
the other wishes they had decided to ask for. If they said
&ldquo;No,&rdquo; it would be very rude, and they had all been
taught manners, and had learned a little too, which is not at all the same
thing. A sigh of relief broke from all lips when the Sand&ndash;fairy
said:</p>
<p>&ldquo;If I do I shan&rsquo;t have strength to give you a second wish;
not even good tempers, or common sense, or manners, or little things like
that.&rdquo;</p>
</div>

<div class="bookPage" id="p37" name="37">
<p>&ldquo;We don&rsquo;t want you to put yourself out at all about
<em>these</em> things, we can manage them quite well ourselves,&rdquo;
said Cyril eagerly; while the others looked guiltily at each other, and
wished the Fairy would not keep all on about good tempers, but give them
one good rowing if it wanted to, and then have done with it.</p>
<p>&ldquo;Well,&rdquo; said the Psammead, putting out his long
snail&rsquo;s eyes so suddenly that one of them nearly went into the round
boy&rsquo;s eye of Robert, &ldquo;let&rsquo;s have the little wish
first.&rdquo;</p>
<p>&ldquo;We don&rsquo;t want the servants to notice the gifts you give
us.&rdquo;</p>
<p>&ldquo;Are kind enough to give us,&rdquo; said Anthea in a whisper.</p>
<p>&ldquo;Are kind enough to give us, I mean,&rdquo; said Robert.</p>
<p>The Fairy swelled himself out a bit, let his breath go, and
said&mdash;</p>
<p>&ldquo;I&rsquo;ve done <em>that</em> for you&mdash;it was quite
easy. People don&rsquo;t notice things much, anyway. What&rsquo;s the next
wish?&rdquo;</p>
<p>&ldquo;We want,&rdquo; said Robert slowly, &ldquo;to be rich beyond the
dreams of something or other.&rdquo;</p>
<p>&ldquo;Avarice,&rdquo; said Jane.</p>
</div>

<div class="bookPage" id="p38" name="38">
<p>&ldquo;So it is,&rdquo; said the Fairy unexpectedly. &ldquo;But it
won&rsquo;t do you much good, that&rsquo;s one comfort,&rdquo; it muttered
to itself. &ldquo;Come &mdash;I can&rsquo;t go beyond dreams, you know! How
much do you want, and will you have it in gold or notes?&rdquo;</p>
<p>&ldquo;Gold, please&mdash;and millions of it.&rdquo;&mdash;</p>
<p>&ldquo;This gravel&ndash;pit full be enough?&rdquo; said the Fairy in
an offhand manner.</p>
<p>&ldquo;Oh <em>yes</em>!&rdquo;&mdash;</p>
<p>&ldquo;Then get out before I begin, or you&rsquo;ll be buried alive in
it.&rdquo;</p>
<p>It made its skinny arms so long, and waved them so frighteningly, that
the children ran as hard as they could towards the road by which carts used
to come to the gravel&ndash;pits. Only Anthea had presence of mind enough
to
shout a timid &ldquo;Good&ndash;morning, I hope your whisker will be
better to&ndash;morrow,&rdquo; as she ran.</p>
<p>On the road they turned and looked back, and they had to shut their
eyes, and open them very slowly, a little bit at a time, because the sight
was too dazzling for their eyes to be able to bear it. It was something
like trying to look at the sun at high noon on Midsummer Day. For the whole
of the sand&ndash;pit was full, right up to the very top,</p>
</div>

<div class="bookPage" id="p39" name="39"><p>with new shining
gold pieces, and all the little sand&ndash;martins&rsquo; little front
doors were covered out of sight. Where the road for the carts wound into
the gravel&ndash;pit the gold lay in heaps like stones lie by the
roadside, and a great bank of shining gold shelved down from where it lay
flat and smooth between the tall sides of the gravel&ndash;pit. And all
the gleaming heap was minted gold. And on the sides and edges of these
countless coins the midday sun shone and sparkled, and glowed and gleamed
till the quarry looked like the mouth of a smelting furnace, or one of the
fairy halls that you see sometimes in the sky at sunset.</p>
<p>The children stood with their mouths open, and no one said a word.</p>
<p>At last Robert stooped and picked up one of the loose coins from the
edge of the heap by the cart&ndash;road, and looked at it. He looked on
both sides. Then he said in a low voice, quite different to his own,
&ldquo;It&rsquo;s not sovereigns.&rdquo;</p>
<p>&ldquo;It&rsquo;s gold, anyway,&rdquo; said Cyril. And now they all
began to talk at once. They all picked up the golden treasure by handfuls
and let it run through their fingers like water, and the chink it made as
it fell was wonderful music. At first they quite forgot to think of
spending the money, it was so nice to play with. Jane sat down between two</p>
</div>

