<p>&ldquo;A puncture?&rdquo; he was saying. &ldquo;Can I not be of any
assistance? If you could allow me&mdash;?&rdquo;</p>
<p>There was a stifled explosion of laughter behind the
pig&ndash;pail&mdash;the grown&ndash;up Lamb (otherwise Devereux) turned
the tail of an angry eye in its direction.</p>
<p>&ldquo;You&rsquo;re very kind,&rdquo; said the lady, looking at the
Lamb. She looked rather shy, but, as
the boys put it, there didn&rsquo;t seem to be any nonsense about her.</p>
<p>&ldquo;But oh,&rdquo; whispered Cyril behind the pig&ndash;pail,
&ldquo;I should have thought he&rsquo;d had enough bicycle&ndash;mending
for one day&mdash; and if she only knew that really and truly he&rsquo;s
only a whiny&ndash;piny, silly little baby!&rdquo;</p>
<p>&ldquo;He&rsquo;s <em>not</em>,&rdquo; Anthea murmured angrily.
&ldquo;He&rsquo;s a dear&mdash;if people only let him alone. It&rsquo;s our
own precious Lamb still, whatever silly idiots may turn him
into&mdash;isn&rsquo;t he, Pussy?&rdquo;</p>
<p>Jane doubtfully supposed so.</p>
<p>Now, the Lamb&mdash;whom I must try to remember to call St.
Maur&mdash;was examining the lady&rsquo;s bicycle and talking to her with a
very grown&ndash;up manner indeed. No one could possibly have supposed, to
see and hear him, that only that very morning he had been a chubby child of
two years breaking other people&rsquo;s Waterbury watches. Devereux (as he
ought to be called for the future) took</p>
</div>

<div class="bookPage" id="p206" name="206">
<p>out a gold watch when he had mended
the lady&rsquo;s bicycle, and all the onlookers behind the pig&ndash;pail
said &ldquo;Oh!&rdquo;&mdash; because it seemed so unfair that the Baby, who had only that morning destroyed two cheap but
honest watches, should now, in the grown&ndash;upness Cyril&rsquo;s folly
had raised him to, have a real gold watch&mdash;with a chain and seals!</p>
<p>Hilary (as I will now term him) withered his brothers and sisters with a
glance, and then said to the lady&mdash;with whom he seemed to be quite
friendly&mdash;</p>
<p>&ldquo;If you will allow me, I will ride with you as far as the Cross
Roads; it is getting late, and there are tramps about.&rdquo;</p>
<p>No one will ever know what answer the young lady intended to give to
this gallant offer, for, directly Anthea heard it made, she rushed out,
knocking against the pig&ndash;pail, which overflowed in a turbid stream,
and caught the Lamb (I suppose I ought to say Hilary) by the arm. The
others followed, and in an instant the four dirty children were visible,
beyond disguise.</p>
<p>&ldquo;Don&rsquo;t let him,&rdquo; said Anthea to the lady, and she
spoke with intense earnestness; &ldquo;he&rsquo;s not fit to go with
anyone!&rdquo;</p>
<p>&ldquo;Go away, little girl!&rdquo; said St. Maur (as
we will now call him) in a terrible voice. &ldquo;Go home at
once!&rdquo;</p>
</div>

<div class="bookPage" id="p207" name="207">
<p>&ldquo;You&rsquo;d much better not have anything to do with him,&rdquo;
the now reckless Anthea went on. &ldquo;He doesn&rsquo;t know who he is.
He&rsquo;s something very different from what you think he is.&rdquo;</p>
<p>&ldquo;What do you mean?&rdquo; asked the lady not unnaturally, while
Devereux (as I must term the grown&ndash;up Lamb) tried vainly to push
Anthea away. The others backed her up, and she stood solid as a rock.</p>
<p>&ldquo;You just let him go with you,&rdquo; said Anthea,
&ldquo;you&rsquo;ll soon see what I mean! How would you like to suddenly
see a poor little helpless baby spinning along downhill beside you with its
feet up on a bicycle it had lost control of?&rdquo;</p>
<p>The lady had turned rather pale.</p>
<p>&ldquo;Who are these very dirty children?&rdquo; she asked the
grown&ndash;up Lamb (sometimes called St. Maur in these pages).</p>
<p>&ldquo;I don&rsquo;t know,&rdquo; he lied miserably.</p>
<p>&ldquo;Oh, Lamb! how <em>can</em> you?&rdquo; cried
Jane,&mdash;&ldquo;when you know perfectly well you&rsquo;re our
own little baby brother that we&rsquo;re so fond of. We&rsquo;re his big
brothers and sisters,&rdquo; she explained, turning to the lady, who with
trembling hands was now turning her bicycle towards the gate, &ldquo;and
we&rsquo;ve got to take care of him. And we must get him home</p>
</div>

<div class="bookPage" id="p208" name="208">
<p>before sunset, or I don&rsquo;t know whatever will become of us. You see,
he&rsquo;s sort of under a spell&mdash;enchanted&mdash;you know what I
mean!&rdquo;</p>
<p>Again and again the Lamb (Devereux, I mean) had tried to stop
Jane&rsquo;s eloquence, but Robert and Cyril held him, one by each leg, and
no proper explanation was possible. The lady rode hastily away, and
electrified her relatives at dinner by telling them of her escape from a
family of dangerous lunatics. &ldquo;The little girl&rsquo;s eyes were
simply those of a maniac. I can&rsquo;t think how she came to be at
large,&rdquo; she said.</p>
<p>When her bicycle had whizzed away down the road, Cyril spoke
gravely.</p>
<p>&ldquo;Hilary, old chap,&rdquo; he said, &ldquo;you must have had a
sunstroke or something. And the things you&rsquo;ve been saying to that
lady! Why, if we were to tell you the things you&rsquo;ve said
when you are yourself again, say to&ndash;morrow morning, you
wouldn&rsquo;t even understand them&mdash;let alone believe them! You trust
to me, old chap, and come home, now, and if you&rsquo;re not yourself in
the morning we&rsquo;ll ask the milkman to ask the doctor to
come.&rdquo;</p>
<p>The poor grown&ndash;up Lamb (St. Maur was really one of his Christian
names) seemed now too bewildered to resist.</p>
