<p>moustache appeared on the lip of one
who was still&mdash;except as to the face&mdash;a
two&ndash;year&ndash;old baby in a linen smock and white open&ndash;work
socks.</p>
<p>&ldquo;Oh, I wish it wouldn&rsquo;t! Oh, I wish it
wouldn&rsquo;t! You boys might wish as well!&rdquo; They all wished hard,
for the sight was enough to dismay the most heartless. They all wished so
hard, indeed, that they felt giddy and almost lost consciousness; but the
wishing was quite vain, for, when the wood ceased to whirl round, their
dazzled eyes were riveted at once by the spectacle of a very
proper&ndash;looking young man in flannels and a straw hat&mdash;a young
man who wore the same little black moustache which just before they had
actually seen growing upon the Baby&rsquo;s lip. This, then, was the
Lamb&mdash;grown up! Their own Lamb! It was a terrible moment. The
grown&ndash;up Lamb moved gracefully across the moss and settled himself
against the trunk of the sweet chestnut. He tilted the straw hat over his
eyes. He was evidently weary. He was going to sleep. The Lamb&mdash;the
original little tiresome beloved Lamb often went to sleep at odd times and
in unexpected places. Was this new Lamb in the grey flannel suit and the
pale green necktie like the other Lamb? or had his mind grown up together
with his body?</p>
<p>That was the question which the others, in 
a hurried council held among the yellowing bracken a few yards from the
sleeper, debated eagerly.</p>
</div>

<div class="bookPage" id="p198" name="198">
<p>&ldquo;Whichever it is, it&rsquo;ll be just as awful,&rdquo; said
Anthea. &ldquo;If his inside senses are grown up too, he won&rsquo;t stand
our looking after him; and if he&rsquo;s still a baby inside of him how on
earth are we to get him to do anything? And it&rsquo;ll be getting on for
dinner&ndash;time in a minute&rdquo;&mdash;</p>
<p>&ldquo;And we haven&rsquo;t got any nuts,&rdquo; said Jane.</p>
<p>&ldquo;Oh, bother nuts!&rdquo; said Robert; &ldquo;but dinner&rsquo;s
different&mdash;I didn&rsquo;t have half enough dinner yesterday.
Couldn&rsquo;t we tie him to the tree and go home to our dinners and come
back afterwards?&rdquo;</p>
<p>&ldquo;A fat lot of dinner we should get if we went back without the
Lamb!&rdquo; said Cyril in scornful misery. &ldquo;And it&rsquo;ll be just
the same if we go back with him in the state he is now. Yes, I know
it&rsquo;s my doing; don&rsquo;t rub it in! I know I&rsquo;m a beast, and
not fit to live; you can take that for settled, and say no more about it.
The question is, what are we going to do?&rdquo;</p>
<p>&ldquo;Let&rsquo;s wake him up, and take him into Rochester or Maidstone
and get some grub at a pastrycook&rsquo;s,&rdquo; said Robert
hopefully.</p>
<p>&ldquo;Take him?&rdquo; repeated Cyril. &ldquo;Yes&mdash;do! It&rsquo;s
all my fault&mdash;I don&rsquo;t deny that&mdash;but you&rsquo;ll find
you&rsquo;ve got your work cut out for you if you try to take that young
man anywhere. The Lamb always was spoilt, but now he&rsquo;s</p>
</div>

