</div>

<div class="bookPage" id="p199" name="199">
<p>grown up he&rsquo;s a demon&mdash;simply. I can see it. Look at his
mouth.&rdquo;</p>
<p>&ldquo;Well then,&rdquo; said Robert, &ldquo;let&rsquo;s wake him up and
see what <em>he&rsquo;ll</em> do. Perhaps <em>he&rsquo;ll</em> take
<em>us</em> to Maidstone and stand Sam. He ought to have a lot of money
in the pockets of those extra&ndash;special bags. We <em>must</em>
have dinner, anyway.&rdquo;</p>
<p>They drew lots with little bits of bracken. It fell to Jane&rsquo;s lot
to waken the grown&ndash;up Lamb.</p>
<p>She did it gently by tickling his nose with a twig of wild honeysuckle.
He said &ldquo;Bother the flies!&rdquo; twice, and then opened his
eyes.</p>
<p>&ldquo;Hullo, kiddies!&rdquo; he said in a languid tone, &ldquo;still
here? What&rsquo;s the giddy hour? You&rsquo;ll be late for your
grub!&rdquo;</p>
<p>&ldquo;I know we shall,&rdquo; said Robert bitterly.</p>
<p>&ldquo;Then cut along home,&rdquo; said the grown&ndash;up Lamb.</p>
<p>&ldquo;What about your grub, though?&rdquo; asked Jane.</p>
<p>&ldquo;Oh, how far is it to the station, do you think? I&rsquo;ve a sort
of notion that I&rsquo;ll run up to town and have some lunch at the
club.&rdquo;</p>
<p>Blank misery fell like a pall on the four others. The
Lamb&mdash;alone&mdash;unattended&mdash;would go to town and have lunch at
a club! Perhaps he would also have tea there. Perhaps sunset would come
upon him amid the dazzling luxury of</p>
</div>

<div class="bookPage" id="p200" name="200">
<p>club&ndash;land, and a helpless cross
sleepy baby would find itself alone amid unsympathetic waiters, and would
wail miserably for &ldquo;Panty&rdquo; from the depths of a club
arm&ndash;chair ! The picture moved Anthea almost to tears.</p>
<p>&ldquo;Oh no, Lamb ducky, you mustn&rsquo;t do that!&rdquo; she cried
incautiously.</p>
<p>The grown&ndash;up Lamb frowned. &ldquo;My dear Anthea,&rdquo; he said,
&ldquo;how often am I to tell you that my name is Hilary or St. Maur or
Devereux?&mdash;any of my baptismal names are
free to my little brothers and sisters, but <em>not</em>
&lsquo;Lamb&rsquo;&mdash;a relic of foolish and far&ndash;off
childhood.&rdquo;</p>
<p>This was awful. He was their elder brother now, was he? Well, of course
he was, if he was grown up&mdash;since they weren&rsquo;t. Thus, in
whispers, Anthea and Robert.</p>
<p>But the almost daily adventures resulting from the Psammead wishes were
making the children wise beyond their years.</p>
<p>&ldquo;Dear Hilary,&rdquo; said Anthea, and the others choked at the
name, &ldquo;you know father didn&rsquo;t wish you to go to London. He
wouldn&rsquo;t like us to be left alone without you to take care of us. Oh,
deceitful beast that I am!&rdquo; she added to herself.</p>
</div>

<div class="bookPage" id="p201" name="201">
<p>&ldquo;Look here,&rdquo; said Cyril, &ldquo;if you&rsquo;re our elder
brother, why not behave as such and take us over to Maidstone and give us a
jolly good blow&ndash;out, and we&rsquo;ll go on the river
afterwards?&rdquo;</p>
<p>&ldquo;I&rsquo;m infinitely obliged to you,&rdquo; said the Lamb
courteously, &ldquo;but I should prefer solitude. Go home to your
lunch&mdash;I mean your dinner. Perhaps I may look in about
tea&ndash;time&mdash;or I may not be home till after you are in your
beds.&rdquo;</p>
<p>Their beds! Speaking glances flashed between the wretched four. Much bed
there would be for them if they went home without the Lamb.</p>
<p>&ldquo;We promised mother not to lose sight of you if we took you
out,&rdquo; Jane said before the others could stop her.</p>
<p>&ldquo;Look here, Jane,&rdquo; said the grown&ndash;up Lamb, putting
his hands in his pockets and looking down at her, &ldquo;little girls
should be seen and not heard. You kids must learn not to make yourselves a
nuisance. Run along home now&mdash;and perhaps, if you&rsquo;re good,
I&rsquo;ll give you each a penny to&ndash;morrow.&rdquo;</p>
<p>&ldquo;Look here,&rdquo; said Cyril, in the best &ldquo;man to
man&rdquo; tone at his command, &ldquo;where are you going, old man? You
might let Bobs and me come with you&mdash;even if you don&rsquo;t want the
girls.&rdquo;</p>
</div>

