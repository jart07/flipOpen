<div class="bookPage" id="p195" name="195">
<p>And Cyril went.</p>
<p>&ldquo;What an awful long time babies <em>stay</em> babies,&rdquo;
said Cyril after the Lamb had taken his watch out of his pocket while he
wasn&rsquo;t noticing, and with coos and clucks of naughty rapture had
opened the case and used the whole thing as a garden spade, and when even
immersion in a wash&ndash;hand basin had failed to wash the mould from the
works and make the watch go again. Cyril had said several things in the
heat of the moment; but now he was calmer, and had even consented to carry
the Lamb part of the way to the woods. Cyril had persuaded the others to
agree to his plan, and not to wish for anything more till they really did
wish it. Meantime it seemed good to go
to the woods for nuts, and on the mossy grass under a sweet
chestnut&ndash;tree the five were sitting. The Lamb was pulling up the
moss by fat handfuls, and Cyril was gloomily contemplating the ruins of his
watch.</p>
<p>&ldquo;He does grow,&rdquo; said Anthea. &ldquo;Doesn&rsquo;t oo,
precious?&rdquo;</p>
<p>&ldquo;Me grow,&rdquo; said the Lamb cheerfully&mdash;&ldquo;me grow big
boy, have guns an&rsquo;
mouses&mdash;an&rsquo;&mdash;an&rsquo;&rdquo;&mdash; Imagination or
vocabulary gave out here. But anyway it was the longest speech the Lamb had
ever made, and it charmed everyone, even Cyril, who tumbled the Lamb over
and rolled him in the moss to the music of delighted squeals.</p>
</div>

<div class="bookPage" id="p196" name="196">
<p>&ldquo;I suppose he&rsquo;ll be grown up some day,&rdquo; Anthea was
saying, dreamily looking up at the blue of the sky that showed between the
long straight chestnut&ndash;leaves. But at that moment the Lamb,
struggling gaily with Cyril, thrust a stoutly&ndash;shod little foot
against his brother&rsquo;s chest; there was a crack!&mdash;the innocent
Lamb had broken the glass of father&rsquo;s second&ndash;best Waterbury
watch, which Cyril had borrowed without leave.</p>
<p>&ldquo;Grow up some day!&rdquo; said Cyril bitterly, plumping the Lamb
down on the grass. &ldquo;I daresay he will&mdash;when nobody wants him to.
I wish to goodness he would&rdquo;&mdash;</p>
<p>&ldquo;<em>Oh</em>, take care!&rdquo; cried Anthea in an agony of
apprehension. But it was too late&mdash;like music to a song her words and
Cyril&rsquo;s came out together&mdash;</p>
<p>Anthea&mdash;&ldquo;Oh, take care!&rdquo;</p>
<p>Cyril&mdash;&ldquo;Grow up now!&rdquo;</p>
<p>The faithful Psammead was true to its promise, and there, before the
horrified eyes of its brothers and sisters, the Lamb suddenly and violently
grew up. It was the most terrible moment. The change was not so sudden as
the wish&ndash;changes usually were. The Baby&rsquo;s face changed first.
It grew thinner and larger, lines came in the forehead, the eyes grew more
deep&ndash;set and darker in colour, the mouth grew longer and thinner;
most terrible of all, a little dark</p>
