<div class="bookPage" id="p202" name="202">
<p>This was really rather noble of Cyril, for he never did care much about
being seen in public with the Lamb, who of course after sunset would be a
baby again.</p>
<p>The &ldquo;man to man&rdquo; tone succeeded.</p>
<p>&ldquo;I shall just run over to Maidstone on my bike,&rdquo; said the
new Lamb airily, fingering the little black moustache. &ldquo;I can lunch
at The Crown&mdash;and perhaps I&rsquo;ll have a pull on the river; but I
can&rsquo;t take you all on the machine&mdash;now, can I? Run along home,
like good children.&rdquo;</p>
<p>The position was desperate. Robert exchanged a despairing look with
Cyril. Anthea detached a pin from her waistband, a pin whose withdrawal
left a gaping chasm between skirt and bodice, and handed it furtively to
Robert&mdash;with a grimace of the darkest and deepest meaning. Robert
slipped away to the road. There, sure enough, stood a bicycle&mdash;a
beautiful new free&ndash;wheel. Of course Robert understood at once that
if the Lamb was grown up he <em>must</em> have a bicycle. This had
always been one of Robert&rsquo;s own reasons for wishing to be grown up.
He hastily began to use the pin&mdash;eleven punctures in the back tyre,
seven in the front. He would have made the total twenty&ndash;two but for
the rustling of the yellow hazel&ndash;leaves, which warned him of
the approach of the others. He hastily leaned a hand on the wheel, and was
rewarded by the &ldquo;whish&rdquo; of what was left of the air escaping
from eighteen neat pin&ndash;holes.</p>
</div>

<div class="bookPage" id="p203" name="203">
<p>&ldquo;Your bike&rsquo;s run down,&rdquo; said Robert, wondering how he
could so soon have learned to deceive.</p>
<p>&ldquo;So it is,&rdquo; said Cyril.</p>
<p>&ldquo;It&rsquo;s a puncture,&rdquo; said Anthea, stooping down, and
standing up again with a thorn which she had got ready for the purpose.
&ldquo;Look here.&rdquo;</p>
<p>The grown&ndash;up Lamb (or Hilary, as I suppose one must now call him)
fixed his pump and blew up the tyre. The punctured state of it was soon
evident.</p>
<p>&ldquo;I suppose there&rsquo;s a cottage somewhere near&mdash;where one
could get a pail of water?&rdquo; said the Lamb.</p>
<p>There was; and when the number of punctures had been made manifest, it
was felt to be a special blessing that the cottage provided &ldquo;teas for
cyclists&rdquo;. It provided an odd sort of tea&ndash;and&ndash;hammy
meal for the Lamb and his brothers. This was paid for out of
the fifteen shillings which had been earned by Robert when he was a
giant&mdash;for the Lamb, it appeared, had unfortunately no money about
him. This was a great disappointment for the others; but it is a thing that
will happen, even to the most grown&ndash;up of us. However, Robert had
enough to eat, and that was something. Quietly but persistently the
miserable four took it in turns to try to persuade the Lamb (or St. Maur)
to spend the rest of the day in the</p>
</div>

<div class="bookPage" id="p204" name="204">
<p>woods. There was not very much of the
day left by the time he had mended the eighteenth puncture. He looked up
from the completed work with a sigh of relief, and suddenly put his tie
straight.</p>
<p>&ldquo;There&rsquo;s a lady coming,&rdquo; he said
briskly&mdash;&ldquo;for goodness&rsquo; sake, get out of the way. Go
home&mdash;hide&mdash;vanish somehow! I can&rsquo;t be seen with a pack of
dirty kids.&rdquo; His brothers and sisters were indeed rather dirty,
because, earlier in the day, the Lamb, in his infant state, had sprinkled a
good deal of garden soil over them. The grown&ndash;up Lamb&rsquo;s voice
was so tyrant&ndash;like, as Jane said afterwards, that they
actually retreated to the back garden, and left him with his little
moustache and his flannel suit to meet alone the young lady, who now came
up the front garden wheeling a bicycle.</p>
<p>The woman of the house came out, and the young lady spoke to
her&mdash;the Lamb raised his hat as she passed him&mdash;and the children
could not hear what she said, though they were craning round the corner by
the pig&ndash;pail and listening with all their ears. They felt it to be
&ldquo;perfectly fair,&rdquo; as Robert said, &ldquo;with that wretched
Lamb in that condition.&rdquo;</p>
<p>When the Lamb spoke, in a languid voice heavy with politeness, they
heard well enough.</p>
