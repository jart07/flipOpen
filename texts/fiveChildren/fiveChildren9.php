<div class="bookPage" id="p209" name="209">
<p>&ldquo;Since you seem all to be as mad as the whole worshipful company
of hatters,&rdquo; he said bitterly, &ldquo;I suppose I <em>had</em>
better take you home. But you&rsquo;re not to suppose I shall pass this
over. I shall have something to say to you all to&ndash;morrow
morning.&rdquo;</p>
<p>&ldquo;Yes, you will, my Lamb,&rdquo; said Anthea under her breath,
&ldquo;but it won&rsquo;t be at all the sort of thing you think it&rsquo;s
going to be.&rdquo;</p>
