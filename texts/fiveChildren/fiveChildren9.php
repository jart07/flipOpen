<h2>CHAPTER IX<br />
GROWN UP</h2>
<p><strong>CYRIL</strong> had once pointed out that ordinary life is full of
occasions on which a wish would be most useful. And this thought filled his
mind when he happened to wake early on the morning after the morning after
Robert had wished to be bigger than the baker&rsquo;s boy, and had been it.
The day that lay between these two days had been occupied entirely by
getting the governess&ndash;cart home from Benenhurst.</p>
<p>Cyril dressed hastily; he did not take a bath, because tin baths are so
noisy, and he had no wish to rouse Robert, and he slipped off alone, as
Anthea had once done, and ran through the dewy morning to the
sand&ndash;pit. He dug up the Psammead very carefully and kindly, and
began the conversation by asking
it whether it still felt any ill effects from the contact with the tears of
Robert the day before yesterday. The Psammead was in a good temper. It
replied politely.</p>
<p>&ldquo;And now, what can I do for you?&rdquo; it said. &ldquo;I suppose
you&rsquo;ve come here so early to ask for something for
yourself&mdash;something your brothers and sisters aren&rsquo;t to</p>
</div>

<div class="bookPage" id="p194" name="194">
<p>know about, eh? Now, do be persuaded for your own good! Ask for a good fat
Megatherium and have done with it.&rdquo;</p>
<p>&ldquo;Thank you&mdash;not to&ndash;day, I think,&rdquo; said Cyril
cautiously. &ldquo;What I really wanted to say was&mdash;you know how
you&rsquo;re always wishing for things when you&rsquo;re playing at
anything?&rdquo;</p>
<p>&ldquo;I seldom play,&rdquo; said the Psammead coldly.</p>
<p>&ldquo;Well, you know what I mean,&rdquo; Cyril went on impatiently.
&ldquo;What I want to say is: won&rsquo;t you let us have our wish
just when we think of it, and just where we happen to be? So that we
don&rsquo;t have to come and disturb you again,&rdquo; added the crafty
Cyril.</p>
<p>&ldquo;It&rsquo;ll only end in your wishing for something you
don&rsquo;t really want, like you did about the castle,&rdquo; said the
Psammead, stretching its brown
arms and yawning. &ldquo;It&rsquo;s always the same since people left off
eating really wholesome things. However, have it your own way.
Good&ndash;bye.&rdquo;</p>
<p>&ldquo;Good&ndash;bye,&rdquo; said Cyril politely.</p>
<p>&ldquo;I&rsquo;ll tell you what,&rdquo; said the Psammead suddenly,
shooting out its long snail&rsquo;s eyes,&mdash;&ldquo;I&rsquo;m getting
tired of you&mdash;all of you. You have no more sense than so many oysters.
Go along with you!&rdquo;</p>
