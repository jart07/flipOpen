<h2>CHAPTER VII<br />
A SIEGE AND BED</h2>
<p><strong>THE</strong> children were sitting in the gloomy banqueting&ndash;hall,
at the end of one of the long bare wooden tables. There was now no hope.
Martha had brought in the dinner, and the dinner was invisible, and
unfeelable too; for, when they rubbed their hands along the table, they
knew but too well that for them there was nothing there <em>but</em>
table.</p>
<p>Suddenly Cyril felt in his pocket.</p>
<p>&ldquo;Right, <em>oh!</em>&rdquo; he cried. &ldquo;Look here!
Biscuits.&rdquo;</p>
<p>Rather broken and crumbled, certainly, but still biscuits. Three whole
ones, and a generous handful of crumbs and fragments.</p>
<p>&ldquo;I got them this morning&mdash;cook&mdash;and I&rsquo;d quite
forgotten,&rdquo; he explained as he divided them with scrupulous fairness
into four heaps.</p>
<p>They were eaten in happy silence, though they tasted a little oddly,
because they had been in Cyril&rsquo;s pocket all the morning with a hank
of tarred twine, some green fir&ndash;cones, and a ball of cobbler&rsquo;s
wax.</p>
</div>

<div class="bookPage" id="p151" name="151">
<p>&ldquo;Yes, but look here, Squirrel,&rdquo; said Robert;
&ldquo;you&rsquo;re so clever at explaining about invisibleness and all
that. How is it the biscuits are here, and all the bread and meat and
things have disappeared?&rdquo;</p>
<p>&ldquo;I don&rsquo;t know,&rdquo; said Cyril after a pause,
&ldquo;unless it&rsquo;s because <em>we</em> had them. Nothing about
<em>us</em> has changed. Everything&rsquo;s in my pocket all
right.&rdquo;</p>
<p>&ldquo;Then if we <em>had</em> the mutton it would be real,&rdquo;
said Robert. &ldquo;Oh, don&rsquo;t I wish we could find it!&rdquo;</p>
<p>&ldquo;But we can&rsquo;t find it. I suppose it isn&rsquo;t ours till
we&rsquo;ve got it in our mouths.&rdquo;</p>
<p>&ldquo;Or in our pockets,&rdquo; said Jane, thinking of the
biscuits.</p>
<p>&ldquo;Who puts mutton in their pockets, goose&ndash;girl?&rdquo; said
Cyril. &ldquo;But I know&mdash;at any rate, I&rsquo;ll try it!&rdquo;</p>
<p>He leaned over the table with his face about
an inch from it, and kept opening and shutting his mouth as if he were
taking bites out of air.</p>
<p>&ldquo;It&rsquo;s no good,&rdquo; said Robert in deep dejection.
&ldquo;You&rsquo;ll only&mdash; Hullo!&rdquo;</p>
<p>Cyril stood up with a grin of triumph, holding a square piece of bread
in his mouth. It was quite real. Everyone saw it. It is true that, directly
he bit a piece off, the rest vanished; but it was all right, because he
knew he had it</p>
</div>

<div class="bookPage" id="p152" name="152">
<p>in his hand though he could neither see nor feel it. He took
another bite from the air between his fingers, and it turned into bread as
he bit. The next moment all the others were following his example, and
opening and shutting their mouths an inch or so from the
bare&ndash;looking table. Robert captured a slice of mutton, and&mdash;but
I think I will draw a veil over the rest of this painful scene. It is
enough to say that they all had enough mutton, and that when Martha came to
change the plates she said she had never seen such a mess in all her born
days.</p>
<p>The pudding was, fortunately, a plain suet roly&ndash;poly, and in
answer to Martha&rsquo;s
questions the children all with one accord said that they would
<em>not</em> have treacle on it&mdash;nor jam, nor
sugar&mdash;&ldquo;Just plain, please,&rdquo; they said. Martha said,
&ldquo;Well, I never&mdash;what next, I wonder!&rdquo; and went away.</p>
<p>Then ensued another scene on which I will not dwell, for nobody looks
nice picking up slices of suet pudding from the table in its mouth, like a
dog.</p>
<p>The great thing, after all, was that they had had dinner; and now
everyone felt more courage to prepare for the attack that was to be
delivered before sunset. Robert, as captain, insisted on climbing to the
top of one of the towers to reconnoitre, so up they all went. And now they</p>
</div>

<div class="bookPage" id="p153" name="153">
<p>could see all round the castle, and could see, too, that beyond the moat,
on every side, the tents of the besieging party were pitched. Rather
uncomfortable shivers ran down the children&rsquo;s backs as they saw that
all the men were busy cleaning or sharpening their arms,
re&ndash;stringing their bows, and polishing their shields. A large party
came along the road, with horses dragging along the great trunk of a tree;
and Cyril felt quite pale, because he knew this was for a
battering&ndash;ram.</p>
<p>&ldquo;What a good thing we&rsquo;ve got a moat,&rdquo; he said;
&ldquo;and what a good thing the drawbridge is up&mdash;I should never have
known how to work it.&rdquo;</p>
<p>&ldquo;Of course it would be up in a besieged castle.&rdquo;</p>
<p>&ldquo;You&rsquo;d think there ought to have been soldiers in it,
wouldn&rsquo;t you?&rdquo; said Robert.</p>
<p>&ldquo;You see you don&rsquo;t know how long it&rsquo;s been
besieged,&rdquo; said Cyril darkly; &ldquo;perhaps most of the brave
defenders were killed quite early in the siege and all the provisions
eaten, and now there are only a few intrepid survivors&mdash;that&rsquo;s
us, and we are going to defend it to the death.&rdquo;</p>
<p>&ldquo;How do you begin&mdash;defending to the death, I mean?&rdquo;
asked Anthea.</p>
<p>&ldquo;We ought to be heavily armed&mdash;and then shoot at them when
they advance to the attack.&rdquo;</p>
</div>

<div class="bookPage" id="p154" name="154">
<p>&ldquo;They used to pour boiling lead down on besiegers when they got
too close,&rdquo; said Anthea. &ldquo;Father showed me the holes on purpose
for pouring it down through at Bodiam Castle. And there are holes like it
in the gate&ndash;tower here.&rdquo;</p>
<p>&ldquo;I think I&rsquo;m glad it&rsquo;s only a game; it <em>is</em>
only a game, isn&rsquo;t it?&rdquo; said Jane.</p>
<p>But no one answered.</p>
<p>The children found plenty of strange weapons in the castle, and if they
were armed at all it was soon plain that they would be, as Cyril said,
&ldquo;armed heavily&rdquo;&mdash;for these swords and lances and crossbows
were far too weighty even for Cyril&rsquo;s manly strength; and as for the
longbows, none of the children could even begin to bend them. The daggers
were better; but Jane hoped that the besiegers would not come close enough
for daggers to be of any use.</p>
<p>&ldquo;Never mind, we can hurl them like javelins,&rdquo; said Cyril,
&ldquo;or drop them on people&rsquo;s heads. I say&mdash;there are lots of
stones on the other side of the courtyard. If we took some of those up?
Just to drop on their heads if they were to try swimming the
moat.&rdquo;</p>
<p>So a heap of stones grew apace, up in the room above the gate; and
another heap, a shiny spiky dangerous&ndash;looking heap, of daggers and
knives.</p>
</div>

<div class="bookPage" id="p155" name="155">
<p>As Anthea was crossing the courtyard for
more stones, a sudden and valuable idea came to her.</p>
<p>She went to Martha and said, &ldquo;May we have just biscuits for tea?
We&rsquo;re going to play at besieged castles, and we&rsquo;d like the
biscuits to provision the garrison. Put mine in my pocket, please, my hands
are so dirty. And I&rsquo;ll tell the others to fetch theirs.&rdquo;</p>
<p>This was indeed a happy thought, for now with four generous handfuls of
air, which turned to biscuits as Martha crammed it into their pockets, the
garrison was well provisioned till sundown.</p>
<p>They brought up some iron pots of cold water to pour on the besiegers
instead of hot lead, with which the castle did not seem to be provided.</p>
<p>The afternoon passed with wonderful quickness. It was very exciting; but
none of them, except Robert, could feel all the time that this was real
deadly dangerous work. To the others, who had only seen the camp and the
besiegers from a distance, the whole thing seemed half a game of
make&ndash;believe, and half a splendidly distinct and perfectly safe
dream. But it was only now and then that Robert could feel this.</p>
<p>When it seemed to be tea&ndash;time the biscuits were eaten with water
from the deep well in the courtyard, drunk</p>
</div>

<div class="bookPage" id="p156" name="156">
<p>out of horns. Cyril insisted on
putting by eight of the biscuits, in case anyone should feel faint in
stress of battle.</p>
<p>Just as he was putting away the reserve biscuits in a sort of little
stone cupboard without a door, a sudden sound made him drop three. It was
the loud fierce cry of a trumpet.</p>
<p>&ldquo;You see it <em>is</em> real,&rdquo; said Robert, &ldquo;and
they are going to attack.&rdquo;</p>
<p>All rushed to the narrow windows.</p>
<p>&ldquo;Yes,&rdquo; said Robert, &ldquo;they&rsquo;re all coming out of
their tents and moving about like ants. There&rsquo;s that Jakin dancing
about where the bridge joins on. I wish he could see me put my tongue out
at him! Yah!&rdquo;</p>
<p>The others were far too pale to wish to put their tongues out at
anybody. They looked at Robert with surprised respect. Anthea
said&mdash;</p>
<p>&ldquo;You really <em>are</em> brave, Robert.&rdquo;</p>
<p>&ldquo;Rot!&rdquo; Cyril&rsquo;s pallor turned to redness now, all in a
minute. &ldquo;He&rsquo;s been getting ready
to be brave all the afternoon. And I wasn&rsquo;t ready, that&rsquo;s all.
I shall be braver than he is in half a jiffy.&rdquo;</p>
</div>

<div class="bookPage" id="p157" name="157">
<p>&ldquo;Oh dear!&rdquo; said Jane, &ldquo;what does it matter which of
you is the bravest? I think Cyril was a perfect silly to wish for a castle,
and I don&rsquo;t want to play.&rdquo;</p>
<p>&ldquo;It <em>isn&rsquo;t</em>&rdquo;&mdash;Robert was beginning
sternly, but Anthea interrupted&mdash;</p>
<p>&ldquo;Oh yes, you do,&rdquo; she said coaxingly; &ldquo;it&rsquo;s a
very nice game, really, because they can&rsquo;t possibly get in, and if
they do the women and children are always spared by civilised
armies.&rdquo;</p>
<p>&ldquo;But are you quite, quite sure they <em>are</em>
civilised?&rdquo; asked Jane, panting. &ldquo;They seem to be such a long
time ago.&rdquo;</p>
<p>&ldquo;Of course they are.&rdquo; Anthea pointed cheerfully through the
narrow window. &ldquo;Why look at the little flags on their lances, how
bright they are&mdash;and how fine the leader is! Look, that&rsquo;s
him&mdash;isn&rsquo;t it, Robert?&mdash;on the grey horse.&rdquo;</p>
<p>Jane consented to look, and the scene was almost too pretty to be
alarming. The green turf, the white tents, the flash of pennoned
lances, the gleam of armour, and the bright colours of scarf and
tunic&mdash;it was just like a splendid coloured picture. The trumpets were
sounding, and when the trumpets stopped for breath the children could hear
the cling&ndash;clang of armour and the murmur of voices.</p>
</div>

<div class="bookPage" id="p158" name="158">
<p>A trumpeter came forward to the edge of the moat, which now seemed very
much narrower than at first, and blew the longest and loudest blast they
had yet heard. When the blaring noise had died away, a man who was with the
trumpeter shouted&mdash;</p>
<p>&ldquo;What ho, within there!&rdquo; and his voice came plainly to the
garrison in the gate&ndash;house.</p>
<p>&ldquo;Hullo there!&rdquo; Robert bellowed back at once.</p>
<p>&ldquo;In the name of our Lord the King, and of our good lord and trusty
leader Sir Wulfric de Talbot, we summon this castle to surrender&mdash;on
pain of fire and sword and no quarter. Do ye surrender?&rdquo;</p>
<p>&ldquo;<em>No</em>,&rdquo; bawled Robert, &ldquo;of course we
don&rsquo;t! Never, <em>Never</em>, <em>NEVER!</em>&rdquo;</p>
<p>The man answered back&mdash;</p>
<p>&ldquo;Then your fate be on your own heads.&rdquo;</p>
<p>&ldquo;Cheer,&rdquo; said Robert in a fierce whisper. &ldquo;Cheer to
show them we aren&rsquo;t afraid, and rattle the daggers to make more
noise. One, two, three! Hip, hip, hooray! Again&mdash;Hip, hip, hooray! One
more&mdash;Hip, hip, hooray!&rdquo; The cheers were rather high and weak,
but the rattle of the daggers lent them strength and depth.</p>
</div>

<div class="bookPage" id="p159" name="159">
<p>There was another shout from the camp across the moat&mdash; and then
the beleaguered fortress felt that the attack had indeed begun.</p>
<p>It was getting rather dark in the room above the great gate, and Jane
took a very little courage as she remembered that sunset
<em>couldn&rsquo;t</em> be far off now.</p>
<p>&ldquo;The moat is dreadfully thin,&rdquo; said Anthea.</p>
<p>&ldquo;But they can&rsquo;t get into the castle even if they do swim
over,&rdquo; said Robert. And as he spoke he heard feet on the stair
outside&mdash;heavy feet and the clank of steel. No one breathed for a
moment. The steel and the feet went on up the turret stairs. Then Robert
sprang softly to the door. He pulled off his shoes.</p>
<p>&ldquo;Wait here,&rdquo; he whispered, and stole quickly and softly
after the boots and the spur&ndash;clank. He peeped into the upper room.
The man was there&mdash;and it was Jakin, all dripping with
moat&ndash;water, and he was fiddling about with the machinery which
Robert felt sure worked the drawbridge. Robert banged the door suddenly,
and turned the great key in the lock, just as Jakin sprang to the inside of
the door. Then he tore downstairs and into the little turret at the foot of
the tower where the biggest window was.</p>
</div>

<div class="bookPage" id="p160" name="160">
<p>&ldquo;We ought to have defended <em>this</em>!&rdquo; he cried to
the others as they followed him. He was just in time. Another man had swum
over, and his fingers were on the window&ndash;ledge. Robert never knew
how the man had managed to climb up out of the water. But he saw the
clinging fingers, and hit them as hard as he could with an iron bar that he
caught up from the floor. The man fell with a plop&ndash;plash into the
moat&ndash;water. In another moment Robert was outside the little room,
had banged its door and was shooting home the enormous bolts, and calling
to Cyril to lend a hand.</p>
<p>Then they stood in the arched gate&ndash;house, breathing hard and
looking at each other.</p>
<p>Jane&rsquo;s mouth was open.</p>
<p>&ldquo;Cheer up, Jenny,&rdquo; said Robert,&mdash;&ldquo;it won&rsquo;t
last much longer.&rdquo;</p>
<p>There was a creaking above, and something rattled and shook. The
pavement they stood on seemed to tremble. Then a crash told them that the
drawbridge had been lowered to its place.</p>
<p>&ldquo;That&rsquo;s that beast Jakin,&rdquo; said Robert.
&ldquo;There&rsquo;s still the portcullis; I&rsquo;m almost certain
that&rsquo;s worked from lower down.&rdquo;</p>
</div>

<div class="bookPage" id="p161" name="161">
<p>And now the drawbridge rang and echoed hollowly to the hoofs of horses
and the tramp of armed men.</p>
<p>&ldquo;Up&mdash;quick!&rdquo; cried Robert. &ldquo;Let&rsquo;s drop
things on them.&rdquo;</p>
<p>Even the girls were feeling almost brave now. They followed Robert
quickly, and under his directions began to drop stones out through the long
narrow windows. There was a confused noise below, and some groans.</p>
<p>&ldquo;Oh dear!&rdquo; said Anthea, putting down the stone she was just
going to drop out. &ldquo;I&rsquo;m afraid we&rsquo;ve hurt
somebody!&rdquo;</p>
<p>Robert caught up the stone in a fury.</p>
<p>&ldquo;I should just hope we *had!*&rdquo; he said, &ldquo;I&rsquo;d
give something for a jolly good boiling kettle of lead. Surrender,
indeed!&rdquo;</p>
<p>And now came more tramping, and a pause, and then the thundering thump
of the battering&ndash;ram. And the little room was almost quite dark.</p>
<p>&ldquo;We&rsquo;ve held it,&rdquo; cried Robert, &ldquo;we
<em>won&rsquo;t</em> surrender! The sun <em>must</em> set in a
minute. Here&mdash;they&rsquo;re all jawing underneath again. Pity
there&rsquo;s no time to get more stones! Here, pour that water down on
them. It&rsquo;s no good, of course, but they&rsquo;ll hate it.&rdquo;</p>
<p>&ldquo;Oh dear!&rdquo; said Jane; &ldquo;don&rsquo;t you think
we&rsquo;d better surrender?&rdquo;</p>
</div>

<div class="bookPage" id="pf188" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil188.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p162" name="162">
<p>&ldquo;Never!&rdquo; said Robert; &ldquo;we&rsquo;ll have a parley if
you like, but we&rsquo;ll never surrender. Oh, I&rsquo;ll be a soldier when
I grow up&mdash;you just see if I don&rsquo;t. I won&rsquo;t go into the
Civil Service, whatever anyone says.&rdquo;</p>
<p>&ldquo;Let&rsquo;s wave a handkerchief and ask for a parley,&rdquo; Jane
pleaded. &ldquo;I don&rsquo;t believe the sun&rsquo;s going to set
to&ndash;night at all.&rdquo;</p>
<p>&ldquo;Give them the water first&mdash;the brutes!&rdquo;
said the bloodthirsty Robert. So Anthea tilted the pot over the nearest
lead&ndash;hole, and poured. they heard a splash below, but no one below
seemed to have felt it. And again the ram battered the great door. Anthea
paused.</p>
<p>&ldquo;How idiotic,&rdquo; said Robert, lying flat on the floor and
putting one eye to the lead hole. &ldquo;Of course the holes go straight
down into the gate&ndash;house&mdash;that&rsquo;s for when the enemy has
got past the door and the portcullis, and almost all is lost. Here hand me
the pot.&rdquo; He crawled on to the three&ndash;cornered
window&ndash;ledge in the middle of the wall, and, taking the pot from
Anthea, poured the water out through the arrow&ndash;slit.</p>
</div>

<div class="bookPage" id="p163" name="163">
<p>And as he began to pour, the noise of the battering&ndash;ram and the
trampling of the foe and the shouts of &ldquo;Surrender!&rdquo; and
&ldquo;De Talbot for ever!&rdquo; all suddenly stopped and went out like
the snuff of a candle; the little dark room seemed to whirl round and turn
topsy&ndash;turvy, and when the children came to themselves there they
were safe and sound, in the big front bedroom of their own house&mdash;the
house with the ornamental nightmare iron&ndash;top to the roof.</p>
<p>They all crowded to the window and looked out. The moat and the tents
and the besieging force were all gone&mdash;and there was the garden with
its tangle of dahlias and marigolds and asters and late roses, and the
spiky iron railings and the quiet white road.</p>
<p>Everyone drew a deep breath.</p>
<p>&ldquo;And that&rsquo;s all right!&rdquo; said Robert. &ldquo;I told you
so! And, I say, we didn&rsquo;t surrender, did we?&rdquo;</p>
<p>&ldquo;Aren&rsquo;t you glad now I wished for a castle?&rdquo; asked
Cyril.</p>
<p>&ldquo;I think I am <em>now</em>,&rdquo; said Anthea slowly.
&ldquo;But I wouldn&rsquo;t wish for it again, I think, Squirrel
dear!&rdquo;</p>
<p>&ldquo;Oh, it was simply splendid!&rdquo; said Jane unexpectedly.
&ldquo;I wasn&rsquo;t frightened a bit.&rdquo;</p>
</div>

<div class="bookPage" id="pf191" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil191.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p164" name="164">
<p>&ldquo;Oh, I say!&rdquo; Cyril was beginning, but Anthea stopped
him.</p>
<p>&ldquo;Look here,&rdquo; she said, &ldquo;it&rsquo;s just come into my
head. This is the very first thing we&rsquo;ve wished for that hasn&rsquo;t
got us into a row. And there hasn&rsquo;t been the least little scrap of a
row about this. Nobody&rsquo;s raging downstairs, we&rsquo;re safe and sound,
we&rsquo;ve had an awfully jolly day&mdash;at least not jolly exactly, but
you know what I mean. And we know now how brave Robert is&mdash; and Cyril
too, of course,&rdquo; she added hastily, &ldquo;and Jane as well. And we
haven&rsquo;t got into a row with a single grown&ndash;up.&rdquo;</p>
<p>The door was opened suddenly and fiercely.</p>
<p>&ldquo;You ought to be ashamed of yourselves,&rdquo; said the voice of
Martha, and they could tell by her voice that she was very angry indeed.
&ldquo;I thought you couldn&rsquo;t last through the day without getting up
to some doggery! A person can&rsquo;t take a breath of air on the front
doorstep but you must be emptying the wash&ndash;hand jug on to their
heads! Off you go to bed, the lot of you, and try to get up better children
in the morning. Now then&mdash;don&rsquo;t let me have to tell you twice.
If I find any of you not in bed in ten minutes I&rsquo;ll let you know it,
that&rsquo;s all! A new cap, and everything!&rdquo;</p>
</div>

<div class="bookPage" id="p165" name="165">
<p>She flounced out amid a disregarded chorus of regrets and apologies. The
children were very sorry, but really it was not their faults. You
can&rsquo;t help it if you are pouring water on a
besieging foe, and your castle suddenly changes into your house&mdash;and
everything changes with it except the water, and that happens to fall on
somebody else&rsquo;s clean cap.</p>
<p>&ldquo;I don&rsquo;t know why the water didn&rsquo;t change into
nothing, though,&rdquo; said Cyril.</p>
<p>&ldquo;Why should it?&rdquo; asked Robert. &ldquo;Water&rsquo;s water
all the world over.&rdquo;</p>
<p>&ldquo;I expect the castle well was the same as ours in the
stable&ndash;yard,&rdquo; said Jane. And that was really the case.</p>
<p>&ldquo;I thought we couldn&rsquo;t get through a wish&ndash;day without
a row,&rdquo; said Cyril; &ldquo;it was much too good to be true. Come on,
Bobs, my military hero. If we lick into bed sharp she won&rsquo;t be so
frumious, and perhaps she&rsquo;ll bring us up some supper. I&rsquo;m jolly
hungry! Good&ndash;night, kids.&rdquo;</p>
<p>&ldquo;Good&ndash;night. I hope the castle won&rsquo;t come creeping
back in the night,&rdquo; said Jane.</p>
<p>&ldquo;Of course it won&rsquo;t,&rdquo; said Anthea briskly, &ldquo;but
Martha will&mdash;not in the night, but in a minute. Here, turn round,
I&rsquo;ll get that knot out of your pinafore strings.&rdquo;</p>
</div>

<div class="bookPage" id="p166" name="166">
<p>&ldquo;Wouldn&rsquo;t it have been degrading for Sir
Wulfric de Talbot,&rdquo; said Jane dreamily, &ldquo;if he could have known
that half the besieged garrison wore pinafores?&rdquo;</p>
<p>&ldquo;And the other half knickerbockers. Yes&mdash;frightfully. Do
stand still&mdash;you&rsquo;re only tightening the knot,&rdquo; said
Anthea.</p>

