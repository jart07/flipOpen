<h2>CHAPTER X<br />
SCALPS</h2>
<p><strong>PROBABLY</strong> the day would have been a greater success if Cyril had
not been reading <em>The Last of the Mohicans</em>. The story was
running in his head at breakfast, and as he took his third cup of tea he
said dreamily, &ldquo;I wish there were Red Indians in England&mdash;not
big ones, you know, but little ones, just about the right size for us to
fight.&rdquo;</p>
<p>Everyone disagreed with him at the time, and no one attached any
importance to the incident. But when they went down to the sand&ndash;pit
to ask for a hundred pounds in two&ndash;shilling pieces with Queen
Victoria&rsquo;s head on, to prevent mistakes&mdash;which they had always
felt to be a really reasonable wish that must turn out well&mdash;they
found out that they had done it again! For the Psammead, which was very
cross and sleepy, said&mdash;</p>
<p>&ldquo;Oh, don&rsquo;t bother me. You&rsquo;ve had your wish.&rdquo;</p>
<p>&ldquo;I didn&rsquo;t know it,&rdquo; said Cyril.</p>
<p>&ldquo;Don&rsquo;t you remember yesterday?&rdquo; said the
Sand&ndash;fairy, still more disagreeably. &ldquo;You asked me to let you
have your wishes wherever you happened to be, and you wished this morning,
and you&rsquo;ve got it.&rdquo;</p>
</div>

<div class="bookPage" id="p214" name="214">
<p>&ldquo;Oh, have we?&rdquo; said Robert. &ldquo;What is it?&rdquo;</p>
<p>&ldquo;So you&rsquo;ve forgotten?&rdquo; said the Psammead, beginning to
burrow. &ldquo;Never mind; you&rsquo;ll know soon enough. And I wish you
joy of it! A nice thing you&rsquo;ve let yourselves in for!&rdquo;</p>
<p>&ldquo;We always do, somehow,&rdquo; said Jane sadly.</p>
<p>And now the odd thing was that no one could remember anyone&rsquo;s
having wished for anything that morning. The wish about the Red Indians had
not stuck in anyone&rsquo;s head. It was a most anxious morning. Everyone
was trying to remember what had been wished for, and no one could, and
everyone kept expecting something awful to happen every minute. It was most
agitating; they knew, from what the Psammead had said, that they must have
wished for something more than usually undesirable, and they spent several
hours in most agonising uncertainty. It was not till nearly dinner&ndash;time
that Jane tumbled over <em>The Last of the Mohicans</em>,&mdash;which
had, of course, been left face downwards on the floor,&mdash;and when
Anthea had picked her and the book up she suddenly said, &ldquo;I
know!&rdquo; and sat down flat on the carpet.</p>
<p>&ldquo;Oh, Pussy, how awful! It was Indians he wished
for&mdash;Cyril&mdash;at breakfast, don&rsquo;t you remember? He said,
&lsquo;I wish there were Red Indians in England,&rsquo;&mdash;and now there
are, and they&rsquo;re going about scalping people all over the country, as
likely as not.&rdquo;</p>
</div>

<div class="bookPage" id="p215" name="215">
<p>&ldquo;Perhaps they&rsquo;re only in Northumberland and Durham,&rdquo;
said Jane soothingly. It was almost impossible to believe that it could
really hurt people much to be scalped so far away as that.</p>
<p>&ldquo;Don&rsquo;t you believe it!&rdquo; said Anthea. &ldquo;The
Sammyadd said we&rsquo;d let ourselves in for a nice thing. That means
they&rsquo;ll come <em>here</em>. And suppose they scalped the
Lamb!&rdquo;</p>
<p>&ldquo;Perhaps the scalping would come right again at sunset,&rdquo;
said Jane; but she did not speak so hopefully as usual.</p>
<p>&ldquo;Not it!&rdquo; said Anthea. &ldquo;The things that grow out of
the wishes don&rsquo;t go. Look at the fifteen shillings! Pussy, I&rsquo;m
going to break something, and you must let me have every penny of money
you&rsquo;ve got. The Indians will come <em>here</em>, don&rsquo;t you
see? That spiteful Psammead as good as said so. You see what my plan is?
Come on!&rdquo;</p>
<p>Jane did not see at all. But she followed her sister meekly into their
mother&rsquo;s bedroom.</p>
<p>Anthea lifted down the heavy water&ndash;jug&mdash;it had a pattern of
storks and long grasses on it, which Anthea never forgot. She carried it
into the dressing&ndash;room, and carefully emptied the water out of it
into the bath. Then she took the jug back into the bedroom and dropped it
on the floor. You know how a jug always breaks if you happen to drop it by
accident. If you happen to drop it on purpose, it is quite different.
Anthea dropped that jug three times, and it was as unbroken as ever. So at
last she had to take her father&rsquo;s boot&ndash;tree and break the jug
with that in cold blood. It was heartless work.</p>
</div>

<div class="bookPage" id="p216" name="216">
<p>Next she broke open the missionary&ndash;box
</div>

<div class="bookPage" id="p217" name="217">
with the poker. Jane told her that it was wrong, of course, but Anthea shut
her lips very tight and then said&mdash;</p>
<p>&ldquo;Don&rsquo;t be silly&mdash;it&rsquo;s a matter of life and
death.&rdquo;</p>
<p>There was not very much in the missionary&ndash;box,&mdash;only
seven&ndash;and&ndash;fourpence,&mdash;but the girls between them had
nearly four shillings. This made over eleven shillings, as you will easily
see.</p>
<p>Anthea tied up the money in a corner of her pocket&ndash;handkerchief.
&ldquo;Come on, Jane!&rdquo; she said, and ran down to the farm. She knew
that the farmer was going into Rochester that afternoon. In fact it had
been arranged that he was to take the four children with him. They had
planned this in the happy hour when they believed that they were going to
get that hundred pounds, in two&ndash;shilling pieces, out of the
Psammead. They had arranged to pay the farmer two shillings each for the
ride. Now Anthea hastily explained to him that they could not go, but would
he take Martha and the Baby instead? He agreed, but he was not pleased to
get only half&ndash;a&ndash;crown instead of eight shillings.</p>
</div>

<div class="bookPage" id="p218" name="218">
<p>Then the girls ran home again. Anthea was agitated, but not flurried.
When she came to think it over afterwards, she could not help seeing that
she had acted with the most far&ndash;seeing promptitude, just like a born
general. She fetched a little box from her corner drawer, and went to find
Martha, who was laying the cloth and not in the best of tempers.</p>
<p>&ldquo;Look here,&rdquo; said Anthea. &ldquo;I&rsquo;ve broken the
toilet&ndash;jug in mother&rsquo;s room.&rdquo;</p>
<p>&ldquo;Just like you&mdash;always up to some mischief,&rdquo; said
Martha, dumping down a salt&ndash;cellar with a bang.</p>
<p>&ldquo;Don&rsquo;t be cross, Martha dear,&rdquo; said Anthea.
&ldquo;I&rsquo;ve got enough money to pay for a new one&mdash;if only
you&rsquo;ll be a dear and go and buy it for us. Your cousins keep a
china&ndash;shop, don&rsquo;t they? And I would like you to get it
to&ndash;day, in case mother comes home to&ndash;morrow. You know she
said she might, perhaps.&rdquo;</p>
<p>&ldquo;But you&rsquo;re all going into town yourselves,&rdquo; said
Martha.</p>
<p>&ldquo;We can&rsquo;t afford to, if we get the new jug,&rdquo; said
Anthea; &ldquo;but we&rsquo;ll pay for you to go,
</div>

<div class="bookPage" id="p219" name="219">
if you&rsquo;ll take the Lamb. And I say Martha, look here&mdash;I&rsquo;ll
give you my Liberty box, if you&rsquo;ll go. Look, it&rsquo;s most awfully
pretty&mdash;all inlaid with real silver and ivory and ebony like King
Solomon&rsquo;s temple.&rdquo;</p>
<p>&ldquo;I see,&rdquo; said Martha,&mdash;&ldquo;no, I don&rsquo;t want
your box, miss. What you want is to get the precious Lamb off your hands
for the afternoon. Don&rsquo;t you go for to think I don&rsquo;t see
through you!&rdquo;</p>
<p>This was so true that Anthea longed to deny it at once. Martha had no
business to know so much. But she held her tongue.</p>
<p>Martha set down the bread with a bang that made it jump off its
trencher.</p>
<p>&ldquo;I <em>do</em> want the jug got,&rdquo; said Anthea softly.
&ldquo;You <em>will</em> go, won&rsquo;t you?&rdquo;</p>
<p>&ldquo;Well, just for this once, I don&rsquo;t mind; but mind you
don&rsquo;t get into none of your outrageous mischief while I&rsquo;m
gone&mdash; that&rsquo;s all!&rdquo;</p>
<p>&ldquo;He&rsquo;s going earlier than he thought,&rdquo; said Anthea
eagerly. &ldquo;You&rsquo;d better hurry and get dressed. Do put on that
lovely purple frock, Martha, and the hat with the pink cornflowers, and the
yellow&ndash;lace collar. Jane&rsquo;ll
</div>

<div class="bookPage" id="p220" name="220">
finish laying the cloth, and I&rsquo;ll wash the Lamb and get him
ready.&rdquo;</p>
<p>As she washed the unwilling Lamb, and hurried him into his best clothes,
Anthea peeped out of the window from time to time; so far all was
well&mdash;she could see no Red Indians. When with a rush and a scurry and
some deepening of the damask of Martha&rsquo;s complexion she and the Lamb
had been got off, Anthea drew a deep breath.</p>
<p>&ldquo;<em>He&rsquo;s</em> safe!&rdquo; she said, and, to
Jane&rsquo;s horror, flung herself down on the floor and burst into floods
of tears. Jane did not understand at all how a person could be so brave and
like a general, and then suddenly give way and go flat like an
air&ndash;balloon when you prick it. It is better not to go flat, of
course, but you will observe that Anthea did not give way till her aim was
accomplished. She had got the dear Lamb out of danger&mdash;she felt
certain the Red Indians would be round the White House or nowhere&mdash;the
farmer&rsquo;s cart would not come back till after sunset, so she could
afford to cry a little. It was partly with joy that she cried, because she
had done what she
</div>

<div class="bookPage" id="p221" name="221">
meant to do. She cried for about three minutes, while Jane hugged her
miserably and said at five&ndash;second intervals, &ldquo;Don&rsquo;t cry,
Panther dear!&rdquo;</p>
<p>Then she jumped up, rubbed her eyes hard with the corner of her
pinafore, so that they kept red for the rest of the day, and started to
tell the boys. But just at that moment cook rang the dinner&ndash;bell,
and nothing could be said till they had all been helped to minced beef.
Then cook left the room, and Anthea told her tale. But it is a mistake to
tell a thrilling tale when people are eating minced beef and boiled
potatoes. There seemed somehow to be something about the food that made the
idea of Red Indians seem flat and unbelievable. The boys actually laughed,
and called Anthea a little silly.</p>
<p>&ldquo;Why,&rdquo; said Cyril, &ldquo;I&rsquo;m almost sure it was
before I said that that Jane said she wished it would be a fine
day.&rdquo;</p>
<p>&ldquo;It wasn&rsquo;t,&rdquo; said Jane briefly.</p>
<p>&ldquo;Why, if it was Indians,&rdquo; Cyril went on&mdash;&ldquo;salt,
please, and mustard&mdash;I must have something to make this mush go
down,&mdash;if it was Indians, they&rsquo;d have been infesting the
</div>

<div class="bookPage" id="p222" name="222">
place long before this&mdash;you know they would. I believe it&rsquo;s the
fine day.&rdquo;</p>
<p>&ldquo;Then why did the Sammyadd say we&rsquo;d let ourselves in for a
nice thing?&rdquo; asked Anthea. She was feeling very cross. She knew she
had acted with nobility and discretion, and after that it was very hard to
be called a little silly, especially when she had the weight of a burglared
missionary&ndash;box and about seven&ndash;and&ndash;fourpence, mostly
in coppers, lying like lead upon her conscience.</p>
<p>There was a silence, during which cook took away the mincy plates and
brought in the treacle&ndash;pudding. As soon as she had retired, Cyril
began again.</p>
<p>&ldquo;Of course I don&rsquo;t mean to say,&rdquo; he admitted,
&ldquo;that it wasn&rsquo;t a good thing to get Martha and the Lamb out of
the light for the afternoon; but as for Red Indians&mdash;why, you know
jolly well the wishes always come that very minute. If there was going to
be Red Indians, they&rsquo;d be here now.&rdquo;</p>
<p>&ldquo;I expect they are,&rdquo; said Anthea; &ldquo;they&rsquo;re
lurking amid the undergrowth, for anything you know. I do think
you&rsquo;re most beastly unkind.&rdquo;</p>
</div>

<div class="bookPage" id="p223" name="223">
<p>&ldquo;Indians almost always <em>do</em> lurk, really, though,
don&rsquo;t they?&rdquo; put in Jane, anxious for peace.</p>
<p>&ldquo;No, they don&rsquo;t,&rdquo; said Cyril tartly. &ldquo;And
I&rsquo;m not unkind, I&rsquo;m only truthful. And I say it was utter rot
breaking the water&ndash;jug; and as for the missionary&ndash;box, I
believe it&rsquo;s a treason&ndash;crime, and I shouldn&rsquo;t wonder if
you could be hanged for it, if any of us was to split&rdquo;&mdash;</p>
<p>&ldquo;Shut up, can&rsquo;t you?&rdquo; said Robert; but Cyril
couldn&rsquo;t. You see, he felt in his heart that if there
<em>should</em> be Indians they would be entirely his own fault, so he
did not wish to believe in them. And trying not to believe things when in
your heart you are almost sure they are true, is as bad for the temper as
anything I know.</p>
<p>&ldquo;It&rsquo;s simply idiotic,&rdquo; he said, &ldquo;talking about
Indians, when you can see for yourselves that it&rsquo;s Jane who&rsquo;s
got her wish. Look what a fine day it
is&mdash;<em>OH!</em>&mdash;&rdquo;</p>
<p>He had turned towards the window to point out the fineness of the
day&mdash;the others turned too&mdash;and a frozen silence caught at Cyril,
and none of the others felt at all like breaking it.
</div>

<div class="bookPage" id="p224" name="224">
For there, peering round the corner of the window, among the red leaves of
the Virginia creeper, was a face&mdash;a brown face, with a long nose and a
tight mouth and very bright eyes. And the face was painted in coloured
patches. It had long black hair, and in the hair were feathers!</p>
<p>Every child&rsquo;s mouth in the room opened, and stayed open. The
treacle&ndash;pudding was growing white and cold on their plates. No one
could move.</p>
<p>Suddenly the feathered head was cautiously withdrawn, and the spell was
broken. I am sorry to say that Anthea&rsquo;s first words were very like a
girl.</p>
<p>&ldquo;There, now!&rdquo; she said. &ldquo;I told you so!&rdquo;</p>
<p>Treacle&ndash;pudding had now definitely ceased to charm. Hastily
wrapping their portions in a <em>Spectator</em> of the week before the
week before last, they hid them behind the crinkled&ndash;paper
stove&ndash;ornament, and fled upstairs to reconnoitre, and to hold a
hurried council.</p>
<p>&ldquo;Pax,&rdquo; said Cyril handsomely when they reached their
mother&rsquo;s bedroom. &ldquo;Panther, I&rsquo;m sorry if I was a
brute.&rdquo;</p>
</div>

<div class="bookPage" id="p225" name="225">
<p>&ldquo;All right,&rdquo; said Anthea, &ldquo;but you see now!&rdquo;</p>
<p>No further trace of Indians, however, could be discerned from the
windows.</p>
<p>&ldquo;Well,&rdquo; said Robert, &ldquo;what are we to do?&rdquo;</p>
<p>&ldquo;The only thing I can think of,&rdquo; said Anthea, who was now
generally admitted to be the heroine of the day, &ldquo;is&mdash;if we
dressed up as like Indians as we can, and looked out of the windows, or
even went out, they might think we were the powerful leaders of a large
neighbouring tribe, and&mdash;and not do anything to us, you know, for fear
of awful vengeance.&rdquo;</p>
<p>&ldquo;But Eliza, and the cook?&rdquo; said Jane.</p>
<p>&ldquo;You forget&mdash;they can&rsquo;t notice anything,&rdquo; said
Robert. &ldquo;They wouldn&rsquo;t notice anything out of the way, even if
they were scalped or roasted at a slow fire.&rdquo;</p>
<p>&ldquo;But would they come right at sunset?&rdquo;</p>
<p>&ldquo;Of course. You can&rsquo;t be really scalped or burned to death
without noticing it, and you&rsquo;d be sure to notice it next day, even if
it escaped your attention at the time,&rdquo; said Cyril. &ldquo;I think
Anthea&rsquo;s right, but we shall want a most awful lot of
feathers.&rdquo;</p>
</div>

<div class="bookPage" id="p226" name="226">
<p>&ldquo;I&rsquo;ll go down to the hen&ndash;house,&rdquo; said Robert.
&ldquo;There&rsquo;s one of the turkeys in there&mdash;it&rsquo;s not very
well. I could cut its feathers without it minding much. It&rsquo;s very
bad&mdash; doesn&rsquo;t seem to care what happens to it. Get me the
cutting&ndash;out scissors.&rdquo;</p>
<p>Earnest reconnoitring convinced them all that no Indians were in the
poultry&ndash;yard. Robert went. In five minutes he came back&mdash;pale,
but with many feathers.</p>
<p>&ldquo;Look here,&rdquo; he said, &ldquo;this is jolly serious. I cut
off the feathers, and when I turned to come out there was an Indian
squinting at me from under the old hen&ndash;coop. I just brandished the
feathers and yelled, and got away before he could get the coop off the top
of himself. Panther, get the coloured blankets off our beds, and look
slippy, can&rsquo;t you?&rdquo;</p>
<p>It is wonderful how like an Indian you can make yourselves with blankets
and feathers and coloured scarves. Of course none of the children happened
to have long black hair, but there was a lot of black calico that had been
got to cover school&ndash;books with. They
</div>

<div class="bookPage" id="p227" name="227">
cut strips of this into a sort of fine fringe, and fastened it round their
heads with the amber&ndash;coloured ribbons off the girls&rsquo; Sunday
dresses. Then they stuck turkeys&rsquo; feathers in the ribbons. The calico
looked very like long black hair, especially when the strips began to curl
up a bit.</p>
<p>&ldquo;But our faces,&rdquo; said Anthea, &ldquo;they&rsquo;re not at
all the right colour. We&rsquo;re all rather pale, and I&rsquo;m sure I
don&rsquo;t know why, but Cyril is the colour of putty.&rdquo;</p>
<p>&ldquo;I&rsquo;m not,&rdquo; said Cyril.</p>
<p>&ldquo;The real Indians outside seem to be brownish,&rdquo; said Robert
hastily. &ldquo;I think we ought to be really
<em>red</em>&mdash;it&rsquo;s sort of superior to have a red skin, if
you are one.&rdquo;</p>
<p>The red ochre cook used for the kitchen bricks seemed to be about the
reddest thing in the house. The children mixed some in a saucer with milk,
as they had seen cook do for the kitchen floor. Then they carefully painted
each other&rsquo;s faces and hands with it, till they were quite as red as
any Red Indian need be&mdash;if not redder.</p>
<p>They knew at once that they must look
</div>

<div class="bookPage" id="p228" name="228">
very terrible when they met Eliza in the passage, and she screamed aloud.
This unsolicited testimonial pleased them very much. Hastily telling her
not to be a goose, and that it was only a game, the four blanketed,
feathered, really and truly Redskins went boldly out to meet the foe, I say
boldly. That is because I wish to be polite.
At <SIC CORR="any rate">anyrate, they went.</p>
<p>Along the hedge dividing the wilderness from the garden was a row of
dark heads, all highly feathered.</p>
<p>&ldquo;It&rsquo;s our only chance,&rdquo; whispered Anthea. &ldquo;Much
better than to wait for their blood&ndash;freezing attack. We must pretend
like mad. Like that game of cards where you pretend you&rsquo;ve got aces
when you haven&rsquo;t. Fluffing they call it, I think. Now then.
Whoop!&rdquo;</p>
<p>With four wild war&ndash;whoops&mdash;or as near them as English
children could be expected to go without any previous practice&mdash;they
rushed through the gate and struck four warlike attitudes in the face of
the line of Red Indians. These were all about the same height, and that
height was Cyril&rsquo;s.</p>
</div>

<div class="bookPage" id="p229" name="229">
<p>&ldquo;I hope to goodness they can talk English,&rdquo; said Cyril
through his attitude.</p>

<p>Anthea knew they could, though she never knew how she came to know it.
She had a white towel tied to a walking&ndash;stick. This was a flag of
truce, and she waved it, in the hope that the Indians would know what it
was. Apparently they did&mdash;for one who was browner than the others
stepped forward.</p>
<p>&ldquo;Ye seek a pow&ndash;wow?&rdquo; he said in excellent English.
&ldquo;I am Golden Eagle, of the mighty tribe of
Rock&ndash;dwellers.&rdquo;</p>
<p>&ldquo;And I,&rdquo; said Anthea, with a sudden inspiration, &ldquo;am
the Black Panther&mdash;chief of the&mdash;the&mdash;the&mdash;Mazawattee
tribe. My brothers&mdash;I don&rsquo;t mean&mdash;yes, I do&mdash;the
tribe&mdash;I mean the Mazawattees&mdash;are in ambush below the brow of
yonder hill.&rdquo;</p>
<p>&ldquo;And what mighty warriors be these?&rdquo; asked Golden Eagle,
turning to the others.</p>
<p>Cyril said he was the great chief Squirrel, of the Moning Congo tribe,
and, seeing that Jane was sucking her thumb and could evidently think of no
name for herself, he
</div>

<div class="bookPage" id="p230" name="230">
added, &ldquo;This great warrior is Wild Cat&mdash;Pussy Ferox we call it
in this land&mdash;leader of the vast Phiteezi tribe.&rdquo;</p>
<p>&ldquo;And thou, valorous Redskin?&rdquo; Golden Eagle inquired suddenly
of Robert, who, taken unawares, could only reply that he was Bobs, leader
of the Cape Mounted Police.</p>
<p>&ldquo;And now,&rdquo; said Black Panther, &ldquo;our tribes, if we just
whistle them up, will far outnumber your puny forces; so resistance is
useless. Return, therefore, to your own land, O brother, and smoke pipes of
peace in your wampums with your squaws and your medicine&ndash;men, and
dress yourselves in the gayest wigwams, and eat happily of the juicy
fresh&ndash;caught moccasins.&rdquo;</p>
<p>&ldquo;You&rsquo;ve got it all wrong,&rdquo; murmured Cyril angrily. But
Golden Eagle only looked inquiringly at her.</p>
<p>&ldquo;Thy customs are other than ours, O Black Panther,&rdquo; he said.
&ldquo;Bring up thy tribe, that we may hold pow&ndash;wow in state before
them, as becomes great chiefs.&rdquo;</p>
<p>&ldquo;We&rsquo;ll bring them up right enough,&rdquo; said
</div>

<div class="bookPage" id="p231" name="231">
Anthea, &ldquo;with their bows and arrows, and tomahawks and
scalping&ndash;knives, and everything you can think of, if you don&rsquo;t
look sharp and go.&rdquo;</p>
<p>She spoke bravely enough, but the hearts of all the children were
beating furiously, and their breath came in shorter and shorter gasps. For
the little real Red Indians were closing up round them&mdash;coming nearer
and nearer with angry murmurs&mdash;so that they were the centre of a crowd
of dark cruel faces.</p>
<p>&ldquo;It&rsquo;s no go,&rdquo; whispered Robert. &ldquo;I knew it
wouldn&rsquo;t be. We must make a bolt for the Psammead. It might help us.
If it doesn&rsquo;t&mdash;well, I suppose we shall come alive again at
sunset. I wonder if scalping hurts as much as they say.&rdquo;</p>
<p>&ldquo;I&rsquo;ll wave the flag again,&rdquo; said Anthea. &ldquo;If
they stand back, we&rsquo;ll run for it.&rdquo;</p>
<p>She waved the towel, and the chief commanded his followers to stand
back. Then, charging wildly at the place where the line of Indians was
thinnest, the four children started to run. Their first rush knocked down
some half&ndash;dozen Indians, over whose
</div>

<div class="bookPage" id="p232" name="232">
blanketed bodies the children leaped, and made straight for the
sand&ndash;pit. This was no time for the safe easy way by which carts go
down&mdash;right over the edge of the sand&ndash;pit they went, among the
yellow and pale purple flowers and dried grasses, past the little
sand&ndash;martins&rsquo; little front doors, skipping, clinging,
bounding, stumbling, sprawling, and finally rolling.</p>
<p>Yellow Eagle and his followers came up with them just at the very spot
where they had seen the Psammead that morning.</p>
<p>Breathless and beaten, the wretched children now awaited their fate.
Sharp knives and axes gleamed round them, but worse than these was the
cruel light in the eyes of Golden Eagle and his followers.</p>
<p>&ldquo;Ye have lied to us, O Black Panther of the Mazawattees&mdash;and
thou, too, Squirrel of the Moning Congos. These also, Pussy Ferox of the
Phiteezi, and Bobs of the Cape Mounted Police,&mdash;these also have lied
to us, if not with their tongues, yet by their silence. Ye have lied under
the cover of the Truce&ndash;flag of the Pale&ndash;face. Ye have no
followers.
</div>

<div class="bookPage" id="p233" name="233">
Your tribes are far away&mdash;following the hunting trail. What shall be
their doom?&rdquo; he concluded, turning with a bitter smile to the other
Red Indians.</p>
<p>&ldquo;Build we the fire!&rdquo; shouted his followers; and at once a
dozen ready volunteers started to look for fuel. The four children, each
held between two strong little Indians, cast despairing glances round them.
Oh, if they could only see the Psammead!</p>
