<h2>CHAPTER XI (AND LAST)<br />
THE LAST WISH</h2>
<p><strong>OF</strong> course you, who see above that this is the eleventh (and
last) chapter, know very well that the day of which this chapter tells must
be the last on which Cyril, Anthea, Robert, and Jane will have a chance of
getting anything out of the Psammead, or Sand&ndash;fairy.</p>
<p>But the children themselves did not know this. They were full of rosy
visions, and, whereas on other days they had often found it extremely
difficult to think of anything really nice to wish for, their brains were
now full of the most beautiful and sensible ideas. &ldquo;This,&rdquo; as
Jane remarked afterwards, &ldquo;is always the way.&rdquo; Everyone was up
extra early that morning, and these plans were hopefully discussed in the
garden before breakfast. The old idea of one hundred pounds in modern
florins
</div>

<div class="bookPage" id="p280" name="280">
was still first favourite, but there were others that ran it
close&mdash;the chief of these being the &ldquo;pony each&rdquo; idea. This
had a great advantage. You could wish for a pony each during the morning,
ride it all day, have it vanish at sunset and wish it back again next day.
Which would be an economy of litter and stabling. But at breakfast two
things happened. First, there was a letter from mother. Granny was better,
and mother and father hoped to be home that very afternoon. A cheer arose.
And of course this news at once scattered all the before&ndash;breakfast
wish&ndash;ideas. For everyone saw quite plainly that the wish of the day
must be something to please mother and not to please themselves.</p>
<p>&ldquo;I wonder what she <em>would</em> like,&rdquo; pondered
Cyril.</p>
<p>&ldquo;She&rsquo;d like us all to be good,&rdquo; said Jane primly.</p>
<p>&ldquo;Yes&mdash;but that&rsquo;s so dull for us,&rdquo; Cyril rejoined;
&ldquo;and, besides, I should hope we could be that without
sand&ndash;fairies to help us. No; it must be something splendid, that we
couldn&rsquo;t possibly get without wishing for.&rdquo;</p>
<p>&ldquo;Look out,&rdquo; said Anthea in a warning voice;
</div>

<div class="bookPage" id="p281" name="281">
&ldquo;don&rsquo;t forget yesterday. Remember, we get our wishes now just
wherever we happen to be when we say &lsquo;I wish.&rsquo; Don&rsquo;t
let&rsquo;s let ourselves in for anything silly&mdash;to&ndash;day of all
days.&rdquo;</p>
<p>&ldquo;All right,&rdquo; said Cyril. &ldquo;You needn&rsquo;t
jaw.&rdquo;</p>
<p>Just then Martha came in with a jug full of hot water for the
teapot&mdash;and a face full of importance for the children.</p>
<p>&ldquo;A blessing we&rsquo;re all alive to eat our breakfasses!&rdquo;
she said darkly.</p>
<p>&ldquo;Why, whatever&rsquo;s happened?&rdquo; everybody asked.</p>
<p>&ldquo;Oh, nothing,&rdquo; said Martha, &ldquo;only it seems
nobody&rsquo;s safe from being murdered in their beds nowadays.&rdquo;</p>
<p>&ldquo;Why,&rdquo; said Jane as an agreeable thrill of horror ran down
her back and legs and out at her toes, &ldquo;<em>has</em> anyone been
murdered in their beds?&rdquo;</p>
<p>&ldquo;Well&mdash;not exactly,&rdquo; said Martha; &ldquo;but they might
just as well. There&rsquo;s been burglars over at Peasmarsh
Place&mdash;Beale&rsquo;s just told me&mdash;and they&rsquo;ve took every
single one of Lady Chittenden&rsquo;s diamonds and jewels and things, and
she&rsquo;s a&ndash;goin&rsquo; out of one fainting fit into another,
</div>

<div class="bookPage" id="p282" name="282">
with hardly time to say &lsquo;Oh, my diamonds!&rsquo; in between. And Lord
Chittenden&rsquo;s away in London.&rdquo;</p>
<p>&ldquo;Lady Chittenden,&rdquo; said Anthea; &ldquo;we&rsquo;ve seen her.
She wears a red&ndash;and&ndash;white dress, and she has no children of
her own and can&rsquo;t abide other folkses&rsquo;.&rdquo;</p>
<p>&ldquo;That&rsquo;s her,&rdquo; said Martha. &ldquo;Well, she&rsquo;s
put all her trust in riches, and you see how she&rsquo;s served. They say
the diamonds and things was worth thousands of thousands of pounds. There
was a necklace and a river&mdash;whatever that is&mdash;and no end of

bracelets; and a tarrer and ever so many rings. But there, I mustn&rsquo;t
stand talking and all the place to clean down afore your ma comes
home.&rdquo;</p>
<p>&ldquo;I don&rsquo;t see why she should ever have had such a lot of
diamonds,&rdquo; said Anthea when Martha had flounced off. &ldquo;She was
rather a nasty lady, I thought. And mother hasn&rsquo;t any diamonds, and
hardly any jewels&mdash;the topaz necklace, and the sapphire ring daddy
gave her when they were engaged, and the garnet star, and the little pearl
brooch with great&ndash;grandpapa&rsquo;s hair in it,&mdash;that&rsquo;s
about all.&rdquo;</p>
</div>

<div class="bookPage" id="p283" name="283">
<p>&ldquo;When I&rsquo;m grown up I&rsquo;ll buy mother no end of
diamonds,&rdquo; said Robert, &ldquo;if she wants them. I shall make so
much money exploring Africa I shan&rsquo;t know what to do with
it.&rdquo;</p>
<p>&ldquo;Wouldn&rsquo;t it be jolly,&rdquo; said Jane dreamily, &ldquo;if
mother could find all those lovely things, necklaces and rivers of diamonds
and tarrers?&rdquo;</p>
<p>&ldquo;<em>Ti&ndash;aras</em>,&rdquo; said Cyril.</p>
<p>&ldquo;Ti&ndash;aras, then&mdash;and rings and everything in her room
when she came home? I wish she would.&rdquo;</p>
<p>The others gazed at her in horror.</p>
<p>&ldquo;Well, she <em>will</em>,&rdquo; said Robert;
&ldquo;you&rsquo;ve wished, my good Jane&mdash;and our only chance now is
to find the Psammead, and if it&rsquo;s in a good temper it
<em>may</em> take back the wish and give us another. If
not&mdash;well&mdash;goodness knows what we&rsquo;re in for!&mdash;the
police, of course, and&mdash;Don&rsquo;t cry, silly! We&rsquo;ll stand by
you. Father says we need never be afraid if we don&rsquo;t do anything
wrong and always speak the truth.&rdquo;</p>
<p>But Cyril and Anthea exchanged gloomy glances. They remembered how
convincing
</div>

<div class="bookPage" id="p284" name="284">
the truth about the Psammead had been once before when told to the
police.</p>
<p>It was a day of misfortunes. Of course the Psammead could not be found.
Nor the jewels, though every one of the children searched their
mother&rsquo;s room again and again.</p>
<p>&ldquo;Of course,&rdquo; Robert said, &ldquo;<em>we</em>
couldn&rsquo;t find them. It&rsquo;ll be mother who&rsquo;ll do that.
Perhaps she&rsquo;ll think they&rsquo;ve been in the house for years and
years, and never know they are stolen ones at all.&rdquo;</p>
<p>&ldquo;Oh yes!&rdquo; Cyril was very scornful; &ldquo;then mother will
be a receiver of stolen goods, and you know jolly well what
<em>that&rsquo;s</em> worse than.&rdquo;</p>
<p>Another and exhaustive search of the sand&ndash;pit failed to reveal
the Psammead, so the children went back to the house slowly and sadly.</p>
<p>&ldquo;I don&rsquo;t care,&rdquo; said Anthea stoutly,
&ldquo;we&rsquo;ll tell mother the truth, and she&rsquo;ll give back the
jewels&mdash;and make everything all right.&rdquo;</p>
<p>&ldquo;Do you think so?&rdquo; said Cyril slowly. &ldquo;Do you think
she&rsquo;ll believe us? Could anyone believe about a Sammyadd unless
they&rsquo;d seen it? She&rsquo;ll think we&rsquo;re pretending.
</div>

<div class="bookPage" id="p285" name="285">
Or else she&rsquo;ll think we&rsquo;re raving mad, and then we shall be
sent to Bedlam. How would you like it?&rdquo;&mdash;he turned suddenly on
the miserable Jane,&mdash;&ldquo;how would you like it, to be shut up in an
iron cage with bars and padded walls, and nothing to do but stick straws in
your hair all day, and listen to the howling and ravings of the other
maniacs? Make up your minds to it, all of you. It&rsquo;s no use telling
mother.&rdquo;</p>
<p>&ldquo;But it&rsquo;s true,&rdquo; said Jane.</p>
<p>&ldquo;Of course it is, but it&rsquo;s not true enough for
grown&ndash;up people to believe it,&rdquo; said Anthea.
&ldquo;Cyril&rsquo;s right. Let&rsquo;s put flowers in all the vases, and
try not to think about diamonds. After all, everything has come right in
the end all the other times.&rdquo;</p>
<p>So they filled all the pots they could find with flowers&mdash;asters
and zinnias, and loose&ndash;leaved late red roses from the wall of the
stable&ndash;yard, till the house was a perfect bower.</p>
<p>And almost as soon as dinner was cleared away mother arrived, and was
clasped in eight loving arms. It was very difficult indeed not to tell her
all about the Psammead at once, because they had got into the habit of
telling
</div>

<div class="bookPage" id="p286" name="286">
her everything. But they did succeed in not telling her.</p>
<p>Mother on her side, had plenty of tell them&mdash;about Granny and
Granny&rsquo;s pigeons, and Auntie Emma&rsquo;s lame tame donkey. She was
very delighted with the flowery&ndash;boweryness of the house; and
everything seemed so natural and pleasant, now that she was home again,
that the children almost thought they must have dreamed the Psammead.</p>
<p>But, when Mother moved towards the stairs to go up to her bedroom and
take off her bonnet, the eight arms clung round her just as if she only had
two children, one the Lamb and the other an octopus.</p>
<p>&ldquo;Don&rsquo;t go up, mummy darling,&rdquo; said Anthea, &ldquo;let
me take your things up for you.&rdquo;</p>
<p>&ldquo;Or I will,&rdquo; said Cyril.</p>
<p>&ldquo;We want you to come and look at the rose&ndash;tree,&rdquo; said
Robert.</p>
<p>&ldquo;Oh, don&rsquo;t go up!&rdquo; said Jane helplessly.</p>
<p>&ldquo;Nonsense, dears,&rdquo; said mother briskly, &ldquo;I&rsquo;m not
such an old woman yet that I can&rsquo;t take my bonnet off in the proper
place. Besides, I must wash these black hands of mine.&rdquo;</p>
</div>

<div class="bookPage" id="pp287" name="287">
<p>So up she went, and the children, following her, exchanged glances of
gloomy foreboding.</p>
<p>Mother took off her bonnet&mdash;it was a very pretty hat, really, with
white roses in it,&mdash;and when she had taken it off she went to the
dressing&ndash;table to do her pretty hair.</p>
<p>On the table between the ring&ndash;stand and the pin&ndash;cushion
lay a green leather case. Mother opened it.</p>
<p>&ldquo;Oh, how lovely!&rdquo; she cried. It was a ring, a large pearl
with shining many&ndash;lighted diamonds set round it. &ldquo;Wherever did
this come from?&rdquo; mother asked, trying it on her wedding finger, which
it fitted beautifully. &ldquo;However did it come here?&rdquo;</p>
<p>&ldquo;I don&rsquo;t know,&rdquo; said each of the children
truthfully.</p>
<p>&ldquo;Father must have told Martha to put it here,&rdquo; mother said.
&ldquo;I&rsquo;ll run down and ask her.&rdquo;</p>
<p>&ldquo;Let me look at it,&rdquo; said Anthea, who knew Martha would not
be able to see the ring. But when Martha was asked, of course she denied
putting the ring there, and so did Eliza and cook.</p>
</div>

<div class="bookPage" id="p288" name="288">
<p>Mother came back to her bedroom, very much interested and pleased about
the ring. But, when she opened the dressing&ndash;table drawer and found a
long case containing an almost priceless diamond necklace, she was more
interested still, though not so pleased. In the wardrobe, when she went to
put away her &ldquo;bonnet,&rdquo; she found a tiara and several brooches,
and the rest of the jewellery turned up in various parts of the room during
the next half&ndash;hour. The children looked more and more uncomfortable,
and now Jane began to sniff.</p>
<p>Mother looked at her gravely.</p>
<p>&ldquo;Jane,&rdquo; she said, &ldquo;I am sure you know something about
this. Now think before you speak, and tell me the truth.&rdquo;</p>
<p>&ldquo;We found a Fairy,&rdquo; said Jane obediently.</p>
<p>&ldquo;No nonsense, please,&rdquo; said her mother sharply.</p>
<p>&ldquo;Don&rsquo;t be silly, Jane,&rdquo; Cyril interrupted. Then he
went on desperately. &ldquo;Look here, mother, we&rsquo;ve never seen the
things before, but Lady Chittenden at Peasmarsh Place
</div>

<div class="bookPage" id="p289" name="289">
lost all her jewellery by wicked burglars last night. Could this possibly
be it?&rdquo;</p>
<p>All drew a deep breath. They were saved.</p>
<p>&ldquo;But how could they have put it here? And why should they?&rdquo;
asked mother, not unreasonably. &ldquo;Surely it would have been easier and
safer to make off with it?&rdquo;</p>
<p>&ldquo;Suppose,&rdquo; said Cyril, &ldquo;they thought it better to wait
for&mdash;for sunset&mdash;nightfall, I mean, before they went off with it.
No one but us knew that you were coming back to&ndash;day.&rdquo;</p>
<p>&ldquo;I must send for the police at once,&rdquo; said mother
distractedly. &ldquo;Oh, how I wish daddy were here!&rdquo;</p>
<p>&ldquo;Wouldn&rsquo;t it be better to wait till he <em>does</em>
come?&rdquo; asked Robert, knowing that his father would not come home
before sunset.</p>
<p>&ldquo;No, no; I can&rsquo;t wait a minute with all this on my
mind,&rdquo; cried mother. &ldquo;All this&rdquo; was the heap of
jewel&ndash;cases on the bed. They put them all in the wardrobe, and
mother locked it. Then mother called Martha.</p>
<p>&ldquo;Martha,&rdquo; she said, &ldquo;has any stranger been into my
room since I&rsquo;ve been away? Now, answer me truthfully.&rdquo;</p>
</div>

<div class="bookPage" id="p290" name="290">
<p>&ldquo;No, mum,&rdquo; answered Martha; &ldquo;leastways, what I mean to
say&rdquo;&mdash;</p>
<p>She stopped.</p>
<p>&ldquo;Come,&rdquo; said her mistress kindly; &ldquo;I see someone has.
You must tell me at once. Don&rsquo;t be frightened. I&rsquo;m sure you
haven&rsquo;t done anything wrong.&rdquo;</p>
<p>Martha burst into heavy sobs.</p>
<p>&ldquo;I was a&ndash;goin&rsquo; to give you warning this very day,
mum, to leave at the end of my month, so I was,&mdash;on account of me
being going to make a respectable young man happy. A gamekeeper he is by
trade, mum&mdash;and I wouldn&rsquo;t deceive you&mdash;of the name of
Beale. And it&rsquo;s as true as I stand here, it was your coming home in
such a hurry, and no warning given, out of the kindness of his heart it
was, as he says, &lsquo;Martha, my beauty,&rsquo; he says,&mdash;which I
ain&rsquo;t, and never was, but you know how them men will go
on,&mdash;&lsquo;I can&rsquo;t see you a&ndash;toiling and
a&ndash;moiling and not lend a &rsquo;elping &rsquo;and; which mine is a
strong arm and it&rsquo;s yours, Martha, my dear,&rsquo; says he. And so he
helped me a&ndash;cleanin&rsquo; of the windows&mdash;but outside, mum,
the whole time,
</div>

<div class="bookPage" id="p291" name="291">
and me in; if I never say another breathing word it&rsquo;s the gospel
truth.&rdquo;</p>
<p>&ldquo;Were you with him the whole time?&rdquo; asked her mistress.</p>
<p>&ldquo;Him outside me in, I was,&rdquo; said Martha; &ldquo;except for
fetching up a fresh pail and the leather that that slut of a Eliza&rsquo;d
hidden away behind the mangle.&rdquo;</p>
<p>&ldquo;That will do,&rdquo; said the children&rsquo;s mother. &ldquo;I
am not pleased with you, Martha, but you have spoken the truth, and that
counts for something.&rdquo;</p>
<p>When Martha had gone, the children clung round their mother.</p>
<p>&ldquo;Oh, mummy darling,&rdquo; cried Anthea, &ldquo;it isn&rsquo;t
Beale&rsquo;s fault, it isn&rsquo;t really! He&rsquo;s a great dear; he is,
truly and honourably, and as honest as the day. Don&rsquo;t let the police
taken him, mummy! oh, don&rsquo;t, don&rsquo;t, don&rsquo;t!&rdquo;</p>
<p>It was truly awful. Here was an innocent man accused of robbery through
that silly wish of Jane&rsquo;s, and it was absolutely useless to tell the
truth. All longed to, but they thought of the straws in the hair and the
shrieks of the
</div>

<div class="bookPage" id="p292" name="292">
other frantic maniacs, and they could not do it.</p>
<p>&ldquo;Is there a cart hereabouts?&rdquo; asked mother feverishly.
&ldquo;A trap of any sort? I must drive in to Rochester and tell the police
at once.&rdquo;</p>
<p>All the children sobbed, &ldquo;There&rsquo;s a cart at the farm, but,
oh, don&rsquo;t go!&mdash;don&rsquo;t go!&mdash;oh, don&rsquo;t
go!&mdash;wait until daddy comes home!&rdquo;</p>
<p>Mother took not the faintest notice. When she had set her mind on a
thing she always went straight through with it; she was rather like Anthea
in this respect.</p>
<p>&ldquo;Look here, Cyril,&rdquo; she said, sticking on her hat with long
sharp violet&ndash;headed pins, &ldquo;I leave you in charge. Stay in the
dressing&ndash;room. You can pretend to be swimming boats in the bath, or
something. Say I give you leave. But stay here, with the landing door open;
I&rsquo;ve locked the other. And don&rsquo;t let anyone go into my room.
Remember, no one knows the jewels are there except me, and all of you, and
the wicked thieves who put them there. Robert, you stay in the garden and
watch the windows.
</div>

<div class="bookPage" id="p293" name="293">
If anyone tries to get in you must run and tell the two farm men that
I&rsquo;ll send up to wait in the kitchen. I&rsquo;ll tell them there are
dangerous characters about&mdash;that&rsquo;s true enough. Now, remember, I
trust you both. But I don&rsquo;t think they&rsquo;ll try it till after
dark, so you&rsquo;re quite safe. Good&ndash;bye, darlings.&rdquo;</p>
<p>And she locked her bedroom door and went off with the key in her
pocket.</p>
<p>The children could not help admiring the dashing and decided way in
which she had acted. They thought how useful she would have been in
organising escape from some of the tight places in which they had found
themselves of late in consequence of their ill&ndash;timed wishes.</p>
<p>&ldquo;She&rsquo;s a born general,&rdquo; said Cyril,&mdash;&ldquo;but I
don&rsquo;t know what&rsquo;s going to happen to us. Even if the girls were
to hunt for that beastly Sammyadd and find it, and get it to take the
jewels away again, mother would only think we hadn&rsquo;t looked out
properly and let the burglars sneak in and nick them&mdash;or else the
police will think <em>we&rsquo;ve</em> got them&mdash;or else that
she&rsquo;s been fooling them. Oh, it&rsquo;s a pretty
</div>

<div class="bookPage" id="p294" name="294">
decent average ghastly mess this time, and no mistake!&rdquo;</p>
<p>He savagely made a paper boat and began to float it in the bath, as he
had been told to do.</p>
<p>Robert went into the garden and sat down on the worn yellow grass, with
his miserable head between his helpless hands.</p>
<p>Anthea and Jane whispered together in the passage downstairs, where the
cocoanut matting was&mdash;with the hole in it that you always caught your
foot in if you were not careful. Martha&rsquo;s voice could be heard in the
kitchen,&mdash;grumbling loud and long.</p>
<p>&ldquo;It&rsquo;s simply too dreadfully awful,&rdquo; said Anthea.
&ldquo;How do you know all the diamonds are there, too? If they
aren&rsquo;t the police will think mother and father have got them, and
that they&rsquo;ve only given up some of them for a kind of desperate
blind. And they&rsquo;ll be put in prison, and we shall be branded
outcasts, the children of felons. And it won&rsquo;t be at all nice for
father and mother either,&rdquo; she added, by a candid afterthought.</p>
<p>&ldquo;But what can we <em>do</em>?&rdquo; asked Jane.</p>
</div>

<div class="bookPage" id="p295" name="295">
<p>&ldquo;Nothing&mdash;at least we might look for the Psammead again.
It&rsquo;s a very, <em>very</em> hot day. He may have come out to warm
that whisker of his.&rdquo;</p>
<p>&ldquo;He won&rsquo;t give us any more beastly wishes
to&ndash;day,&rdquo; said Jane flatly. &ldquo;He gets crosser and crosser
every time we see him. I believe he hates having to give wishes.&rdquo;</p>
<p>Anthea had been shaking her head gloomily&mdash;now she stopped shaking
it so suddenly that it really looked as though she were pricking up her
ears.</p>
<p>&ldquo;What is it?&rdquo; asked Jane. &ldquo;Oh, have you thought of
something?&rdquo;</p>
<p>&ldquo;Our one chance,&rdquo; cried Anthea dramatically; &ldquo;the last
lone&ndash;lorn forlorn hope. Come on.&rdquo;</p>
<p>At a brisk trot she led the way to the sand&ndash;pit.</p>
<p>Oh, joy!&mdash;there was the Psammead, basking in a golden sandy hollow
and preening its whiskers happily in the glowing afternoon sun. The moment
it saw them it whisked round and began to burrow&mdash;it evidently
preferred its own company to theirs. But Anthea was too quick for it. She
caught it by its furry shoulders gently but firmly, and held it.</p>
</div>

<div class="bookPage" id="p296" name="296">
<p>&ldquo;Here&mdash;none of that!&rdquo; said the Psammead. &ldquo;Leave
go of me, will you?&rdquo;</p>
<p>But Anthea held him fast.</p>
<p>&ldquo;Dear kind darling Sammyadd,&rdquo; she said breathlessly.</p>
<p>&ldquo;Oh yes&mdash;it&rsquo;s all very well,&rdquo; it said; &ldquo;you
want another wish, I expect. But I can&rsquo;t keep on slaving from morning
till night giving people their wishes. I must have <em>some</em> time
to myself.&rdquo;</p>
<p>&ldquo;Do you hate giving wishes?&rdquo; asked Anthea gently, and her
voice trembled with excitement.</p>
<p>&ldquo;Of course I do,&rdquo; it said. &ldquo;Leave go of me or
I&rsquo;ll bite!&mdash;I really will&mdash;I mean it. Oh, well, if you
chose to risk it.&rdquo; Anthea risked it and held on.</p>
<p>&ldquo;Look here,&rdquo; she said, &ldquo;don&rsquo;t bite
me&mdash;listen to reason. If you&rsquo;ll only do what we want
to&ndash;day, we&rsquo;ll never ask you for another wish as long as we
live.&rdquo;</p>
<p>The Psammead was much moved.</p>
<p>&ldquo;I&rsquo;d do anything,&rdquo; it said in a tearful voice.
&ldquo;I&rsquo;d almost burst myself to give you one wish after another, as
long as I held out, if you&rsquo;d
</div>

<div class="bookPage" id="p297" name="297">
only never, never ask me to do it after to&ndash;day. If you knew how I
hate to blow myself out with other people&rsquo;s wishes, and how
frightened I am always that I shall strain a muscle or something. And then
to wake up every morning and know you&rsquo;ve <em>got</em> to do it.
You don&rsquo;t know what it is&mdash;you don&rsquo;t know what it is, you
don&rsquo;t!&rdquo; Its voice cracked with emotion, and the last
&ldquo;don&rsquo;t&rdquo; was a squeak.</p>
<p>Anthea set it down gently on the sand.</p>
<p>&ldquo;It&rsquo;s all over now,&rdquo; she said soothingly. &ldquo;We
promise faithfully never to ask for another wish after
to&ndash;day.&rdquo;</p>
<p>&ldquo;Well, go ahead,&rdquo; said the Psammead; &ldquo;let&rsquo;s get
it over.&rdquo;</p>
<p>&ldquo;How many can you do?&rdquo;</p>
<p>&ldquo;I don&rsquo;t know&mdash;as long as I can hold out.&rdquo;</p>
<p>&ldquo;Well, first, I wish Lady Chittenden may find she&rsquo;s never
lost her jewels.&rdquo;</p>
<p>The Psammead blew itself out, collapsed, and said,
&ldquo;Done.&rdquo;</p>
<p>&ldquo;I wish,&rdquo; said Anthea more slowly, &ldquo;mother
mayn&rsquo;t get to the police.&rdquo;</p>
<p>&ldquo;Done,&rdquo; said the creature after the proper interval.</p>
</div>

<div class="bookPage" id="p298" name="298">
<p>&ldquo;I wish,&rdquo; said Jane suddenly, &ldquo;mother could forget all
about the diamonds.&rdquo;</p>
<p>&ldquo;Done,&rdquo; said the Psammead; but its voice was weaker.</p>
<p>&ldquo;Wouldn&rsquo;t you like to rest a little?&rdquo; asked Anthea
considerately.</p>
<p>&ldquo;Yes, please,&rdquo; said the Psammead; &ldquo;and before we go
any further, will you wish something for me?&rdquo;</p>
<p>&ldquo;Can&rsquo;t you do wishes for yourself?&rdquo;</p>
<p>&ldquo;Of course not,&rdquo; it said; &ldquo;we were always expected to
give each other our wishes&mdash;not that we had any to speak of in the
