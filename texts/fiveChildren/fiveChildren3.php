<h2>CHAPTER III<br />
BEING WANTED</h2>
<p><strong>THE</strong> morning after the children had been in possession of
boundless wealth, and had been unable to buy anything really useful or
enjoyable with it, except two pairs of cotton gloves, twelve penny buns, an
imitation crocodile&ndash;skin purse, and a ride in a pony&ndash;cart,
they awoke without any of the enthusiastic happiness which they had felt on
the previous day when they remembered how they had had the luck to find a
Psammead, or Sand&ndash;fairy, and to receive its promise to grant them a
new wish every day. For now they had had two wishes, Beauty and Wealth, and
neither had exactly made them happy. But the happening of strange things,
even if they are not completely pleasant things, is more amusing than those
times when nothing happens but meals,
and they are not always completely pleasant, especially on the days when it
is cold mutton or hash.</p>
<p>There was no chance of talking things over before breakfast, because
everyone overslept itself, as it happened, and it needed a vigorous and
determined struggle to get dressed so as to be only ten minutes late</p>
</div>

<div class="bookPage" id="p59" name="59">
<p>for breakfast. During this meal some efforts were made to deal with the
question of the Psammead in an impartial spirit, but it is very difficult
to discuss anything thoroughly and at the same time to attend faithfully to
your baby brother&rsquo;s breakfast needs. The Baby was particularly lively
that morning. He not only wriggled his body through the bar of his high
chair, and hung by his head, choking and purple, but he collared a
tablespoon with desperate suddenness, hit Cyril heavily on the head with
it, and then cried because it was taken away from him. He put his fat fist
in his bread&ndash;and&ndash;milk, and demanded &ldquo;nam,&rdquo; which
was only allowed for tea. He sang, he put his feet on the table&mdash;he
clamoured to &ldquo;go walky&rdquo;. The conversation was something like
this&mdash;</p>
<p>&ldquo;Look here&mdash;about that Sandy&ndash;fairy&mdash;Look
out!&mdash;he&rsquo;ll have the milk over.&rdquo;</p>
<p>Milk removed to a safe distance.</p>
<p>&ldquo;Yes&mdash;about that Fairy&mdash; No, Lamb dear, give Panther the
narky poon.&rdquo;</p>
<p>Then Cyril tried. &ldquo;Nothing we&rsquo;ve had yet has turned
out&mdash;He nearly had the mustard that time!&rdquo;</p>
<p>&ldquo;I wonder whether we&rsquo;d better
wish&mdash;Hullo!&mdash;you&rsquo;ve done it now, my boy!&rdquo; And, in a
flash of glass and pink baby&ndash;paws, the bowl of golden carp in the
middle of the</p>
</div>

<div class="bookPage" id="p60" name="60">
<p>table rolled on its side and poured a flood of mixed water
and goldfish into the Baby&rsquo;s lap and into the laps of the others.</p>
<p>Everyone was almost as much upset as the goldfish; the Lamb only
remaining calm. When the pool on the floor had been mopped up, and the
leaping, gasping goldfish had been collected and put back in the water, the
Baby was taken away to be entirely re&ndash;dressed by Martha, and most of
the others had to change completely. The pinafores and jackets that had
been bathed in gold&ndash;fish&ndash;and&ndash;water were hung out to
dry, and then it turned out that Jane must either mend the dress she had torn the day before or appear all
day in her best petticoat. It was white and soft and frilly, and trimmed
with lace, and very. very pretty, quite as pretty as a frock, if not more
so. Only it was <em>not</em> a frock, and Martha&rsquo;s word was law.
She wouldn&rsquo;t let Jane wear her best frock, and she refused to listen
for a moment to Robert&rsquo;s suggestion that Jane should wear her best
petticoat and call it a dress.</p>
<p>&ldquo;It&rsquo;s not respectable,&rdquo; she said. And when people say
that, it&rsquo;s no use anyone&rsquo;s saying anything. You will find this
out for yourselves some day.</p>
<p>So there was nothing for it but for Jane to mend her frock. The hole had
been torn the day before when she happened to tumble down in the High
Street of Rochester,</p>
</div>

<div class="bookPage" id="p61" name="61">
<p>just where a water&ndash;cart had passed on its
silvery way. She had grazed her knee, and her stocking was much more than
grazed, and her dress was cut by the same stone which had attended to the
knees and the stocking. Of course the others were not such sneaks as to
abandon a comrade in misfortune, so they all sat on
the grass&ndash;plot round the sundial, and Jane darned away for dear
life. The Lamb was still in the hands of Martha having its clothes changed,
so conversation was possible.</p>
<p>Anthea and Robert timidly tried to conceal their inmost thought, which
was that the Psammead was not to be trusted; but Cyril said&mdash;</p>
<p>&ldquo;Speak out&mdash;say what you&rsquo;ve got to say&mdash;I hate
hinting, and &lsquo;don&rsquo;t know,&rsquo; and sneakish ways like
that.&rdquo;</p>
<p>So then Robert said, as in honour bound: &ldquo;Sneak
yourself&mdash;Anthea and me weren&rsquo;t so goldfishy as you two were, so
we got changed quicker, and we&rsquo;ve had time to think it over, and if
you ask me&rdquo;&mdash;</p>
<p>&ldquo;I didn&rsquo;t ask you,&rdquo; said Jane, biting off a needleful
of thread as she had always been strictly forbidden to do. (Perhaps you
don&rsquo;t know that if you bite off ends of cotton and swallow them they
wind tight round your heart and kill you? My nurse told me this, and she
told me also about the earth going round the sun. Now what is one to
believe&mdash;what with nurses and science?)</p>
</div>

<div class="bookPage" id="p62" name="62">
<p>&ldquo;I don&rsquo;t care who asks or who doesn&rsquo;t,&rdquo; said
Robert, &ldquo;but Anthea and I think the Sammyadd is a spiteful brute. If
it can give us our wishes I suppose it can give itself its own, and I feel
almost sure it wishes every time that our wishes shan&rsquo;t do us any
good. Let&rsquo;s let the tiresome beast alone, and just go and have a
jolly good game of forts, on our own, in the chalk&ndash;pit.&rdquo;</p>
<p>(You will remember that the happily&ndash;situated house where these
children were spending their holidays lay between a chalk&ndash;quarry and
a gravel&ndash;pit.)</p>
<p>Cyril and Jane were more hopeful&mdash;they generally were.</p>
<p>&ldquo;I don&rsquo;t think the Sammyadd does it on purpose,&rdquo; Cyril
said; &ldquo;and, after all, it <em>was</em> silly to wish for
boundless wealth. Fifty pounds in two&ndash;shilling pieces would have
been much more sensible. And wishing to be beautiful as the day was simply
donkeyish. I don&rsquo;t want to be disagreeable, but it <em>was</em>.
We must try to find a really useful wish, and wish it.&rdquo;</p>
<p>Jane dropped her work and said&mdash;</p>
<p>&ldquo;I think so too, it&rsquo;s too silly to have a chance like this
and not use it. I never heard of anyone else outside a book who had such a
chance; there must be simply heaps of things we could wish for that
wouldn&rsquo;t turn out Dead Sea fish, like these two things have. Do
let&rsquo;s think hard,</p>
</div>

<div class="bookPage" id="p63" name="63">
<p>and wish something nice, so that we can have a real
jolly day&mdash;what there is left of it.&rdquo;</p>
<p>Jane darned away again like mad, for time was indeed getting on, and
everyone began to talk at once. If you had been there you could not
possibly have made head or tail of the talk, but these children were used
to talking &ldquo;by fours,&rdquo; as soldiers march, and each of them
could say what it had to say quite comfortably, and listen to the agreeable
sound of its own voice, and at the same time have three&ndash;quarters of
two sharp ears to spare for listening to what the others said. That is an
easy example in multiplication of vulgar fractions, but, as I daresay you
can&rsquo;t do even that, I won&rsquo;t ask you to tell me whether &frac34;
&times; 2 = 1 &frac12;, but I will ask you to believe me that this was the
amount of ear each child was able to lend to the others. Lending ears was common in Roman times, as
we learn from Shakespeare; but I fear I am getting too instructive.</p>
<p>When the frock was darned, the start for the gravel&ndash;pit was
delayed by Martha&rsquo;s insisting on everybody&rsquo;s washing its
hands&mdash;which was nonsense, because nobody had been doing anything at
all, except Jane, and how can you get dirty doing nothing? That is a
difficult question, and I cannot answer it on paper. In real life I could
very soon show you&mdash;or you me, which is much more likely.</p>
</div>

<div class="bookPage" id="p64" name="64">
<p>During the conversation in which the six ears were lent (there were four
children, so <em>that</em> sum comes right), it had been decided that
fifty pounds in two&ndash;shilling pieces was the right wish to have. And
the lucky children, who could have anything in the wide world by just
wishing for it, hurriedly started for the gravel&ndash;pit to express
their wishes to the Psammead. Martha caught them at the gate, and insisted
on their taking the Baby with them.</p>
<p>&ldquo;Not want him indeed! Why, everybody &rsquo;ud want him, a duck!
with all their hearts
they would; and you know you promised your ma to take him out every blessed
day,&rdquo; said Martha.</p>
<p>&ldquo;I know we did,&rdquo; said Robert in gloom, &ldquo;but I wish the
Lamb wasn&rsquo;t quite so young and small. It would be much better fun
taking him out.&rdquo;</p>
<p>&ldquo;He&rsquo;ll mend of his youngness with time,&rdquo; said Martha;
&ldquo;and as for his smallness, I don&rsquo;t think you&rsquo;d fancy
carrying of him any more, however big he was. Besides he can walk a bit,
bless his precious fat legs, a ducky! He feels the benefit of the
new&ndash;laid air, so he does, a pet!&rdquo;</p>
<p>With this and a kiss, she plumped the Lamb into Anthea&rsquo;s arms, and
went back to make new pinafores on the sewing&ndash;machine. She was a
rapid performer on this instrument.</p>
</div>

<div class="bookPage" id="p65" name="65">
<p>The Lamb laughed with pleasure, and said, &ldquo;Walky wif Panty,&rdquo;
and rode on Robert&rsquo;s back with yells of joy, and tried to feed Jane
with stones, and altogether made himself so agreeable that nobody could
long be sorry that he was of the party.</p>
<p>The enthusiastic Jane even suggested that they should devote a week&rsquo;s wishes to assuring the Baby&rsquo;s future, by asking such gifts for him as the good fairies give to Infant
Princes in proper fairy&ndash;tales, but Anthea soberly reminded her that
as the Sand&ndash;fairy&rsquo;s wishes only lasted till sunset they could
not ensure any benefit to the Baby&rsquo;s later years; and Jane owned that
it would be better to wish for fifty pounds in two&ndash;shilling pieces,
and buy the Lamb a three&ndash;pound fifteen rocking&ndash;horse, like
those in the Army and Navy Stores list, with part of the money.</p>
<p>It was settled that, as soon as they had wished for the money and got
it, they would get Mr. Crispin to drive them into Rochester again, taking
Martha with them if they could not get out of taking her. And they would
make a list of the things they really wanted before they started. Full of
high hopes and excellent resolutions, they went round the safe slow
cart&ndash;road to the gravel&ndash;pits, and as they went in between the
mounds of gravel a sudden thought came to them, and would have turned their
ruddy cheeks pale if they had been children in a</p>
</div>

<div class="bookPage" id="pf74" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil074.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p66" name="66">
<p>book. Being real live 
children, it only made them stop and look at each other with rather blank
and silly expressions. For now they remembered that yesterday, when they
had asked the Psammead for boundless wealth, and it was getting ready to
fill the quarry with the minted gold of bright guineas&mdash;millions of
them&mdash;it had told the children to run along outside the quarry for
fear they should be buried alive in the heavy splendid treasure. And they
had run. And so it happened that they had not had time to mark the spot
where the Psammead was, with a ring of stones, as before. And it was this
thought that put such silly expressions on their faces.</p>
<p>&ldquo;Never mind,&rdquo; said the hopeful Jane, &ldquo;we&rsquo;ll soon
find him.&rdquo;</p>
<p>But this, though easily said, was hard in the doing. They looked and
they looked, and though they found their seaside spades, nowhere could they
find the Sand&ndash;fairy.</p>
<p>At last they had to sit down and rest&mdash;not at all because they were
weary or disheartened of course, but because the Lamb insisted on being put
down, and you cannot look very carefully after anything you may have happened to lose in the
sand if you have an active baby to look after at the same time. Get someone
to drop your best knife in the sand next time you go to the seaside, and
then take your baby brother with you when you go to look for it, and you
will see that I am right.</p>
</div>

<div class="bookPage" id="p67" name="67">
<p>The Lamb, as Martha had said, was feeling the benefit of the country
air, and he was as frisky as a sandhopper. The elder ones longed to go on
talking about the new wishes they would have when (or if) they found the
Psammead again. But the Lamb wished to enjoy himself.</p>
<p>He watched his opportunity and threw a handful of sand into
Anthea&rsquo;s face, and then suddenly burrowed his own head in the sand
and waved his fat legs in the air. Then of course the sand got into his
eyes, as it had into Anthea&rsquo;s, and he howled.</p>
<p>The thoughtful Robert had brought one solid brown bottle of
ginger&ndash;beer with him, relying on a thirst that had never yet failed
him. This had to be uncorked hurriedly&mdash;it was the only
wet thing within reach, and it was necessary to wash the sand out of the
Lamb&rsquo;s eyes somehow. Of course the ginger hurt horribly, and he
howled more than ever. And, amid his anguish of kicking, the bottle was
upset and the beautiful ginger&ndash;beer frothed out into the sand and
was lost for ever.</p>
<p>It was then that Robert, usually a very patient brother, so far forgot
himself as to say&mdash;</p>
</div>

<div class="bookPage" id="p68" name="68">
<p>&ldquo;Anybody would want him, indeed! Only they don&rsquo;t; Martha
doesn&rsquo;t, not really, or she&rsquo;d jolly well keep him with her.
He&rsquo;s a little nuisance, that&rsquo;s what he is. It&rsquo;s too bad.
I only wish everybody <em>did</em> want him with all their hearts; we
might get some peace in our lives.&rdquo;</p>
<p>The Lamb stopped howling now, because Jane had suddenly remembered that
there is only one safe way of taking things out of little children&rsquo;s
eyes, and that is with your own soft wet tongue. It is quite easy if you
love the Baby as much as you ought to.</p>
<p>Then there was a little silence. Robert was not proud of himself for
having been so cross, and the others were not proud of him either.
You often notice that sort of silence when someone has said something it
ought not to&mdash;and everyone else holds its tongue and waits for the one
who oughtn&rsquo;t to have said it is sorry.</p>
<p>The silence was broken by a sigh&mdash;a breath suddenly let out. The
children&rsquo;s heads turned as if there had been a string tied to each
nose, and someone had pulled all the strings at once.</p>
<p>And everyone saw the Sand&ndash;fairy sitting quite close to them, with
the expression which it used as a smile on its hairy face.</p>
<p>&ldquo;Good&ndash;morning,&rdquo; it said; &ldquo;I did that quite
easily! Everyone wants him now.&rdquo;</p>
</div>

<div class="bookPage" id="p69" name="69">
<p>&ldquo;It doesn&rsquo;t matter,&rdquo; said Robert sulkily, because he
knew he had been behaving rather like a pig. &ldquo;No matter who wants
him&mdash;there&rsquo;s no one here to&mdash;anyhow.&rdquo;</p>
<p>&ldquo;Ingratitude,&rdquo; said the Psammead, &ldquo;is a dreadful
vice.&rdquo;</p>
<p>&ldquo;We&rsquo;re not ungrateful,&rdquo; Jane made haste to say,
&ldquo;but we didn&rsquo;t <em>really</em> want that wish. Robert only
just said it. Can&rsquo;t you take it back and give us a new
one?&rdquo;</p>
<p>&ldquo;No&mdash;I can&rsquo;t,&rdquo; the Sand&ndash;fairy said
shortly; &ldquo;chopping and changing&mdash;it&rsquo;s not business. You ought to be
careful what you <em>do</em> wish. There was a little boy once,
he&rsquo;d wished for a Plesiosaurus instead of an Ichthyosaurus, because
he was too lazy to remember the easy names of everyday things, and his
father had been very vexed with him, and had made him go to bed before
tea&ndash;time, and wouldn&rsquo;t let him go out in the nice flint boat
along with the other children,&mdash;it was the annual school&ndash;treat
next day,&mdash;and he came and flung himself down near me on the morning
of the treat, and he kicked his little prehistoric legs about and said he
wished he was dead. And of course then he was.&rdquo;</p>
<p>&ldquo;How awful!&rdquo; said the children all together.</p>
</div>

<div class="bookPage" id="p70" name="70">
<p>&ldquo;Only till sunset, of course,&rdquo; the Psammead said;
&ldquo;still it was quite enough for his father and mother. And he caught
it when he woke up&mdash;I can tell you. He didn&rsquo;t turn to
stone&mdash;I forget why&mdash;but there must have been some reason. They
didn&rsquo;t know being dead is only being asleep, and you&rsquo;re bound
to wake up somewhere or other, either where you go to sleep or in some
better place. You may be sure he caught it, giving them such a turn. Why, he wasn&rsquo;t allowed to
taste Megatherium for a month after that. Nothing but oysters and
periwinkles, and common things like that.&rdquo;</p>
<p>All the children were quite crushed by this terrible tale. They looked
at the Psammead in horror. Suddenly the Lamb perceived that something brown
and furry was near him.</p>
<p>&ldquo;Poof, poof, poofy,&rdquo; he said, and made a grab.</p>
<p>&ldquo;It&rsquo;s not a pussy,&rdquo; Anthea was beginning, when the
Sand&ndash;fairy leaped back.</p>
<p>&ldquo;Oh, my left whisker!&rdquo; it said; &ldquo;don&rsquo;t let him
touch me. He&rsquo;s wet.&rdquo;</p>
<p>Its fur stood on end with horror&mdash;and indeed a good deal of the
ginger&ndash;beer had been spilt on the blue smock of the Lamb.</p>
</div>

<div class="bookPage" id="p71" name="71">
<p>The Psammead dug with its hands and feet, and vanished in an instant and
a whirl of sand.</p>
<p>The children marked the spot with a ring of stones.</p>
<p>&ldquo;We may as well get along home,&rdquo; said Robert.
&ldquo;I&rsquo;ll say I&rsquo;m sorry; but anyway if it&rsquo;s no good
it&rsquo;s no harm, and we know where the sandy thing is for
to&ndash;morrow.&rdquo;</p>
<p>The others were noble. No one reproached Robert at all. Cyril picked up
the Lamb, who was now quite himself again, and off they went by the safe
cart&ndash;road.</p>
<p>The cart&ndash;road from the gravel&ndash;pits joins the road almost
directly.</p>
<p>At the gate into the road the party stopped to shift the Lamb from
Cyril&rsquo;s back to Robert&rsquo;s. And as they paused a very smart open
carriage came in sight, with a coachman and a groom on the box, and inside
the carriage a lady&mdash;very grand indeed, with a dress all white lace
and red ribbons and a parasol all red and white&mdash;and a white fluffy
dog on her lap with a red ribbon round its neck. She looked at the
children, and particularly at the Baby, and she smiled at him. The children
were used to this, for the Lamb was, as all the servants said, a
&ldquo;very taking child&rdquo;. So they waved their hands politely to the
lady and expected her to drive on. But she did not. Instead she made the
coachman stop. And she beckoned to Cyril, and when he went up to the
carriage she said:</p>
</div>

<div class="bookPage" id="p72" name="72">
<p>&ldquo;What a dear darling duck of a baby!
Oh, I <em>should</em> so like to adopt it! Do you think its mother
would mind?&rdquo;</p>
<p>&ldquo;She&rsquo;d mind very much indeed,&rdquo; said Anthea
shortly.</p>
<p>&ldquo;Oh, but I should bring it up in luxury, you know. I am Lady
Chittenden. You must have seen my photograph in the illustrated papers.
They call me a Beauty, you know, but of course that&rsquo;s all nonsense.
Anyway&rdquo;&mdash;</p>
<p>She opened the carriage door and jumped out. She had the wonderfullest
red high&ndash;heeled shoes with silver buckles. &ldquo;Let me hold him a
minute,&rdquo; she said. And she took the Lamb and held him very awkwardly,
as if she was not used to babies.</p>
<p>Then suddenly she jumped into the carriage with the Lamb in her arms and
slammed the door and said, &ldquo;Drive on!&rdquo;</p>
<p>The Lamb roared, the little white dog barked, and the coachman
hesitated.</p>
<p>&ldquo;Drive on, I tell you!&rdquo; cried the lady; and the coachman
did, for, as he said afterwards, it was as much as his place was worth not
to.</p>
</div>

