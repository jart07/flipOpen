<div class="bookPage" id="p73" name="73">
<p>The four children looked at each other, and then with one accord they 
rushed after the carriage and held on behind.
Down the dusty road went the smart carriage, and after it, at
double&ndash;quick time, ran the twinkling legs of the Lamb&rsquo;s
brothers and sisters.</p>
<p>The Lamb howled louder and louder, but presently his howls changed by
slow degree to hiccupy gurgles, and then all was still and they knew he had
gone to sleep.</p>
<p>The carriage went on, and the eight feet that twinkled through the dust
were growing quite stiff and tired before the carriage stopped at the lodge
of a grand park. The children crouched down behind the carriage, and the
lady got out. She looked at the Baby as it lay on the carriage seat, and
hesitated.</p>
<p>&ldquo;The darling&mdash;I won&rsquo;t disturb it,&rdquo; she said, and
went into the lodge to talk to the woman there about a setting of Buff
Orpington eggs that had not turned out well.</p>
<p>The coachman and footman sprang from the box and bent over the sleeping
Lamb.</p>
<p>&ldquo;Fine boy&mdash;wish he was mine,&rdquo; said the coachman.</p>
<p>&ldquo;He wouldn&rsquo;t favour <em>you</em> much,&rdquo; said the
groom sourly; &ldquo;too &rsquo;andsome.&rdquo;</p>
</div>

<div class="bookPage" id="pf85" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil085.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p74" name="74">
<p>The coachman pretended not to hear. He said:</p>
<p>&ldquo;Wonder at her now&mdash;I do really! Hates kids. Got none of her
own, and can&rsquo;t abide other folkses&rsquo;.&rdquo;</p>
<p>The children, crouching in the white dust under the carriage, exchanged
uncomfortable glances.</p>
<p>&ldquo;Tell you what,&rdquo; the coachman went on firmly, &ldquo;blowed
if I don&rsquo;t hide the little nipper in the hedge and tell her his
brothers took &rsquo;im! Then I&rsquo;ll come back for him
afterwards.&rdquo;</p>
<p>&ldquo;No, you don&rsquo;t,&rdquo; said the footman. &ldquo;I&rsquo;ve
took to that kid so as never was. If anyone&rsquo;s to have him, it&rsquo;s
me&mdash;so there!&rdquo;</p>
<p>&ldquo;Stow your gab!&rdquo; the coachman rejoined. &ldquo;You
don&rsquo;t want no kids, and, if you did, one kid&rsquo;s the same as
another to you. But I&rsquo;m a married man and a judge of breed. I knows a
firstrate yearling when I sees him. I&rsquo;m a&ndash;goin&rsquo; to
&rsquo;ave him, an&rsquo; least said soonest mended.&rdquo;</p>
<p>&ldquo;I should &rsquo;a&rsquo; thought,&rdquo; said the footman
sneeringly, &ldquo;you&rsquo;d a&rsquo;most enough. What with Alfred,
an&rsquo; Albert, an&rsquo; Louise, an&rsquo; Victor Stanley, and Helena
Beatrice, an another&rdquo;&mdash;</p>
</div>

<div class="bookPage" id="p75" name="75">
<p>The coachman hit the footman in the chin&mdash;the footman hit the
coachman in the waistcoat&mdash;the next minute the two were fighting here
and there, in and out, up and down, and all over everywhere, and the little
dog jumped on the box of the carriage and began barking like mad.</p>
<p>Cyril, still crouching in the dust, waddled on bent legs to the side of
the carriage farthest from the battlefield. He unfastened the door of the
carriage&mdash;the two men were far too much occupied with their quarrel to
notice anything&mdash;took the Lamb in his arms, and, still stooping,
carried the sleeping baby a dozen yards along the road to where a stile led
into a wood. The others followed, and there among the hazels and young oaks
and sweet chestnuts, covered by high strong&ndash;scented bracken, they
all lay hidden till the angry voices of the men were hushed at the angry
voice of the red&ndash;and&ndash;white lady, and, after a 
long and anxious search, the carriage at last drove away.</p>
<p>&ldquo;My only hat!&rdquo; said Cyril, drawing a deep breath as the
sound of wheels at last died away. &ldquo;Everyone <em>does</em> want
him now&mdash;and no mistake! That Sammyadd has done us again! Tricky
brute! For any sake, let&rsquo;s get the kid safe home.&rdquo;</p>
</div>

<div class="bookPage" id="p76" name="76">
<p>So they peeped out, and finding on the right hand only lonely white
road, and nothing but lonely white road on the left, they took courage, and
the road, Anthea carrying the sleeping Lamb.</p>
<p>Adventures dogged their footsteps. A boy with a bundle of faggots on his
back dropped his bundle by the roadside and asked to look at the Baby, and
