<h2>CHAPTER VIII<br />
BIGGER THAN THE BAKER&rsquo;S BOY</h2>
<p>&ldquo;<strong>LOOK</strong> here,&rdquo; said Cyril. &ldquo;I&rsquo;ve got an
idea.&rdquo;</p>
<p>&ldquo;Does it hurt much?&rdquo; said Robert sympathetically.</p>
<p>&ldquo;Don&rsquo;t be a jackape! I&rsquo;m not humbugging.&rdquo;</p>
<p>&ldquo;Shut up, Bobs!&rdquo; said Anthea.</p>
<p>&ldquo;Silence for the Squirrel&rsquo;s oration,&rdquo; said Robert.</p>
<p>Cyril balanced himself on the edge of the water&ndash;butt in the
backyard, where they all happened to be, and spoke.</p>
<p>&ldquo;Friends, Romans, countrymen&mdash;and women&mdash;we found a
Sammyadd. We have had wishes. We&rsquo;ve had wings, and being beautiful as
the day&mdash;ugh!&mdash;that was pretty jolly beastly if you
like&mdash;and wealth and castles, and that rotten gipsy business with the
Lamb. But
we&rsquo;re no forrader. We haven&rsquo;t really got anything worth having
for our wishes.&rdquo;</p>
<p>&ldquo;We&rsquo;ve had things happening,&rdquo; said Robert,
&ldquo;that&rsquo;s always something.&rdquo;</p>
</div>

<div class="bookPage" id="p168" name="168">
<p>&ldquo;It&rsquo;s not enough, unless they&rsquo;re the right
things,&rdquo; said Cyril firmly. &ldquo;Now I&rsquo;ve been
thinking&rdquo;&mdash;</p>
<p>&ldquo;Not really?&rdquo; whispered Robert.</p>
<p>&ldquo;In the silent what&rsquo;s&ndash;its&ndash;names of the night.
It&rsquo;s like suddenly being asked something out of history&mdash;the
date of the Conquest or something; you know it all right all the time, but
when you&rsquo;re asked it all goes out of your head. Ladies and gentlemen,
you know jolly well that when we&rsquo;re all rotting about in the usual
way heaps of things keep cropping up, and then real earnest wishes come
into the heads of the beholder&rdquo;&mdash;</p>
<p>&ldquo;Here, here!&rdquo; said Robert.</p>
<p>&ldquo;&mdash;of the beholder, however stupid he is,&rdquo; Cyril went
on. &ldquo;Why, even Robert might happen to think of a really useful wish
if he didn&rsquo;t injure his poor little brains trying so hard to
think.&mdash;Shut up, Bobs, I tell you!&mdash;You&rsquo;ll have the whole
show over.&rdquo;</p>
<p>A struggle on the edge of a water&ndash;butt is exciting, but damp.
When it was over, and the boys were partially dried, Anthea said&mdash;</p>
<p>&ldquo;It really was you began it, Bobs. Now honour is satisfied, do let
Squirrel go on. We&rsquo;re wasting the whole morning.&rdquo;</p>
</div>

<div class="bookPage" id="p169" name="169">
<p>&ldquo;Well then,&rdquo; said Cyril, still wringing the water out of the
tails of his jacket, &ldquo;I&rsquo;ll call it pax if Bobs will.&rdquo;</p>
<p>&ldquo;Pax then,&rdquo; said Robert sulkily. &ldquo;But I&rsquo;ve got a
lump as big as a cricket ball over my eye.&rdquo;</p>
<p>Anthea patiently offered a dust&ndash;coloured handkerchief, and Robert
bathed his wounds in silence. &ldquo;Now, Squirrel,&rdquo; she said.</p>
<p>&ldquo;Well then&mdash;let&rsquo;s just play bandits, or forts, or
soldiers, or any of the old games. We&rsquo;re dead sure to think of
something if we try not to. You always do.&rdquo;</p>
<p>The others consented. Bandits was hastily chosen for the game.
&ldquo;It&rsquo;s as good as anything else,&rdquo; said Jane gloomily. It
must be owned that Robert was at first but a half&ndash;hearted bandit,
but when Anthea had borrowed from Martha the red spotted handkerchief in
which the keeper had brought her mushrooms that morning, and had tied up
Robert&rsquo;s head with it so that he could be the wounded hero who had
saved the bandit captain&rsquo;s life the day before, he cheered up
wonderfully. All were soon armed. Bows and arrows slung on the back look
well; and umbrellas and cricket stumps stuck through the belt give a fine
impression of the wearer&rsquo;s being armed to the teeth. The white cotton
hats that men wear in the country nowadays have a very brigandish effect
when a few turkey&rsquo;s feathers are stuck in them. The Lamb&rsquo;s
mail&ndash;cart</p>
</div>

<div class="bookPage" id="p170" name="170">
<p>was covered with a red&ndash;and&ndash;blue checked
tablecloth, and made an admirable baggage&ndash;waggon. The Lamb asleep
inside of it was not at all in the way. So the banditti set out along the
road that led to the sand&ndash;pit.</p>
<p>&ldquo;We ought to be near the Sammyadd,&rdquo; said Cyril, &ldquo;in
case we think of anything suddenly.&rdquo;</p>
<p>It is all very well to make up your minds to play bandits&mdash;or
chess, or ping&ndash;pong, or any other agreeable game&mdash;but it is not
easy to do it with spirit when all the wonderful
wishes you can think of, or can&rsquo;t think of, are waiting for you round
the corner. The game was dragging a little, and some of the bandits were
beginning to feel that the others were disagreeable things, and were saying
so candidly, when the baker&rsquo;s boy came along the road with loaves in
a basket. The opportunity was not one to be lost.</p>
<p>&ldquo;Stand and deliver!&rdquo; cried Cyril.</p>
<p>&ldquo;Your money or your life!&rdquo; said Robert.</p>
<p>And they stood on each side of the baker&rsquo;s boy. Unfortunately he
did not seem to enter into the spirit of the thing at all. He was a
baker&rsquo;s boy of an unusually large size. He merely said&mdash;</p>
<p>&ldquo;Chuck it now, d&rsquo;ye hear!&rdquo; and pushed the bandits
aside most disrespectfully.</p>
</div>

<div class="bookPage" id="p171" name="171">
<p>Then Robert lassoed him with Jane&rsquo;s skipping&ndash;rope, and
instead of going round his shoulders, as Robert intended, it went round his
feet and tripped him up. The basket was upset, the beautiful new loaves
went bumping and bouncing all over the dusty chalky road. The girls ran to
pick them up, and all in a moment Robert and the
baker&rsquo;s boy were fighting it out, man to man, with Cyril to see fair
play, and the skipping&ndash;rope twisting round their legs like an
interested snake that wished to be a peace&ndash;maker. It did not
succeed; indeed the way the boxwood handles sprang up and hit the fighters
on the shins and ankles was not at all peace&ndash;making. I know this is
the second fight&mdash;or contest&mdash;in this chapter, but I can&rsquo;t
help it. It was that sort of day. You know yourself there are days when
rows seem to keep on happening, quite without your meaning them to. If I
were a writer of tales of adventure such as those which used to appear in
<em>The Boys of England</em> when I was young, of course I should be
able to describe the fight, but I cannot do it. I never can see what
happens during a fight, even when it is only dogs. Also, if I had been one
of these <em>Boys of England</em> writers, Robert would have got the
best of it. But I am like George Washington&mdash;I cannot tell a lie, even
about a cherry&ndash;tree, much less about a fight, and I cannot conceal
from you that Robert was badly beaten, for the second time that
day. The baker&rsquo;s boy blacked his other eye, and, being ignorant of
the first rules of fair play and gentlemanly</p>
</div>

<div class="bookPage" id="p172" name="172">
<p>behaviour, he also pulled Robert&rsquo;s hair, and kicked him on the knee. Robert always used to say
he could have licked the butcher if it hadn&rsquo;t been for the girls. But
I am not sure. Anyway, what happened was this, and very painful it was to
self&ndash;respecting boys.</p>
<p>Cyril was just tearing off his coat so as to help his brother in proper
style, when Jane threw her arms round his legs and began to cry and ask him
not to go and be beaten too. That &ldquo;too&rdquo; was very nice for
Robert, as you can imagine&mdash;but it was nothing to what he felt when
Anthea rushed in between him and the baker&rsquo;s boy, and caught that
unfair and degraded fighter round the waist, imploring him not to fight any
more.</p>
<p>&ldquo;Oh, don&rsquo;t hurt my brother any more!&rdquo; she said in
floods of tears. &ldquo;He didn&rsquo;t mean it&mdash;it&rsquo;s only play.
And I&rsquo;m sure he&rsquo;s very sorry.&rdquo;</p>
<p>You see how unfair this was to Robert. Because, if the baker&rsquo;s boy
had had any right and chivalrous instincts, and had yielded to Anthea&rsquo;s pleading and
accepted her despicable apology, Robert could not, in honour, have done
anything to him at any future time. But Robert&rsquo;s fears, if he had
any, were soon dispelled. Chivalry was a stranger to the breast of the
baker&rsquo;s boy. He pushed Anthea away very roughly, and he chased Robert
with</p>
</div>

