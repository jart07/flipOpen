<div class="bookPage" id="p177" name="177">
<p>Robert crouched behind a haystack in the farmyard, that is at the
corner, and when he heard the boy come whistling along he jumped out at him
and caught him by the collar.</p>
<p>&ldquo;Now,&rdquo; he said, and his voice was about four times its usual
size, just as his body was four times its, &ldquo;I&rsquo;m going to teach
you to kick boys smaller than you.&rdquo;</p>
<p>He lifted up the baker&rsquo;s boy and set him on the top of the
haystack, which was about sixteen feet from the ground, and then he sat
down on the roof of the cowshed and told the baker&rsquo;s boy exactly what
he thought of him. I don&rsquo;t think the boy heard it all&mdash;he was in
a sort of trance of terror. When Robert has said everything he could think
of, and some things twice over, he shook the boy and said&mdash;</p>
<p>&ldquo;And now get down the best way you can,&rdquo; and left him.</p>
<p>I don&rsquo;t know how the baker&rsquo;s boy got down, but I do know
that he missed the cart, and got into the very hottest of hot water when he
turned up at last at the bakehouse. I am sorry for him, but, after all, it
was quite right that he should be taught that English boys mustn&rsquo;t
use their feet when they fight, but their fists. Of course the water he got
into only became hotter when he tried to tell his master about the boy he
had licked and the giant as high as a church, because no one could possibly
believe</p>
</div>

<div class="bookPage" id="p178" name="178">
<p>such a tale as that. Next day the tale was believed&mdash;but that
was too late to be of any use to the baker&rsquo;s boy.</p>
<p>When Robert rejoined the others he found them in the garden. Anthea had
thoughtfully asked Martha to let them have dinner out there&mdash;because
the dining&ndash;room was rather small, and it would have been so awkward
to have a brother the size of Robert in there. The Lamb, who had slept
peacefully during the whole stormy morning, was now found to be sneezing,
and Martha said he had a cold and would be better indoors.</p>
<p>&ldquo;And really it&rsquo;s just as well,&rdquo; said Cyril,
&ldquo;for I don&rsquo;t believe he&rsquo;d ever have stopped screaming if
he&rsquo;d once seen you the awful size you are!&rdquo;</p>
<p>Robert was indeed what a draper would call an
&ldquo;out&ndash;size&rdquo; in boys. He found himself able to step right
over the iron gate in the front garden.</p>
<p>Martha brought out the dinner&mdash;it was cold veal and baked potatoes,
with sago pudding and stewed plums to follow.</p>
<p>She of course did not notice that Robert was anything but the usual
size, and she gave him as much meat and potatoes as usual and no more. You
have no idea how small your usual helping of dinner looks when you are many
times your proper size. Robert groaned, and asked</p>
</div>

<div class="bookPage" id="pf209" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil209.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p179" name="179">
<p>for more bread. But
Martha would not go on giving more bread for ever. She was in a hurry,
because the keeper intended to call on his way to Benenhurst Fair, and she
wished to be dressed smartly before he came.</p>
<p>&ldquo;I wish <em>we</em> were going to the Fair,&rdquo; said
Robert.</p>
<p>&ldquo;You can&rsquo;t go anywhere that size,&rdquo; said Cyril.</p>
<p>&ldquo;Why not?&rdquo; said Robert. &ldquo;They have giants at fairs,
much bigger ones than me.&rdquo;</p>
<p>&ldquo;Not much, they don&rsquo;t,&rdquo; Cyril was beginning, when Jane
screamed &ldquo;Oh!&rdquo; with such loud suddenness that they all thumped
her on the back and asked whether she had swallowed a
plum&ndash;stone.</p>
<p>&ldquo;No,&rdquo; she said, breathless from being thumped,
&ldquo;it&rsquo;s&mdash;it&rsquo;s not a plum&ndash;stone. It&rsquo;s an
idea. Let&rsquo;s take Robert to the Fair, and get them to give us money
for showing him! Then we really <em>shall</em> get something out of the
old Sammyadd at last!&rdquo;</p>
<p>&ldquo;Take me indeed!&rdquo; said Robert indignantly. &ldquo;Much more
likely me take you!&rdquo;</p>
<p>And so it turned out. The idea appealed irresistibly to everyone but
Robert, and even he was brought round by Anthea&rsquo;s suggestion that he
should have a double share of any money they might make. There was a little
old</p>
</div>

<div class="bookPage" id="p180" name="180">
<p>pony&ndash;trap in the coach&ndash;house&mdash;the kind that is
called a governess&ndash;cart. It seemed desirable to get to the Fair as
quickly as possible, so Robert&mdash; who could now take enormous steps and
so go very fast indeed&mdash;
consented to wheel the others in this. It was as easy to him now as
wheeling the Lamb in the mail&ndash;cart had been in the morning. The
Lamb&rsquo;s cold prevented his being of the party.</p>
<p>It was a strange sensation being wheeled in a pony&ndash;carriage by a
giant. Everyone enjoyed the journey except Robert and the few people they
passed on the way. These mostly went into what looked like some kind of
standing&ndash;up fits by the roadside, as Anthea said. Just outside
Benenhurst, Robert hid in a barn, and the others went on to the Fair.</p>
<p>There were some swings, and a hooting&ndash;tooting blaring
merry&ndash;go&ndash;round, and a shooting&ndash;gallery and
cocoanut&ndash;shies. Resisting an impulse to win a cocoanut,&mdash;or at
least to attempt the enterprise,&mdash;Cyril went up to the woman who was
loading little guns before the array of glass bottles on strings against a
sheet of canvas.</p>
<p>&ldquo;Here you are, little gentleman!&rdquo; she said. &ldquo;Penny a
shot!&rdquo;</p>
<p>&ldquo;No, thank you,&rdquo; said Cyril, &ldquo;we are here on business,
not on pleasure. Who&rsquo;s the master?&rdquo;</p>
<p>&ldquo;The what?&rdquo;</p>
</div>

<div class="bookPage" id="pf213" name="">
<img src="illustrations/fiveChildren/nesbit-fivechil213.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p181" name="181">
<p>&ldquo;The master&mdash;the head&mdash;the boss of the show.&rdquo;</p>
<p>&ldquo;Over there,&rdquo; she said, pointing to a stout man in a dirty
linen jacket who was sleeping in the sun; &ldquo;but I don&rsquo;t advise
you to wake him sudden. His temper&rsquo;s contrary, especially these hot
days. Better have a shot while you&rsquo;re waiting.&rdquo;</p>
<p>&ldquo;It&rsquo;s rather important,&rdquo; said Cyril.
&ldquo;It&rsquo;ll be very profitable to him. I think he&rsquo;ll be sorry
if we take it away.&rdquo;</p>
<p>&ldquo;Oh, if it&rsquo;s money in his pocket,&rdquo; said the woman.
&ldquo;No kid now? What is it?&rdquo;</p>
<p>&ldquo;It&rsquo;s a <em>giant</em>.&rdquo;</p>
<p>&ldquo;You <em>are</em> kidding?&rdquo;</p>
<p>&ldquo;Come along and see,&rdquo; said Anthea.</p>
<p>The woman looked doubtfully at them, then she called to a ragged little
girl in striped stockings and a dingy white petticoat that came below her
brown frock, and leaving her in charge of the
&ldquo;shooting&ndash;gallery&rdquo; she turned to Anthea and said,
&ldquo;Well, hurry up! But if you <em>are</em> kidding, you&rsquo;d
best say so. I&rsquo;m as mild as milk myself, but my Bill he&rsquo;s a
fair terror and&rdquo;&mdash;</p>
<p>Anthea led the way to the barn. &ldquo;It really <em>is</em> a
giant,&rdquo; she said. &ldquo;He&rsquo;s a giant little boy&mdash;in
Norfolks like my brother&rsquo;s there. And we didn&rsquo;t bring him up to
the Fair</p>
</div>

<div class="bookPage" id="p182" name="182">
<p>because people do stare so, and they seem to go into kind of
standing&ndash;up fits when they see him. And we thought perhaps
you&rsquo;d like to show him and get pennies; and if you like to pay us
something, you can&mdash;only, it&rsquo;ll have to be rather a lot, because
we promised him he should have a double share of whatever we
made.&rdquo;</p>
<p>The woman murmured something indistinct, of which the children could
only hear the words, &ldquo;Swelp me!&rdquo; &ldquo;balmy,&rdquo; and
&ldquo;crumpet,&rdquo; which conveyed no definite idea to their minds.</p>
<p>She had taken Anthea&rsquo;s hand, and was holding it very firmly; and
Anthea could not help wondering what would happen if Robert should have
wandered off or turned his proper size during the interval. But she knew
that the Psammead&rsquo;s gifts really did last till sunset, however
inconvenient their lasting might be; and she did not think somehow,
that Robert would care to go out alone while he was that size.</p>
<p>When they reached the barn and Cyril called &ldquo;Robert!&rdquo; there
was a stir among the loose hay, and Robert began to come out. His hand and
arm came first&mdash;then a foot and leg. When the woman saw the hand she
said &ldquo;My!&rdquo; but when she saw the foot she said &ldquo;Upon my
civvy!&rdquo; and when, by slow and heavy degrees, the whole of
Robert&rsquo;s enormous bulk was at last completely disclosed, she drew</p>
</div>

<div class="bookPage" id="p183" name="183">
<p>a long breath and began to say many things, compared with which
&ldquo;balmy&rdquo; and &ldquo;crumpet&rdquo; seemed quite ordinary. She
dropped into understandable English at last.</p>
<p>&ldquo;What&rsquo;ll you take for him?&rdquo; she said excitedly.
&ldquo;Anything in reason. We&rsquo;d have a special van
built&mdash;leastways, I know where there&rsquo;s a second&ndash;hand one
would do up handsome&mdash;what a baby elephant had, as died. What&rsquo;ll
you take? He&rsquo;s soft, ain&rsquo;t he? Them giants mostly is&mdash;but
I never see&mdash;no never! What&rsquo;ll you take? Down on the nail.
We&rsquo;ll treat him like a king,
and give him first&ndash;rate grub and a doss fit for a bloomin&rsquo;
dook. He must be dotty or he wouldn&rsquo;t need you kids to cart him
about. What&rsquo;ll you take for him?&rdquo;</p>
<p>&ldquo;They won&rsquo;t take anything,&rdquo; said Robert sternly.
&ldquo;I&rsquo;m no more soft than you are&mdash;not so much, I
shouldn&rsquo;t wonder. I&rsquo;ll come and be a show for to&ndash;day if
you&rsquo;ll give me&rdquo;&mdash;he hesitated at the enormous price he was
about to ask&mdash;&ldquo;if you&rsquo;ll give me fifteen
shillings.&rdquo;</p>
<p>&ldquo;Done,&rdquo; said the woman, so quickly that Robert felt he had
been unfair to himself, and wished he had asked thirty. &ldquo;Come on
now&mdash;and see my Bill&mdash;and we&rsquo;ll fix a price for the season.
I dessay you might get as much as two quid a week reg&rsquo;lar. Come
on&mdash;and make yourself as small as you can, for gracious&rsquo;
sake!&rdquo;</p>
</div>

<div class="bookPage" id="p184" name="184">
<p>This was not very small, and a crowd gathered quickly, so that it was at
the head of an enthusiastic procession that Robert entered the trampled
meadow where the Fair was held, and passed over the stubbly yellow dusty
grass to the door of the biggest tent.
He crept in, and the woman went to call her Bill. He was the big sleeping
man, and he did not seem at all pleased at being awakened. Cyril, watching
through a slit in the tent, saw him scowl and shake a heavy fist and a
sleepy head. Then the woman went on speaking very fast. Cyril heard
&ldquo;Strewth,&rdquo; and &ldquo;biggest draw you ever, so help me!&rdquo;
and he began to share Robert&rsquo;s feeling that fifteen shillings was
indeed far too little. Bill slouched up to the tent and entered. When he
beheld the magnificent proportions of Robert he said but
little,&mdash;&ldquo;Strike me pink!&rdquo; were the only words the
children could afterwards remember,&mdash;but he produced fifteen
shillings, mainly in sixpences and coppers, and handed it to Robert.</p>
<p>&ldquo;We&rsquo;ll fix up about what you&rsquo;re to draw when the
show&rsquo;s over to&ndash;night,&rdquo; he said with hoarse heartiness.
&ldquo;Lor&rsquo; love a duck! you&rsquo;ll be that happy with us
you&rsquo;ll never want to leave us. Can you do a song now&mdash;or a bit
of a breakdown?&rdquo;</p>
<p>&ldquo;Not to&ndash;day,&rdquo; said Robert, rejecting the
idea of trying to sing &ldquo;As once in May,&rdquo; a favourite of his
mother&rsquo;s, and the only song he could think of at the moment.</p>
</div>

<div class="bookPage" id="p185" name="185">
<p>&ldquo;Get Levi and clear them bloomin&rsquo; photos out. Clear the
tent. Stick up a curtain or suthink,&rdquo; the man went on.
&ldquo;Lor&rsquo;, what a pity we ain&rsquo;t got no tights his size ! But
we&rsquo;ll have &rsquo;em before the week&rsquo;s out. Young man, your
fortune&rsquo;s made. It&rsquo;s a good thing you came to me, and not to
some chaps as I could tell you on. I&rsquo;ve known blokes as beat their
giants, and starved &rsquo;em too; so I&rsquo;ll tell you straight,
you&rsquo;re in luck this day if you never was afore. &rsquo;Cos I&rsquo;m
a lamb, I am&mdash;and I don&rsquo;t deceive you.&rdquo;</p>
<p>&ldquo;I&rsquo;m not afraid of anyone&rsquo;s beating
<em>me</em>,&rdquo; said Robert, looking down on the
&ldquo;lamb.&rdquo; Robert was crouched on his knees, because the tent was
not big enough for him to stand upright in, but even in that position he
could still look down on most people. &ldquo;But I&rsquo;m awfully
hungry&mdash;I wish you&rsquo;d get me something to eat.&rdquo;</p>
<p>&ldquo;Here, &rsquo;Becca,&rdquo; said hoarse Bill. &ldquo;Get him some
grub&mdash; the best you&rsquo;ve got, mind!&rdquo;
Another whisper followed, of which the children only heard, &ldquo;Down in
black and white&mdash;first thing to&ndash;morrow.&rdquo;</p>
<p>Then the woman went to get the food&mdash;it was only bread and cheese
when it came, but it was delightful to the large and empty Robert; and the
man went to post sentinels round the tent, to give the alarm if Robert
should attempt to escape with his fifteen shillings.</p>
</div>

<div class="bookPage" id="p186" name="186">
<p>&ldquo;As if we weren&rsquo;t honest,&rdquo; said Anthea indignantly
when the meaning of the sentinels dawned on her.</p>
<p>Then began a very strange and wonderful afternoon.</p>
<p>Bill was a man who knew his business. In a very little while, the
photographic views, the spyglasses you look at them through so that they
really seem rather real, and the lights you see them by, were all packed
away. A curtain&mdash;it was an old red&ndash;and&ndash;black carpet
really&mdash;was run across the tent. Robert was concealed behind, and Bill
was standing on a trestle&ndash;table outside the tent making a speech. It
was rather a good speech. It began by
saying that the giant it was his privilege to introduce to the public that
day was the eldest son of the Emperor of San Francisco, compelled through
an unfortunate love affair with the Duchess of the Fiji Islands to leave
his own country and take refuge in England&mdash;the land of
liberty&mdash;where freedom was the right of every man, no matter how big
he was. It ended by the announcement that the first twenty who came to the
tent door should see the giant for threepence apiece. &ldquo;After
that,&rdquo; said Bill, &ldquo;the price is riz, and I don&rsquo;t
undertake to say what it won&rsquo;t be riz to. So now&rsquo;s yer
time.&rdquo;</p>
</div>

<div class="bookPage" id="p187" name="187">
<p>A young man squiring his sweetheart on her afternoon out was the first
to come forward. For that occasion his was the princely attitude&mdash;no
expense spared&mdash;money no object. His girl wished to see the giant?
Well, she should see the giant, even though seeing the giant cost
threepence each and the other entertainments were all penny ones.</p>
<p>The flap of the tent was raised&mdash;the couple entered. Next moment a
wild shriek from the girl thrilled all present. Bill
slapped his leg. &ldquo;That&rsquo;s done the trick!&rdquo; he whispered to
&rsquo;Becca. It was indeed a splendid advertisement of the charms of
Robert. When the girl came out she was pale and trembling, and a crowd was
round the tent.</p>
<p>&ldquo;What was it like?&rdquo; asked a bailiff.</p>
<p>&ldquo;Oh!&mdash;horrid!&mdash;you wouldn&rsquo;t believe,&rdquo; she
said. &ldquo;It&rsquo;s as big as a barn, and that fierce. It froze the
blood in my bones. I wouldn&rsquo;t ha&rsquo; missed seeing it for
anything.&rdquo;</p>
<p>The fierceness was only caused by Robert&rsquo;s trying not to laugh.
But the desire to do that soon left him, and before sunset he was more
inclined to cry than to laugh, and more inclined to sleep than either. For,
by ones and twos and threes people kept coming in all the afternoon, and
Robert had to shake hands with those who wished it, and</p>
</div>

<div class="bookPage" id="p188" name="188">
<p>to allow himself to be punched and pulled and patted and thumped, so that people might make
sure he was really real.</p>
<p>The other children sat on a bench and watched and waited, and were very
bored indeed. It seemed to them that this was the hardest way of earning money
that could have been invented. And only fifteen shillings! Bill had taken
four times that already, for the news of the giant had spread, and
tradespeople in carts, and gentlepeople in carriages, came from far and
near. One gentleman with an eyeglass, and a very large yellow rose in his
buttonhole, offered Robert, in an obliging whisper ten pounds a week to
appear at the Crystal Palace. Robert had to say &ldquo;No.&rdquo;</p>
<p>&ldquo;I can&rsquo;t,&rdquo; he said regretfully. &ldquo;It&rsquo;s no
use promising what you can&rsquo;t do.&rdquo;</p>
<p>&ldquo;Ah, poor fellow, bound for a term of years, I suppose! Well,
here&rsquo;s my card; when your time&rsquo;s up come to me.&rdquo;</p>
<p>&ldquo;I will&mdash;if I&rsquo;m the same size then,&rdquo; said Robert
truthfully.</p>
<p>&ldquo;If you grow a bit, so much the better,&rdquo; said the
gentleman.</p>
<p>When he had gone, Robert beckoned Cyril and said&mdash;</p>
<p>&ldquo;Tell them I must and will have an easy. And I want my
tea.&rdquo;</p>
</div>

