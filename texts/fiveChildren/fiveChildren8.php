<img src="illustrations/fiveChildren/nesbit-fivechil203.jpg" alt="Five Children and It Illustrations" />
</div>

<div class="bookPage" id="p173" name="173">
<p>kicks and unpleasant conversation right down the road to the
sand&ndash;pit, and there, with one last kick, he landed him in a heap of
sand.</p>
<p>&ldquo;I&rsquo;ll larn you, you young varmint!&rdquo; he said, and went
off to pick up his loaves and go about his business. Cyril, impeded by
Jane, could do nothing without hurting her, for she clung round his legs
with the strength of despair. The baker&rsquo;s boy went off red and damp
about the face; abusive to the last, he called them a pack of silly idiots,
and disappeared round the corner. Then Jane&rsquo;s grasp loosened. Cyril
turned away in silent dignity to follow Robert, and the girls followed him,
weeping without restraint.</p>
<p>It was not a happy party that flung itself
down in the sand beside the sobbing Robert. For Robert was
sobbing&mdash;mostly with rage. Though of course I know that a really
heroic boy is always dry&ndash;eyed after a fight. But then he always
wins, which had not been the case with Robert.</p>
<p>Cyril was angry with Jane; Robert was furious with Anthea; the girls
were miserable; and not one of the four was pleased with the baker&rsquo;s
boy. There was, as French writers say, &ldquo;a silence full of
emotion.&rdquo;</p>
<p>Then Robert dug his toes and his hands into the sand and wriggled in his
rage. &ldquo;He&rsquo;d better wait till I&rsquo;m grown up</p>
</div>

<div class="bookPage" id="p174" name="174">
<p>&mdash;the
cowardly brute! Beast!&mdash;I hate him! But I&rsquo;ll pay him out. Just
because he&rsquo;s bigger than me.&rdquo;</p>
<p>&ldquo;You began,&rdquo; said Jane incautiously.</p>
<p>&ldquo;I know I did, silly&mdash;but I was only rotting&mdash;and he
kicked me&mdash;look here&rdquo;&mdash;</p>
<p>Robert tore down a stocking and showed a purple bruise touched up with
red.</p>
<p>&ldquo;I only wish I was bigger than him, that&rsquo;s all.&rdquo;</p>
<p>He dug his fingers in the sand, and sprang
up, for his hand had touched something furry. It was the Psammead, of
course &mdash;&ldquo;On the look&ndash;out to make sillies of them as
usual,&rdquo; as Cyril remarked later. And of course the next moment
Robert&rsquo;s wish was granted, and he was bigger than the baker&rsquo;s
boy. Oh, but much, much bigger. He was bigger than the big policeman who
used to be at the crossing at the Mansion House years ago,&mdash;the one
who was so kind in helping old ladies over the crossing,&mdash;and he was
the biggest man <em>I</em> have ever seen, as well as the kindest. No
one had a foot&ndash;rule in its pocket, so Robert could not be
measured&mdash;but he was taller than your father would be if he stood on
your mother&rsquo;s head, which I am sure he would never be unkind enough
to do. He must have been ten or eleven feet high, and as broad as a boy of
that height ought to be. His Norfolk suit had</p>
</div>

<div class="bookPage" id="p175" name="175">
<p>fortunately grown too, and
now he stood up in it&mdash;with one of his enormous stockings turned down
to show the gigantic bruise on his vast leg. Immense tears of fury still
stood on his flushed giant face. He looked so surprised, and he was so
large to be wearing an Eton collar, that the others could not help laughing.</p>
<p>&ldquo;The Sammyadd&rsquo;s done us again,&rdquo; said Cyril.</p>
<p>&ldquo;Not us&mdash;<em>me</em>,&rdquo; said Robert. &ldquo;If
you&rsquo;d got any decent feeling you&rsquo;d try to make it make you the
same size. You&rsquo;ve no idea how silly it feels,&rdquo; he added
thoughtlessly.</p>
<p>&ldquo;And I don&rsquo;t want to; I can jolly well see how silly it
looks,&rdquo; Cyril was beginning; but Anthea said&mdash;</p>
<p>&ldquo;Oh, <em>don&rsquo;t</em>! I don&rsquo;t know what&rsquo;s the
matter with you boys to&ndash;day. Look here, Squirrel, let&rsquo;s play
fair. It is hateful for poor old Bobs, all alone up there. Let&rsquo;s ask
the Sammyadd for another wish, and, if it will, I do really think we ought
to be made the same size.&rdquo;</p>
<p>The others agreed, but not gaily; but when they found the Psammead, it
wouldn&rsquo;t.</p>
<p>&ldquo;Not I,&rdquo; it said crossly, rubbing its face with its feet.
&ldquo;He&rsquo;s a rude violent boy, and it&rsquo;ll do him good to be the
wrong size for a bit. What did he want to come digging me out with his
nasty wet hands for? He nearly touched me! He&rsquo;s a perfect savage. A
boy of the Stone Age would have had more sense.&rdquo;</p>
</div>

<div class="bookPage" id="p176" name="176">
<p>Robert&rsquo;s hands had indeed been wet&mdash;with tears.</p>
<p>&ldquo;Go away and leave me in peace, do,&rdquo; the Psammead went on.
&ldquo;I can&rsquo;t think why you don&rsquo;t wish for something sensible
&mdash; something to eat or drink, or good manners, or good tempers. Go
along with you, do!&rdquo;</p>
<p>It almost snarled as it shook its whiskers, and turned a sulky brown
back on them. The most hopeful felt that further parley was vain.</p>
<p>They turned again to the colossal Robert.</p>
<p>&ldquo;Whatever shall we do?&rdquo; they said; and they all said it.</p>
<p>&ldquo;First,&rdquo; said Robert grimly, &ldquo;I&rsquo;m going to
reason with that baker&rsquo;s boy. I shall catch him at the end of the
road.&rdquo;</p>
<p>&ldquo;Don&rsquo;t hit a chap littler than yourself, old man,&rdquo;
said Cyril.</p>
<p>&ldquo;Do I look like hitting him?&rdquo; said Robert scornfully.
&ldquo;Why, I should <em>kill</em> him. But I&rsquo;ll give him
something to remember. Wait till I pull up my stocking.&rdquo; He pulled up
his stocking, which was as large as a small bolster&ndash;case, and strode
off. His strides were six or seven feet long, so that it was quite easy for him to be at the
bottom of the hill, ready to meet the baker&rsquo;s boy when he came down
swinging the empty basket to meet his master&rsquo;s cart, which had been
leaving bread at the cottages along the road.</p>
</div>

<div class="bookPage" id="pf207" name="">
