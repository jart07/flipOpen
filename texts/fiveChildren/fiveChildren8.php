<p>Tea was provided, and a paper hastily pinned on the tent. It
said&mdash;</p>
<p>CLOSED FOR HALF AN HOUR WHILE THE GIANT GETS HIS TEA.</p>
<p>Then there was a hurried council.</p>
<p>&ldquo;How am I to get away?&rdquo; said Robert. &ldquo;I&rsquo;ve been
thinking about it all the afternoon.&rdquo;</p>
<p>&ldquo;Why, walk out when the sun sets and you&rsquo;re your right size.
They can&rsquo;t do anything to us.&rdquo;</p>
<p>Robert opened his eyes. &ldquo;Why, they&rsquo;d nearly kill us,&rdquo;
he said, &ldquo;when they saw me get my right size. No, we must think of
some other way. We <em>must</em> be alone when the sun sets.&rdquo;</p>
<p>&ldquo;I know,&rdquo; said Cyril briskly, and he went to the door,
outside which Bill was smoking a clay pipe and talking in a low voice to
&rsquo;Becca. Cyril heard him say&mdash;&ldquo;Good as havin&rsquo; a
fortune left you.&rdquo;</p>
<p>&ldquo;Look here,&rdquo; said Cyril, &ldquo;you can let people come in
again in a minute. He&rsquo;s nearly finished his tea. But he
<em>must</em> be left alone when the sun sets. He&rsquo;s very queer at
that
time of day, and if he&rsquo;s worried I won&rsquo;t answer for the
consequences.&rdquo;</p>
<p>&ldquo;Why&mdash;what comes over him?&rdquo; asked Bill.</p>
</div>

<div class="bookPage" id="p190" name="190">
<p>&ldquo;I don&rsquo;t know; it&rsquo;s&mdash;it&rsquo;s a sort of a
<em>change</em>,&rdquo; said Cyril candidly. &ldquo;He isn&rsquo;t at
all like himself&mdash;you&rsquo;d hardly know him. He&rsquo;s very queer
indeed. Someone&rsquo;ll get hurt if he&rsquo;s not alone about
sunset.&rdquo; This was true.</p>
<p>&ldquo;He&rsquo;ll pull round for the evening, I
s&rsquo;pose?&rdquo;</p>
<p>&ldquo;Oh yes&mdash;half an hour after sunset he&rsquo;ll be quite
himself again.&rdquo;</p>
<p>&ldquo;Best to humour him,&rdquo; said the woman.</p>
<p>And so, at what Cyril judged was about half an hour before sunset, the
tent was again closed, &ldquo;whilst the giant gets his supper.&rdquo;</p>
<p>The crowd was very merry about the giant&rsquo;s meals and their coming
so close together.</p>
<p>&ldquo;Well, he can pick a bit,&rdquo; Bill owned. &ldquo;You see he has
to eat hearty, being the size he is.&rdquo;</p>
<p>Inside the tent the four children breathlessly arranged a plan of
retreat.</p>
<p>&ldquo;You go <em>now</em>,&rdquo; said Cyril to the girls,
&ldquo;and get along home as fast as you can. Oh, never mind the beastly
pony&ndash;cart; we&rsquo;ll get that to&ndash;
morrow. Robert and I are dressed the same. We&rsquo;ll manage somehow, like
Sydney Carton did. Only, you girls <em>must</em> get out, or it&rsquo;s
all no go. We can run, but you can&rsquo;t&mdash;whatever you may think.
No, Jane, it&rsquo;s no</p>
</div>

<div class="bookPage" id="p191" name="191">
<p>good Robert going out and knocking people down. The
police would follow him till he turned his proper size, and then arrest him
like a shot. Go you must! If you don&rsquo;t I&rsquo;ll never speak to you
again. It was you got us into this mess really, hanging round
people&rsquo;s legs the way you did this morning. <em>Go</em>, I tell
you!&rdquo;</p>
<p>And Jane and Anthea went.</p>
<p>&ldquo;We&rsquo;re going home,&rdquo; they said to Bill.
&ldquo;We&rsquo;re leaving the giant with you. Be kind to him.&rdquo; And
that, as Anthea said afterwards, was very deceitful, but what were they to
do?</p>
<p>When they had gone, Cyril went to Bill.</p>
<p>&ldquo;Look here,&rdquo; he said, &ldquo;he wants some ears of
corn&mdash;there&rsquo;s some in the next field but one. I&rsquo;ll just
run and get it. Oh, and he says can&rsquo;t you loop up the tent at the
back a bit? He says he&rsquo;s stifling for a breath of air. I&rsquo;ll see
no one peeps in at him. I&rsquo;ll cover him up, and he can take a nap
while I go for the corn.
He *will* have it&mdash;there&rsquo;s no holding him when he gets like
this.&rdquo;</p>
<p>The giant was made comfortable with a heap of sacks and an old
tarpaulin. The curtain was looped up, and the brothers were left alone.
They matured their plans in whispers. Outside, the
merry&ndash;go&ndash;round blared out its comic tunes, screaming now and
then to attract public notice.</p>
</div>

<div class="bookPage" id="p192" name="192">
<p>Half a minute after the sun had set, a boy in a Norfolk suit came out
past Bill.</p>
<p>&ldquo;I&rsquo;m off for the corn,&rdquo; he said, and mingled quickly
with the crowd.</p>
<p>At the same instant a boy came out of the back of the tent past
&rsquo;Becca, posted there as sentinel.</p>
<p>&ldquo;I&rsquo;m off after the corn,&rdquo; said this boy also. And he,
too moved away quietly and was lost in the crowd. The front&ndash;door boy
was Cyril; the back&ndash;door was Robert&mdash;now, since sunset, once
more his proper size. They walked quickly through the field, and along the
road, where Robert caught Cyril up. Then they ran. They were home as soon
as the girls were, for it was a long way, and they ran most
of it. It was indeed a <em>very</em> long way, as they found when they
had to go and drag the pony&ndash;trap home next morning, with no enormous
Robert to wheel them in it as if it were a mail&ndash;cart, and they were
babies and he was their gigantic nursemaid.</p>
<p>*   *   *   *   *   *</p>
<p>I cannot possibly tell you what Bill and &rsquo;Becca said when they
found that the giant had gone. For one thing, I do not know.</p>

